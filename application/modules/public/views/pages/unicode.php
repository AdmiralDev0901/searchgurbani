            <h4 class="top-heading no-top">ENABLING UNICODE GURMUKHI / PUNJABI SUPPORT ON YOUR COMPUTER</h4>
            <h4>1. What Is Unicode?:</h4>
            <ul>
                <li class="black">It is the international standard whose goal is to specify a code matching every character needed by every
                    written human language to a single code point (integer). Unicode Consortium (unicode.org) - a non-profit
                    organization founded to develop, extend and promote use of the Unicode Standard - specifies the representation
                    of text in modern software products and standards.
                    <p>Unicode UTF-8 is mainly used on websites on the internet to render code text which needs a connection like
                        broadband in order to view the website well. </p>
                </li>
                <li class="black">It is the <strong>international standard</strong> that includes support for all major scripts of the World and
                    is adopted by all current major computer operating systems (OS). This solves a major problem for creators of
                    texts, as it is now possible to fully transcribe texts in multiple languages without requiring ASCII (American
                    Standard Code for Information Interchange) transliterations, special fonts or browsing software. Enabling it
                    also takes care of both left-to-right and right-to-left scripts.
                </li>
                <li class="black"> Unicode has become an industry standard. Microsoft software uses Unicode at its core. Hence, whether we realize
                    it or not, we are using Unicode already!
                </li>
                <li class="black"><strong>Indian (Indic) languages</strong>: it provides support for many Indic scripts including
                    <strong>Gurmukhi</strong>.
                </li>
            </ul>
            <h4>2. What are advantages of using Unicode for Gurmukhi?:</h4>
            <p>With a Unicode compatible computer system you can:</p>
            <ul>
                <li class="black">Search the entire web in Gurmukhi as you can do now for English. Major search engines already support Unicode
                    Gurmukhi.
                </li>
                <li>Create programs in Gurmukhi.</li>
                <li>Have your web page titles and text in Gurmukhi.</li>
                <li class="black">If you have a Unicode Gurmukhi keyboard installed on your computer, you may create the "message" of your email
                    to us in Gurmukhi (in conjunction with the "Contact Us" feature of our website).
                </li>
                <li class="black">Also, if you have a Unicode Gurmukhi keyboard installed on your computer, you may create your "message" while
                    using our "Tell-A-Friend" feature provided the computer of your friend you are sending the message to is Unicode
                    compatible, otherwise his/her browser will not display it correctly.
                </li>
                <li>Sort and organize data on your entire computer with ease.</li>
                <li class="black">Exchange data with other users without having to worry about specific fonts, confusion or data loss; and avoid
                    the hassles of upper-case lower-case and spacing problems that happen when many available non-Unicode Gurmukhi
                    fonts are used. For example, if a Chinese guy in China opens a Gurmukhi document, it will open it as Gurmukhi
                    document and not as Chinese.
                </li>
                <li>Name files and folders using Gurmukhi.</li>
                <li class="black">Documents and web-pages made with Unicode text,&nbsp;when viewed with an appropriate web-browser on a computer with
                    support for Unicode, will always be viewed&nbsp;in the right script even if the font in which web-pages are made is
                    not installed into the system (just as English text is always English, even if the font in which it is made is
                    missing).
                </li>
                <li>You can search in Gurmukhi in search engines; and you can chat by typing in Gurmukhi.</li>
                <li>You can name files and folders on your computer in Gurmukhi ...</li>
            </ul>
            <h4>3. What are disadvantages of using Unicode for Gurmukhi?:</h4>
            <p class="black">Although Unicode is a good solution for Gurmukhi, some minor disadvantages are as follows:</p>
            <ul>
                <li class="black"> People with older computers may not take full advantage of this new international standard.</li>
                <li class="black">Not all features of Gurmukhi (particularly the older Gurmukhi used in the Bani) can be represented yet. However,
                    this is not a problem for modern Punjabi.
                </li>
                <li class="black">Using Unicode Gurmukhi requires some readjustment in the way it is approached in comparison to font-based
                    Gurmukhi. For example, you may have to use a different keyboard layout.
                </li>
            </ul>
