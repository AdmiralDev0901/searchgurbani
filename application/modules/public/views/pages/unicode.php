            <h4 align="left">7. Manually Setting up Unicode:</h4>
            <p class="black" align="left">Although it's possible to enable Unicode support on some OS, but it is our understanding that this may
                quite be a tricky process on some relatively older OS. Some Web browsers can handle UTF-8, some can't. And those
