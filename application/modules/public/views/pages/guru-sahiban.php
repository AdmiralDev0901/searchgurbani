<h2>The Sikh Gurus</h2>


<p align="center"><em><strong>&quot;If a hundred moons were to come out,<br/>
            and a thousand suns were to rise;<br/>
            in spite of all this illumination,<br/>
            all would be pitch dark without the Guru.&quot;</strong></em><strong><em><br/>
        </em>(Guru Angad, 2nd prophet of the Sikhs)</strong></p>
<p>Guru Nanak received a Hukam (commandment) from God instructing him to spread a message of truth based on devotion to
    One God, honesty, and compassion. The soul of Guru Nanak passed on to nine successors, who elaborated on the first
    Guru's teachings to give form to this new religion. The final form was given by the tenth and last Guru, Gobind
    Singh, who started the Sikh initiation ceremony: initiated Sikhs formed a community of saint-soldiers known as
    Khalsa, the Pure Ones. Guru Gobind Singh also made it clear that, after him, the Guru-eternal for Sikhs would be
    their holy scripture, Guru Granth Sahib.</p>
