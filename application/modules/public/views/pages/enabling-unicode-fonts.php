<h3> Inputting Indic text</h3>
<p>Specific keyboard layouts can be enabled in System Preferences, in the International pane. Switching among enabled
    keyboard layouts is done through the input menu in the upper right corner of the screen. The input menu appears as
    an icon indicating the current input method or keyboard layout &mdash; often a flag identified with the country,
    language, or script. Specific instructions are available from the &quot;Help&quot; menu (search for &quot;Writing
    text in other languages&quot;).</p>
<p>A way to activate the keyboard(s) for Devanagari (Hindi etc.):</p>
<p>i) Open &quot;International&quot; located within System Preferences and select the &quot;Input Menu&quot; tab. (ii)
    Check the option for &quot;Devanagari&quot; and/or &quot;Devanagari - QWERTY&quot;. (iii) Check the &quot;Show input
    menu in menu bar&quot; option at the bottom of the &quot;International&quot; panel. Close the panel, and the new
    keyboard(s) should be available for selection when you click on the menu bar icon (upper right corner).</p>
