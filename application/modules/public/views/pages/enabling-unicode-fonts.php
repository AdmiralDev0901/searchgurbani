<h2>Mac OS X</h2>
<h3>Viewing Indic text</h3>
<p>You do not need to do anything to enable viewing of Indic text as long as you use <a
        href="http://en.wikipedia.org/wiki/Safari_%28web_browser%29" title="Safari (web browser)">Safari</a> or most
    other Cocoa applications, which fully support rearrangement and substitution for <a
        href="http://en.wikipedia.org/wiki/Apple_Advanced_Typography" title="Apple Advanced Typography">AAT</a>-based
    fonts. <a href="http://en.wikipedia.org/wiki/Firefox_%28web_browser%29" title="Firefox (web browser)">Firefox</a> up
    to version 2.0 does not support Indic script rendering at all because it does not use <a
        href="http://en.wikipedia.org/wiki/Apple_Type_Services_for_Unicode_Imaging"
        title="Apple Type Services for Unicode Imaging">ATSUI</a> (Firefox renders little rectangles instead). <a
        href="http://en.wikipedia.org/wiki/Opera_%28Internet_suite%29" title="Opera (Internet suite)">Opera</a> also
    provides some support, although considerable bugs remain as of version 9.2 (though Opera at least renders the
    glyphs).</p>
