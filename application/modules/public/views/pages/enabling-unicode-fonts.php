<h3>Viewing Indic text in Win XP</h3>
<ol>
    <li>Go to Start &gt; Control Panel.</li>
    <li>If you are in &quot;Category View&quot; select the icon that says &quot;Date, Time, Language and Regional
        Options&quot; and then select Regional and Language Options&quot;.
    </li>
    <li>If you are in Classic View select the icon that says &quot;Regional and Language Options&quot;.</li>
    <li>Select the &quot;Languages&quot; tab and make sure you select the option saying &quot;Install files for complex
        script and right-to-left languages (including Thai)&quot;. A confirmation message should now appear - press
        &quot;OK&quot; on this confirmation message.
    </li>
    <li>Allow the OS to install necessary files from the Windows XP CD and then reboot if prompted.</li>
</ol>
