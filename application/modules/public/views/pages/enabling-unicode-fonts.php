<h3>Inputting Indic text Win XP</h3>
<p>Windows XP have inbuilt InScript Keyboards for nearly all Indian languages. You can add them via Control Panel. You
    must follow the steps above before you perform the remaining steps.</p>
<ul>
    <li>In the &quot;Regional and Language Options&quot;, click the &quot;Languages&quot; tab.</li>
    <li>Click on the &quot;Details&quot; tab.</li>
    <li>Click the &quot;Add&quot; button to add a keyboard for your particular language.</li>
    <li>In the drop-down box, select your required Indian language.</li>
    <li>Make sure the check box labelled &quot;Keyboard layout/IME&quot; is selected and ensure you select an
        appropriate keyboard.
    </li>
    <li>Now select &quot;OK&quot; to save changes.</li>
</ul>
<p>You can use the combination ALT + SHIFT to switch between different keyboard layouts (e.g. from a UK Keyboard to
    Gurmukhi and vice-versa). If you want a language bar, you can select it by pressing the &quot;Language Bar...&quot;
    button on the &quot;Text Services and Input Languages&quot; dialog and then selecting &quot;Show the language bar on
    my desktop&quot;. The language bar enables you to visually select the keyboard layout you are using.</p>
