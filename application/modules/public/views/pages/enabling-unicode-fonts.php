<h2 align="center">Enabling Unicode Fonts</h2>
<p>Unicode is a multi-byte alphabet which can represent all major world scripts, and many obscure ones as well. This
    solves a major problem for creators of etexts, as it is now possible to fully transcribe texts in multiple languages
    without requiring ASCII transliterations, special fonts or browsing software. Unicode enabling also takes care of
    right-to-left scripts more-or-less automatically. </p>
<h3 align="left">Manually Selecting Unicode Encoding</h3>
<p> You may need to also manually select 'Unicode (UTF-8)' in certain browsers. For instance, under </p>
<p><strong>Internet Explorer</strong>, you can select 'View | Encoding', and 'Unicode (UTF-8)'. </p>
<p>Under <strong>Netscape/Firefox</strong>, this is 'View | Character Encoding'. </p>
<h3> For proper display of Gurmukhi &amp; Hindi scripts, <a
        href="http://en.wikipedia.org/wiki/Wikipedia:Enabling_complex_text_support_for_Indic_scripts" target="_blank">
        Unicode text rendering</a> must be enabled.</h3>
