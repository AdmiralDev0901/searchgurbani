                        <p class="mytext"><strong><em>Placement and count of rahau verses: </em></strong></p>
                        <p class="mytext"> All shabads of the Gurus have rahau verses, with numerals, placed at the end of the
                            first padas of the shabads, except shabads of Guru Tegh Bhadur, where the rahau verses are placed in
                            the beginning of the shabads. </p>
                        <p class="mytext"> In the specialist composition 'Anajali' there is no rahau verse in it. In all the
                            Ashtpadis the numbered rahau verses are placed at the end of the first pada. In one Ashtpadi (page
                            1018), there is no rahau verse in it. </p>
