                            the title of ashtpadi), 1 shabad (2 padas), 2 sloaks, </p>
                        <p class="mytext"> Bhagat Namdev - 1 shabad (4 padas) Bhagat Kabir - 1 shabad (4 padas) Bhagat Jaidev -
                            1 shabad (2 padas) Bhagat Kabir - 1 shabad (3 padas) </p>
                        <p class="mytext"> Bhagat Ravidas - 2 shabads (1=2 padas, 1=3 padas) </p>
