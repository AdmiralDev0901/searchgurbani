                                <td bgcolor="#eaeaea"><p class="blue">Maru is an old raga seldom heard in concerts today. Some
                                        theorists equate it with Maruva or 'Marva. In the Ragmala, Maru is a putra (son) of
                                        Malkaunsa. It is found in other classifications from the 14th to the early 19th century.
                                        Maru was used by Guru Nanak, Guru Amar Das, Guru Ram Das, Guru Arjan and Guru Tegh
                                        Bahadur for 144 hymns, two vars plus a large number of slokas. One of its variations is
                                        Maru Kafi. Maru is assigned to the hours of sunset and is considered suitable for the
                                        cold season. The mood is quiet and contemplative. The tonal material given here is for
                                        Maru Bihag, Bilaval thata. <br/>
                                        This <span
                                            style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">raga </span>is
                                        associated with warriors and is used to inspire people to acts of heroism. Guru Amardas
                                        calls that person a warrior who fights against his mind and its evil inclinations:<br/>
                                        &ldquo;Valiant and the most distinguished are the persons who grapple with their
                                        minds.<br/>
                                        They who recognise their won self ever remain united with their God&rdquo;[14]<br/>
                                        Kabir gives a similar connotations of the spiritual hero--<strong>Soora:</strong><br/>
                                        <strong>&ldquo;</strong>He alone is known to be a warrior who fights for the sake of his
                                        religion,<br/>
                                        He dies, cut piece by piece, but never deserts the battle field&rdquo;</p>
