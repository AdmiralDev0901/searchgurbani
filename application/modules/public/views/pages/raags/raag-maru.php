                        <p style="font-weight: bold"><span
                                style="font-size: 12px; color: #003366; border: #003366 none; font-family: Tahoma, sans-serif, Arial, Verdana;">Introduction : </span>
                        </p>
                        <p class="mytext"> According to Indian Schools of music this raga is a very old and popular raga. It is
                            also known as raga Malav, Marva,and Marvic. It belongs to Marva thaht. It is classified as a son of
                            raga Malkaus. </p>
                        <p class="mytext"> This raga is also mentioned as a son of raga Malkausak in the Ragamala listed at the
                            end of Guru Granth Sahib. According to Late Ustad Ameer Khan this raga gives a feeling of separation
                            and gloomy mood. It makes one sad after listening to this raga. </p>
                        <p class="mytext"> The scale and notes of the raga are as follows: </p>
                        <p class="mytext"> Arohi (ascending scale) - sa magama pa dha ni sa </p>
                        <p class="mytext"> Avrohi (descending scale) - sa ni dha pa ma dha pa ma pa ga sa </p>
                        <p class="mytext"> The vadi (most popular) note is 'ga' and samvadi (second most popular) note is
                            sa'. </p>
                        <p class="mytext">This raga is sung at the third part of the day i.e., from 12 noon to 3 p.m. The season
                            of its recitation is winter (shisher) i.e., during December, January. In Guru Granth Sahib it has
                            hymns from pages 989- 1106 (118 pages). </p>
