        <h3 class="no-top">Timings of Gurbani Raags</h3>


        <p class="black">There is an inextricable link between music and our moods. A lullaby soothes us to sleep; a
            love song arouses
            our amorousness; pop music sets our feet tapping; while devotional songs evoke our spirituality. Our sages
            and musicologists of yore studied this relationship between music and the human psyche and evolved raags
            that linked our changing moods to changes in nature.</p>
        <p class="black">What emerged was what we refer to as the time cycle of raags. According to this theory there is
            a special
            raag for each period of the day., representing each mood. Such a finely tuned understanding of the mood
            created by different musical notes does not exist in any system of music, anywhere in the world.</p>
        <p class="black">The first cycle raag corresponds to that twilight hour, just before dawn, when the colour of
            the sky begins
            to change, the last to the velvety night, after sunset. When you see the changing colours of the sky at the
            break of the day or the glorious hues of sunset, you may feel the same quiet wonder at the sheer beauty of
            nature, yet that you experience at daybreak may be different from your reactions to dusk.</p>
        <p class="black">Another interesting aspect of raag and Gurbani classification is understood by studying daily
            time-cycles. A
            raag has a preferred timing associated with it. There are some morning raags, evening raags, afternoon
            raags, etc. The timings of raags also complement the changes in human moods and heart during a twenty-four
            hour time cycle.</p>
        <p class="black">Upon classification of thirty-one main raags used in Guru Granth Sahib based on the prescribed
            raag timings,
            we find that no raags fall under the time zone 12 AM - 3 AM. It is interesting that the Gurus chose not to
            use any raag with this time cycle because one would normally sleep or engaged in your meditation or nitnem
            during this period.</p>


