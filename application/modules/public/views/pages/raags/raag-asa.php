        <h3 class="no-top maroon">Gurbani Raag: Asa </h3>
        <hr/>

        <span class="blue">
          <p>Asa is a very old raga, once popular in the Punjab but seldom heard in concerts today. In the Ragmala this is a ragini of raga Megha. However, today it is assigned to the Bilaval thata.&nbsp; Asa is a devotional raga for the cold season and is performed in the early morning just before sunrise. However, it is also known as a twilight melody with a calm mystical mood. Asa was used by Guru Nanak, Guru Angad, Guru Amar Das, Guru Ram Das, Guru Arjan and Guru Tegh Bahadur.In the Gurmat Sangeet style Aasa in a very important raag. The melodius notes of raag Asa are heard in every particle of the sacred land of Punjab. The golden rays of every dawn enter with the melodius tune of this raag and the redness of every dusk when it hides in the lap of nature, the melodious tunes of this raag sing the praises &ldquo;Balhaaree kudarath vasiaa&rdquo;. 
          <br/>
        <strong>Asa raga </strong>literally means the melody of hope. As the Gurus emphasised the   singing of God&rsquo;s praises before dawn, this raga is conducive to kirtan before day-break. It is a soothing and pleasing <span>raga, </span>appropriate for   the singing of the <strong>Asa-di-var, </strong>the morning-prayer of the Sikhs. Guru   Ramdas&rsquo;s Chhants ser the tone of this blissful composition:</p>
          <p align="center"><strong>&ldquo;My eyes   are damp with the nectar of the Lord;<br/>
          My soul is filled with His love&rdquo; [7].</strong></p>
        </span>
        <span class="blue">
        <p>Aroh : Sa, Re Ma, Pa, Dha Saˆˆ</p>
        <p>Avroh : Saˆˆ Nee Dha Pa, Ma, Ga Re Sa Re Ga Sa</p>
        <p>Sur : Gandhar and Nishad are forbidden notes in ascending scale, rest of the notes (swar) are sharp.</p>
        <p>Thaat : Bilawal</p>
        <p>Jaati : Aurav – Sampooran</p>
        <p>Time : sunrise and sunset (morning &amp; evening)</p>
        <p>Vadi : Madhym (Ma)</p>
        <p>Samvadi : Sharaj (Sa)</p>
        <p>Main sur : Re Ma, Pa, Dha Saˆˆ, Nee Dha Pa Ma, Ga Sa Re Ga Sa</p>
        
        <p align="center">&nbsp;</p>



        <p>Asa is a crooked (vakra) raga in that approaches to certain notes have to be made from a set position. Its variants as given in the Holy Book are Kafi and Asavari, both of which have many features in common with Asa. This raga may have originated in Maharashtra about the time of the major Muslim invasions. Its pleasing sound made it suitable bhajans by the Hindu devotees.</p>
        </span>

        <p align="left"><strong>Introduction : </strong></p>
        <p> In Indian Musical system ragas are divided into two main categories: the Northern system and the Southern system.
            Raga Asa belongs to the Northern system and is very popular in Punjab, it evokes calm mystical mood and lends itself
            very well to rendering devotional songs and hymns. According to one school of thought, in fact, Guru Nanak Dev is
            the author of this raga. The notes of this raga are: </p>
        <p> Arohi (ascending scale) - sa re ma pa dha ni pa dha sa (omitted note is -ga) <br/>
            Avrohi (descending scale) - sa ni dha pa ma ga re ga sa <br/>
            The vadi (most popular) note is 'ma' and samvadi (second most popular) note is 'sa' </p>
        <p> This raga is recommended to be sung at the fourth part of the night i.e., from 3 a.m - 6 a.m.. The season of its
            recitation is winter (hement) i.e., during November and December. In the Sikh tradition this raga is also sung in
            the evening. </p>
        <p> This raga is listed in the Ragamala as a consort (ragini) of Raga Megh, but in Guru Granth Sahib it is employed as a
            full fledged raga. </p>
        <p> This raga has been linked with other sub-ragas in Guru Granth Sahib as under: </p>
        <p><em><strong>Raga Asawari -</strong></em></p>
        <p> Asawari is an independent raga, but its links with raga Asa is found only in Sikh School of music. It has note 'dha'
            as most popular note and note 'ga' as the second most popular note. </p>
        <p> In Avrohi, notes ga, dha and ni are soft (komal) rest of the notes are sharp (tever) </p>
        <p><strong>Kafi - </strong></p>
        <p> This raga has been linked with ragas Asa, Tilang, Suhi and Maru in Guru Granth Sahib. Some school of music classify
            it as a complete raga while other call it a ragini (consort of a pure raga). Muslim Sufi saints sing most of their
            qwalis and thurnris in this raga. In its notes, 'ga' and 'ni' are soft notes while others are sharp notes. The
            recommended time of its singing is the fourth part of the day. </p>
        <p> In this raga ( and also in raga Kanra) many shabads titled as <strong> 'Partal' </strong> have been composed. In
            these shabads the 'tal' is frequently changed/ repeated. </p>
        <p> A title <strong>'Sudhang' </strong>has also been used in this raga, which means 'pure scales'. It is not the name of
            any raga or sub-raga. </p>
        <p> In Guru Granth Sahib hymns composed in this raga are on pages 347-488 (142 pages). </p>
