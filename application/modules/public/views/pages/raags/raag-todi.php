                                            <td height="314"><p><span class="blue">A ragini .of Dipak in the Ragmala, Todi is today the head of a thata. It is considered one of the most important of the north Indian ragas. Todi was used by the Gurus for 32 hymns. This is a raga for the late morning hours and the mood is gentle, with an aura of adoration. The texts composed to this raga emphasize that no matter what problems man meets or what worldly affairs distract the mind, devotion to the Lord brings one back to the path of release from worldliness. Generally used for singing praises of noble men and kings, this </span><span
                                                        style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">raga </span><span
                                                        class="blue">was   quite popular at Akbar&rsquo;s Court. Tansen sang </span><span
                                                        style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">Mian-ki-todi </span><span
                                                        class="blue">praise of   Emperor Akbar. The Gurus, however, used this </span><span
                                                        style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">raga </span><span
                                                        class="blue">to sing the praises of   God. Guru Arjan sang as follows:<br/>
                                                        &ldquo;I have just one God, my Lord; I know no   other.<br/>
                            The Lord&rsquo;s praise is my way of life, occupation and caste;<br/>
                            Hearing   the </span><span style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">kirtan </span><span
                                                        class="blue">of the Lord, I feel great joy&rdquo;[10].</span></p>
