                        <p style="font-weight: bold"><span
                                style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">Introduction </span>
                        </p>
                        <p class="blue"> Raga Todi, which comes under Todi that, is one of the most exalted ragas of Northern
                            India. It is classified as a ragini of a combination of raga Dhanasri, Asawari and Khat. It is
                            mentioned as a consort of Raga Bhairo in Nad-Binod, consort of Malkaus in Sur-Tal group, consort of
                            raga Deepak in Budh Prakash-Darpan and consort of raga Basant in Shivmat The mood of this raga is
                            very tender and compassionate. </p>
                        <p class="blue"> In the Ragamala listed at the end of Guru Granth Sahib it is mentioned as a consort of
                            raga Deepak. </p>
                        <p class="blue"> The scale and notes of the raga are as follows: </p>
                        <p class="blue"> Arohi (ascending scale) - ni re ga ma dha ni sa </p>
                        <p class="blue"> Avrohi (descending scale) - ni dha pa ma dha ma ga ma ga re sa </p>
                        <p class="blue"> The vadi (most popular) note is 'dha' and samvadi (second most popular) note is
                            'ga' </p>
                        <p class="blue">This raga is sung at the first part of the day i.e., from 6 a.m. to 9 a.m. The season of
                            its recitation is winter (shisher) i.e., during December and January. In Guru Granth Sahib it has
                            hymns from pages 711- 718 (8 pages). </p>
                        <p class="blue"><strong> The Composers: </strong></p>
                        <p class="blue"> The composers of bani (hymns) in this raga are: </p>
                        <p class="blue" style="font-weight: bold"><em> Gurus </em></p>
                        <ul>
                            <li class="blue"> Guru Ramdas</li>
                            <li class="blue"> Guru Arjan Dev</li>
                            <li class="blue"> Guru Tegh Bahadur</li>
                        </ul>
                        <p class="blue" style="font-weight: bold"><em>Bhagats: </em></p>
                        <ul>
                            <li class="blue"> Namdev</li>
                        </ul>
                        <p class="blue"><strong> The Structure: </strong></p>
                        <p class="blue"> The sequence of the structure of compositions in this raga are: </p>
                        <p class="blue"><strong><em>Gurubani: </em></strong></p>
                        <ul>
                            <li class="blue"> Shabads (2, 5 ,4 padas)</li>
                            <li class="blue"> Shabads - Chhants</li>
                        </ul>
                        <p class="blue"><strong><em>Bhagatbani: </em></strong></p>
                        <ul>
                            <li class="blue"> Shabads ( 2, 4 padas)</li>
                        </ul>
