                        <p class="blue" style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                        <p class="blue"> All shabads of the Gurus have a rahau verse in them, some numbered some not numbered,
                            and all rahaus have been in the beginning of the shabads. </p>
                        <p class="blue"> In Bhagat Bard the rahau verses have numerals and are placed as: first shabad at the
                            end of the first pada, and second and third shabads in the beginning of the shabads </p>
