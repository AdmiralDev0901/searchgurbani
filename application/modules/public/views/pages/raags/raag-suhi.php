                        <p style="font-weight: bold"><span
                                style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">Introduction : </span>
                        </p>
                        <p class="blue"> This raga is also called raga Suha and raga Suhbi and is listed as a consort of raga
                            Meg. It is made up of a union of raga Bharvi, Sri, Kanra and Sarang. Some musical schools list it as
                            a daughter-in-law of raga Bharay. </p>
                        <p class="blue"> This raga under its present name is not mentioned in the Ragamala listed at the end of
                            Guru Granth Sahib. The raginis of raga Meg mentioned in the ragamala are: Sorath, Gaud, Malarai, Asa
                            and Suho. Many authors are of the view that Suho is raga Suhi. </p>
                        <p class="blue">The scale and notes of the raga are as follows: </p>
                        <p class="blue"> Arohi (ascending scale) - sa re ga ma, re ga ma pa, ni dha ni sa </p>
                        <p class="blue"> Avrohi (descending scale) - sa ni dha pa, ni dha pa, ma ga, dha pa ma ga re, pa ma ga
                            re ga re sa </p>
                        <p class="blue"> The vadi (most popular) note is 'pa' and samvadi (second most popular) note is </p>
                        <p class="blue">This raga is sung at the first part of the day i.e., from 6 a.m. to 9 a.m. The season of
                            its recitation is spring (basant) i.e., during February and March. In Guru Granth Sahib it has hymns
                            from pages 728 - 794 (65 pages). </p>
                        <p class="blue"><strong>The Composers: </strong></p>
                        <p class="blue"> The composers of bani (hymns) in this raga are: </p>
                        <p class="blue"><strong><em>Gurus </em></strong></p>
                        <ul>
                            <li class="blue"> Guru Nanak</li>
                            <li class="blue"> Guru Angad</li>
                            <li class="blue"> Guru Amardas</li>
                            <li class="blue"> Guru Ramdas</li>
                            <li class="blue"> Guru Arjan Dev</li>
                        </ul>
