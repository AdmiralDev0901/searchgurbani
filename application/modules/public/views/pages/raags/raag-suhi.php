                                <td bgcolor="#eaeaea"><p class="blue">Suhi is classified in the Ragmala as a raoni of Nfeglta.
                                        It was a favourite with Muslims and was considered proper for the hot season. Tociav
                                        this raga belongs to the Ktifi thata and its performance time is late morning. In the
                                        Holy Book one variant is given, Raga Suhi Lalit. Sfilii was used by Guru Nanak, Guru
                                        Angad, Guru Amar Das, Guru Ram Das and Guru Arjan for 130 hymns, a var plus many
                                        slokas. </p>
                                    <p><span class="blue">This is morning </span><span
                                            style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">raga </span><span
                                            class="blue">and a happy one. The Gurus have composed hymns   concerning married life in this </span><span
                                            style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">raga</span><span
                                            class="blue"><strong>. </strong>These are <strong>Ghorian, Lavan,   Suchaji, Kuchaji </strong>and <strong>Gunwanti. </strong>There are several references to the   life of the householder and particularly to the condition of the housewife. Her   longing for her spouse becomes very keen and irresistible, as the night   deepens:<br/>
                                            &ldquo;I have neither beauty now bewitching eyes nor decent manners, now   sweet speech.<br/>
                        The bride bedecks herself with divine knowledge.<br/>
                        If her   spouse loves her, she becomes a happy wife&rdquo;[11].<br/>
                      </span></p>
                                    <p class="blue"><b>Aroh</b> : Sa Re Ga Nla Pa, Ni Dha Ni Sa </p>
                                    <p class="blue"><b>Avroh </b>: Sa Ni Dha Pa, Nla Ga, Re Ga Re, Sa </p>
                                    <p class="blue"><b>Vadi</b> : Pa </p>
                                    <p class="blue"><b>Samvadi </b>: Sa </p></td>
