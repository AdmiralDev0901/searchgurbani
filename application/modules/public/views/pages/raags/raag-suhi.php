                            <li class="blue"> Farid</li>
                        </ul>
                        <p class="blue"><strong> The Structure </strong></p>
                        <p class="blue"> The sequence of the structure of compositions in this raga are: </p>
                        <p class="blue"><strong><em> Gurubani: </em></strong></p>
                        <ul>
                            <li class="blue"> Shabads (1,2,3,4, and 5 padas)</li>
                            <li class="blue"> Shabads ( 8 padas Ashtpadis)</li>
                            <li class="blue">Shabads (Chhants)</li>
                            <li class="blue"> Shabads (Specialist compositions)</li>
                            <li class="blue"> Shabads (var)</li>
                        </ul>
                        <p class="blue"><strong>Matrix </strong></p>
                        <p class="blue" style="font-weight: bold"><em> VISUAL ANALYSIS </em></p>
                        <p class="blue" style="font-weight: bold"><em> Count of the use of Managals: </em></p>
                        <ul>
                            <li class="blue"> Complete Mool Mantar = 1 at page 728</li>
                            <li class="blue"> Ik-ongkar Satgur Prasadh = 42</li>
                        </ul>
                        <p class="blue" style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                        <p class="blue"> All shabads and ashtpadis have rahau verses with numerals and are placed at the end of
                            the first padas of the shabads. Specialist compositions 'Suchajji and Kuchajji of Guru Nanak and
                            'Gunwanti' of Guru Arjan have no rahau verses in them. </p>
                        <p class="blue"> The Bhagat Bani has rahau verse with numerals and is placed at the end of the first
                            padas of the shabads. </p>
