                                <td bgcolor="#eaeaea"><p class="blue"><b>MALAR</b> (MALLAR or MALHAR) Malar is one of the
                                        rainy-season ragas performed from June to September. During the monsoons, Malar can be
                                        sung at any time of the day or night; otherwise, it is designated for late evening or
                                        early morning. Its mood is joyful because the rains cause the crops to grow and the
                                        flowers to bloom. Malar is frequently combined with other ragas, particularly Megha.
                                        Tansen added some changes to Malhar and this raga is known as Mian ki Malhar. In the
                                        Ragmala, Gaund-Malar is described as a ragini of Megha and is the only one with a Malhar
                                        name. Today the Malhar ragas are assigned to the Kafi thata. A favourite of Hindu
                                        musicians, Malhar was used by Guru Nanak, Guru Angad, Guru Amar Das, Guru Ram Das, and
                                        Guru Arjan. The pure Malhar is seldom performed today, and it might be heard in one of
                                        its combinations. </p>
                                    <p class="blue"><b>Aroh </b>: Sa, Re Ga Ma, Ma Re Pa, Ni Dha Ni Sa </p>
                                    <p class="blue"><b>Avroh</b> : Sa, Dha Ni Pa, 'via Ga Ma, Re Sa </p>
                                    <p class="blue"><b>Pakar</b> : Sa Re Ga Ma, Ma Re Pa, Dha Ni Pa, Ma Re Sa </p>
                                    <p class="blue"><b>Vadi:</b> Ma</p>
                                    <p class="blue"><b>Samvadi:</b> Sa</p></td>
