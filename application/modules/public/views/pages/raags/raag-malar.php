                        <p><strong> Introduction : </strong></p>
                        <p> It is a very serene raga. It helps to create a congenial atmosphere and spreads the fragrance of
                            love, unity and divinity </p>
                        <p> This raga is mentioned as first ragini of of raga Meg in the Ragamala listed at the end of Guru
                            Granth Sahib. </p>
                        <p>The scale and notes of the raga are as follows: </p>
                        <p> Arohi (ascending scale) - sa re ga ma, ma re pa, ni dha ni sa </p>
                        <p> Avrohi (descending scale) - sa, dha ni pa, ma ga ma, re sa ma re sa </p>
                        <p> The vadi (most popular) note is 'ma' and samvadi (second most popular) note is 'sa'. </p>
                        <p>This raga is sung in the third part of the night i.e., 12 a.m. 3 a.m. The season of its recitation is
                            rainy (varsha) i.e., during July - August. In Guru Granth Sahib it has hymns from pages 1254 - 1293
                            ( 40 pages). </p>
                        <p><strong>The Composers: </strong></p>
                        <p> The composers of bani (hymns) in this raga are: </p>
                        <p><strong><em>Gurus </em></strong></p>
                        <ul>
                            <li> Guru Nanak Dev</li>
                            <li> Guru Angad</li>
                            <li> Guru Amardas</li>
                            <li> Ramdas</li>
                            <li> Guru Allan Dev</li>
                        </ul>
                        <p><strong><em>Bhagats: </em></strong></p>
                        <ul>
                            <li> Namdev</li>
                            <li> Ravidas</li>
                        </ul>
                        <p style="font-weight: bold"><em>The structure: </em></p>
                        <p> The sequence of the structure of compositions in this raga are: </p>
                        <p style="font-weight: bold"><em>Gurubani: </em></p>
                        <ul>
                            <li> Shabads (2 - 5padas)</li>
                            <li> Ashtpadis</li>
                            <li> Chhant'</li>
                            <li> Var</li>
                        </ul>
                        <p><strong><em>Bhagatbani: </em></strong></p>
                        <ul>
                            <li> Shabads (3, 5 padas)</li>
                        </ul>
                        <p style="font-weight: bold"><em>Matrix </em></p>
                        <p style="font-weight: bold"><em> VISUAL ANALYSIS </em></p>
                        <p style="font-weight: bold"><em> Count of the use of Managals: </em></p>
                        <ul>
                            <li> Complete Mool Mantar = 1 at page 1254</li>
                            <li> Ik-ongkar Satgur Prasadh = 18</li>
                        </ul>
                        <p style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                        <p> First 35 shabads have one rahau verse, numbered and placed at the end of the first padas of the
                            shabads; in the next 18 shabads, the placing of the rahau verses is in the beginning of the shabads;
                            in the next 4 shabads the placing is again at the end of the first padas of the shabads, in the last
                            8 shabads the placing is again shifted at the start of the shabads. </p>
                        <p> In the ashtpadis, the rahau verses are placed at the end of the first padas, and all verses have
                            numeral 1 with them. </p>
                        <p> In the Bhagat Bath the rahau verses have numerals, except the last shabad, and are placed in the
                            beginning of the shabads. </p>
