                        <p><strong> Introduction : </strong></p>
                        <p> According to Indian Schools of music this raga is a very old, popular, simple and melodious raga.
                            Its effect is very cool, so it is normally sung at noon time to give to the listeners a cooling
                            effect. The snake-charmers also use this raga to intoxicate poisonous snakes with its melody. This
                            raga is sung in many variations e.g., </p>
                        <ul>
                            <li> Sadh Sarang,</li>
                            <li> Madhmad Sarang</li>
                            <li> Bindrabani Sarang</li>
                            <li> Lankdehan Sarang</li>
                            <li> Mia ki Sarang,</li>
                            <li> Gaund Sarang,</li>
                            <li> Jaldhar Sarang,</li>
                            <li> Surdasi Sarang,</li>
                            <li> Nur Sarang,</li>
                            <li> Samant Sarang</li>
                            <li>Wadhans Sarang</li>
                        </ul>
                        <p>This raga is mentioned as a son of raga Sri in the Ragamala listed at the end of Guru Granth
                            Sahib. </p>
                        <p>The scale and notes of the raga are as follows: </p>
                        <p> Arohi (ascending scale) - sa re ma pa ni sa </p>
                        <p> Avrohi (descending scale) - sa ni pa ma re sa </p>
                        <p> The wadi. (most popular) note is 're' and samvadi (second most popular) note is 'pa'. </p>
                        <p>This raga is normally sung in the second part of the day i.e., 9 a.m. 12 noon. The season of its
                            recitation is rainy (varsha) i.e., during July - August. In Guru Granth Sahib it has hymns from
                            pages 1197 - 1253 ( 57 pages). </p>
                        <p><strong> The Composers: </strong></p>
                        <p> The composers of bani (hymns) in this raga are: </p>
                        <p><strong><em>Gurus </em></strong></p>
                        <ul>
                            <li> Guru Nanak Dev</li>
                            <li> Guru Angad</li>
                            <li> Guru Amardas</li>
                            <li> Ramdas</li>
                            <li> Guru Arjan Dev</li>
                            <li> Guru Tegh Bahadur</li>
                        </ul>
                        <p style="font-weight: bold"><em> Bhagats: </em></p>
                        <ul>
                            <li> Kabir</li>
                            <li> Namdev</li>
                            <li> Ravidas</li>
                            <li> Parmanand</li>
                            <li> Surdas</li>
                        </ul>
                        <p style="font-weight: bold"><em> The structure: </em></p>
                        <p> The sequence of the structure of compositions in this raga are: </p>
                        <p style="font-weight: bold"><em> Gurubani: </em></p>
                        <ul>
                            <li> Shabads (2, 4, 5padas)</li>
                            <li> Ashtpadis</li>
                            <li> Chhant</li>
                            <li> Var</li>
                        </ul>
                        <p><strong><em>Bhagatbani: </em></strong></p>
                        <ul>
                            <li> Shabads (2-5 padas)</li>
                        </ul>
