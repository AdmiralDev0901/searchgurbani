                                <td bgcolor="#eaeaea"><p class="blue">Jaitsri does not appear in the Ragmala nor is it found in
                                        the modern literature on the subject. Bhatkhande gives,Jait-Kalyan but this is not to be
                                        confused with the above. However, jaitsri does appear in a 17th century classification,
                                        but not in later ones. Guru Ram Das, Guru Arjan and Guru Tegh Bahadur composed 30 hymns,
                                        a var and several slokas to this raga. Today raga Jait is found under the Marva thata
                                        and is assigned to the evening hours. A mood of gentle quietness and mystery pervades
                                        this raga. The texts describe the meditative thoughts of a devotee who has surrendered
                                        himself to his Guru and Lord. Raga Jait has two forms and the second includes some
                                        elements from Sin Raga and perhaps this is nearest the original Jaitsri. </p>
                                    <p class="blue"><b>Aroh</b> : Sa Ga Ma Pa Ni Sa </p>
                                    <p class="blue"><b>Avroh</b> : Sa Ni _Dha Pa Ma Ga Re Sa </p>
                                    <p class="blue"><b>Pakar</b> : Sa, Ga Pa M'a Dha Pa M'a Ga,M'a &nbsp;Ga Re Sa Vadi : Ga </p>
                                    <p class="blue"><b>Samvadi</b> : Ni </p>
                                    <p class="blue">Because of the two different ways of singing this raga, melodic patterns are
                                        not fixed</p></td>
