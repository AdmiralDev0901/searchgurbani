                        <p><strong> Introduction : </strong></p>
                        <p> According to Norther school of Music this raga is classified as a ragini. It is made up of a mixture
                            of Dhanasri, Dhani and Sri ragas. </p>
                        <p> This raga is not included in the Ragamala listed at the end of Guru Granth Sahib. </p>
                        <p> The scale and notes of the raga are as follows: </p>
                        <p> Arohi (ascending scale) - sa ga ma pa ni sa </p>
                        <p> Avrohi (descending scale) - sa ni dha pa ma ga re sa </p>
                        <p> The vadi (most popular) note is 'ga' and samvadi (second most popular) note is 'na'. </p>
                        <p> This raga is sung at the third part of the day i.e., from 12 p.m. to 3 p.m. The season of its
                            recitation is winter (shisher) i.e., during December and January. In Guru Granth Sahib it has hymns
                            from pages 696 - 710 (15 pages). </p>
                        <p><strong>The Composers: </strong></p>
                        <p> The composers of bani (hymns) in this raga are: </p>
                        <p style="font-weight: bold"><em>Gurus: </em></p>
                        <ul>
                            <li>Guru Ramdas</li>
                            <li> Guru Arjan Dev</li>
                            <li>Guru Tegh Bhadur</li>
                        </ul>
                        <p><strong><em>Bhagats: </em></strong></p>
                        <ul>
                            <li>Ravidas</li>
                        </ul>
                        <p><strong> The Structure: </strong></p>
                        <p> The sequence of the structure of compositions in this raga are: </p>
                        <p><strong><em> Guru bani: </em></strong></p>
                        <ul>
                            <li> Shabads (2,3,4 padas)</li>
                            <li>Shabads-Chhants'</li>
                            <li> Var</li>
                        </ul>
                        <p> The word 'Shud' is written at the end of the var. For the explanation of this comment please refer
                            to page. </p>
                        <p style="font-weight: bold">Bhagatbani:</p>
                        <ul>
                            <li> Shabads (6 padas)</li>
                        </ul>
                        <p style="font-weight: bold"> Matrix </p>
                        <p style="font-weight: bold"><em>VISUAL ANALYSIS </em></p>
                        <p style="font-weight: bold"><em>Count of the use of Managals: </em></p>
                        <p> Complete Mool Mantar = nil Ik-ongkar Satgur Prasadh = 10 </p>
                        <p><span style="font-weight: bold"> <em>Placement and count of rahau verses: </em></span></p>
                        <p> In Guru Ramdas's six shabads listed in Ghar 1, there is a rahau verse, without numeral '1', placed
                            at the end of the first padas of the shabads. In further five shabads listed in Ghar '2' there is no
                            rahau verse in them. </p>
                        <p> In Guru Arjan's 13 shabads, there is a rahau verse in all of them, with numeral '1' with them, and
                            the rahau verses are placed at the beginning of the shabads. </p>
                        <p> In Guru Tegh Bahadur's 3 shabads the rahau verse with numeral '1' are placed in the beginning of the
                            shabads. </p>
                        <p> In Bhagat Bani there is one shabad of Bhagat Ravidas, which has rahau verse with numeral, and is
                            placed in the beginning of the shabad. </p>
