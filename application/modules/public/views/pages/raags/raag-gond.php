                                            <td><p class="blue">The Ragmala records Gaund and Gund as putras (sons) of Siri
                                                    Raga, but does not give Gond. The possibility exists that Gone is a regional
                                                    raga derived from that group of ragas with similar names and characterized
                                                    by phrases from other ragas e.g. Bilaval, Kanara and Malar. Such names as
                                                    Gattnda, Gand, Gounda, Gaundi, Goundgiri, and Gunda appear in
                                                    classifications from the 11 th to the 17th centuries. For those still known
                                                    today (Gaudi, Goundgiri, and Goud) performance rules are obscure.
                                                    Performance time is late afternoon or early evening and the mood is
                                                    contemplative and dignified. Gond was used by Guru Ram Das and Guru Arjan
                                                    (29 hymns). The texts beseech man to depend solely on the Lord for all
                                                    benefits since it is He who has given him all his blessings. </p>
                                                <p class="blue"><b>Aroh</b> : Sa Re Ga Ma, Pa Dha Ni Dha Ni Sa </p>
