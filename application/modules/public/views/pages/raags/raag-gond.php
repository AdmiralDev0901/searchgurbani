                        <p style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                        <p> All shabads and ashtpadis of the Gurus have rahau verse with number (except one shabad at page 868),
                            placed at the end of the first padas of the shabads </p>
                        <p> In Bhagat Bani a similar pattern is adopted, except one shabad of Namdev (page 874) where the verse
                            is placed in the beginning of the shabads. </p>
