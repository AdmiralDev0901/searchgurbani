                        <p><strong> Introduction : </strong></p>
                        <p> This raga is a very old raga. In religious music it is sung in conjunction with raga Bilawal and is
                            known as 'Bilawal Gaund'. </p>
                        <p> This raga is mentioned as a son of rag Sri in the Ragamala listed at the end of Guru Granth
                            Sahib. </p>
                        <p> The scale and notes of the raga are as follows: </p>
                        <p> Arohi (ascending scale) - sa re ga ma pa dha ni dha ni sa </p>
                        <p> Avrohi (descending scale) - sa ni dha ni pa ma ga, re sa </p>
                        <p> The vadi (most popular) note is 'sa' and samvadi (second most popular) note is 'ma'. </p>
                        <p> This raga is sung at the third part of the day i.e., from 12.00 noon. to 3 p..m. The season of its
                            recitation is winter (hement) i.e., during November, December. In Guru Granth Sahib it has hymns
                            from pages 859 - 875 (17 pages). </p>
                        <p><span style="font-weight: bold">The Composers: </span></p>
                        <p> The composers of bani (hymns) in this raga are: </p>
                        <p style="font-weight: bold"><em>Gurus </em></p>
                        <ul>
                            <li> Guru Ramdas</li>
                            <li> Guru Arjan Dev</li>
                        </ul>
                        <p style="font-weight: bold"><em>Bhagats: </em></p>
                        <ul>
                            <li> Kabir</li>
                            <li> Namdev</li>
                            <li> Ravidas</li>
                        </ul>
                        <p><span style="font-weight: bold">The Structure: </span></p>
                        <p> The sequence of the structure of compositions in this raga are: </p>
                        <p style="font-weight: bold"><em>Gurubani: </em></p>
                        <ul>
                            <li> Shabads (2,3,4, 5 and 6 padas)</li>
                            <li> Shabads (8 padas, Ashtpadis)</li>
                        </ul>
