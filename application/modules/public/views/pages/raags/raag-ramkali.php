                        <p style="font-weight: bold"><span
                                style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">Introduction : </span>
                        </p>
                        <p class="blue"> According to one school of thought this raga is a ragini of Bhairav thath. This raga is
                            not mentioned in the Ragamala listed at the end of Guru Granth Sahib. </p>
                        <p class="blue"> The scale and notes of the raga are as follows: </p>
                        <p class="blue"> Arohi (ascending scale) - sa re ga ma ga pa dha sa </p>
                        <p class="blue"> Avrohi (descending scale) - sa ni dha, ni dha pa, ma ga, re sa </p>
                        <p class="blue"> The vadi (most popular) note is 'pa' and samvadi (second most popular) note is 'sa
                            '. </p>
                        <p class="blue">This raga is sung at the first part of the day i.e., from 6 a.m. to 9 a..m. The season
                            of its recitation is spring (basant) i.e., during February, March. In Guru Granth Sahib it has hymns
                            from pages 876 - 974 (99 pages). </p>
                        <p class="blue" style="font-weight: bold"> The Composers: </p>
                        <p class="blue"> The composers of bani (hymns) in this raga are: </p>
                        <p class="blue"><strong><em> Gurus </em></strong></p>
                        <ul>
                            <li class="blue"> Guru Nanak</li>
                            <li class="blue"> Guru Angad</li>
