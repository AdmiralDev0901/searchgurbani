                        <ul>
                            <li class="blue"> Satta &amp; Balwand</li>
                            <li class="blue"> Baba Sundar</li>
                        </ul>
                        <p><span class="blue" style="font-weight: bold">The Structure: </span></p>
                        <p class="blue"> The sequence of the structure of compositions in this raga are: </p>
                        <p class="blue"><strong><em> Gurubani: </em></strong></p>
                        <ul>
                            <li class="blue"> Shabads (2,3,4, 5 and 6 padas)</li>
                            <li class="blue"> Shabads (8 padas, Ashtpadis)</li>
                            <li class="blue"> Specialist compositions</li>
                            <li class="blue"> Var</li>
                        </ul>
                        <p class="blue" style="font-weight: bold"> Matrix </p>
                        <p class="blue" style="font-weight: bold"><em>VISUAL ANALYSIS </em></p>
                        <p class="blue"><em>Count of the use of Managals: </em></p>
                        <ul>
                            <li class="blue"> Complete Mool Mantar = 1 at page 876</li>
                            <li class="blue"> Ik-ongkar Satgur Prasadh = 25</li>
                        </ul>
                        <p class="blue" style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                        <p class="blue"> First 54 shabads have rahau verses with numeral, placed at the end of the first padas
                            (except shabad 53); rest all the shabads have rahau verses at the start of the first pada. In
                            Ashtpadis, the rahau verses, with numerals are placed at the end of the first padas of the
                            ashtpadis. In the specialist banis the position is as follows: </p>
                        <ul>
                            <li class="blue"> Anand Shib - no rahau verse</li>
                            <li class="blue"> Sadh - no rahau verse</li>
                            <li class="blue"> Dakhni Onkar and Sidh Ghosht - one rahau verse at the end of the first pada. In
                                Bhagat Bani there are rahau verses, with numerals placed at the end of the first padas of the
                                shabads.
                            </li>
                        </ul>
