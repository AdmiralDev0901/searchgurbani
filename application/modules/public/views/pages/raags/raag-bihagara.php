                        <p><strong> Introduction : </strong></p>
                        <p> Raga Bihagra is made up of ragas Kedara, Gauri and Siam. There is no mention of this raga in the
                            Ragamala listed at the end of Guru Granth Sahib. In the Kali Nath School of music it is listed as a
                            consort of raga Bhairav, whereas in the Bharat School it is listed as a son of raga Deepak, and in
                            the Hindi Shabad Sagar it is listed as a son of raga Hindol. In Nad Binod's school it is titled as
                            'Bihag, and Chhaya Bihag'. According to Bhai Kahn Singh it is a form of raga Bilawal. </p>
                        <p> The scale and notes of the raga are as follows: </p>
                        <p> Arohi (ascending scale) - ni sa ga ma pa ni sa (omitted notes are -ga, ni) <br/>
                            Avrohi (descending scale) - sa ni dha pa na-dha-pa ma-ga-ma ga re sa <br/>
                            The vadi (most popular) note is 'ga' and samvadi (second most popular) note is 'ni' </p>
                        <p> This raga is sung at the second part of the night i.e., from 9 p.m. to 12 a.m. The season of its
                            recitation is winter (hement) i.e., during November and Decembe. In Guru Granth Sahib it has hymns
                            from pages 537 - 556 ( 20 pages). </p>
                        <p><strong> The Composers: </strong></p>
                        <p> The composers of bani (hymns) in this raga are: </p>
                        <p><strong><em>Gurus </em></strong></p>
                        <ul>
                            <li> Guru Nanak Dev</li>
                            <li> Guru Amardas</li>
                            <li> Guru Ramdas</li>
                            <li> Guru Arjan Dev</li>
                            <li> Guru Tegh Bahadur</li>
                        </ul>
                        <p><strong><em>Bhagats: </em></strong></p>
                        <p> There is no Bhagat bani in this raga. </p>
                        <p><strong>The structure: </strong></p>
                        <p> The sequence of the structure of compositions in this raga are: </p>
                        <p><strong><em> Gurubani: </em></strong></p>
                        <ul>
                            <li> Shabads ( 3 and 4 padas)</li>
                            <li> Shabads Chhant</li>
                            <li> Var</li>
                        </ul>
                        <p style="font-weight: bold">Matrix </p>
                        <p style="font-weight: bold"><em> VISUAL ANALYSIS </em></p>
                        <p style="font-weight: bold"><em> Count of the use of Managals: </em></p>
                        <p> Complete Mool Mantar = 1 at page 537 <br/>
                            Ik-ongkar Satgur Prasadh = 5 </p>
                        <p><span style="font-weight: bold"> <em>Placement and count of rahaus in this raga: </em></span></p>
                        <p> In this raga there are only two shabads, one of Guru Arjan Dev and one of Guru Tegh Bahadur, rest of
                            the compositions are chhants and a var. Both shabads have rahau verse in them, in the first shabad
                            of Guru Ramdas, it is placed after the first pada, whereas in the second shabad of Guru Tegh Bahadur
                            it is placed in the beginning of the shabad. </p>
