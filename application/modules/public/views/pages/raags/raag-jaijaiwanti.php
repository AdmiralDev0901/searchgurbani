                        <p><strong> Introduction : </strong></p>
                        <p> According to Bhai Kahn Singh Nabha this raga is a mixture of Bilawal and Sorath ragas. There is no
                            mention of this raga in any ancient book of Indian music. According to Dr. Rattan Singh jaggi, this
                            raga belongs to Kmach thata. </p>
                        <p>This raga is not mentioned in the Ragamala listed at the end of Guru Granth Sahib. </p>
                        <p> The scale and notes of the raga are as follows: </p>
                        <p> Arohi (ascending scale) - re ga re sa , re ga, ma pa dha pa, ni sa Avrohi (descending scale) - sa ni
                            dha pa, dha ma ga re, re ga re sa </p>
                        <p> The vadi (most popular) note is 're' and samvadi (second most popular) note is 'pa'. </p>
                        <p>This raga is sung in the first part of the night i.e., 6 p.m. 9 p.m. The season of its recitation is
                            summer (grikham) i.e., during May - June. In Guru Granth Sahib it has hymns from pages 1352 - 1353 (
                            2 page). </p>
                        <p><strong> The Composers: </strong></p>
                        <p> The composers of bard (hymns) in this raga are: </p>
                        <p><strong><em>Gurus </em></strong></p>
                        <ul>
                            <li> Tegh Bahadur</li>
                        </ul>
                        <p style="font-weight: bold"><em>Bhagats: </em></p>
                        <p> There is no Bhagat Bard in this raga </p>
                        <p style="font-weight: bold"><em>The structure: </em></p>
                        <p> The sequence of the structure of compositions in this raga <strong>are: </strong></p>
                        <p><strong><em>Gurubani: </em></strong></p>
                        <ul>
                            <li> Shabads (2 padas)</li>
                        </ul>
