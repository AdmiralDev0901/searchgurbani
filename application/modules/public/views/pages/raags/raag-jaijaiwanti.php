                                <td bgcolor="#eaeaea"><p class="blue">Jaijavanti was used only by Guru Tegh Bahadur for four
                                        hymns. This raga does not appear in the Ragmala but was known as Javanta as early as the
                                        14th century. Today it is regarded as an important raga belonging to the Khainaj thata.
                                        This majestic and highly arresting raga is assigned to the night hours.</p>
                                    <p class="blue">&nbsp;<b>Aroh</b> : Sa, Re Ga Ma Pa, Ni Sa </p>
                                    <p class="blue"><b>Avroh</b> : Sa Ni Dha Pa, Dha Ma, Re Ga Re Sa </p>
                                    <p class="blue"><b>Pakar</b> : Re Ga Re Sa, Ni Dha Pa Re </p>
                                    <p class="blue"><b>Vadi </b>.: Re </p>
                                    <p class="blue"><b>Samvadi </b>: Pa </p></td>
