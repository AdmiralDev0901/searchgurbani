        <p><strong> Introduction : </strong></p>
        <p> Raga Berari comes from Marwa Thath. It is mentioned as a consort of Raga Malkaus in Berari Sarsut, a consort of raga
            Basant in Shivmat, a consort of raga Pancham in Kalinath-mat and a consort of raga Bhairo in Hanumat The mood of
            this raga is very tender and compassionate. </p>
        <p> In the Ragamala listed at the end of Guru Granth Sahib it is mentioned as a consort of raga Sri. </p>
        <p> The scale and notes of the raga are as follows: </p>
        <p> Arohi (ascending scale) - sa re ga pa ma ga pa dhi ni sa </p>
        <p> Avrohi (descending scale) - sa ni dha pa ma ga re sa </p>
        <p> The vadi (most popular) note is 'ga' and samvadi (second most popular) note is 'dha'. </p>
        <p>This raga is sung at the third part of the day i.e., from 12 noon to 3 a.m. The season of its recitation is winter
            (sharad) i.e., during October, November. In Guru Granth Sahib it has hymns from pages 719- 720 (2 pages). </p>
        <p><strong> The Composers: </strong></p>
