        <h3 class="no-top maroon">Gurbani Raag: Bairari</h3>
        <hr/>
        <div class="blue"><p>This raga appears in the Ragmala as the first ragini of Sin Raga. In the Mesakarna. Ragmala (1509),
                which is almost the same as that of the Guru Granth Sahib, the first ragini of Siri Raga is given as Vairati.
                However, modern sources do not give Bairari nor Vairati but Barari and Varari as well as Varati are . listed.
                Kaufmann believes that all of these names refer to the same raga, Barari. Whether this is the same as the old
                Bairari is open to question. The possibility always exists that Bairari was a regional tune. It was used by Guru
                Ram Das for six short hymns and by Guru Arjan for one. The performance time for Bairari is during the evening
                hours and it is currently assigned to the Marva thata. It resembles Purva Kalyan, the main difference being the
                use of Pa which is strong in Bairari and weak in Purya-Kalyan. Popley places Bairari in the same group as Siri
                Raga and this would agree with the Ragmala. </p>
           <p>Raag Bairari is counted as an uncommon and not a very well known Raag. In medieval times there is hardly any other raag which can be compared with this raag as far as the prevalence of number of varietiesis concerned. This is a difficult raag. Only highly accredited singers can sing this with purity (correctly). This raag is very melodious. &ldquo;Har Nirbhuau Nirvair Nirankaar&rdquo; &ldquo;Kaaea sarir basat hai&rdquo; and &ldquo;sarab sookh har naam vaddaaee |&rdquo;all these shabads are in Raag Bairari . Also &ldquo;Sant janaa mil har jas gaaeiou&rdquo; which is commonly sung at the end of Diwans is in Raag Bairari.</p>
<p>Aroh : Nee Re Ga Pa, Ma Ga, Ma Dha Saˆˆ</p>
<p>Avroh : Saˆˆ Nee Dha Pa, Ma Ga, Pa Ga, Re Sa.</p>
<p>Sur : Rishab is flat, Madhym is teever. Rest of the notes (swar) are sharp.</p>
<p>Thaat : Marva</p>
<p>Jaati : Vakar – Sampooran</p>
<p>Time : Fourth quarter of the day (Evening)</p>
<p>Vadi : Gandhar (Ga)</p>
<p>Samvadi : Dhaiwat (Dha)</p>
<p>Main sur : Pa Dha Ga, Ma Dha, Ma Ga, Re Ga, Ma Ga, Re Sa</p>
