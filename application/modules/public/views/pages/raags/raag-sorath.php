                        <p style="font-weight: bold"><span
                                style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">Introduction </span>
                        </p>
                        <p class="blue"> According to Hanumant School of Music raga Sorath is a ragini (consort) of raga Megh.
                            It is made up of a unison of Sindhvi, Kanra, Kafi and Malar ragas. This raga is included in the
                            Ragamala listed at the end of Guru Granth Sahib. The scale and notes of the raga are as
                            follows: </p>
                        <p class="blue"> Arohi .(ascending scale) - sa re ma pa ni sa </p>
                        <p class="blue"> Avrohi (descending scale) - sa re ni dha pa dha ma ga re ni sa </p>
                        <p class="blue"> The vadi (most popular) note is 're' and samvadi (second most popular) note is
                            'dha'. </p>
                        <p align="left" class="blue"> This raga is sung at the third part of the night i.e., from 12 a.m. to 3
                            a.m. The season of its recitation is winter (sharad) i.e., during October and November. In Guru
                            Granth Sahib it has hymns from pages 595 - 659( 64 pages). </p>
                        <p class="blue"><strong> The Composers: </strong></p>
                        <p class="blue"> The composers of bath (hymns) in this raga are: </p>
                        <p class="blue"><strong><em>Gurus </em></strong></p>
                        <ul>
                            <li class="blue"> Guru Nanak</li>
                            <li class="blue"> Guru Angad</li>
                            <li class="blue"> Guru Amardas</li>
                            <li class="blue"> Guru Ramdas</li>
                            <li class="blue"> Guru Arjan Dev</li>
                            <li class="blue"> Guru Tegh Bahadur</li>
                        </ul>
                        <p class="blue"><strong><em> Bhagats: </em></strong></p>
                        <ul>
                            <li class="blue"> Kabir</li>
                            <li class="blue"> Namdev</li>
                            <li class="blue"> Ravidas</li>
                            <li class="blue"> Bhikhen</li>
                        </ul>
                        <p class="blue"><strong> The Structure: </strong></p>
                        <p class="blue"> The sequence of the structure of compositions in this raga are: </p>
                        <p class="blue"><strong><em> Gurubani: </em></strong></p>
                        <ul>
                            <li class="blue"> Shabads (2, 3 ,4 and 5 padas)</li>
                            <li class="blue">Shabads (8 padas called Ashtpadis)</li>
                            <li class="blue"> Var</li>
                        </ul>
