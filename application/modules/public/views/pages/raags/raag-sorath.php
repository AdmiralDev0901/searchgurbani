                                <td bgcolor="#eaeaea"><p><span class="blue">Raga Sorathi appears in the Ragraala as a raging of Raga Niegha; today it belongs to the Khamaj thata. Besides Guru Nanak, Sorathi was used by Guru Nanak, Gurfi Amar DAs. Guru Ram Das, Guru Arjan and Guru Tegh Bahadur for a total of 150 ltvmtns plus numerous slokas. Sorathi belongs to the cold season and is performed in the first quarter of night. The mood is light and cheerful, with a pleasing sound resembling Raga Desh. The texts composed to this raga show how the words of the Guru can enlighten the mind. All fears vanish and one is tilled with bliss. <br/>
                      It is a </span><span style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">ragini of Megh raga </span><span
                                            class="blue">and is sung at night. The Gurus have referred   to the darkness of superstition and </span><span
                                            style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">maya </span><span
                                            class="blue">which envelopes the individual.   Guru Nanak says:</span></p>
                                    <p align="center" class="blue">&ldquo;This mammon is the enticer of the world, o brother,
                                        <br/>
                                        All the deeds done in its wake are sinful&rdquo;[8].</p>
                                    <p class="blue"><b>Aroh</b> : Sa Re Ma Pa Ni Sa </p>
                                    <p class="blue"><b>Avroh</b> : Sa Re _Ni Dha, Nia Pa Dha Ma Ga Re Ni Sa </p>
                                    <p class="blue"><b>Vadi </b>: Re </p>
                                    <p class="blue"><b>Samvadi</b> : Dha </p>
                                    <p class="blue">The melodies are characterized by sweeping phrases with glides connecting
                                        all leaps, even the shorter ones. Movement is moderately fast.</p></td>
