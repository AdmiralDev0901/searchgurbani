                        <p class="blue" style="font-weight: bold"><em> VISUAL ANALYSIS </em></p>
                        <p class="blue"><em> Count of the use of Managals: </em></p>
                        <p class="blue"> Complete Mool Mantar = 1 at page 595<br/>
                            Ik-ongkar Satgur Prasadh = 19 </p>
                        <p class="blue" style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                        <p class="blue"> All shabads and ashtpadis of the Gurus except Guru Tegh Bahadur (pages 595-631) have
                            rahau verses in them, but none of the verses has numeral '1' with them. The rahau verses have been
                            placed after the first padas of the shabads. In Guru Tegh Bhadur's compositions (pages 631 - 634),
                            the rahau verses have numerals '1' with them and all rahau verses have been placed in the beginning
                            of the shabad. In the Bhagat bani the rahau verses have been placed after the first padas of the
                            shabads. </p>
