                        <p><strong> Introduction : </strong></p>
                        <p> Raga Devghandhari is one of the oldest raga of the Indian School of Music. According to one one
                            belief it is made up of ragas Asawari, Bhairo and Devgiri. In the Ragamala listed at the end of Guru
                            Granth Sahib it is classified as a consort of raga Malkausak. In the Kali Nath School of music it is
                            listed as a consort of Sri raga, whereas in the Nad Binod's school it is titled as 'Dev Ghandar' and
                            is classified as a son of raga Bhairo. According to Bhai Kahn Singh it is a consort of raga
                            Bilawal. </p>
                        <p> The scale and notes of the raga are as follows: </p>
                        <p> Arohi (ascending scale) - sa re ma pa dha sa (omitted notes are -ga, ni) <br/>
                            Avrohi (descending scale) - sa ni dha ma ga re sa <br/>
                            The vadi (most popular) note is 'sa' and samvadi (second most popular) note is 'pa'. </p>
                        <p> This raga is sung at the first part of the day i.e., from 6 a.m to 9 a.m. The season of its
                            recitation is winter (shisher) i.e., during December and January. In Guru Granth Sahib it has hymns
                            from pages 527 - 536 (9 pages). </p>
                        <p><strong>The Composers: </strong></p>
                        <p> The composers of bani (hymns) in this raga are: </p>
                        <p style="font-weight: bold"><em>Gurus </em></p>
                        <ul>
                            <li> Guru Ramdas</li>
                            <li> Guru Arjan Dev</li>
                            <li> Guru Tegh Bahadur</li>
                        </ul>
                        <p style="font-weight: bold"><em>Bhagats: </em></p>
                        <p> There is no Bhagat bath in this raga. </p>
