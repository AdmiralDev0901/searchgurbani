                        <p><strong> Introduction : </strong></p>
                        <p> Raga Wadhans is a complete raga of Khmaj (thath) School of Music. It is sung in two different
                            forms: </p>
                        <ul>
                            <li> Aurav (raga of five notes), in this form note ga and dha are omitted,</li>
                            <li> Khaurav sampooran (where arohi has 6 notes (ga is omitted) whereas avrohi has 7 notes).</li>
                        </ul>
                        <p> This raga is not included in the Ragamala listed at the end of Guru Granth Sahib. </p>
                        <p> The scale and notes of first form are as follows: </p>
                        <p> Arohi (ascending scale) - sa re ma pa dha ni pa ni sa </p>
                        <p> Avrohi (descending scale) - sa ni pa dha ma ga re sa ni sa </p>
                        <p> The vadi (most popular) note is 'pa' and samvadi (second most popular) note is 'ra '. </p>
                        <p>The scale and notes of the second form are as follows: </p>
                        <p> Arohi - sa re ma pa dha ni pa ni sa </p>
                        <p> Avrohi - sa ni pa dha pa ma re sa </p>
                        <p align="left"> This raga is sung at the fourth part of the day i.e., from 3 p.m to 6 p.m. The season
                            of its recitation is winter (shisher) i.e., during December and January. In Guru Granth Sahib it has
                            hymns from pages 557 - 594 (38 pages). </p>
                        <p><strong> The Composers: </strong></p>
                        <p> The composers of bani (hymns) in this raga are: <br/>
                            <strong><em>Gurus </em></strong></p>
                        <ul>
                            <li> Guru Nanak</li>
                            <li> Guru Amardas</li>
                            <li> Guru Ramdas</li>
                            <li> Guru Arjan Dev</li>
                        </ul>
                        <p><strong><em>Bhagats: </em></strong></p>
                        <p> There is no Bhagat bani in this raga. </p>
                        <p><strong>The structure: </strong></p>
                        <p> The sequence of the structure of compositions in this raga <strong>are: </strong></p>
                        <p><strong><em>Gurubani: </em></strong></p>
                        <ul>
                            <li> Shabads ( 1,3,4 and 5 padas)</li>
                            <li> Shabads ( 8 padas called Ashtpadis)</li>
                            <li> Shabads (Chhants- including verses titled 'Gorian' sung at marriage (happy) ceremonies)</li>
                            <li>Specialist compositions titled 'Alajnian' sung at death (sad) ceremonies</li>
                            <li> Var</li>
                        </ul>
                        <p><strong> Matrix </strong></p>
                        <p style="font-weight: bold"><em> VISUAL ANALYSIS </em></p>
                        <p style="font-weight: bold"><em>Count of the use of Managals: </em></p>
                        <p>Complete Mool Mantar = 1 at page 557<br/>
                            Ik-ongkar Satgur Prasadh = 14 </p>
                        <p><span style="font-weight: bold"><em>Placement and count of rahaus in this raga: </em></span></p>
                        <p>There are one rahau verse in all shabads composed in Ghar 1, placed after the first padas. Shabads
                            composed in Ghar 2 have no rahau verse in them. All Ashtpadis have rahau verse in them placed after
                            the first pada. </p>
                        <p><em><span style="font-weight: bold">Diversification of headings and subheadings in this raga: </span></em>
                        </p>
                        <p> There is no diversification in the title of the heading. On page 582, after title 'Wadhans Mehla 3,
                            Mehla teeja (third) is also added. </p>
