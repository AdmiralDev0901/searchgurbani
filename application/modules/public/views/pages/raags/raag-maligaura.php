                        <p><strong> Introduction : </strong></p>
                        <p> According to Indian Schools of music this raga is a complete raga of Marwa thaht. This raga is a
                            mixture of raga Mali and raga Gaura. It is classified as a son of raga Malkaus. It is an ancient
                            raga, its description is not available in the modern music books. </p>
                        <p> This raga is not included in the Ragamala listed at the end of Guru Granth Sahib. </p>
                        <p> The scale and notes of the raga are as follows: </p>
                        <p> Arohi (ascending scale) - sa re ga ma ga pa dha ni sa </p>
                        <p> Avrohi (descending scale) - sa ni dha pa ma ga re sa </p>
                        <p> The vadi (most popular) note is 're' and samvadi (second most popular) note is 'dha'. </p>
                        <p>This raga is sung at the third part of the day i.e., from 12 noon to 3 p.m. The season of its
                            recitation is winter (sharad) i.e., during October, November. In Guru Granth Sahib it has hymns from
                            pages 984- 988 (5 pages). </p>
                        <p><strong> The Composers: </strong></p>
                        <p> The composers of bani (hymns) in this raga are: </p>
