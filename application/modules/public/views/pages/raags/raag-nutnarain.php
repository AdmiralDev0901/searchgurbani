                                <td bgcolor="#eaeaea"><p class="blue">In the Ragmala, Nat is given as putra (son) of Megha while
                                        today Nat Narain appears under the Bilaval thata and is assigned to the evening hours.
                                        This raga was used by Guru Ram Das and Guru Arjan. Nat Narain is pictured as a warrior
                                        riding to battle. In the Holy Book, the fight against sin is never ending but those who
                                        seek refuge in the Lord have their suffering removed. </p>
                                    <p class="blue"><b>Aroh</b> : Sa Ga Ma Re, M'a Pa' Dha Ni Sa </p>
                                    <p class="blue"><b>Avroh</b> : Sa Dha Pa M'a Dha Pa, Ga Ma Re Sa </p>
                                    <p class="blue"><b>Pakar </b>: Sa Ma Ga Ma Pa, Dha Pa, Ma, Ga Ma Re Sa </p>
                                    <p class="blue"><strong>Vadi: </strong>: Sa </p>
                                    <p class="blue"><strong>Samvadi</strong> : Re </p></td>
