                        <p><strong> Introduction: </strong></p>
                        <p> According to Bhai Kahn Singh Nabha this raga emerges from the Bilawal thaht. </p>
                        <p> In the Ragamala listed at the end of Guru Granth Sahib, this raga is listed as a son of raga
                            Megh. </p>
                        <p> The scale and notes of the raga are as follows: </p>
                        <p> Arohi (ascending scale) - sa re ga ma ga pa dha ni sa </p>
                        <p> Avrohi (descending scale) - sa ni pa ma re sa </p>
                        <p> The vadi (most popular) note is 'pa' and samvadi (second most popular) note is 're'. </p>
                        <p>This raga is sung at the third part of night i.e., from 12 a.m. to 3 a.m. The season of its
                            recitation is rainy (varsha) i.e., during July August. In Guru Granth Sahib it has hymns from pages
                            975- 983 (9 pages). </p>
                        <p><strong> The Composers: </strong></p>
                        <p> The composers of bani (hymns) in this raga are: </p>
                        <p><strong><em>Gurus </em></strong></p>
                        <ul>
                            <li> Guru Ramdas</li>
                            <li> Guru Arjan Dev</li>
                        </ul>
                        <p><strong><em>Bhagats: </em></strong></p>
                        <p> No bhagat bani </p>
                        <p style="font-weight: bold"><em>The structure: </em></p>
                        <p> The sequence of the structure of compositions in this raga are: </p>
                        <p style="font-weight: bold"><em>Gurubani: </em></p>
                        <ul>
                            <li> Shabads (2,3,4, padas)</li>
                            <li> Shabads (8 padas, Ashtpadis)</li>
                        </ul>
                        <p style="font-weight: bold"> Matrix </p>
