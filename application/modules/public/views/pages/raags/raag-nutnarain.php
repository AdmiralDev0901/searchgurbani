                        <p><span style="font-weight: bold"><em> Count of the use of Managals: </em></span></p>
                        <ul>
                            <li> Complete Mool Mantar = 1 at page 975</li>
                            <li>Ik-ongkar Satgur Prasadh = 5</li>
                        </ul>
                        <p><strong><em>Placement and count </em></strong><em> of <strong>rahau verses: </strong></em></p>
                        <p> All shabads and ashtpadis, except one ashtpadi (page 983, ashtpadi 6) have rahau verses with numeral
                            placed in the beginning of the compositions. </p>
