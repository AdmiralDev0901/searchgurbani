                        <p><strong> Introduction </strong></p>
                        <p> Raga Gujri is an old raga and is popular to sing devotional hymns. It is a very melodious raga. It
                            has been given varied status by different schools of music. Some schools call it a consort of raga
                            Bhairo while others call it a consort of raga Megh, still others call it a consort of raga Deepak
                            and many call it a consort of raga Sri. According to Guru Granth Sahib's ragamala it is a consort of
                            raga Deepak. In the Sikh encyclopaedia of Bhai Kahn Singh it has been linked with raga Todi. The
                            Pastik School of Music has given 9 forms of this raga: </p>
                        <ul>
                            <li> Maharashtra Gujri</li>
                            <li> Dakhni Gujri</li>
                            <li> Dravri Gujri</li>
                            <li> North Gujri</li>
                            <li> Saurashtra Gujri</li>
                            <li> Mangal Gujri</li>
                            <li> Ramkali Gujri</li>
                            <li> Bahula Gujri</li>
                            <li> Siam Gujri</li>
                        </ul>
                        <p> The notes of this raga are: </p>
                        <p> Arohi (ascending scale) - sa re ga ma dha ni sa (omitted note is -pa) <br/>
                            Avrohi (descending scale) - sa ni dha ma ga re sa <br/>
                            The vadi (most popular) note is 'dha' and samvadi (second most popular) note is 'ra' </p>
                        <p>This raga is recommended to be sung at the fourth part of the day i.e., from 3pm - 6 p.m.. The season
                            of its recitation is rainy (varsha) i.e., during July - August. In the Sikh tradition this raga is
                            also sung in the second part of the day. </p>
                        <p><strong> In </strong> Guru Granth Sahib hymns composed in this raga are on pages 489-526 (37pages).
                        </p>
