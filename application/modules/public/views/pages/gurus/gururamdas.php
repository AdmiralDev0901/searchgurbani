                                <p>Baba Srichand, the eldest son of Guru Nanak, had founded a religious sect of his own
                                    known as
                                    Udasis. He visited Amritsar and came to see Guru Ram Das. On seeing Guru's long
                                    flowing
                                    beard, Baba Srichand asked him jocularly why he grew it that long. The Guru replied,&quot;To
                                    wipe the dust from the holy feet of the saints like you.&quot; 'Your this sweet
                                    humility is
                                    the magic that makes you so great and makes me feel so small,' replied Srichand.
                                    Baba
                                    Srichand promised the Guru his co-operation. After that the Udasis spared no pains
                                    to serve
                                    Sikh eligion. It is said that after the death of Banda Bahadur, when the Mughal
                                    rulers
                                    decided to root out Sikh religion, it was the Udasis who kept the Divine spark of
                                    the Sikh
                                    faith alight.</p>
                                <p><a name="FREE KITCHEN" id="FREE KITCHEN">FREE KITCHEN (GURU KA LANGAR):</a></p>
                                <p>Like his predecessors, Guru Ram Das carried on the work of Langar in a more elaborate
                                    and
                                    methodical way. As in the past strict adherence was made to the term 'Pangat' in
                                    Langar.
                                    Anybody irrespective of race, caste, creed, religion or sex, could partake of food
                                    without
                                    any hesitation. The caste system and pilgrimages were decried and superstitions were
                                    denounced.</p>
