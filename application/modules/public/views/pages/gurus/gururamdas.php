                                <p>Guru Ram Das left Goindwal for his new colony. Many Sikhs followed the Guru and
                                    settled
                                    there. At first this city was called Ramdaspur, which is now called Amritsar. A
                                    revenue
                                    collector of Patti in district of Lahore had five daughters, the fifth daughter was
                                    very
                                    religious. One day the father asked who gave them to eat and drink. The first four
                                    daughters
                                    said that it were their parents who had provided them with food and other
                                    necessities of
                                    life, but the fifth daughter told her parents that God was the only Cherisher of His
                                    creation. On hearing this reply her father got very angry and remarked,&quot;I shall
                                    see if
                                    God will protect you.&quot;</p>
                                <p>One day a crippled leper came to the town and the father married his fifth daughter
                                    to him to
                                    teach her a lesson. She willfully accepted him as her true husband. She put him in a
                                    basket
                                    and carried him on her head, and begged from door to door to maintain their
                                    livelihood. One
                                    day she left him under a tree near a pool of water and went to the nearby colony to
                                    beg for
                                    food. The leper saw some crows (black in color) dipping in the water and they turned
                                    white
                                    when came out of the water. The leper thought that the water had some miraculous
                                    healing
                                    property. So he left his basket and crawled into the water, and lo, the leprosy at
                                    once
                                    disappeared from the body except one finger which was left out of water. When the
                                    lady came
                                    back, she did not believe the story of the healed leper. Ultimately they went to
                                    Guru Ram
                                    Das, who confirmed saying that the pool possesseth such extraordinary efficacy which
                                    the man
                                    alleged. Upon this the couple became Guru's followers and they worked in the
                                    excavation of
                                    the tank later on.</p>
                                <p>The tree under the shade of which she left her husband, is still standing there and
                                    is called
                                    'Dukhbhanjni Beri'. The pool was known as Amritsar- tank of nectar, the place itself
                                    came to
                                    be known as the city of Amritsar. The work was not finished by Guru Ram Das but it
                                    was
                                    completed by his successor, Guru Arjan Dev.</p>
