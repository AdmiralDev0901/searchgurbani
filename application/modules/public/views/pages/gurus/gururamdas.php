                                <p>Guru Ram Das composed a hymn known as 'Lawan' in Suhi Mohalla 4, page 773 of Guru
                                    Granth
                                    Sahib, and asked his Sikhs to recite them to solemnize marriages. The Sabad 'Lawan'
                                    embodies
                                    in itself a lesson for the couple to develop true love for each other. In reality
                                    this Sabad
                                    is for a human being to develop love for the divine ridegroom.</p>
                                <p>The Guru composed the following Sabad to instruct his Sikhs in the practice of their
                                    religion:</p>
                                <p>&quot;He who calleth himself a Sikh of the true Guru, should rise early and meditate
                                    on
                                    God;</p>
                                <p>He should make an effort early in the morning, bathe in the inner tank of nectar;</p>
                                <p>Repeat God's Name under Guru's instruction, and all his sins and transgressions shall
                                    be
                                    erased;</p>
                                <p>At sunrise he should sing the Guru's hymns, and whether sitting or standing meditate
                                    on God's
                                    Name;</p>
                                <p>The disciple who at every breath meditateth on God, will be dear to the Guru; The
                                    Guru
                                    imparteth</p>
                                <p>instruction to that disciple to whom my Lord bestows His grace;</p>
                                <p>The servant Nanak prayeth for the dust of the feet of such a disciple of the Guru who
                                    himself
                                    repeateth</p>
                                <p>God's Name and causeth others to do so.&quot;</p>
                                <p>(Gauri Ki Var- Mohalla 4, p-305-6)</p>
