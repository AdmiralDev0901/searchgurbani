                                <p align="center">&nbsp;</p>
                                <p>&nbsp;The early history of Guru Ram Das is referred to in the previous chapter. Many
                                    writers
                                    have expressed their opinion that Akbar granted the land only to Guru Ram Das on
                                    which he
                                    founded the city of Ramdaspur (later known as Amritsar), and so they contend that
                                    the city
                                    of Amritsar was not founded during the time of Guru Amar Das. It is also a
                                    well-known fact
                                    of Sikh history that Emperor Akbar came to Goindwal and he dined with the common
                                    people in
                                    the Guru's langar before he could see the Guru. It seems that the Emperor granted
                                    the estate
                                    in the name of Bibi Bhani at that time, and Jethaji being her husband, was made
                                    incharge of
                                    the estate by Guru Amar Das. Guru Nanak awarded the Guruship to Bhai Lehna and not
                                    to his
                                    sons, and it is quite obvious that in order to avoid hostility of his sons towards
                                    his
                                    successor, he asked Guru Angad to move to Khadur instead of continuing to live in
                                    Kartarpur.
                                    The same circumstances prevailed during the reign of second and third Gurus. It is,
                                    therefore, quite likely that the planning of a new city was called for during the
                                    reign of
                                    Guru Amar Das as mentioned in the previous part.</p>
