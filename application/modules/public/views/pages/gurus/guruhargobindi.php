                            <td><p>The Invincible Guru </p>
                                <p>Several fruitless efforts were made on the life of Guru Hargobind right from his
                                    infancy. A
                                    snake-charmer was bribed who let loose a poisonous snake. But the Great Guru
                                    overpowered the
                                    snake. </p></td>
