                            <td><p align="center">Bandi Chhod Satguru </p>
                                <p align="left">Guru Hargobind ordained Sikhs to arm themselves to resist injustice and
                                    tyranny.
                                    This annoyed Emperor Jehangir who ordered incarceration of the Guru in the Gwalior
                                    Fort.
                                    Soon realising his mistake, the Emperor ordered his release but the Guru refused to
                                    accept
                                    the offer till 52 innocent 'Rajas' were also freed. Instructions were issued that
                                    whosoever
                                    holds the Guru's dress will be freed. Guru Ji wore a special apron with 52 strings
                                    attached
                                    to it. Each Raja held one string and walked into freedom. </p></td>
