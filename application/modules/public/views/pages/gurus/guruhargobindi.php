                <td colspan="2" align="left" valign="top"><p>Guru Har Gobind was born to Guru Arjan Dev on June 19, 1595
                        at
                        Wadali, a village near Amritsar. The period of Guru's early life is alluded to in the previous
                        chapter.
                        After the Martyrdom of his father (Guru Arjan), the Guru caused the Adi Granth to be read by
                        Bhai Buddha
                        and the musicians of the temple sang the Guru's hymns. This lasted for ten days. When the final
                        rites
                        were over, Bhai Buddha started the ceremony of Guruship. It should be remembered here that when
                        Guru
                        Arjan's wife went to Bhai Buddha for boon of a son, she had prepared the meals with her own
                        hands, and
                        she took bread with onions. Bhai Buddha while &nbsp;eating had said,&quot; The Guru is the owner
                        of the
                        storehouse, but I have received an order to open it. As you have given me food to my heart's
                        content, so
                        shall you have a son to your heart's content. He shall be very handsome and brave, possess
                        spiritual and
                        temporal power, become a mighty hunter, ride on royal steeds, wear two swords, be puissant in
                        battle,
                        and trample on the Mughals. As I crush these onions you have brought to me, so shall your son
                        crush the
                        heads of his enemies,</p>
                    <p>and be at once a great warrior and exalted Guru. His shall not be the humble seat of a village
                        Guru, but
                        a gorgeous imperial throne.&quot; As usual Bhai Buddha placed before the Guru a seli (a woolen
                        cord worn
                        as a necklace or twisted round the head by the former Gurus) and a turban, as appurtenances of
                        his
                        calling. The Guru ordered the seli to be placed in the treasury and reminding him about his
                        prophecy
                        said to Bhai Buddha,&quot;My endeavors shall be to fulfil thy prophecy. My seli shall be a
                        swordbelt,
                        and I shall wear my turban with a royal aigrette. Give me a sword to wear instead of seli.&quot;
                        The
                        sword was brought but Bhai Buddha placed it on the wrong side of Guru Har Gobind. The Guru said,&quot;Bring
                        another one, I shall wear two swords.&quot; He wore two swords which were emblems of Spiritual
                        and
                        Temporal authority- Piri and Miri- the combination of 'Bhakti and Shakti'. </p>
                    <p><strong><a name="1" id="1">AKAL TAKHAT:</a></strong></p>
                    <p>The martyrdom of Guru Arjan was an unparallel act in the history of mankind. The Guru had all the
                        superpowers. He could have averted the situation in any way he liked, but he went through all
                        that
                        torture to show to the world how in all thick and thin one should cheerfully submit to the sweet
                        Will of
                        God. As a atter of fact, the contents of the Adi Granth were not meant for the Yogis, Sidhas and
                        Sanyasis or the Muslim Suffis only, who sit in seclusion in the caves of the Himalayas and
                        worship the
                        Almighty by denouncing the world. Instead the teachings of the Adi Granth were meant for the
                        family men.
                        Leading the family life, the Gurus gave practical examples as how to live according to Guru's
                        Word.</p>
                    <p>The cruel and torturous execution of Guru Arjan aroused a very strong wave of angry feelings
                        among the
                        masses. The enlightened, but not passive, sufferings of the Guru instilled a new spirit and life
                        into
                        the people and they resolved to exert and sacrifice themselves for the sake of righteousness.
                        For
                        centuries, countless Hindu men, women and children had fallen under the Muslim sword and this
                        did not
                        soften the stone hearts of their oppressors; but rather they had become more cruel and brutal.
                        Sometimes
                        it might be possible to reform the evil doer by opposing untruth and injustice through
                        non-violent
                        methods. The silent resistance and suffering for righteous cause might sometimes enable the
                        tyrant to
                        see his evil actions and he might be improved. History stands witness that no amount of non-
                        violence
                        can succeed against a tyrant who is hardened and steeped in criminal oppressive ways and who
                        pays no
                        heed to basic values of moral and civilized conduct. Against such men, non-violence is only
                        another name
                        of disgraceful cowardice in their dictionary. Such power drunk men must be faced bravely with a
                        stick
                        bigger than theirs. After the inauguration, some Masands represented to the Guru's mother that
                        the
                        preceding five Gurus never handled arms; if Emperor Jahangir heard about this, he would be angry
                        and
                        where would they (Sikhs) hide? She showed courage to the Masands, however, she remonstrated with
                        the
                        young Guru,&quot;My son, we have no treasure, no state revenue, no landed property and no army.
                        If you
                        walk in the way of your father and grandfather, you will be happy.&quot; The Guru recited the
                        following
                        verse:</p>
                    <p>&quot;The Lord who is the Searcher of all hearts Is my own Guardian.&quot;</p>
                    <p>(Bhairon Mohalla 5, p-1136)</p>
                    <p>and said,&quot;Have no anxiety and everything shall be according to the Will of God.&quot;</p>
