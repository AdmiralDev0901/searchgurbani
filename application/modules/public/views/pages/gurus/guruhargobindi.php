                    <p>The Guru issued an order to the Masands that he would be pleased with those who brought offerings
                        of arms
                        and horses instead of money. He laid down the foundation of Akal Takhat (Timeless Throne) in
                        1606 (the
                        fifth day of light half of month of Har, Sambat 1663) just in front of Hari Mandar, and it was
                        completed
                        in 1609. Akal Takhat was built of solid bricks on a raised platform of about ten feet in height
                        and
                        looked like a throne. The Guru took his seat on it. He built Akal Takhat a few yards in front of
                        Hari
                        Mandar with a view that a Sikh at Akal Takhat should not forget that spiritual elevation was as
                        essential as his social obligations. As a matter of fact, the Guru wanted his followers to be
                        'saint-soldiers', extremely cultured, highly moral with spiritual height and be ever-ready to
                        measure
                        swords with demonic forces. Bhai Buddha on seeing the Guru in military harness, mildly
                        remonstrated with
                        him. Instead the Guru replied,&quot;In the Guru's house religion and worldly enjoyment shall be
                        combined- the caldron to supply the poor and the needy, and the scimitar to smite the
                        oppressors.&quot;
                        (This should be noted by those Sikhs who say that worldly and practical affairs should be kept
                        separate
                        from religion in our Gurdwaras).</p>
                    <p>Several warriors and wrestlers came to the Guru for service. He enrolled fifty-two heroes as his
                        body-guard and this formed the nucleus of his future army. About five hundred young persons came
                        from
                        all over the Punjab to enlist in his service. He made Bhai Bidhi Chand, Bhai Jetha, Bhai Piara,
                        Bhai
                        Langaha, and Bhai Pirana, each captain of a troop of one hundred horse. People began to wonder
                        how the
                        Guru could continue to maintain such an army. The Guru quoted:</p>
                    <p>&quot;God provideth every one with his daily food; why, O man, art thou immersed planning;</p>
                    <p>He putteth their food even before the insects which He created in rocks and stones.&quot;</p>
                    <p>(Gujri Mohalla 5, p-495)</p>
                    <p>Akal Takhat grew into an institution which symbolized in itself the idea that the use of sword
                        for the
                        protection of righteousness and for self-defence was called for. Here the Guru sitting on his
                        throne,
                        would watch wrestling bouts and military feats of his disciples performed in the open arena
                        opposite to
                        the Akal Takhat. As all intricate cases and disputes were finally decided here by the Guru, the
                        Akal
                        Takhat served the purpose of a Supreme Court for the Sikhs. Besides throne, the Guru adopted all
                        other
                        emblems of royalty- the umbrella, the swords, the crest and the hawk, and thus the Sikhs called
                        him a
                        true king or 'Sacha Padshah'- a king in all appearance but in deeds and in purity as holy and
                        great as
                        previous Gurus. People looked towards Akal Takhat for guidance in their secular affairs. This
                        custom
                        became so significant that the decision once taken at Akal Takhat was followed by the Sikhs
                        enthusiastically and this was the reason that they were always able to overcome every peril. The
                        development of this custom contributed a lot towards the consolidation of the Sikh Movement.</p>
                    <p>Some writers charge that lure of politics and glamour of arms led the Guru away from the true
                        path of a
                        religious and spiritual leader. Their judgement is altogether unfounded. There was no political
                        motive
                        of Guru Har Gobind and there is nothing else to substantiate this allegation. Secondly his daily
                        routine
                        was to go to Hari Mandar, listen Asa di Var and then give religious instructions to his
                        followers. He
                        took keen interest in propagation of his religion and appointed preachers in the various regions
                        of the
                        country. He himself undertook tours to various places in Punjab to propagate his faith. However
                        the
                        policy of the Guru symbolized in itself the response to the challenge of the time. Bhai Gurdas
                        justifies
                        the Guru's change in the policy under peculiar circumstances:</p>
                    <p>&quot;Just as one has to tie pail's neck while taking out water, Just as to get 'Mani', snake is
                        to be
                        killed;</p>
                    <p>Just as to get Kasturi from deer's neck, deer is to be killed;</p>
                    <p>Just as to get oil, oil seeds are to be crushed;</p>
                    <p>To get kernel, pomegranate is to be broken;</p>
                    <p>Similarly to correct senseless people, sword has to be taken up.&quot;</p>
                    <p>(Bhai Gurdas, Var-34, pauri 13)</p>
                    <p>Guru Har Gobind appears to have been the first Guru Who systematically turned his attention to
                        the chase.
                        His daily routine at Amritsar was:- He rose before day-break, bathed, dressed in full armor, and
                        then
                        went to Hari Mandar to worship. There he heard Japji and Asa di Var being recited. He then
                        preached to
                        his Sikhs. After the concluding prayer, breakfast was served indiscriminately to the Guru's
                        troops and
                        followers as they sat in rows for the purpose. He would rest for some time and then would go to
                        the
                        chase, accompanied by an army of forest beaters, hounds, tamed leopards and hawks of every
                        variety. Late
                        in the afternoon he sat on his throne and give audience to his visitors and followers. Minstrels
                        sang
                        the Guru's hymns and at twilight the 'Sodar' was read. At the conclusion of the service musical
                        instruments of many sorts were played. At the end all adjourned for their evening repast. A
                        sacred
                        concert was afterwards held in which hymns were sung. Next followed the minstrel Abdulla's
                        martial songs
                        to inspire the Sikhs with love of heroic deeds and dispel feelings unworthy of warriors. The
                        Sohila was
                        then read after which the Guru retired to his private apartment.</p>
