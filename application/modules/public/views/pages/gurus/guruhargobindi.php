                    <p>Chandu was fearful that the Guru might avenge his father. His daughter was still unmarried and he
                        wrote
                        to the Guru for her alliance which was again refused. He, therefore, once again represented to
                        Emperor
                        Jahangir against the Guru. Upon this Jahangir summoned the Guru to Delhi through Wazir Khan.
                        After
                        careful consideration the Guru agreed to go to Delhi and assigned the secular duties of the Hari
                        Mandar
                        to Bhai Buddha and its spiritual duties to Bhai Gurdas. He instructed,&quot;The Har Mandar is
                        specially
                        devoted to God's service, wherefore it should ever be respected. It should never be defiled with
                        any
                        impurity of the human body. No gambling, wine-drinking, light behavior with women, or slander,
                        should be
                        allowed therein. No one should steal, utter a falsehood, smoke tobacco, or contrive litigation
                        in its
                        precincts. Sikhs, holy men, guests, strangers, the poor and the friendless should ever receive
                        hospitality from Sikhs. My people should ever be humble, repeat God's Name, promote their faith,
                        meditate on Guru's words, and keep all his commandments.&quot; The Guru then went to Delhi.
                        Through the
                        good offices of Wazir Khan, the Emperor received the Guru with great apparent respect. Seeing
                        him very
                        young and already installed as Guru, the Emperor had a good deal of spiritual discussion in
                        order to
                        test his knowledge of divinity.</p>
                    <p>The Emperor having heard that the Guru loved the chase requested him to accompany him one day on
                        a
                        hunting excursion. In the forest a tiger rushed towards the Emperor. Elephants and horses took
                        fright,
                        bullets and arrows were discharged towards the tiger but in vain. The Emperor was completely
                        paralysed
                        with fear and called upon the Guru to save him who alighted from his horse, and taking his sword
                        and
                        shield ran between the tiger and the Emperor. As the tiger sprang, he dealt him a blow with his
                        sword
                        and the tiger fell lifeless on the ground. The Emperor thanked his God that he was saved by the
                        Guru
                        through his heroic endeavor.</p>
                    <p>It was time for the Emperor to visit Agra and he invited the Guru to accompany him. He, after
                        repeated
                        invitations, consented to go. When they both arrived in Agra, the Guru was received with great
                        rejoicing
                        by the people. Seeing increasing friendship between the Emperor and the Guru, Chandu said to
                        himself,&quot;The
                        Guru will take revenge on me whenever he finds an opportunity. I shall only be safe if by some
                        means I
                        succeed in having broken this friendship or having him imprisoned, and thus I should apply all
                        efforts
                        to that end.&quot;</p>
                    <p>The Emperor fell ill and he sent for his astrologer to check upon his stars and find the remedy.
                        Chandu
                        took advantage of the situation and bribed the astrologer heavily to sever connection between
                        the Guru
                        and theEmperor. The astrologer accordingly suggested that a holy man of God should go to the
                        Fort of
                        Gwalior and</p>
                    <p>pray for the Emperor's recovery there. Chandu on the other hand advised the Emperor that Guru
                        HarGobind
                        was the holiest of men and thus played double role. Jahangir requested the Guru to go to
                        Gwalior, the
                        latter accepted it without hesitation as another mission awaited him there.</p>
                    <p>There was joy in the Fort when it was known that the Guru was coming. There were fifty-two
                        Indianprinces
                        (Rajas) imprisoned in the Gwalior Fort who were spending their days in lamentation and misery.
                        They
                        believed that they would be released by the Guru's intercession. Hari Das, the governor of the
                        Fort, was
                        happy too, since he had been longing to have 'darshan' (holy sight). He went forth to receive
                        the Guru
                        and prostrated before the Master. The Guru met the princes, comforted them and gave them peace,
                        making
                        them happy even in adversity.</p>
                    <p>Chandu wrote couple of letters to the governor of the Fort, urging him to poison the Guru and put
                        an end
                        to him. Hari Das, however, put all letters before the Guru as he received them; since he had
                        become his
                        devotee. The Guru recited the following Sabad at that time:</p>
                    <p>&quot;The slanderer shall crumble down</p>
                    <p>Like a wall of Kallar; hear, ye brethren, thus shall be known.</p>
                    <p>The slanderer is glad when he seeth a fault; on seeing anything good he is filled with grief.</p>
                    <p>He meditateth evil all day long, but it befalleth not; the evil-minded man dieth meditating
                        evil.</p>
                    <p>The slanderer forgetteth God, and when death approacheth, quarrelleth with God's saint.</p>
                    <p>The Lord Himself preserveth Nanak, what can wretched man do?&quot;</p>
                    <p>(Bilawal Mohalla 5, p-823)</p>
                    <p>Jahangir recovered from illness. The Guru was still in the Gwalior Fort. When the Emperor heard
                        Wazir
                        Khan's pleading on behalf of the Guru (some say, also the pleading of Mian Mir), he ordered that
                        the
                        Guru should be presented to him. On hearing this the imprisoned Rajas were very much distressed.
                        The
                        Master would not leave the Fort unless all the Rajas were also released. The Emperor conceded to
                        his
                        wish and released all the fifty-two princes. From this the Guru is still remembered in Gwalior
                        as Bandi
                        Chhor- the Great Deliverer, the holy man who freed the prisoners. There still stands a shrine
                        'Bandi
                        Chhor' in the historic Fort of Gwalior.</p>
                    <p>Mian Mir brought home to the Emperor the innocence of Guru Arjan and how under his cruel orders,
                        the
                        great divine Master had been tortured to death. The Emperor, however, washed his hands clean of
                        this sin
                        and held Chandu entirely responsible for this crime, who was then arrested by the Emperor's
                        order and
                        taken to Lahore to be executed there. He was paraded through the streets of Lahore, people threw
                        filth
                        on him, and cursed him. A grain-parcher struck him on the head with an iron ladle and Chandu
                        died. When
                        the Emperor heard Chandu's death, he remarked that he richly deserved this fate. The Guru,
                        however,
                        prayed that as Chandu had suffered torment for his sins in this life, God would pardon him
                        hereafter.</p>
                    <p>Sujan, a Masand from Kabul who had amassed great wealth from tithes and offerings, heard that
                        Guru Har
                        Gobind had great love for the horses. He looked far and near and ultimately found a horse of
                        rare beauty
                        and speed which he purchased for a lakh of rupees to make an offering to the Guru. When Sujan
                        was
                        crossing the river Indus, the eye of an officer fell on the horse which was of a rare strain and
                        beauty
                        and he ultimately took away the horse saying that the animal should go to the Emperor. Sujan
                        told the
                        Guru how he was robbed of the horse. The Guru recommended patience and predicted that nobody but
                        himself
                        (Guru) would ride that horse.</p>
                    <p>When the Emperor desired to mount, the horse shook its head which was considered a bad omen.
                        After
                        sometimes the horse fell ill and would neither eat nor drink. All known medicines were tried but
                        in
                        vain. When the horse was on the verge of death, the head Qazi (Rustam Khan) suggested that if
                        the holy
                        Quran was read for him, he might recover. Upon this the horse was presented to the Qazi.</p>
                    <p>When the Qazi was leading the horse home, the animal neighed as it passed through the Guru's tent
                        (Guru
                        was at Lahore at that time). Through negotiations with the Qazi, the horse was purchased for ten
                        thousand rupees. The Guru patted on the neck of the horse and it started recovering its
                        strength.</p>
