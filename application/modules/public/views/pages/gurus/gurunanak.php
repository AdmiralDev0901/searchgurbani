                <p class="black">Guru Nanak was born in 1469 at Rai Bhoeki Talwandi now nown as Nankana Sahib situated
                    in
                    Punjab
                    province of West Pakistan. This place is about 55 miles north-west of Lahore. His father, Mehta Kalu
                    was a
                    Patwari- an accountant of land revenue in the government. Guru's mother was Mata Tripta and he had
                    one older
                    sister, Bibi Nanki. From the very childhood, Bibi Nanki saw in him the Light of God but she did not
                    reveal
                    this secret to anyone. She is known as the first disciple of Guru Nanak.</p>
