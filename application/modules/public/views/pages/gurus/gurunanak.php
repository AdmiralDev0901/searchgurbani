        <p align="left"><a name="COBRA" id="COBRA"><strong>COBRA SERVES THE DIVINE MASTER</strong></a></p>
        <p class="black">As usually is the case in villages, the father sent his son to graze the buffaloes in the
            pastures. One
            day while the Guru was grazing the buffaloes, he fell asleep under a tree and the herd destroyed the crops
            in the
            neighboring fields. When the owner saw his crops damaged, he became furious and lodged a complaint with Rai
            Bular,
            an officer-in-charge of that area. Rai Bular sent for the son and his father to adjust the quarrel. The Guru
            told
            them that no damage was done to the crops; rather it was blessed by God. Rai Bular sent his messengers to
            inspect
            the fields. But to everybody's surprise the investigators could not find any damage in the fields rather the
            crops
            were doubly blossoming. The field where this miracle happened is now known as Kiara Sahib.</p>
        <p class="black"> On another day the Guru was sent to graze the buffaloes in the pastures and he fell asleep
            under the
            shade of a tree. As the sun rose higher, the shadow moved away. A big cobra came out of its den and provided
            shadow
            with its hood over the face of the Divine Master. Rai Bular happened to pass by that side with his
            attendants. When
            he saw this strange scene, he was convinced that the boy was a man of God. Upon seeing the people, the cobra
            retreated to its den and Rai Bular touched Guru's feet in great reverence and thus became Guru's
            disciple.</p>
        <p align="left"><a name="SECLUSION" id="SECLUSION"><strong>GURU SITS IN SECLUSION</strong></a></p>
        <p class="black">As he grew a little older, he avoided company and sought seclusion. For days he would sit
            silent
            in
            solitude and spent his time in meditation. Parents became anxious about his health and to them his
            unworldliness
            appeared insane. One day they sent for their physician Hari Das. The physician came and began to feel Guru's
            pulse.
            He withdrew his arm and asked, &quot;O physician, what art thou doing?&quot; The physician replied that he
            was
            diagnosing his disease. Upon this the Guru laughed and then uttered the following Sabad:<br/>
            <br/>
            <em>&quot;They have sent for the physician for me!<br/>
                <br/>
                He taketh my hand and feeleth my pulse.<br/>
                <br/>
                What can a pulse disclose?<br/>
                <br/>
                The pain lies deep in the heart.<br/>
                <br/>
                Physician, go back and heal thyself,<br/>
                <br/>
                Diagnose thy own disease,<br/>
                <br/>
                Then thou mayst diagnose the disease of others<br/>
                <br/>
                And call thyself a physician.&quot; (Malar ki Var, Mohalla 1 p-1279)</em><br/>
            <br/>
            <br/>
            Hari Das was familiar with such cases of deranged mind and thus asked, &quot;So you think that I am sick too
            and
            need a cure.&quot; The Guru replied, &quot;You suffer from the sickness of your soul. Egoism is the disease.
            It
            separates us from the source of life, God Himself.&quot; Hari Das asked if there was any remedy. The Guru
            replied,<br/>
            <br/>
            <em>&quot;When man shall possess the Name of the Bright One,<br/>
                <br/>
                His body shall become like gold and his soul be made pure;<br/>
                <br/>
                All his pain and disease shall be dispelled,<br/>
                <br/>
                And he shall be saved, Nanak, by the true Name.&quot; (Malar Mohalla 1, p-1256)</em><br/>
            <br/>
            After a good deal of discussion, Hari Das bowed before the Divine Master and told his parents to leave
            anxiety about
            their son as he was born 'A healer of the world's sickened souls.'</p>
