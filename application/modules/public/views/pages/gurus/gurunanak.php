        <p class="black"><a name="GURU'S SCHOOLING" id="GURU'S SCHOOLING"><strong>GURU'S SCHOOLING</strong></a><br/>
            At the age of seven, Guru Nanak was sent to school, which was run by teacher, Pandit Gopal Das, at his
            village.
            As usual the teacher started the lesson with an alphabet but the teacher was wonder-stuck when the Guru
            asked
            him to explain the meanings of the letters of the alphabet. However at the helplessness of his teacher, the
            Guru
            wrote the meanings of each and every letter of the alphabet. This was the first Divine Message delivered by
            Guru
            Nanak. This was an explanation of deeper truth about human beings and God and the way to realize God in
            terms of
            the alphabet. The teacher stood abashed before the Divine Master and bowed to him. He then took him back to
            his
            father and said, &quot;Mehtaji, your son is an Avtar (prophet) and has come to redeem the victims of Kalyug
            (the
            age of Falsehood). He is destined to be a world Teacher, there is nothing that I can teach him.&quot;
            <br/>
            <br/>
            Many writers believe that Guru Nanak was first sent to different schools belonging to the Hindus and Muslims
            to
            learn about Vedas (Hindu Scriptures) and Quran (Muslim Scripture), and only after obtaining the knowledge
            from
            those scriptures, he started his religion. According to Malcolm, Guru Nanak is said to have learnt all
            earthly
            scenes from Khizr -the Prophet Elias. &quot;There is a reason to believe,&quot; writes Cunningham, &quot;that
            in
            his youth he made himself familiar with the popular creeds both of Mohammadans and the Hindus and that he
            gained
            a general knowledge of the Quran and Brahmanical Shastras.&quot;
            <br/>
            <br/>
            It seems that all these scholars of history have not grasped the basic fundamental fact about the divinity
            of
            Guru Nanak. He was born with divine status, thus, his teachings were heavenly. These writers seem to be very
            much ignorant of the fact that Guru Nanak was an Embodiment of Divine Light. He was a celestial being and
            his
            divine attributes put him above mankind and its schools. Historians have failed to visualize the splendor in
            Guru's Jot. Heavenly Spirit does not learn from man-made institutions. He was a heavenly messenger and a
            born
            world teacher who taught the mankind the path of righteousness and truth. Guru Nanak's divinity is above all
            earthly institutions and their teachings. The Message that Guru Nanak gave to this world, came to him direct
            from God as he confirms himself:
            <br/>
            <br/>
            &quot;O Lalo as comes the Divine Word from God to me So do I narrate it.&quot; (Tilang Mohalla 1,
            p-722)<br/>
            <br/>
            &quot;I am saying what He commandeth me to say.&quot; (Wadhans Mohalla 1, p-566)<br/>
            <br/>
            It is also mentioned in the Janamsakhi (biography) that many times Guru Nanak said to his companion Mardana,
            &quot;Mardana, play the rebec, the Divine Word is coming.&quot; This confirms the fact that education from
            the
            Hindu and Muslim religious institutions, had no bearing at all on the Divine Word that Guru Nanak received
            from
            God and delivered to this world. To say that Guru went to different institutions to learn, is violating the
            sanctity of Guruship.
        </p>
        <p class="black"><strong><a name="SACRED THREAD" id="SACRED THREAD">CEREMONY OF SACRED THREAD</a><br/>
            </strong><br/>
            Guru Nanak was nine years old and according to the custom among the higher castes of Hindus, he was required
            to
            invest himself with the sacred thread called 'Janaeu'. Great preparations were made by his father for this
            ceremony.
            The family priest named Hardyal, started chanting Mantras (Hindu hymns) and was ready to put the thread
            around
            Guru's neck when he refused to wear it. The whole assembly was astonished. They tried to persuade him every
            way to
            wear the Janaeu but in vain. Then the Guru uttered the following Sabad:<br/>
            <br/>
            <em>&quot;Though men commit countless thefts, countless adulteries,<br/>
                <br/>
                utter countless falsehoods and countless words of abuse;<br/>
                <br/>
                Though they commit countless robberies and villainies night<br/>
                <br/>
                and day against their fellow creatures;<br/>
                <br/>
                Yet the cotton thread is spun, and the Brahman cometh to twist it.<br/>
                <br/>
                For the ceremony they kill a goat and cook and eat it, and<br/>
                <br/>
                everybody then saith 'Put on the Janaeu'.<br/>
                <br/>
                When it becometh old, it is thrown away, and another is put on,<br/>
                <br/>
                Nanak, the string breaketh not if it is strong.&quot; (Asa di Var, Mohalla 1, p-471)<br/>
                <br/>
                The priest in utter despair asked, &quot;What kind of sacred thread O Nanak, would you wear?&quot; The
                Guru
                replied,<br/>
                <br/>
                &quot;Out of the cotton of compassion<br/>
                <br/>
                Spin the thread of contentment<br/>
                <br/>
                Tie knots of continence,<br/>
                <br/>
                Give it twist of truth.<br/>
                <br/>
                That would make a Janaeu for the soul,<br/>
                <br/>
                If thou have it, O Brahman, put it on me.<br/>
                <br/>
                Such a thread once worn will never break<br/>
                <br/>
                Nor get soiled, burnt or lost,<br/>
                <br/>
                The man who weareth such a thread is blessed.&quot; (Asa di Var, Slok Mohalla 1, p-471)</em></p>
