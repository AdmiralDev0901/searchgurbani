        <p align="left">There lived a Sadhu (monk), Tapa in Khadur. He was worshipped as a Guru by the Khahira Jats
            only. Tapa
            had jealousy against the Guru and contended reverence shown to Guru by his followers. He maintained that he
            should
            be<br/>
            worshipped instead of the Guru since Guru was a family man and not an ascetic.<br/>
            One year there were no monsoons and as a result there was a drought in the land. People were distressed and
            went to
            Tapa for his help to procure rain. Tapa told them that he was a monk, yet no one worshipped him and instead
            everybody worshipped the family man (Guru), and so he asked them to go to the Guru and ask him to procure
            rain for
            them. They went to the Guru who replied,&quot;Be satisfied with God's Will.&quot; They came back to Tapa who
            told
            them,&quot;If you expel the Guru from the town, I will bring rain within twenty-four hours.&quot; Ultimately
            the
            Guru left the town and went seven villages away from Khadur where Tapa had no influence.<br/>
            <br/>
            When Baba Amar Das arrived in Khadur next morning, he found the Guru's house empty. On inquiry the people
            narrated
            the whole story to Babaji. In the meantime Tapa failed to bring any rain. Upon this Baba Amar Das asked the
            people
            if a lamp could be substituted for the sun. He asked them to punish Tapa ifthey wanted rain. It so happened
            that as
            Tapa was being punished, the rain came in torrents. After that the people went to the Guru to ask for
            forgiveness
            for their acts.<br/>
            <br/>
            When Guru Angad heard of Tapa's punishment, he felt much grieved and addressed to Amar as,&quot;You have not
            obtained the fruits of my companionship, which are peace, forbearance and forgiveness.&quot; On hearing this
            Babaji
            fell at the feet of the Guru and humbly sought his pardon. He confessed that he got Tapa punished because he
            could
            not take Guru's insult and promised to obey the Guru's instructions in future.<br/>
            <br/>
            One night in March 1552, it rained all night, cold winds blew and lightning flashed. Baba Amar Das brought a
            pitcher
            of water from river Beas for his Master. While he was coming to the Guru's house, he struck against a wooden
            peg
            which a weaver had driven into the ground, and he fell into the loompit. It was a weavers' colony and when
            they
            heard the thud of his fall, one of the weavers' wife said,&quot;Who could it be at this early hour? It must
            be that
            homeless Amru who sleeps not, who knows no rest and who tires not. He is ever bringing water from the river
            and
            firewood from the forest; and what a Guru to serve!&quot; The Master felt the twitch and was deeply moved.
            He
            embraced Baba Amar Das who was seventy-three years old then and said,&quot;My Amar Das, he will be the home
            of the
            homeless, the honor of the unhonored, the strength of the strengthless, the support of the supportless, the
            emancipator of the captive.&quot; After that Guru Angad<br/>
            installed Baba Amar Das in his seat, put five paise and a coco-nut before him, and asked Bhai Buddha to put
            saffron
            tilak of Guruship on his forehead. He was then declared as Guru Amar Das:</p>
        <p align="left">&quot;Jot uha jugat sai seh kaya pher paltiai.&quot; (Ramkali ki Var- Rai Balwand, p-966)<br/>
            <br/>
            'Divine Light is the same<br/>
            <br/>
            Way and Mode are the same The Master has only changed the body.&quot;<br/>
            <br/>
            (Translation of the above)</p>
        <p align="left">Guru Angad directed him to live in Goindwal and left for his heavenly abode on March 29,
            1552.<br/>
            <br/>
            Submission to Guru's order and worship of God, was the guiding principle in selection of the Guruship.
            Inspite of
            the opposition of his sons and relations, Guru Angad conferred the Guruship on Baba Amar Das who was proved
            to be
            the<br/>
            fittest and the most worthy for the Divine throne of Guru Nanak.</p>
