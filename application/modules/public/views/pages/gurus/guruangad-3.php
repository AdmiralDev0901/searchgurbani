        <p>One day a man, Gobind, came to the Guru and said that if he became victorious in a lawsuit against his
            relations, he
            would found a city in honor of the Guru. Fortune favored him and he started to found the city on the bank of
            the
            river Beas. He began the work but what was done during the day, was in some mysterious manner undone at
            night.
            Gobind came to the Guru and prayed to him to grant him his desire to build the city.<br/>
            <br/>
            Upon this the Guru sent Baba Amar Das to help him. Babaji prayed to God for His ssistance. The city's work
            proceeded
            without any further delay and Baba Amar Das named it Gobindwal and later on it was called Goindwal. Gobind
            did<br/>
            not forget to build a palace in it for his benefactor Amar Das. When the work was successfully completed,
            Gobind
            went to the Guru to offer his thanks and to beg him to come and live in the newly founded city. The Guru did
            not
            wish to leave his town, so he ordered Baba Amar as to go and live in Goindwal by night and come to him by
            day.
            Babaji obeyed the Guru and settled in Goindwal. In the process of time he took with him all his relations
            from
            Basarka and helped them in settling there.<br/>
            <br/>
            Baba Amar Das was now living in Goindwal and his daily routine was- to rise very early in the morning, take
            a
            pitcher of water from the river Beas and proceed to Khadur which was about three miles away. The pitcher of
            water
            was for Guru Angad to bathe with. On the way he would recite Japji. There was a mid-way spot which was
            called
            Damdama or breathing place where he could rest for a while. A temple was erected on this spot later on.
            After
            attending the morning service, Asa di Var, he would fetch water for the Guru's kitchen, clean dishes and
            bring
            firewood from the forest. During the day he would learn Gurbani (Word) from the Guru. In the evening he
            would attend
            Sodar and evening Kirtan. After putting the Guru to rest, he would return walking to Goindwal backwards in
            supreme
            reverence for his Master.<br/>
        </p>
