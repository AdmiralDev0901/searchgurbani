                    <p>Ram Rai was at the Emperor's court in Delhi and when the news of Guru Har Kishen's succession to
                        Guruship
                        reached Ram Rai, he was inflamed with jealousy. The masand Gurdas who attended on Ram Rai
                        comforted him,&quot;
                        There is no need to be sad. Thou hast many disciples in this part of the country. Even the
                        Emperor
                        himself holdeth thee in honor.&quot; Ram Rai was not satisfied,&quot; Knowest thou not, when the
                        Sikhs
                        of this country learn that the Guruship hath been given to my younger brother, they will turn
                        away from
                        me and go to him.&quot; However Ram Rai acted on Gurdas's suggestion and sent his masands in
                        every
                        direction to proclaim his succession and bring him the offerings of the faithful. His masands
                        went in
                        all directions to announce his succession as Guru but the Sikhs who knew Guru's succession,
                        refused to
                        accept Ram Rai as their Guru. At that time Ram Rai decided to lay his case before the Emperor.
                        Thus he
                        addressed Aurangzeb,&quot;Sir, my father has appointed my younger brother as successor to him,
                        and now
                        he has taken possession of his throne, his property and offerings. This misfortune has befallen
                        me on
                        account of my obedience to your Majesty. My father was opposed to you on that account, and at
                        his death
                        he ordered my younger brother never to be reconciled to you, and never to look upon your face.
                        Now I
                        pray thee to summon him to Delhi and order him to exhibit miracles as I have done.&quot;</p>
                    <p>This situation provided the Emperor with a good opportunity to realize his own religious mission.
                        Aurangzeb wanted to convert all Hindus to Islam but he apprehended failure in Punjab because the
                        people
                        greatly revered the Guru there. If the Emperor could get Ram Rai the Guruship, and through him
                        he would
                        perhaps be able to spread Islamic faith in Punjab, or even if he were successful to set both
                        brothers at
                        variance, they would die by mutual slaughter, his purpose should be served.</p>
                    <p>Aurangzeb having contrived this wicked plan, called Raja Jai Singh of Amber (Japer) and ordered
                        him to
                        summon Guru Har Kishen and said,&quot; I wish to see him, be careful that he (Guru) be treated
                        with all
                        respect on his journey.&quot; Ram Rai was delighted to hear the Emperor's decision to summon his
                        brother.</p>
