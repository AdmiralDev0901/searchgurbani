                    <p>Many writers have shown their disbelief over the fact that Guru Har Kishen being a child,
                        possessed high
                        attainments and he taught with all the confidence to those who asked him about truth.
                        Intelligentsia
                        cannot understand the sanctity of Guruship as they attribute every happening to age,
                        intelligence and
                        experience of a person. It should be noted here that Guru Har Kishen because of his divine
                        prerogatives,
                        had the spiritual powers at the age of five. His spiritual attainments came with his status of
                        Guruship.
                        As has been explained before, in Sikh Holy Scripture Guru means Jot or Divine Light, once the
                        Guruship
                        is installed, then a person's age, intelligence or experience has no value. It is then the
                        Divine Light
                        that works in that person. The power of Divine Light is beyond the reach of human intelligence.
                        The
                        intelligentsia cannot perceive or apprehend the power of Divine Light through the media of their
                        technical knowledge. Our so called technical knowledge or the intelligence, in reality is
                        'I-am-ness' or
                        the veil of ego. God is everywhere and within us too, but this veil of ego separates us from Him
                        and it
                        hides the truth from us. According to Gurmat, human being as bride and God as bridegroom, live
                        together
                        but the veil of ego separates them. When by the grace of the perfect Guru, this veil of ego is
                        broken,
                        the bride meets the Beloved, God:</p>
                    <p>&quot;Bride and the Bridegroom live together, but the screen of ego hangs in between;</p>
                    <p>When the perfect Guru breaks the veil of ego, O Nanak, we meet our Beloved, God.&quot;</p>
                    <p>(Malar Mohalla 4, p-1263)</p>
                    <p>It is only the Guru's grace that opens up our inner eyes with which we can perceive the
                        Incomprehensible
                        and enter the stage of Eternal Bliss.</p>
                    <p>&quot;Bisman bism bhaey bismad, Jin bujhaya tis aya swad.&quot;</p>
                    <p>(Gauri Sukhmani Mohalla 5, 16-8, p-285)</p>
                    <p>'Man wondereth at the wonders upon wonders of Creation But it is only he who knoweth God, who</p>
                    <p>obtaineth bliss.'</p>
                    <p>(Translation of the above)</p>
