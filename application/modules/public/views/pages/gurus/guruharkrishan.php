                    <p>Shortly after the above occurrence, the Guru was seized with high fever which was followed by
                        small-pox.
                        His mother sat by him and pleaded,&quot; My son, why art thou intent on thy death? Thou hast
                        only lately
                        been seated on the Guru's throne, thou art still a child, and it is too soon for thee to depart.&quot;
                        The Guru replied,&quot; Be not anxious. My safety is in His Will. He is the Reaper of His crop;
                        it is
                        His pleasure, and sometimes He reaps it while it is still green, half-green and sometimes when
                        it is
                        ripe. The Creator will do what is best.&quot;</p>
                    <p>The Guru was ill for several days. He knew his end had arrived and called for five paise and a
                        coco-nut.
                        He waved his hand three times in the air in token of circumambulating his successor and said,&quot;
                        Baba
                        Bakale,&quot; which meant that his successor would be found at village of Bakala. He then
                        breathed his
                        last on 30th of March, 1664. His body was cremated on the bank of river Jamna where now stands
                        the
                        Gurdwara Bala Sahib.</p>
