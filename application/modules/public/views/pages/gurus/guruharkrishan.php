                    <p>To reach that stage, a person first must seek Guru's grace. In order to do that, one must abandon
                        the
                        sense of egoism and the pride of technical knowledge; and then submit and surrender
                        unconditionally
                        before the Guru and beg for his grace. The answer to the question as to how the Guru at the age
                        of five
                        could possess attainments of such a high magnitude lies not in the reasoning of egoistic mind
                        but in the
                        spiritual vision. The answer to the spiritual power of the young Guru lies in the understanding
                        that the
                        Guru though human in body, is Divine in Spirit. Egoistic minds of the so called scholars
                        prohibit them
                        from understanding the Divinity of the Guru. As long as human mind remains under the
                        intoxication of
                        egoism, the cycle of arguments of intelligence will continue and a person can never perceive the
                        power
                        of Divine Light, cannot comprehend God and can have no knowledge of Him. The egoistic mind will
                        then
                        continue to wonder how a five years old Guru could perform those supernatural acts.</p>
                    <p>Raja Jai Singh had previously heard the Guru's praises and was, therefore, pleased at the
                        prospect of
                        making his acquaintance and of listening to his instruction. He sent his emissary to Kiratpur to
                        request
                        the Guru to come to Delhi. The Guru refused the invitation, for he had been forbidden by his
                        father not
                        to see the Emperor. Raja Jai Singh sent back this communication,&quot; Raja Jai Singh humbly
                        requests
                        the Guru to come to Delhi so that he and the Guru's Sikhs may behold him. The Guru may act as he
                        pleases
                        regarding an interview with the Emperor.&quot; It was made clear by the emissary of Jai Singh
                        that he
                        (Guru) would not be compelled to go to the Emperor.</p>
                    <p>The Guru set out from Kiratpur. On his way he waited for some Sikhs who had come to Kiratpur
                        after his
                        departure. Among them were the maimed and the leprous, all of whom he cured by the imposition of
                        his
                        hands. At Panjokhra, a village near Ambala, came a proud learned Brahman, who without even
                        saluting the
                        Guru, sat down in is presence. He then said,&quot; Thou who callest thyself Sri Har Kishen, must
                        be
                        greater than Lord Krishna. Translate the Gita for me.&quot; At that time, there stood nearby a
                        dumb and
                        illiterate man, Chhaju who hailed from the same village as the Brahman. Chhaju was serving water
                        in the
                        kitchen of the Guru where he had encamped. The Guru asked the Brahman whether he should
                        translate Gita
                        himself or he should get it done from Chhaju. The Brahman thought, Chhaju who could not even
                        talk, how
                        could he translate it? So he replied after a little pause, that Chhaju should do it. The Guru
                        was always
                        carrying a stick with him. He called Chhaju, put his stick on his head and asked him to answer
                        the
                        Brahman's questions. To the utter amazement of the Brahman, Chhaju explained in detail each and
                        every
                        verse that the Brahman asked. Upon this the Brahman fell on the lotus feet of the Guru and
                        apologized
                        for his indiscreet behavior.</p>
                    <p>When the Guru reached Delhi, he was received by Raja Jai Singh who came bare footed to meet the
                        Guru. He
                        requested the Guru to stay at his palace. That palace is now known as Gurudwara Bangla Sahib in
                        New
                        Delhi. Thousands of people of Delhi came to have 'darshan' (holy sight) of the Guru. The sick
                        were
                        healed and those in distress were comforted at the very sight of the holy Master.</p>
