                    <p>The Emperor sent presents at the arrival of the Guru and expressed a wish to see him but the
                        invitation
                        as refused. The Guru said,&quot; My elder brother, Ram Rai, is with the Emperor and he will
                        transact all
                        political affairs with His Majesty, and I better not meddle with him. My mission is to preach
                        the true
                        Name. Ram Rai bears enmity with me on account of Guruship, and if the Emperor shows any favor
                        towards me
                        , Ram Rai will become more hostile and grave dissensions in the family may better be avoided.
                        For this
                        and other reasons my father forbade me to meet the Emperor.&quot;</p>
                    <p>The next day the Emperor's son, prince Muazzam paid a visit and conveyed his father's desire to
                        see the
                        Guru. The Guru emphasized that he had already given the reason not to see the Emperor. If the
                        Emperor
                        desired any religious instructions, he would give it to the prince. If the Emperor understood
                        the
                        religious instructions and acted upon them, the blessing of Guru Nanak would light on him and he
                        would
                        be happy. The prince asked for the instructions and the Guru dictated the following Sabad of
                        Guru
                        Nanak:</p>
                    <p>&quot;If the True God dwell not in the heart,</p>
                    <p>What is eating, what is clothing,</p>
                    <p>What fruit, what clarified butter and sweet molasses, what fine flour and what meat?</p>
                    <p>What dresses, What a pleasant couch for billing and cooing,</p>
                    <p>What an army, what mace-bearers and servants, and what palaces to dwell in?</p>
                    <p>Nanak, except the true Name all things are perishable.&quot;</p>
                    <p>(Var Majh ki Mohalla 1, p-142)</p>
                    <p>On the instruction of Emperor Aurangzeb, Raja Jai Singh agreed to test the Guru whether he
                        possessed any
                        superhuman power. His head queen dressed like a maid servant and sat among other maid servants
                        and
                        queens. The Guru was requested to identify the head queen (Rani) which he immediately did. Upon
                        this
                        Raja Jai Singh and his queens acknowledged the Guru's spiritual power.</p>
                    <p>Cholera and small-pox were raging fiercely in Delhi at that time. The Guru ordered to spare all
                        the
                        offerings to reduce the suffering of the poor. The food, medicine and clothes were distributed
                        among the
                        poor and the sick. He won many followers in this way.</p>
