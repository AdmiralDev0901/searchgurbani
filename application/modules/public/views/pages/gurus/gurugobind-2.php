        <p>Ram Rai, the eldest son of Guru Har Rai, when sent to Delhi on behalf of the Guru, distorted the holy words
            of Guru
            Nanak in the court of Aurangzeb in order to please the Emperor. Upon this the Guru disowned him and
            communicated him
            from the Sikh faith. The Emperor gave him an estate where he founded the town of Dehra Dun and continued to
            live
            there. Ram Rai claimed himself as the real Guru. Being a willing tool in the hands of the Mughal Emperor, he
            continually tried to harm the cause of the Sikhs. Now since Guru Gobind Singh had come to Paunta, which was
            only
            thirty miles from Dehra Dun, Ram Rai became afraid of him and could not muster courage to face him. A
            discussion
            started in Ram Rai's assembly about all this. Hearing on Ram Rai's anxiety, the Guru sent Nand Chand and
            Daya Ram to
            reassure him that no harm would be done to him. Ram Rai on receiving the Guru's message, was very much
            delighted. He
            gave robes of honor to Nand Chand and Daya Ram and decided to be on friendly terms with the Guru.</p>
        <p>It is said that a meeting between the Guru and Ram Rai took place in a ferry in the middle of the stream. Ram
            Rai
            touched the Guru's feet in obeisance and said,&quot; I am fortunate to have obtained a sight of thee. When I
            am
            gone, protect my family........my father Guru Har Rai used to say that someone would be born from our family
            who
            would restore and refit the vessel for the safe conveyance of the souls.&quot; He asked for forgiveness. Ram
            Rai
            while he was in trance, was cremated by his masands in defiance of the entreaties and prayers of his wife,
            Punjab
            Kaur. The Guru then responded to the request of Punjab Kaur and meted out strict punishment to the guilty
            masands
            and rewarded those who had remained faithful to her.</p>
