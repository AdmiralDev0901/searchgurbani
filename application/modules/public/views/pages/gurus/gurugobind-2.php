        <h2 class="no-top">Sri Guru Gobind Singh Sahib Ji</h2>
        <p align="left"><strong><a name="1" id="1">RAJA BHIM CHAND AND THE GURU</a></strong></p>
        <p>Raja Bhim Chand was received in Guru's darbar (court) with great honor. He prayed to the Guru to let him see
            the
            gifts from the Raja of Assam. He was shown all the presents. Bhim Chand was astonished at the magnificence
            of the
            Kabuli tent. He was told that it was the offering of a pious Sikh from Kabul. During this conversation when
            the
            beautifully decorated elephant was let forward, Bhim Chand stood spellbound and expressed his unbounded
            admiration
            of all that he had seen. On his homeward journey his mind burned with jealousy of the Guru's state and
            wealth and he
            made up his mind to take possession of at least the elephant.</p>
