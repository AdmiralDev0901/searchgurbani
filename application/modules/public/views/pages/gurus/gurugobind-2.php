        <p><strong><a name="4" id="4">PIR BUDHU SHAH:</a></strong></p>
        <p>Pir Budhu Shah was a Muslim saint who lived at Sadhaura, about ten or fifteen miles from Paunta Sahib. He was
            well
            known for his piety and had a large number of followers. He had heard of Guru Nanak and his mission. He had
            also
            learned that Guru Nanak's throne was then occupied by Guru Gobind Singh who was staying in the neighborhood.
            Ultimately he decided to visit him. The Guru seated the Pir near him who beseeched,&quot; Pray! tell us how
            one
            meets God Almighty.&quot; During the discussion the Pir humbly submitted to the Master. There was a glow in
            the eyes
            of the Guru which radiated Divine Light and the Pir exclaimed with sudden joy,&quot; Allah-hu- Akbar!&quot;
            - Great
            is God Almighty. After a while the Pir confessed,&quot; Master, I was spiritually blind and you have shown
            me the
            Light.&quot; Blessed are the souls on whom the Guru bestows the divine grace.</p>
