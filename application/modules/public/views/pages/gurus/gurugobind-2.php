        <p>In the meantime the Raja Medani Parkash of Nahan, invited the Guru to visit him. The invitation was accepted
            and he
            left for Nahan. Gulab Rai and Sham Das were made incharge for the defence of Anandpur. The Raja came to
            greet and
            welcome the Guru and then took him to his palace. One day the Raja took him on hunting excursion and
            complained that
            Raja Fateh Shah of Garhwal had often quarrelled with him over the ground on which they were then standing.
            He
            suggested that he would be very pleased if a fort were to be constructed on the spot for protection against
            the
            enemy. The Guru erected a tent on that spot and held a darbar. He laid down foundation stone of the fort.
            With the
            help of the Raja's army and with the zeal and energy of the workmen, the fort was completed within a short
            time. The
            Guru named it Paunta, and started to live there and continued to increase his army.</p>
        <p>Raja F ateh Shah of Garhwal arrived at the conclusion that since the Guru started living near his territory,
            itwould,
            therefore, be politic to be on good terms with him and accordingly he decided to pay a visit to the Guru. He
            was
            received with great honor in the Guru's darbar (court). During his visit the Guru sent his uncle Kirpal to
            him to
            suggest that it would be well if he and the Raja of Nahan were on good terms. Raja Fateh Shah gave his
            consent
            immediately. The Guru then sent for the Raja of Nahan. He brought the two Rajas together in the open court,
            caused
            them to embrace and form a friendship.</p>
        <p>In the meantime a hillman came with tidings of a fierce tiger which was destroying cattle in the
            neighborhood. He
            requested the Guru to free the country from the wild animal. He took the two Rajas and others to the place
            where the
            tiger was said to be residing. On hearing the huntsmen's foot steps, the tiger sat on his haunches looking
            at his
            pursuers. The Guru called on any one who could engage the tiger with sword and shield. No one came forward.
            He then
            took his sword and shield and challenged the tiger. The tiger rose with a roar and sprang at the Guru, who
            received
            him on his shield and striking him on the flank with his sword, cut him in two. The Rajas and the hunting
            party were
            astonished and delighted at his strength and bravery.</p>
