        <p>On his return to the capital, Bhim Chand disclosed his designs to his courtiers . It was decided that a
            messageshould
            be sent to the Guru that Raja Fateh Chand of Garhwal's party was coming with the object of Betrothing his
            daughter
            to Bhim Chand's son, and Bhim Chand desired to borrow the elephant so as to makea display of his wealth to
            his
            guests. When the message was delivered to the Guru, he knew that it was only a trick to obtain permanent
            possession
            of the animal. He sent the reply to Bhim Chand,&quot; The Raja who presented the elephant, requested me not
            to let
            the animal go out of my possession. It is the principle of Guru's house to comply with such requests.&quot;
            It is
            said that the Raja sent his emissaries thrice, the last one being Kesari Chand, the Raja of Jaswal, but the
            Guru did
            not yield and therefore, Bhim Chand's demand was not met. So he got angry and wanted to take revenge.</p>
        <p>Majority of the ma sands felt agitated at the Guru's warlike preparations and they represented to his
            motherto
            dissuade him from such activities lest it should bring some trouble to him. When his mother talked to him
            about it,
            he replied,&quot; Dear mother, I have been sent by the Immortal God. He who worshippeth Him shall be happy;
            but he
            who acteth dishonestly and worshippeth stones shall receive well-merited retribution. This is my commission
            from
            God. If today I give Raja Bhim Chand the elephant, I shall have to pay him tribute tomorrow.&quot; Nand
            Chand then
            joined the conversation and said,&quot; Mother, hath a lion ever feared jackals? Hath any one ever seen the
            light of
            the firefly in bright sunshine? What availeth a drop of water in comparison with the ocean? The Guru is a
            tiger
            brave and splendid as the sun. Shall he fear Bhim Chand?&quot; The Master ended the discussion by saying,&quot;Dear
            mother, heed not the evil advice of the masands. They have become cowards by eating the offerings of the
            Sikhs.&quot;
            The Guru and his troops continued to practise archery and devoted themselves to the chase. The Sikhs kept
            visiting
            continually and make offering of arms. Those who came for military service, were readily received and were
            taught
            the profession of arms. In this way the Guru collected a considerable army.</p>
