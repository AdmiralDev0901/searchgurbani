                    <p>Guru Arjan completed the unfinished work of excavation of tanks- Santokhsar and Amritsar. Bhai
                        Buddha
                        being the most trustworthy disciple, was appointed to supervise the work of construction.
                        Santokhsar was
                        completed in 1587-89. Having completed the tank of nectar, the Guru laid down the foundation of
                        Hari
                        Mandar which is now called Golden Temple, in the center of the tank. It is said that the Sikhs
                        represented to the Guru that Hari Mandar should be the tallest building in the neighborhood. The
                        Guru
                        explained,&quot;Hari Mandar should be the lowest because what is humble, shall be exalted. The
                        more a
                        tree is covered with fruit, the more its branches descend to the earth.&quot;</p>
                    <p>Mian Mir, a famous Muslim saint, was a friend and a devotee of the Guru. The Guru asked Mian Mir
                        to lay
                        the foundation stone of Hari Mandar. Thus, Mian Mir laid the brick in January, 1589. The head
                        mason
                        moved the brick to place it in order. Whereupon the Guru prophesied that since the brick was
                        moved by
                        the mason, the foundation of the temple would be laid again in the coming times. His words were
                        subsequently fulfilled as Ahmad Shah Abdali destroyed the temple and desecrated the tank in
                        1763.
                        However two years later, the great army of the Khalsa recovered possession of the temple, relaid
                        its
                        foundation and reconstructed it.</p>
                    <p>Hindu temples were closed on three sides and their entrances were generally towards the east
                        while Muslim
                        mosques had entrances towards the west. Hari Mandar, the holy Sikh temple had entrance on all
                        four
                        sides. This denotes that God was in all the directions; and secondly four doors in the four
                        directions
                        (east, west, north and south) meant that all the four castes would have equal access to the
                        temple.
                        Whereas the Hindu temples were only open to the chosen classes, the Golden temple was open to
                        all who
                        would seek God. Muslim mosques were open only to Muslim men, the Golden temple was open equally
                        to all
                        men, women and children, irrespective of caste, creed, race, color, sex, religion or
                        nationality. The
                        Adi Granth, Sikh holy Scripture, was placed in the center of the temple.</p>
                    <p>When the construction of the tank and the temple was completed, Guru Arjan uttered the following
                        Sabad in
                        joy and gratitude:</p>
                    <p><em>&quot;The Creator stood in the midst of the work,</em></p>
                    <p><em>And not a hair of any man's head was touched.</em></p>
                    <p><em>The Guru maketh my ablution successful.</em></p>
                    <p><em>And by repeating God's Name, sins shall depart.</em></p>
                    <p><em>saints, Ramdas tank is beautiful,</em></p>
                    <p><em>He who batheth in it shall save himself and the souls of his family.</em></p>
                    <p><em>The whole world shall congratulate him,</em></p>
                    <p><em>And he shall obtain the reward his heart desireth.</em></p>
                    <p><em>He who while meditating on his God</em></p>
                    <p><em>Cometh to bathe here shall be made safe and whole.</em></p>
                    <p><em>He who batheth in the saints' tank</em></p>
                    <p><em>Shall obtain final salvation. Meditating on God's Name,</em></p>
                    <p><em>He shall not suffer transmigration.</em></p>
                    <p><em>He to whom God is merciful Knoweth divine knowledge.</em></p>
                    <p><em>His cares and anxieties shall depart</em></p>
                    <p><em>Who seeketh the protection of Baba Nanak and God.&quot;</em></p>
                    <p><em>(Sorath Mohalla 5, p-623)</em></p>
                    <p>&nbsp;</p>
                    <p>Golden Temple, Amritsar</p>
                    <p>NOTE: It should be pointed out here that bathing alone in the tank of Golden Temple cannot give
                        the
                        desired salvation. The above Sabad should not be taken for any pilgrimage of rituals. It does
                        not mean a
                        mere journey to holy place. Sikh faith rejects all ceremonial acts. In fact there are two tanks
                        in
                        Amritsar- outer tank and inner tank. First one is the outer tank full of water. It is customary
                        for a
                        Sikh to take bath every morning- to clean and purify his body before going in the 'presence of
                        God',
                        that is meditation. Thus outer tank in Amritsar serves that purpose. A Sikh's mission does not
                        complete
                        here. Guru Amar Das confirms this:</p>
                    <p><em>'If mind is sinful, everything is sinful,</em></p>
                    <p><em>By washing the body, mind will not become pure.'</em></p>
                    <p><em>(Wadhans Mohalla 3, p-558)</em></p>
                    <p>Then there is the inner tank called Hari Mandar. That tank is 'Gurbani'- Guru Granth Sahib which
                        is full
                        of 'Praise and Prayer'- NAM. After washing outerself in the outer tank, a Sikh goes to the inner
                        tank to
                        cleanse his sinful mind in the tank of Nam. That is what Guru Nanak calls pilgrimage:</p>
                    <p>&quot;Pilgrimage is Nam..&quot;</p>
                    <p>(Dhanasri Mohalla 1 Chhant, p-687)</p>
                    <p>Without Nam immersing in water countless times, will not deliver salvation. This process of
                        cleansing
                        sinful mind in the inner tank of Nam is the pre-requisite for spiritual growth. It leads to
                        graduation
                        to spiritual consciousness- a breakthrough to Eternal illumination. After cleaning his inner and
                        outer-self, a Sikh realizes the glory of Nam and enters a stage of eternal bliss, and thereby
                        merges
                        with the Eternal Being. A body is dead without life, and life itself is dead without Nam.</p>
                    <p><em>What is then Amritsar?</em></p>
                    <p><em>Then there was no sign of the city of Amritsar, Guru Nanak asked his disciples to bathe in
                            'Amritsar':</em></p>
                    <p><em>&quot;Bikhia mal jai amritsar navo Gur santokh paya.&quot;</em></p>
                    <p><em>(Maru Mohalla 1, p-1043)</em></p>
                    <p><em>'All sins are washed away by bathing in Amritsar And by Guru's grace, contentment is
                            obtained.'</em>
                    </p>
                    <p><em>(Translation of the above)</em></p>
                    <p><em>&quot;Gur sagar amritsar, jo echhai so phal pai.&quot;</em></p>
                    <p><em>(Maru Mohalla 1, p-1011-12) 'Divine Word (Gurbani) is Amritsar Whosoever batheth in it, will
                            get
                            his</em></p>
                    <p><em>desire fulfilled.'</em></p>
                    <p><em>(Translation of the above)</em></p>
                    <p><em>&quot;Untar nirmal amritsar nai.&quot; (Asa Mohalla 3, p-363)</em></p>
                    <p><em>'Impurities of mind are washed away by bathing in Amritsar.'</em></p>
                    <p><em>(Translation of the above)</em></p>
                    <p><em>&quot;Undro trisna agan bujhi Har amritsar nata.&quot;</em></p>
                    <p><em>(Mohalla 3-pauri, p-510)</em></p>
                    <p><em>'The inner fire of desires gone When bathed in Amritsar.'</em></p>
                    <p><em>(Translation of the above)</em></p>
                    <p><em>&quot;Meil gaee man nirmal hoa Amritsar tirath nai.&quot;</em></p>
                    <p><em>(Mohalla 3, p-587)</em></p>
                    <p><em>'Impurities gone and mind becometh pure When batheth in the pilgrimage of Amritsar.'</em></p>
                    <p><em>(Translation of the above)</em></p>
                    <p>All the above verses of Guru Nanak and Guru Amar Das give reference of 'Amritsar' when there was
                        no trace
                        of the city or the tank of Amritsar. That means that Amritsar actually and literally means Tank
                        of
                        Nectar which is Gurbani, the Divine Word. Thus pilgrimage of Amritsar means the pilgrimage of
                        one's mind
                        in the Divine Word, the NAM. Without Nam, bathing hundreds of times cannot purge mind of its
                        impurities
                        and therefore salvation cannot be achieved:</p>
                    <p><em>&quot;Mal haumai dhoti kivai na uterai je sau tirath nai.&quot;</em></p>
                    <p><em>(Sri Rag Mohalla 3, p-39)</em></p>
                    <p><em>The Guru says that without Nam, all other acts to attain salvation, are futile:</em></p>
                    <p><em>&quot;Nam bina phokat sabh karma jiun bajigar bharm bhulai.&quot;</em></p>
                    <p><em>(Parbhati Mohalla 1, p-1343)</em></p>
                    <p><em>'Without Nam, all acts are futile As an actor's role in drama, maketh him not real.'</em></p>
                    <p><em>(Translation of the above)</em></p>
