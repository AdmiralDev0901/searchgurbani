                <td colspan="2" valign="top"><p>Guru Arjan was born in Goindwal, a small town in Amritsar district, on
                        April 15,
                        1563. He was the youngest son of Guru Ram Das and Bibi Bhani. As a child, one day he found his
                        way to
                        the bed of Guru Amar Das who was then resting. His mother ran to fetch the child before he could
                        disturb
                        the Guru, but he had already awakened the Guru, who revealed,&quot;Let him come to me; 'yeh mera
                        dohita
                        bani ka bohita howega'- this grandson of mine shall be a ship to take mankind across the ocean
                        of the
                        world.&quot; Guru Arjan was married to Ganga, daughter of Krishen Chand, a resident of Meo
                        village near
                        Phillor in Jullundhur district. The marriage took place in 1589 when he was about 26 years old.
                        Guru Ram
                        Das began excavation of two tanks named Santokhsar and Amritsar and started the foundation of
                        the city
                        of Ramdaspur. After his father, Guru Arjan applied himself to the task of completing the tanks
                        and
                        extending the city. It was his practice to go every day and superintend the work.</p>
                    <p><a name="MASAND" id="MASAND">MASAND SYSTEM:</a></p>
                    <p>Guru Nanak during his missionary tours had established Sangats at the various places throughout
                        the
                        country. The connection with the center was kept up by the constant visits of the Sikhs to the
                        Guru.
                        During Guru Amar Das's time the missionary work became more regular and methodical. He divided
                        the Sikh
                        spiritual kingdom into twenty-two Manjis. Guru Ram Das established the nucleus of a new order of
                        missionaries called Masands. The word Masand seems to have come from 'Mas Nad' which is shorter
                        form of
                        'Masnedi-Ali' or His Excellency, the title which the Mughal Governors often held. The purpose of
                        this
                        organization was to spread the Sikh faith at a rapid pace and also to collect money for the
                        construction
                        of tanks and the city of Ramdaspur. Guru Arjan organized Masand system afresh. He appointed new
                        Masands
                        of integrity and sincerity to look after the secular as well as spiritual affairs of the Sikhs.
                        Masands
                        were required to collect Daswandh (one-tenth of income) from the Sikhs which was then
                        contributed
                        towards the Guru's treasury (Golak) for the maintenance of the Sikh temples. Some writers are of
                        the
                        opinion that Daswandh was raised under compulsion. Daswandh was never raised under any pressure
                        nor it
                        was considered 'a tax'. Whatever the Sikhs contributed or are contributing even today, is
                        totally out of
                        free will, love and devotion.</p>
                    <p>Masands were required to pay annual visits to Amritsar at the Baisakhi fair to receive
                        instructions from
                        the Guru and to hand over the amount of Daswandh collected. Regular accounts of these offerings
                        were
                        kept and receipts were issued. Apart from financial duties, Masands were vigorous preachers. For
                        the
                        ceremony of initiation the ideal Charanamrit was the one administered by the Guru himself. Since
                        it was
                        not possible for the Guru to be present physically everywhere, the authority was delegated to
                        the local
                        missionaries to enrol new members of the Sikh Sangat by administering Charanamrit prepared by
                        themselves
                        in the prescribed manner. It attracted a large number of converts. During the time of Guru
                        Arjan, there
                        was hardly any place in India where the Sikhs were not found. Masands worked very hard to
                        propagate
                        Sikhism in every corner of the country.</p>
