                    <p>When the tank and the temple were completed, there were great rejoicing. The enormous exertions
                        and
                        sacrifices were made by the Sikhs. The Guru honored all those Sikhs who had put in dedicated
                        service to
                        ensure the completion of the projects. Eminent among those people were: Bhai Buddha, Bhai
                        Bhagtu, Bhai
                        Bahla, Bhai Kalyana, Bhai Ajab, Bhai Ajaib, Bhai Umar Shah, Bhai Sangho, Bhai Salho and Bhai
                        Jetha. Bhai
                        Buddha was made incharge of the Hari Mandar. Bhai Bhagtu was instructed to preach Sikh doctrine
                        in the
                        Malwa region and Bhai Salho was made the superintendent of the city to look after its
                        development. City
                        of Ramdaspur was, in the course of time, called Amritsar. Because of Guru's residence and the
                        central
                        place of worship, Amritsar became the center of the Sikh activities.</p></td>
