        <h2 class="no-top">Sri Guru Nanak Dev Ji</h2>
        <p><strong>GURU AT MEDINA:</strong></p>
        <p class="black">In due time the Guru proceeded to Medina, another holy city of the Muslims where their Prophet Mohammad lived
            for
            many years and breathed his last. He reached at nightfall and stopped outside the town. It happened to be a
            place
            where lepers were segregated and no provision was made for their comfort or treatment. History states that
            the Guru
            healed them all and as a result, the people came in crowds to have holy glimpse of the Guru. After that he
            journeyed
            to Bagdad through Basra.<br/>
        </p>
        <p><a name="11" id="11"></a><strong>GURU AT BAGDAD:</strong></p>
        <p class="black">There lived a very famous Muslim saint, Pir Abdul Kadar who died in Bagdad in 1166 A.D. He was also known as
            Dastgir
            and his successors were called Dastgirs too. The Muslim high priests did not like unethical and immoral
            musical
            verses. Instead of condemning the demoralizing poetry, they outrightly rejected the music ('Rag') itself. So
            according to Muslim Shariat (code of law), music was forbidden. The whole of Sikh scripture is in verse and
            in
            various different forms of Rags and Raginis. In the morning the Guru shouted the call for prayer, on which
            the whole
            population became rapt in silent astonishment. May be he did it differently than the Muslims. Then Mardana
            played
            the rebec and the Guru started the Sabad Kirtan (musical recitation of Gurbani). Whosoever heard was in
            ecstasy. The
            news spread in the city. The high priest Pir Dastgir, another holy man, Bahlol and others came to see the
            Guru.</p>
        <p class="black">According to the Mohammadans there are seven skies above the earth and seven nethers including earth itself.
            The Guru
            began to recite the Japji. When he repeated the twenty-second pauri (stanza) of Japji, the Pir got
            wonder-stuck
            hearing something contrary to the authority of the holy Quran, that there were hundreds of thousands of
            nethers and
            upper regions, and that at last men grew weary of searching for them. The Pir then called upon the Guru to
            give a
            manifestation of what he said. Upon this it is said, the Guru laid his hand on the priest's son and showed
            him upper
            and lower regions described in Japji- pauri 22. To prove whether the boy actually saw those regions, he
            brought
            Parshad (sacred food) from one of those regions and gave it to his father. Both the Pir and Bahlol bowed
            before the
            Guru and asked for his blessings.</p>
