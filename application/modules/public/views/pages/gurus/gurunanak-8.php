        <p><a name="12" id="12"></a><strong>RETURN FROM BAGDAD:</strong></p>
        <p class="black">From Bagdad the Guru passed through Iran, Turkstan and Afghanistan and then reached Kabul. Some writers
            believe that
            the Guru took the popular route from Bagdad towards Tehran, Kandhar and reached Kabul. On his way he passed
            through
            Mehds. Bhai Mani Singh's Janamsakhi makes a reference of his visit to this place. Since the visit of Guru
            Nanak to
            Kabul, the Sikh contacts had been carefully maintained. Sikh preachers were stationed there to disseminate
            the
            teachings of the Guru. At one time Bhai Gurdas also served as one of the Sikh missionaries at Kabul.</p>
        <p class="black">From Kabul the Guru proceeded to Jalalabad, Sultanpur and passed through Khyber Pass to reach Peshawar. There
            are
            Gurdwaras at Jalalabad and Sultanpur to mark his visit. There are springs of water associated with his
            visit. The
            Guru paid a visit to the Gorakh Hatri and had discourse with Jogis. He also went to Hassan Abdal, now known
            as Panja
            Sahib, and sat at the foot of the hill.<br/>
        </p>
        <p><a name="13" id="13"></a><strong>GURU NANAK AND VALI KANDHARI:</strong></p>
        <p class="black">On the top of a small hill, there lived a Muslim Faqir called Vali Kandhari who was well-known in the area
            for
            possessing miraculous powers. Mardana needed water which could only be obtained from Vali. Mardana told Vali
            that
            Guru Nanak had arrived and he advised him to see the Guru, who was a great saint of God. Vali who claimed
            holiness
            exclusively for himself, became offended on hearing the Guru's praises. He refused to give water saying that
            if the
            Guru were such a holy man, he could provide water to Mardana. When this reply was communicated to the Guru,
            he sent
            Mardana back to the Vali with a message that he (Guru) was a poor creature of God, and laid no claims to be
            a saint.
            The Vali paid no heed to this protestation and still refused to provide water.</p>
        <p class="black">Upon this the Guru picked up one stone and a stream of water immediately issued forth. In fact this water
            came out
            from the Vali's tank which dried up. This naturally increased Vali's rage and it is said that through his
            miraculous
            powers he hurled a small hillock upon Guru Nanak's unoffending head. The Guru, on seeing the descending
            hillock,
            held up his right hand, and as it touched the hand of the Divine Master, the hillock came to a standstill.
            With the
            divine touch, the stone melted and softened like wax and left the mark of the Master's palm indelibly deep
            into it.
            Vali Kandhari was very much astonished and at last fell at the feet of the Guru and begged for forgiveness.
            The Guru
            expressed,&quot;O friend, those who live so high, should not be hard at heart like a stone.&quot; Vali was
            blessed
            by the Master.</p>
        <p class="black">The imprint of the Guru's hand (Punja) is still visible on the stone and the pool of crystal clear water
            still flows
            from there. There stands a Gurdwara which is known as 'Punja Sahib'. It is now situated in west
            Pakistan.</p>
