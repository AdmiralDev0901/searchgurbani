        <p><strong><a name="amardas" id="amardas">BABA AMAR DAS COMES TO GURU ANGAD</a></strong></p>
        <p>Baba Amar Das was living in a village called Basarka near Amritsar. He was a firm eliever of Vaishnav faith
            and used
            to fast regularly. Every year he went to Hardwar for pilgrimage, bathed in the river Ganges and would give
            alms to
            the poor. It was the twenty-first year of his pilgrimage and he was sixty-two years old. He was coming back
            from
            Hardwar when he decided to lay down to sleep outside the village of Mihra. Here he met a Vaishnav Sadhu (a
            monk)
            with whom he became so intimate that they cooked for each other. As they continued their journey and as the
            monk
            found Baba Amar Das zealously discharging all the<br/>
            duties of a pious Hindu, he asked him (Baba) who his guru was who taught him such piety and wisdom. Baba
            Amar Das
            replied that he had no guru. On hearing this the monk said,&quot;I have committed a sin by eating from the
            hands of
            a man who has no guru. My ablutions bathing in the Ganges are of no avail now. I an only be purified if I
            return to
            bathe in the Ganges again.&quot; After lamenting like this,<br/>
            the Sadhu departed.<br/>
            <br/>
            This was a great shock to Baba Amar Das and he was jolted in his heart thinking he was man of no guru
            (Nigura):<br/>
            <br/>
            &quot;Satgur bajho gur nahi koee, nigurei ka hai nau bura.&quot; (Rag Asa Mohalla 3, p-435)<br/>
            <br/>
            'Satgur is the competent guru and without that no other guru is worthy of<br/>
            cceptance but if a person has no guru at all, that person's name is sinful.&quot;<br/>
            (translated)<br/>
            <br/>
            He started thinking seriously how he could find a guru and he prayed for that. One day early in the morning
            he heard
            a divine melody which thrilled his heart and he<br/>
            stood spell-bound listening to the hymn. This was voice of Bibi Amro, Guru Angad's daughter, who was
            recently
            married to his ne phew. It was Bibi Amro's routine to rise early, bathe and recite Japji and other hymns of
            Guru
            Nanak. Bibi Amro had recited the following Sabad which was heard by Baba Amar Das:<br/>
            <br/>
            &quot;Neither sisters, sisters-in-law, nor mothers-in-law remain with one;<br/>
            <br/>
            But the true relationship with the Beloved, when found through the Guru, shall never be sundered. I am a
            sacrifice
            to my Guru, I am ever a&nbsp; sacrifice unto him. I have grown weary of wandering so far without a
            Guru;<br/>
            <br/>
            Now the Guru hath united me with my Beloved.<br/>
            <br/>
            (Maru Mohalla 1, p-1015)<br/>
            <br/></p>
            <p class="black">Baba Amar Das asked Bibi Amro whose composition it was. She replied that it was Guru nanak's hymn and she
            had learnt
            it from her father who was the successor to Guru Nanak. Baba Amar Das then requested her to take him to the
            Guru.
            After some days he accompanied Bibi Amro to visit the Guru in Khadur. When Baba Amar Das arrived, the Guru
            on
            account of his relationship, wanted to embrace Babaji and receive him respectfully, but Babaji fell on the
            feet of
            the Guru and said,&quot;Thou art as God and I am only a worm.&quot; Baba Amar Das was so much overwhelmed by
            Guru's
            darshan (holy sight) that it was unbearable for him to leave his presence. The love for the Master sprang so
            deep
            and intense in his heart that he wanted to serve him in every possible way.<br/>
            <br/>
            One day meat was prepared for dinner and Baba Amar Das commented,&quot;If the Guru is the knower of hearts,
            he
            should know that I am a strict Vaishnav and do not touch meat.&quot; Realizing this the Guru ordered the
            Sikh who
            was serving the dinner (langar) that only dal (bean-curry) not meat should be served to him (Baba Amar Das).
            Soon
            after that, Baba Amar Das realized that a disciple, whose practice differed from that of his Guru, must
            inevitably
            fail. He told the cook that if the Guru were kind enough to give him his meat leaving, he would partake of
            it.In
            order to further remove his prejudices, the Guru instructed him,&quot;These are the meats to abstain from-
            others'
            wealth, others' wives, slander, envy, covetousness and pride.&quot; The Guru then recited the Slok Mohalla 1
            of page
            1289 on the subject.</p>

