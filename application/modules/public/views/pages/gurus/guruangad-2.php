        <p>Clipped or imperfect alphabet of Punjabi existed at the time of Guru Nanak, but Guru Angad modified and
            polished the
            existing script. Since the Guru had adopted the modified alphabet, it was called 'Gurmukhi'- spoken through
            the
            mouth of the Guru.<br/>
            The significance of the adoption of this script by Guru Angad lies in the fact that he ejected all other
            scripts,
            and adopted the script which was his own and suited to the language of the people. It also helped to enhance
            their
            culture. The Guru recorded everything onwards in Punjabi in Gurmukhi script.<br/>
        </p>
