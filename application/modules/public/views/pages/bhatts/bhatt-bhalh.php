            <h2>Bhatt Bhalh</h2>
            <hr/>
                <p class="black">Only
                    one Swaiya of Bhatt Bhalh is available comprising of 4 lines, written
                    in praise of Guru Amardas the 3rd Guru. Surprisingly enough, this small
                    poem is impregnated with symbolic imagery, refined poetic vocabulary,
                    beautiful syntex, and impressive expression. He says </p>

                <blockquote>
                    <blockquote>
                        <p class="black"><em>Innumerable
                                rain drops pour from the clouds<br/>
                                &nbsp;Myriad are the colourful flowers <br/>
                                That bloom in Basant (spring) <br/>
                                Countless beams the sun originates, <br/>
                                Countless rays the moon releases.<br/>
                                &nbsp;Unfathomable is the depth of the sea, <br/>
                                Innumerable waves dance their way to the ocean.<br/>
                                &nbsp;Like Shiva's meditation, deep and unbounded,<br/>
                                &nbsp;Infinite is your knowledge, True Guru Amardas! <br/>
                                I can not comprehend the span of your virtues <br/>
                                Says Bhalh the poet, <br/>
                                You yourself are the master of your mystery. </em></p>
                    </blockquote>
                </blockquote>

                <p><br/>
                    Specimen
                    of Bhalh's poetry </p>

                <p> Myriads are
                    the rain drops, vast is the vegetation on earth, Numberless are the colours
                    and flowers of spring, <br/>
                    <br/>
                    Who can count the endless dance of the waves of the Ganges, Who can catch
                    the echo of the song they sing? <br/>
                    <br/>
                    Countless beams of moon and sun are unable to reach The depth of the fathomless
                    king. (sea)</p>
                <p> SHIVA's power
                    of concentration, may assess this countless treasure of the mystic sea, <br/>
                    <br/>
                    But thy praises, O, Guru Amardas, are beyond imagination and behove only
                    thee. (1-22) <font face="Times New Roman, Times" size="2"><br/>
                        <br/>
                        <b>Adi
                            Granth, p. 1396 </b></p>
