            <h2>Bhatt Jalap</h2>
            <hr/>
                <p class="black"> Bhatt
                    Jalap also wrote under the name of Jalh. Five of his Swaiyas eulogies the
                    3rd Guru are included in Adi Granth. He appears to be an intellectual,
                    well n and having lot of knowledge of Hindu mythology. Being a true devotee
                    of Guru Amardas, he preaches complete submission to him in the following
                    Swaiya </p>

                <blockquote>
                    <p><br/>
                        <em>Blessed are the feet that tread the path of Guru Amardas Blessed
                            are the hands that touch his lotus feet </em><br/>
                        <em><br/>
                            Blessed is the tongue that utters his holy name <br/>
                            <br/>
                            Blessed are the eyes that behold his sacred countenance </em></p>

                    <p><em>Blessed
                            are the ears that hear his praise <br/>
                            <br/>
                            Blessed is the heart in which he abides </em></p>

                    <p><em>Blessed
                            is the head that falls on this feet. </em></p>
                </blockquote>
                <p><strong>Specimen of poetry of Bhatt Jalap</strong></p>
                <blockquote>
                    <p class="black"><em>Neither
                            they face grief, nor hunger, nor they have depression, They get not
                            afflicted, their excellence is beyond expression. Serve they not anyone,
                            other than the Lord Great, <br/>
                            <br/>
                            Hundred thousands of souls submit to them for their fate. <br/>
                            <br/>
                            The finest of carpets role under their feet and destiny of people In
                            their hands they hold, <br/>
                            <br/>
                            Their generosity provides daily bread to some, To many they bestow with
                            mines of gold. <br/>
                            <br/>
                            Their life is always a bliss, peaceful and sweet, <br/>
                            <br/>
                            They are calm in tribulation and in adversity discreet. Grace of Guru
                            Amardas is bestowed on whom, <br/>
                            <br/>
                            They prosper, says Jalap, like flowers they bloom. (1-22) </em><br/>
                        <br/>
                        <strong>Adi Granth, p.1396</strong></p>
