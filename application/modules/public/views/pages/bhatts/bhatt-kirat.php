            <h2>Bhatt Kirat</h2>
            <hr/>
                <p class="black">Bhatt
                    Kirat, son of Bhikha, besides being a poet, had the privilege of being
                    an enrolled soldier in the army of Guru Hargobind Sahib, the 6 11 Guru.
                    He had attended the celestial grandeur of the Darbars of Guru Ramdas and
                    Guru Arjun Dev and had also enjoyed later on the benevolence of the Master
                    of Miri and Piri, under whose command, he had fought in the battlefield.
                    When it came to the writing of Swaiyas, he chose to eulogise Guru Amardas
                    and Guru Ramdas. There are eight Swaiyas authored by him included in Adi
                    Granth at pages 1395 to 1405. Out of these, four Swaiyas are in praise
                    of Guru Amardas the 3'd Guru and an equal number in praise of the 4t'
                    Guru. A staunch believer in the house of Gurus, he died fighting under
                    the command of Guru Hargobind Sahib. Extreme humility and complete submission
                    can be observed in the following hymn authored by him. <br/>
                    &nbsp;</p>
                <blockquote>
                    <p><em>A confirmed
                            sinner and idiot of the first water, </em></p>
                    <p><em> The
                            poison I take, and refuse the nectar. </em></p>
                    <p><em> For
                            my wife and son, the delusions i take, </em></p>
                    <p><em> Whose
                            love is selfish and passion fake. <br/>
                            <br/>
                            The nectar of your congregation, celestial and clean, </em></p>
                    <p><em> Vanquishes
                            the death, whose fear I preen. </em></p>
                    <p><em> Prays_Bhatt
                            Kirat, humble and meek, <br/>
                            <br/>
                            Guru Ramdas, your protection, I seek. </em></p>
                </blockquote>
