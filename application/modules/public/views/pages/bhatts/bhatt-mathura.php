            <h2>Bhatt Mathura</h2>
            <hr/>
            <p class="black"> Bhatt Mathura is the author of fourteen Swaiyas, out of which seven are in praise of
                Guru Ramdas the 4th Guru and the other seven in praise of Guru Arjun Dev
                the 5th Guru. He was the great admirer of the house of Nanak and had immense
                faith in the Gurus. He was fully aware of the message of Gurbani (utterances
                of the Gurus) and has very intelligently used the typical vocabulary of
                the scriptures while describing the Holy Grace of the Gurus. Being a scholar,
                he composed his Swaiyas in the language easily understood by the masses,
                which in fact is the secret of his popularity. He was a staunch believer
                of the transmigration of soul. He starts with Guru Nanak and traces the
                history of the first five Masters. He goes to the extent of saying that
                God Himself is residing in the form of Guru Arjun Dev and God Almighty embeded
                His own light in the heart of Guru Nanak. He says</p>
