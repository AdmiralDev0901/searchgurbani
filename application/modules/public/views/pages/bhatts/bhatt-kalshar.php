            <h2>Bhatt Kalshar</h2>
            <hr/>

                <p class="black">Kalshar
                    who is also known as Kalh and Talh was the leader of the group of the
                    Bhatts who had come to the congregation of Guru Arjun Dev. Taking into
                    consideration the total contribution of Swaiyas by the Bhatts, which is
                    123, Bhatt Kalshar alone has contributed 54 Swaiyas which is a considerable
                    share. A detailed account of his Swaiyas, eulogising various Gurus is
                    as follows </p>
                <p><strong>SWAIYAS
                        WRITTEN IN PRAISE OF </strong></p>
                <blockquote>
                    <p> 1st
                        Guru - 10 <br/>
                        <br/>
                        2nd Guru - 10 <br/>
                        <br/>
                        3rd Guru - 9 <br/>
                        <br/>
                        4th Guru - 13 <br/>
                        <br/>
                        5th Guru - 12 <br/>
                        <br/>
                        Total 54 </p>
                </blockquote>
                <p style="font-family: Times New Roman;font-size: xlarge">
                    <p><br/>
                        <b>Here are some representative Swajyas of Bhatt Kalshar </b></p>
                    <blockquote>
                        <p><br/>
                            <em>Nanak enjoyed the Rajyog as in -his heart dwelt the Peerless Lord.
                            </em></p>
                        <p><em>He
                                saved the whole world, dwelling upon His Name alone. <br/>
                                <br/>
                                Sanak and Janak sung his praises, coyntless did alike, throughout ages,</em></p>
                        <p><em>&nbsp;Blessed
                                is Guru Nanak, who took Avtar in human form, and came to earth.
                            </em></p>
                        <p><em>Says
                                Kal the poet, &quot;His victory was hailed all over the earth&quot;
                                <br/>
                                <br/>
                                And it echoed in the underworld as well. <br/>
                                <br/>
                                O, Guru Nanak! You are blessed by the Lord, as He lovingly, dwells in
                                you.(6) </em></p>
                        <p><em>During
                                Satyug, you enjoyed the state of Rajyog, </em></p>
                        <p><em>When
                                you overpowered Bali by becoming a dwarf,</em></p>
                        <p><em>&nbsp;During
                                Treta Yug you were named Rama the king of Raghu clan</em></p>
                        <p><em>&nbsp;During
                                Duapar Yug you were known as Krishna who liberated Kansa </em></p>
                        <p><em>You
                                granted the kingdom to Ugrasena and blest your lovers with fearlessness.
