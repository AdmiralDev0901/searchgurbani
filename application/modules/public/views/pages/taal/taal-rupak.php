                    <td><p>Rupak tal is a common seven beat tal in Northern India.</p>
                        <p>Rupak (7/3). It contains seven matras: it is split into three parts, the first part is of the
                            three
                            matras, the next two matras each. The first matra and sam is khali, while the fourth and
                            sixth matra
                            get a tali (clap) each. This tal is like teevra tal is not khali as follows:</p>
                        <p>Matras 1 2 3 4 5 6 7<br/>
                            Words (bol) DHIN DHA TRIK DHIN DHIN DHA TRIK <br/>
                            Theka +0 2 3</p>
                        <p>Some people do the rupak tal in the following way:</p>
                        <p>Matras 1 2 3 4 5 6 7<br/>
                            Words (bol) TIN TIN NA DHIN NA DHIN NA<br/>
                            Theka +0 2 3</p>
                        <p>Rupak tal may be found in almost any musical style. It is found in film songs, kheyal, gazal,
                            instrumental pieces; virtually any north Indian form. The only forms where it is absent are
                            the
                            dhrupad and dhammar styles of singing; it is only absent in these styles because tivra tal
                            serves
                            the same function.</p>
                        <p>Rupak is unique among the tals in that the first beat ( i.e., the sam) is khali (i.e.,
                            represented by
                            a wave of the hand).</p>
                        <p>The theka of rupak is uncomfortably similar to pashtu tal. The only difference is that many
                            musicians
                            prefer to think of pashtu as consisting entirely of claps; while rupak tal begins with a
                            wave of the
                            hand.</p>
                        <p>The form of rupak is shown below:<br/>
                            CLAPPING/ WAVING ARRANGEMENT:wave, 2, 3, clap, 2, clap, 2<br/>
                            NUMBER OF BEATS : 7<br/>
                            THEKA : Tin Tin Naa Dhin Naa Dhin Naa </p>
