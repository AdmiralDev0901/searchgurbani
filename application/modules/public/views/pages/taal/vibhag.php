                    <td><p>The vibhag in north Indian music is somewhat analogous to the Western concept of the measure
                            or bar.
                            However, in Indian music the cycle (avartan) is much more important than the measure. In the
                            old
                            scriptures the vibhag was often referred to as the &quot;ang&quot;.</p>
                        <p>The vibhag may be any number of beats; however two, three, or four-beats are the most common.
                            The
                            vibhags may be put together in any fashion; but the arrangement is always fixed by
                            convention.
                            Therefore, tintal by convention will always be four vibhags of four beats each, ektal will
                            always be
                            six vibhags of two beats each, etc.</p>
                        <p>The vibhags must be designated with either a clap or a wave of the hand. This convention
                            makes
                            communication easier. In the rhythmic notation (tal lipi) any vibhag which is khali is
                            designated by
                            a zero at the beginning. Any other symbol is used to designate a clap or tali. Usually a
                            number is
                            used which corresponds to the clapping. Therefore a &quot;3&quot; at the beginning of a
                            vibhag would
                            indicate that it was the third clap in the cycle, a &quot;2&quot; would indicate the second
                            clap,
                            etc. The sam is the most important and it is designated with a cross, or an
                            &quot;x&quot;.</p>
                        <p><span class="rmenuheader">by David Courtney, Ph.D</span></p></td>
