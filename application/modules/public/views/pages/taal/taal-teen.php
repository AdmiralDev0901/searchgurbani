                    <td><p>Tintal is without any doubt the most common tal in the north Indian classical system of
                            music. It is
                            variously referred to as teental or trital. These names all reflect the three claps of its
                            vibhags
                            structure.</p>
                        <p>The structure of tintal is so amazingly symmetrical that it presents a very simple rhythmic
                            structure
                            against which a performance can be laid. It is composed of four vibhags of four matras each.
                            These
                            vibhags are represented by a clap, clap, wave, and a clap.</p>
                        <p>The structure of tintal is shown below:<br/>
                            <span style="font-weight: bold">CLAPPING/ WAVING ARRANGEMENT</span> :clap, 2, 3, 4, clap, 2,
                            3, 4,
                            wave, 2, 3, 4, clap, 2, 3, 4<br/>
                            <span style="font-weight: bold">NUMBER OF BEATS</span> :16<br/>
                            <span style="font-weight: bold">THEKA:</span><br/>
                            Dhaa Dhin Dhin Dhaa Dhaa Dhin Dhin Dhaa <br/>
                            Dhaa Tin Tin Naa Naa Dhin Dhin Dhaa </p>
