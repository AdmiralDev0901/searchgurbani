                    <td><p> One cycle of the matras of a tal is called avartan. For example, dadra has six matras; as
                            soon as
                            six matras are completed, we have done one avartan (cycle) of dadra tal.</p>
                        <p>The avartan is the cycle in North Indian Music. It is composed of measures <a
                                href="<?php echo base_url(); ?>taal/vibhag">(vibhag</a>) which are in turn composed of
                            beats (<a
                                href="<?php echo base_url(); ?>taal/matra">matra</a>).</p>
                        <p>The avartan is in some ways comparable to the Western cycle (e.g. a 16 bar blues pattern)
                            with but a
                            few differences. One of the biggest differences is that in Western music the measure is
                            considered
                            inviolate, while in North Indian music the cycle is considered inviolate. That is to say
                            that a
                            Western musician would think nothing of establishing a 16 bar pattern, break the pattern for
                            some
                            artistic reason and then reestablish it; however the measures would all be the same.
                            Conversely,
                            Indian musicians typically will mix the measures. For instance jhaptal is four measures of
                            two-beats, three-beats, two-beats, three-beats respectively, however the overall 10 beat
                            pattern may
                            not be altered.</p>
                        <p>Avartans may be of any number of beats. The most common numbers are 16, 14, 12, 10, 8, 7, and
                            6. Most
                            of the music played in Northern India today is in one of these numbers.</p>
