                    <td><p>The bol is an important part of Indian rhythm. The word bol is derived from the word &quot;Bolna&quot;
                            which means &quot;to speak&quot;. It is a series of syllables which correlate to the various
                            strokes
                            of the tabla and are used to define the tal.</p>
                        <p>Mnemonic syllables are used in tabla, mridangam, and pakhawaj. We can therefore assume that
                            this
                            custom must be many centuries old. Otherwise it would not have such a wide distribution.</p>
                        <p>There is a difference in the way that north Indians and south Indians view these syllables.
                            In the
                            north (Hindustani sangeet) the tal is actually defined by the bol while in the south
                            (Carnatic
                            Sangeet) it is merely a mnemonic aid to the musician.</p>
                        <p>There are numerous example of how north Indian musicians use the bol to define the tal. The
                            case of
                            Tintal is a good example. It has the bols:<br/>
                        </p>
                        <p><span style="font-weight: bold">XDha Dhin Dhin Dha | 2Dha Dhin Dhin Dha | 0Dha Tin Tin Na | 3Na Dhin Dhin Dha |</span><br/>
                        </p>
                        <p>There are other tals which have the same patterns of claps and waves as tintal, but they are
                            considered separate tals because the bol is different.</p>
                        <p>The situation is somewhat different in the south. South Indian tals are defined by the
                            clapping and
                            waving and the syllables are merely technical mnemonics. In south Indian music it is not
                            normal to
                            have different tals sharing the same clapping / waving patterns.</p>
                        <p>It is very common for people to actually equate the bol with the strokes themselvse, however
                            there
                            are differences which make this difficult. For instance there are differences between the
                            bols of
                            the pakhawaj and those of the tabla. One may even find subtle differences between one school
                            of
                            tabla (gharana) and another. The end result is that the bol must ultimately be seen as a
                            mere
                            description of the technique rather than an iron clad prescription.</p>
                        <p><span class="rmenuheader">by David Courtney, Ph.D</span></p></td>
