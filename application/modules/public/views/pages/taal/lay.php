                    <td><p>Lay is the tempo, or speed of a piece. The Hindi term for tempo is &quot;lay&quot; and is
                            derived
                            from the Sanskrit term &quot;laya&quot;. It is a very simple concept, but its application is
                            sometimes complicated.</p>
                        <p><span>It goes without saying that there have to be some practical limit to usable tempi.  One beat every ten minutes would be so slow as to be musically useless.  At the other end of the spectrum we can see that 100 beats per second would be so fast that it would be perceived as a tone and not as a rhythm.  A general breakdown of Indian lay is shown in the following table:<br/>
        </span></p>
