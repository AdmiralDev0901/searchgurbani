                        <p>The table is an idealized breakdown of lay; however, the real world is considerably more
                            complex. For
                            example the designations of ati drut, ati vilambit, etc. are seldom heard among practicing
                            musicians. This tends to stretch the previous table so that there is no longer a 2-1
                            relationship
                            between the various designations. To make matters even more complex, it has been observed
                            that
                            vocalists use a slower definition of time than instrumentalists (Gottlieb 1977a:41).
                            Furthermore the
                            rhythmic concepts of the light and film musicians run at a higher tempo but show a peculiar
                            compression of scale.</p>
                        <p>The tempo of the rhythm or the duration of pace or speed is called laya. It is regular
                            spacing of
                            time. Laya is three kinds: vilambit, madhya and drut.<br/>
                            Vilambit laya: Slow tempo of the rhythm is called vilambit lays. Each beat lasts for about
                            one
                            second. For example, dadra has six matras. Instead of counting six matras (beats) one after
                            the
                            other like 1, 2, 3, 4, 5, 6 vilambit laya will prolong it in the following manner.</p>
                        <p>Vilambit laya ---ISS --- ISS ---ISS ---ISS ---ISS ---ISS<br/>
                            Matra 1 2 3 4 5 6<br/>
                            + (Tali) 0(Khali)<br/>
                            (S stands for prolongation of rhythm)<br/>
                            The following are some of the tals of vilambit lays: ektal, chartal, jhumra , tilwara.<br/>
                            Madhya laya: Medium tempo of the rhythm is called madhya laya. It can be compared to the
                            ticking of
                            about half second of the clock. Some of the tals of madhya lays are teental, jhaptal, dadra,
                            kehrva.<br/>
                            Drut laya: is doubly quicker in tempo than the madhya laya. The tals of drut laya are the
                            same as in
                            madhya tal, the difference being that they are done quicker. Each beat lasts for about
                            one-quarter
                            second. Tarana and chota khayal use drut laya.</p>
                        <p><span style="font-weight: bold">Other Kinds of Laya</span><br/>
                            There are other kinds of laya (tempo) in addition to vilambit, madhya, and drut laya
                            mentioned. They
                            give great joy to the listeners, especially in dhrupad, dhamar, and in instrumental music.
                            These are
                            as follows:<br/>
                            dugan (1/2), tigan (1/3), chaugan (1/4), aar (2/3), kunvaar (7/4), athagan (1/8) </p>
                        <p>Some of the layas which are popular are explained below:<br/>
                            Thhah Laya: In this laya, each anka takes one matra. It is also called Brabar Laya.<br/>
                            Dugan: In this laya there are two parts or anka in one matra:</p>
                        <p>1 2 3<br/>
                            1.2 3.4 5.6<br/>
                            Tigar: In this laya, there are three parts (anka) in one matra:</p>
                        <p>1 2 3 4<br/>
                            123 456 789 10 11 12<br/>
                            Chungan: In this laya, there are four parts (anka) in one matra:</p>
                        <p>1 2 3 4<br/>
                            1234 5678 9 10 11 12 13 14 15 16<br/>
                            Aar: In this laya, two matras are split into three matras. First the matras are divided into
                            three
                            parts and each is doubled up. &ldquo;S&rdquo; is used to complete the sequence:</p>
                        <p>1 S S 2 S S 3 S S 4 S S <br/>
                            Kunvaar: In this laya, five matras are split up into four parts. First the matras are
                            divided into
                            fourth parts and then grouped into five each.</p>
                        <p>1 S S S 2 S S S S 3 S S S 4 S S S 5 S S S<br/>
                            Beaar laya and athagam lays are not common and hence are omitted.</p>
                        <p>The lay or tempo usually changes throughout the performance. These changes in tempo are
                            inextricably
                            linked to the various musical styles. In general we can say that only very short pieces will
                            maintain a fairly steady pace. Most styles will start at one tempo and then increase in
                            speed</p>
