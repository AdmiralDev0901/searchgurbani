            <td><p>The clap of the hands is an important part of both the science and practice of North Indian music. It
                    has a hoary past. An elaborate system of clapping and hand movements is mentioned in the Natya
                    Shastra (circa 200 bc) where it is part of the system of timekeeping known as &quot;kriya&quot;.</p>
                <p>Besides the sam, the matras on which the time-beat falls is called tali (clapping). In the table on
                    this page the first tali shown by a scross and the remaining talis are indicated by numerals.</p>
                <p>The clap of the hands is very important for the conceptualization of Indian rhythms. North Indian
                    musicians use the claps to designate the measures (<a
                        href="<?php echo base_url(); ?>taal/vibhag.php">vibhag</a>) which are highly stressed. The most
                    stressed measure is the beginning of the cycle. This is called the &quot;<a
                        href="<?php echo base_url(); ?>taal/sam.php">sam</a>&quot;, and is the most important beat in
                    the whole cycle.</p>
                <p>The clapping of hands is also of great practical importance. It is a convenient means for the singers
                    and other musicians to communicate with the<a href="<?php echo base_url(); ?>taal/tabla.php">
                        tabla</a> player (tabalji) without having to break the performance.</p>
                <p>The clapping must not be taken only into itself because it exits along with its compliment the wave.
                    This wave or &quot;<a href="<?php echo base_url(); ?>taal/khali.php">khali</a>&quot; is also
                    important in designating the <a href="<?php echo base_url(); ?>taal/vibhag.php">measures</a></p>
                <p class="rmenuheader">by David Courtney, Ph.D</p></td>
