<br/><h2>Sam: The first beat </h2>
<hr/>
<div class="content">
    <table width="98%" border="0" align="center" cellpadding="2" cellspacing="2">
        <tr>
            <td><p>The matra for which a tal begins is called the sam or gur. It is the first beat of the tal. There is
                    an emphasis on the sam, by which it is recognises from the other matras. Some musicians indicated
                    the sam either by a shake of the head or a beat on the knee. It is like the pivot, and all the
                    rhythmic variations must close on the sam.</p>
                <p>The sam is the first beat of the cycle in the north Indian system of tal. The word sam literally
                    means to &quot;conjoin&quot; or &quot;come together&quot;. The sam has a special significance in
                    both the performance and theory of North Indian music.</p>
                <p>One of the main functions of the sam is to establish a point of resolution. Although improvisations
                    and fixed compositions may begin almost anywhere in the cycle (avartan) they almost always resolve
                    on the sam.</p>
                <p>The sam is also a pivotal point. Typically the tabla player keeps time by playing theka and the main
                    musician is free to improvise. This however would be boring if that was the only thing that happens;
                    therefore it is common to trade places. During this, the main musician keeps time by playing the
                    theme (gat or sthai) over and over. This allows the tabla player to take off and improvise. After a
                    period the roles reverse again. The sam is important because it is pivotal to this transition.</p>
                <p>The sam is so important that it has its own notational symbol. In the Bhatkhande system of notation,
                    it is noted with cross such as an &quot;x&quot; or an &quot;+&quot;.</p>
                <p>The sam is almost always a clap of the hands (tali). There is only one exception and that is the case
                    of rupak tal. This lone exception designates the sam with a wave of the hands (khali).</p>
                <p><span class="rmenuheader">by David Courtney, Ph.D</span></p></td>
