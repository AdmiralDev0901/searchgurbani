            <td><p>Dadra tal is six or three beat tal which is extremely common in the the lighter forms of music. It is
                    is commonly found in qawwalis, film songs, bhajans, gazals, and folk music throughout India.</p>
                <p>The name is derived from its association with the dadra style of singing. This is a semiclassical
                    form that is somewhat similar to thumri. The dadra style of singing in turn, derives its name from
                    the place where it began.</p>
                <p>There are a number of reasons for Dadra's extreme popularity. One reason is the ease in performing in
                    three and six beats; it is very symmetrical and posses no great challenge. Another reason for it
                    being so common lies in the Indian taxonomy of tals. Virtually any tal of three, six, and 12-matras
                    of folk origins, is routinely lumped under the title of Dadra. Even though they may have no cultural
                    connections, traditional Indian musicology considers them to be the same tal. Therefore, the large
                    number of musical tributaries contributes greatly to the variety of prakars, its popularity, and the
                    geographical distribution of Dadra.</p>
                <p>The &quot;textbook case&quot; is simple. It is a six-beat tal that is divided into two vibhags of
                    three matras each. The first vibhag is clapped and the second vibhag iswaved.</p>
                <p>Dadra may be played in a variety of tempos. It may be heard anywhere from moderately slow to
                    extremely fast speeds. Only the extremely slow (vilambit) performances are conspicuously absent.</p>
                <p> CLAPPING/ WAVING ARRANGEMENT: clap, 2, 3, wave, 2, 3<br/>
                    NUMBER OF BEATS : 6<br/>
                    THEKA : Dhaa Dhin Naa Dhaa Tin Naa </p>
                <p>Dadra (6/2). It contains six matras, split into two parts, each of three matras. In this first part,
                    there is one tali (hand clap), and in second part, there is one khali (empty). The first matrahas
                    sam, while the fourth matra is khali (empty) as under:</p>
                <p>Matras 1 2 3 4 5 6<br/>
                    Words DHA DHI NA TI TI NA<br/>
                    Theka + 0</p>
