            <td><p>After the sam, the next in importance is khali, meaning &ldquo;empty&rdquo; or &ldquo;blank&rdquo;.
                    At the khali, the beat falls on the right tabala, while the left tabla is empty. The symbol for
                    khali is zero (0). Generally khali marks the commencement of the second half of the cycle
                    (avartan).</p>
                <p>The word &quot;khali&quot; literally means empty. However, in the field of north Indian music it has
                    a special significance. Here the word implies a wave of the hand. This wave of the hand, along with
                    its counterpart, the clap of the hand, forms the basis for timekeeping in north India. This system
                    of timekeeping is known as &quot;kriya.&quot;</p>
                <p>The wave of the hand is used to designate the first beats of measures which are only moderately
                    stresses. Therefore, one almost never finds the khali applied to strongly stressed beats like the
                    &quot;sam&quot; (the first beat of the cycle).</p>
                <p>The khali is especially important in symmetrical metres such as tintal of 16, beats or dadra of 6
                    beats. For such symmetrical tals the khali is indispensable for correct orientation. For example, if
                    there were no khali, tintal would be a confusing string of four beat measures and it would be very
                    difficult to find the beginning of the cycle. Therefore, the khali may be thought of as an
                    index.</p>
                <p>The khali, along with the tali, form a convenient method by which vocalists may communicate with the
                    tabla player (tabalji, or tabliya) without halting the performance. This allows communication
                    between the artists to continue during the performance without a break in the musical flow.</p>
                <p><span class="rmenuheader">by David Courtney, Ph.D</span></p></td>
