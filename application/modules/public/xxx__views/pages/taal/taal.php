            <td><p class="blue"><b>Tal,</b> (variously transliterated as &quot;tala&quot;, &quot;taal&quot; or &quot;taala&quot;)
                    is the Indian system of rhythm. It has been argued that rhythm is fundamental to the creation of any
                    musical system. Certainly from a historic standpoint, rhythm existed many centuries before the word
                    rag was ever used. Given this historical preeminence, it is not surprising that rhythm occupies an
                    important position in the Indian system of music.</p>

                <strong><p> What symmetry is to the plastic arts, rhythm is to music.<br/>
                        (Schopenhauer)</p></strong>

                <p>Just as the &quot;note&quot; is the basis of the melodic compogs:nent of music, the bol (pronounced
                    bowl) is the foundation for taal. Bol literally means speech or syllables. The vocal bols sound very
                    similar to bols played on the percussive instrument. The most common tabla bols are Dha, Dhi/Dhin,
                    Ti/Tin, Ra, Ki, Ta, Na, Tin, and Te. Different schools of percussion may pronounce the same bol
                    differently. Several bols structured in a specific manner and arranged in sub-divisions are called
                    thekas. </p>
                <p>Each bol usually takes up one, halt or quarter of a beat (matra) in a theka. The first beat of a
                    theka is called the sam (pronounced sum). It plays a crucial role in the improvisation structure
                    during a recital -- since it becomes a point of convergence for both the melodic and percussive
                    improvisation. A theka also consists of layers of accents or voids in the first beat of a
                    sub-division. A degree of symmetry, with an elegant manner of the theka leading to the sam, is quite
                    common in the arrangement of the bols in a theka. A theka (also referred to as tool) can
                    theoretically contain between two and 108 beats, although in reality there is no limit. While bols
                    have existed in the percussion repertoire for a long time, thekas are probably a recent phenomenon
                    (perhaps only around 600 years old ) The commonly heard thekas are dadra (6 beats), roopak (7
                    beats), keherwa (8 beats), jhaptaal (10 beats), ektaal (12 beats), chautal (12 beats), dhamar,
                    deepchandi, jhumra (all 14 beats but with different bols and sub-divisions), and teentaal (16
                    beats). Although thekas are usually standard, bols of thekas can vary slightly, depending on the
                    musical school or individual style of the tabla player.</p>
                <p>A raga is totally dependent on tal (pronounced taal). Vocal music, instrumental music and dance rely
                    on rhythm for its effect on the audience. Tal/tala [1] is the means of measurement of time in music
                    or dance. Rhythm is the breaking up of time in small units. Time is cut into pieces at certain
                    regular intervals. Literally tal means the palm of the hand; the time is measured by the clapping of
                    hands (tali) or beats of drums or sticks. Tal is divided into two halves; Bhari (full) starting with
                    sam, and khali (empty) starting with khali. So tal is an organisation of rhythms or different beats
                    in certain groupings which are smaller units of matras. These rhythmic units repeat themselves in
                    cycles. The drummer has to produce the spoken syllable indicating the position of the hand on the
                    drum. </p>
                <p>Tala (also written as (&lsquo;&rsquo;Tal&rsquo;&rsquo;) in Indian music and Gurbani Kirtan refers to
                    a complete and complex system for the execution and transcription of Rhythms and Beats. There exist
                    over 20 different &lsquo;&rsquo;Talas&rsquo;&rsquo; or &lsquo;Beat Patterns&rsquo;. The most common
                    Tala in Classical Indian Music is the Theen Tala. This beat has a cycle of 16 beats divided in 4
                    sectors. Sectors 1,2 and 4 are full while sector 3 is empty. These beat patterns can also be played
                    at different speeds. </p>
                <p>A taal does not have a fixed tempo and can be played at different speeds. In Hindustani classical
                    music a typical recital of a raga falls into two or three parts categorized by the tempo of the
                    music - Vilambit laya (Slow tempo), Madhya laya (Medium tempo) and Drut laya (Fast tempo). In
                    Carnatic Music, there are five categories of tempo namely - Chauka (1 stroke per beat), Vilamba (2
                    strokes per beat), Madhyama(4 beats per beat), Dhuridha(8 strokes per beat), Adi-Dhuridha(16 strokes
                    per beat). But, although the tempo changes, the fundamental rhythm does not.</p>
                <p>Each repeated cycle of a taal is called an avartan. A tala is generally divided into sections
                    (vibhaags), not all of which may have the same number of beats.</p>
                <p>The word tal. Tal literally means &quot;clap&quot;. Today, the <a href="tabla">tabla </a>has replaced
                    the clap in the performance, but the term still reflects the origin. </p>
                <p class="blue"><b>The basic concepts of tal are:<a href="<?php echo base_url(); ?>taal/tali"> tali</a>
                        or bhari,<a href="<?php echo base_url(); ?>taal/khali"> khali</a>, <a
