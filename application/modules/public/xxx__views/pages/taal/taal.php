                <h3>The common taals in Hindustani classical music Are:</h3>
                <p><strong>Dadra - 6 matras</strong><br>
                </p>
                <p>X                    0<br>
                    dhin dhin na | dha thun na<br>
                    Many variations of this theka exist, some going by the name khemta.</p>
                <p><strong>Rupak (7 matras)</strong></p>
                <p><br>
                    0             1             2<br>
                    tin tin na | dhin na | dhin na</p>
                <p><br>
                    <strong>Tivraa - 7 matras</strong><br>
                    (khula bols)</p>
                <p>X               2             3<br>
                    Dha din ta | tite kata | gadi ghene</p>
                <p>OR</p>
                <p>X                        2         3<br>
                    Dha dhere naga | kat ta | dhere naga </p>
                <p><strong>Kaharvaa - 8 matras</strong></p>
                <p>X                    0<br>
                    Dha ge na tin | na ka dhi na</p>
                <p>Kaharvaa has countless variations, including dhumaali, "bhajani", and qawwali <br>
                    etc. It is often counted as 4<br>
                    matras.</p>
                <p><br>
                    <strong>Jhaptaal - 10 matras</strong></p>
                <p>X            2                  0         3<br>
                    Dhin na | dhin dhin na | tin na | dhin dhin na</p>
                <p><br>
                    <strong>Ektaal 12 Matras</strong></p>
                <p>X              0             2        0          3                      4<br>
                    Dhin dhin | dha dha | tu na | kat ta | dhage terekite | dhin dhage </p>
                <p><br>
                    <strong>Chautaal - 12 matras</strong> <br>
                    (khula bols)</p>
                <p>X etc. like Ektaal<br>
                    Dha dha | din ta | kat tage | din ta | tete kata | gadi gene</p>
                <p>Ara Chautaal - 14 matras<br>
                    X 0 2 0 3 4 5<br>
                    Dhin terekite | dhin na | tu na | kat ta | terekite dhin | na dhin |dhin na</p>
                <p><br>
                    <strong>Dhamaar - 14 matras</strong><br>
                    (khula bols)</p>
                <p>X 2 0 3<br>
                    Ka dhe te dhe te | dha - | ga te te | te te ta -</p>
                <p>OR</p>
                <p>Ka dhe te dhe te | dha - | ga di na | di na ta -</p>
                <p><br>
                    <strong>Tintal - 16 matras</strong></p>
                <p><br>
                    X 2 0 3<br>
                    Dha dhin dhin dha | dha dhin dhin dha | dha tin tin ta | tete dhin dhin dha</p>
                <p>The theka becomes very ornamented in slow speeds. The "tete" of 13th matra can <br>
                    also be played "ta" or "tre<br>
                    kre". "ta" is often pronounced "na" for euphony. Strictly speaking, "terekite" <br>
                    should never be substituted for<br>
                    "dhin" of 2nd matra as this brings in the unwanted shadow of tilwara tal.</p>
                <p><br>
                    <strong>Sitaarkhaani - 16 matras</strong></p>
                <p>Vibhags as for tintal<br>
                    Dha gedhin -ge dha | dha gedhin -ge dha | dha getin -ke ta | ta kedhin-ge dha</p>
                <p><br>
                    <strong>Matta taal - 18 matras</strong></p>
                <p>X 0 2 0<br>
                    Dhin terekite | dhin na | ti--kre tina | terekite tina | kena dhidhi|<br>
                    4 5 6 0<br>
                    nadhi dhina | dhina gadha | terekite dhina | gadha terekite </p>
                <p><br>
                    <strong>Tivra - 7 beats</strong><br>
                    (khula bols)</p>
                <p>X 2 3<br>
                    Dha din ta | tete kata | gadi ghene</p>
                <p>OR</p>
                <p>Dha gere naga | ga di | gere naga<br>
                    X            2                          0                  3<br>
                    Dhin na | dhi--kre dhidhi na | ti--kre titi na | dhina gadha terekite</p>
                <p><br>
                    <strong>Dipchandi - 14 matras</strong></p>
                <p>X                2                   0          3<br>
                    Dha dhin - | dha dha tin - | ta tin - | dha dha dhin -</p>
                <p>The last four matras can be played "dha dha dhin dhin". Some tabla players put <br>
                    "na" in the gaps.</p>
                <p><br>
                    <strong>Sultaal - 10 beats</strong><br>
                    (khula bols)</p>
                <p>X             0         2             3              0<br>
                    Dha dha | din ta | tete dha | tete kata | gadi gene</p>
                <p><br>
                    <strong>Jhumra tal - 14 beats</strong></p>
                <p>X                           2                                   0               <br>
                    3</p>
                <p>Dhin -dha terekite | dhin dhin dhage terekite | tin -ta terekite | dhin dhin <br>
                    dhage terekite</p>
                <p><br>
                    <strong>Tilwara - 16 matras</strong></p>
                <p>X                                 2                        0<br>
                    Dha terekite dhin dhin | dha dhage tin tin | ta terekite dhin dhin | dha dhage <br>
                    dhin dhin<br>
                    Used for vilambit vocal, a variant of tintal.</p>
                <p><br>
                    <strong>Upatal Jhampak (also known as Roopam) - 8 1/2 matras</strong></p>
                <p>X            2                  0         3<br>
                    Dhin na | dhin dhin na | tin na | dhidhi na/2 | </p>
                <p><strong>Division Of Beats:</strong><br>
                    <br>
                    Tintal 16 beats divided 4-4-4-4    <br>
                    Dhamar tal 14 beats divided 5-2-3-4    <br>
                    Ektal and Chautal 12 beats divided 2-2-2-2-2-2    <br>
                    Jhaptal 10 beats divided 2-3-2-3    <br>
                    Kaharva tal 8 beats divided 4-4    <br>
                    Rupak tal 7 beats divided 3-2-2    <br>
                    Dadra tal 6 beats divided 3-3    <br>
                    Adha-Chautal  14 beats divided 2-4-4-4.  </p>


