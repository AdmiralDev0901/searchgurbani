                            href="<?php echo base_url(); ?>taal/avartan"> avartan</a>.</b></p>
                <p><b><a href="<?php echo base_url(); ?>taal/tali">Tali </a></b>- Tali is the pattern of clapping. Each
                    tal is characterized by a particular pattern and number of claps.</p>
                <p><b><a href="<?php echo base_url(); ?>taal/khali">Khali</a></b> - Khali is the wave of the hands.
                    These have a characteristic relationship to the claps.</p>
                <p><b><a href="<?php echo base_url(); ?>taal/vibhag">Vibhag (Ang)</a></b> - Vibhag is the measure. Each
                    clap or wave specifies a particular section or measure. These measures may be of any number of
                    beats, yet most commonly 2, 3, 4, or 5 beats are used.</p>
                <p><b><a href="<?php echo base_url(); ?>taal/matra">Matra</a></b> - Matra is the beat. It may be
                    subdivided if required.</p>
                <p><b><a href="<?php echo base_url(); ?>taal/bol">Bol </a></b>- Bol is the mnemonic system where each
                    stroke of the drum has a syllable attached to it. These syllables are known as bol. It is common to
                    consider the bol to be synonymous to the stroke itself.</p>
                <p><b><a href="<?php echo base_url(); ?>taal/theka">Theka </a></b>- Theka is a conventionally
                    established pattern of bols and vibhag (tali, khali) which define the tal.</p>
                <p><b><a href="<?php echo base_url(); ?>taal/lay">Lay </a></b>- Laya is the tempo. The tempo may be
                    either slow (vilambit), medium (madhya), or fast (drut). Additionally ultra-slow may be referred to
                    as ati-vilambit or ultra-fast may be referred to as ati-drut.</p>
                <p><b><a href="<?php echo base_url(); ?>taal/sam">Sam </a></b>- Sam is the biginning of the cycle. The
                    first beat of any cycle is usually stressed.</p>
                <p><b><a href="<?php echo base_url(); ?>taal/avartan">Avartan </a></b>- Avartan is the basic cycle.</p>
