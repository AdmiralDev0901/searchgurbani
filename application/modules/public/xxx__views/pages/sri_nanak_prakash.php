<h2 align="center"><strong>Sri Nanak Prakash</strong></h2>
<hr/>
<div>
    <p class="blue">&lsquo;Sri Gur Partap Suraj Granth&rsquo; authored by &lsquo;Kavi Churamani&rsquo; Bhai Santokh
        Singh ji, Doyen of Nirmala Sect, popularly known as &lsquo;Suraj Parkash&rsquo; is a voluminous classical
        medieval source of Sikh History and Philosophy. Its &lsquo;Katha&rsquo; (Religious Discourse) in almost all
        prominent Gurdwaras is the gauge of its authenticity and popularity among the Sikh masses. In verse, Bhai Sahib&rsquo;s
        great classic writing &lsquo;Sri Nanak Parkash&rsquo; is the first part of Sri Guru Partap Suraj Granth in which
        detailed history of Sri Guru Nanak Dev ji is highlighted. </p>
    <p class="blue">Bhai Santokh Singh's monumental work in Braj verse portraying in comprehensive detail the lives of
        the Ten Gurus of the Sikh faith and the career of Banda Singh Bahadur. Besides being an historical narrative of
        great significance, it is an outstanding creation in the style epic, and is the most voluminous of all poetic
        compositions in Hindi/Punjabi literature. Its language is Braj Bhasa which was the literary Hindi of that time
        though its script is Gurmukhi. Notwithstanding certain drawbacks which scholars with training in modern
        historiography may point out, it remains the most valuable source book on Sikh history of the period of the
        Gurus and, indeed, on the very roots of the entire Sikh tradition. For the massive flow of its poetry, the vast
        range of its figures and images and for the abundance of detail, Sri Gur Pratap Suraj Granth, Suraj Prakash in
        shorter, popular form, is worthy to rank with the classics in this genre.</p>
</div>
