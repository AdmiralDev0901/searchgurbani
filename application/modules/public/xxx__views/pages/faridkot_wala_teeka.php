<hr/>
<p class="subhead">Faridkot Wala Teeka </p>
<p class="blue"> Faridkot Wala Teeka is classical exegesis of Sri Guru Granth Sahib in &lsquo;Braj Bhasha&rsquo; by a
    team of scholars of Nirmala Sect. It was the first attempt in this field prompted by Rulers of Faridkot State in 19
    th century. For all future attempts in this field, it became an ideal prototype. As it was patronized by the Rulers
    of Faridkot State, it came to be known as &lsquo;Faridkot Wala Teeka&rsquo;. </p>
<p class="blue"> Teekas (commentaries) on Guru Granth Sahib have been written in one form or other ever since the
    compilation of Guru Granth Sahib. However, the first formal Teeka in line with the traditional interpretation of
    Sikh scriptures was written by Sant Giani Badan Singh Ji of Dera Sekhwan at the request and encouragement of
    Maharaja Bikram Singh of Faridkot. It took him six and a half years to complete it. It was completed in 1883. This
    Teeka was reviewed by a committee appointed by Mahant Shamer Singh of Patna. After incorporating the comments of
    this committee, the first edition of this Teeka was published [funded] by the Maharaja Balvir Singh of Faridkot in
    1906 which was printed by the Wazir Hind Press (started by Bhai Vir Singh) at Amritsar. The second edition of this
    Teeka was published by Maharaja Harinder Singh of Faridkot in 1928. This Teeka is known as the &quot;Faridkoti
    Teeka.&quot; </p>
<p class="blue"> Pandit Tara Singh Narotam, the contemporary of the author of Faridkoti Teeka, also started the Teeka,
    but he died when he finished it up to Basant Rag. Then some people borrowed it from his heir just to review the
    draft, but never returned it. Only the Teeka of Sri Rag is available from his draft which is in the possesion of
    very few researchers and scholars at this time. It is not generally available.</p>
<p class="red"><em>- Ref. Gurmat Sahit Vivechan (Punjabi, published by Punjab Languages Department). This is an
        extremely useful book on the history of Gurmat literature. It contains 25 essays by top-notch scholars.</em></p>
