            a) Mathura, Jalap and Kirat were sons of Bhika. <br/>
            <br/>
            b) Salh and Bhalh were sons of Sokha. <br/>
            <br/>
            c) gal was son of Tokha. <br/>
            <br/>
            d) Haribans was son of Gokha. <br/>
            <br/>
            e) Kalshar and Gyand were sons of Chokha. </p>
        The above mentioned nine Bhatts, combined with Bhikha and Nal make a group
        of eleven, who came to the congregation at Amritsar during the pontification
        of the 5th Guru. In this connection, it is very important to note that earlier
        Bhatt Bhikha had visited Goindwal and paid a tribute to Guru Amardas, in
        a Swaiya, which is very important and has since assumed the status of an
        historical document. It is a very pungent comment on the prevalent deteriorated
        state of religion in India and the dubious position of clergy. Indirectly
        it also establishes the fact that popularity of Sikh philosophy and the
        credibility of the house of Nanak had already spread far and wide. Bhikha
        says
        <blockquote>
            <font size="1">
                <p><br/>
                    I wandered all over the place <br/>
                    Searching for a hermit with Heavenly Grace. <br/>
                    I met many a mendicant, high flying on the ego fleet<br/>
                    Soft-spoken, polished, vainful and sweet.<br/>
                    In vain I wasted all my time.<br/>
                    &nbsp;None of them was spiritually fine.<br/>
                    &nbsp;Like an empty vessel or hollow pot, <br/>
                    They talked a lot, all but rot. <br/>
                    God in their hands was just a pawn <br/>
                    Shady deals were in their clan <br/>
                    At last I reached the rightful place, <br/>
                    With The Guru Amardas I found solace. <br/>
                    <br/>
                    <span class="rmenuheader">Adi Granth p.1395</p>

                <p><br/>
                    Bhatt Bani is a very important chapter of Adi Granth. Is it important
                    because it is an eulogy to the Gurus or that'it contains social, economic
                    and political events of the era, significant from historical point of
                    view? No. Its significance lies in its relevant worth, for it has glaring
                    clash of ideology. It is a well known fact that Sikhism does not believe
                    in Avtarvad (incarnations) and in the entire Guru Bani, no where any credence
                    has been given to it, except Bhatt Bani. It must be remembered that the
                    composers of Bhatt Bani were all Vaishnavites, followers of Sri Rama and
                    Sri Krishna, who strictly believed in incarnation but at that time they
                    were still in search of spiritual attainment. This fact is evident from
                    the Swaiya of Bhatt Bhikha as quoted above, which highlights their search
                    in this realm. </p>
                Let us consider it from another angle. After the
                Bhatts were able to establish contact with Guru Arjun Dev, they attended
                the congregations personally
                and enjoyed the blissful Kirtan sung in the' Sangat, their belief in the
                house of Nanak became more resolute. Here they got set for the spiritual
                journey under the expert guidance of Guru Arjun Dev. Now came the time
                of
                writing the Swaiyas, they expressed their thoughts with all the sincerity
                at their command. They wrote in the light of their Pauranic knowledge,
                mythological
                background mingled with the new spiritual experience that they gained in
                the Guru's court. It was here that they saw the grandeur and sublimity,
                the concept of Spiritual Temporal authority (Miri and Piri), together.
                Through
                their angle of vision, they saw the Gurus as incarnations of Vishnu, but
                they went to the extent of calling Guru Nanak as personified Bhagwan (God
                himself). This being a very neologistic expression in the Sikh exegesis,
                became extraordinarily conspicuous. One must remember that the Sikh ethos
                does not permit such expressions, which at the most may be termed as vagary
                or the poetic exaggeration. It is worth noting that the Gurus never called
                themselves Bhagwan and never liked to be addressed as such. Guru Gobind
                Singh went to the exted calling himself as the humblest servant of God
                (Akal
                Purakh). He says in autobiography : &quot;I am the humblest servant of God.
                Whosoever calls i Parmeshwar, shall be condemned to Hell&quot;.
                <blockquote><br/>
                    Main hoon Param Purakh ka Dasa<br/>
                    &nbsp;Dekhan aayon jagat tamasha <br/>
                    Jo ham ko Parmeshwar uncharhen, <br/>
                    Te sab Narak Kund men parhen
                    <p> -Guru Gobind Singh (Dasham Granth)</p>
                </blockquote>

                <p><br/>
                    The Bhatts loved the Gurus, treated them as revered Avtars and the accepted
                    continuity of the same light in each one of them. According to them, it
                    wa the same spirit which started with Guru Nanak and was passed on to
                    the success Gurus. Their forms might have been different, but the spirit
                    was the same. A important point which crops up at this stage is that,
                    even though the Sikh culture accepts oneness of the spirit of the Gurus,
                    the Bhatts saw Guru Nanak as the incarnation of Vishnu and the successor
                    Gurus as the incarnation of Guru Nanak. The point which makes it more
                    significant is that it was for the first time that this recognition was
                    recorded in Sikh literature and the full credit goes to Bhatts. Moreover,
                    the theory of incarnation (Avtarvad) as accepted and narrated by the Bhatts
                    is different from what has been the conceptual attitude recorded in Gur
                    Bani. Here we would like to quote the eminent Sikh scholar, Bhai Santokh
                    Singb, who is considered an authority on the history of the Gurus. He
                    has mentioned in his work Suraj Prakash that Bhatts were the incarnations
                    of Vedas. As stated earlier, the , same expression has been repeated by
                    Bhai Kahan Singh Nabha in his Mahan Kosh. Mr. Trumpp has also accepted
                    this version. </p>
                <p><br/>
                    If we consider the Bhatt Bani, vis-a-vis Guru Bani, we find a marked change
                    and deviation from the utterances of the Gurus. While the Gurus in general
                    condemned the Avtarvad, the Bhatts were staunch followers of this tradition
                    and wrote their poetry with this very knowledge and belief, derived from
                    Hindu mythology and Puranic definitions. This is the root point which
                    distinguishes the Bhatt Bani from Guru Bani. The marked difference makes
                    the Bhatt Bani as an appendage to Adi Granth. Of late there have been
                    some voices of dissent asking for disintegration of Bhatt Bani from Guru
                    Granth Sahib but these voices have died down now. On the other hand those
                    who do not want disintegration consider the very idea as sacrilege. </p>
                No doubt, Bhatt Bani was written as eulogy to the Gurus, yet analysing
                it with the diversity of its underlying theme and the undertone atmosphere,
                it may be concluded that the compiler of Adi Granth was much above the
                parochial and sectarian approach. He was radical, open-minded and gave
                liberal space in the Holy Book, to the poetry which could be termed as
                diagonally opposite to the convictions of the Gurus.</div>

