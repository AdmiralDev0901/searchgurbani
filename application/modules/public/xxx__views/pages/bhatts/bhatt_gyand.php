<br/><h2>Bhatt Gyand</h2>
<hr/>
<div class="content">
    <p>Bhatt
        Gyand had composed thirteen Swaiyas, all in praise of Guru Ramdas the
        4th Guru. He starts with Guru Nanak and says that the Spirit of Lord Krishna
        had taken a new form and was incarnated in Guru Nanak Dev. He calls him
        an incarnation of Lord Vishnu and according to him, the same spirit is
        transformed from one Guru to the other. According to him this process
        is like a candle, which carries the chain of light to the other candle
        just with its touch. Bhatt Gyand is important because it is he who wrote
        for the first time about the continuity of the spirit and transmigration
        of soul, from 1st. Guru onwards. Another speciality of Bhatt.Gyand is
        that he used the word 'WAHEGURU' for the first time in Sikh poetry.
    </p>
    <p>It
        is worth mentioning that prior to Bhatt Gyand, the word, 'Waheguru' does
        not seem to have been used in the Sikh scriptures. The credit goes to
        him for introducing the maiden impression which later on became the motif
        or the mainstay of Sikh ethos. </p>
    <p><br/>
        <b>Specimen of the poetry of Bhatt Gyand </b><br/>
        &nbsp;</p>
    <p><em> Hail be
            the Guru, the wonderful and the beautiful Lord, Lotus-eyed, sweet-tongued,
            accompanied by a host of friends, To whom mother Yashodha fed with curd
            and rice. <br/>
            <br/>
            Whose wondrous beauty, always bewitched the mother. When he was away at
            play, bells of his belt, twinkled And their silvery sound enchanted her.
            <br/>
            <br/>
            He was the one who held the pen of destiny in his hand And was the master
            of an overpowering command. <br/>
            <br/>
            Even Shiva and Brahma cherished his wisdom and poise in their heart. He
            is the True, Everlasting and the Primal Being. <br/>
            <br/>
            Hail be the Guru, the wonderful and the beautiful Lord. (1-6) </em><br/>
    </p>

