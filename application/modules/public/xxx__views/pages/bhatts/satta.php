<br/><h2>Satta & Balwand</h2>
<hr/>
<div class="content">
    <p">SATTA, also called Sattã Düm because
    he was a dum or mirãsi by birth, a rabàbi or rebeck­player to Guru Arjan,
    and co composer, with Rãi Balvand, of Rãmkali ki Var, included in the Guru
    Granth Sãhib in the Rãmkali musical measure.</p>
    <p">1 Var Satta was a rebeck player who served Guru Angad, Guru Amar Das,
    Gur Ram Das and Guru Arjun Dev. Along with his fellow musician Balwand
    they jointly composed a ballad which appears in the Guru Granth.</p>
