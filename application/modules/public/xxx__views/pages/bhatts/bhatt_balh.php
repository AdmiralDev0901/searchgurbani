<h2>Bhatt Balh</h2>
<hr/>
<div class="content">
    <p>Bhatt
        Balh is the author of five Swaiyas written in praise of Guru Ramdas, the
        4th Guru. The favourite subject of his Swaiyas has been the transmigration
        of the soul and continuity of the divine light from one Guru to the other.
        He composed poetry, surprisingly in the style and diction of Bhatt Nalh.
        His Swaiyas may be distributed under three distinct categories </p>
    <p> 1.
        Poetry dealing with transmigration of Guru Nanak's soul to the descendants
        of his throne. <br/>
        <br/>
        2. The second category comprises of the poetry dealing with the life and
        philosophy of the earlier Indian saints. <br/>
        <br/>
        3. The third category includes the description of Bhakt and its culmination,
        resulting in the attainment of `Param Pad'. <br/>
        &nbsp;</p>
    <p><em>Concentrate
            on the True Guru, <br/>
            <br/>
            Whose image dispells the darkness of one's eyes <br/>
            <br/>
            Remembering him, one is reminded of the Lord, with all His Glory. Counting
            his name, ends the agony of the soul. <br/>
            <br/>
            Concentrating on him, one is Blessed with miraculous powers,' And the
            Nine Treasures of the heaven are at his disposal . Yes, Balh the bard,
            utter thou the name of Guru Ramdas, <br/>
            <br/>
            Say, &quot;victory to him&quot;, <br/>
            <br/>
            And enjoy forever the association of the saints. (5-54) </em></p>
