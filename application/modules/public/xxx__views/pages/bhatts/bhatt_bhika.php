    <p>Bhikha
        seems to.have joined the congregation of Guru Amardas at Goindwal. He
        has composed only two Swaiyas in praise of the V Guru, that are included
        in Adi Granth at pages 1395-96. As stated earlier, the undermentioned
        Swaiya reveals the background of their mission. It also illustrates how
        and why all the eleven Bhatts came to Guru Arjundev. In fact this Swaiya
        is of historic importance as it comments upon the degraded status of the
        clergy prevalent in India in those days and the importance that the Guru's
        congregations had obtained</p>
    <p><br/>
        BHATT
        BHIKHA(pronounced as Bhikkhã), a Brahman bard of Sultanpur Lodhi in present-day
        Kapurthala district of the Punjab, became a Sikh receiving the rites of
        initiation at the hands of Guru Amar Das. He lived up to the time of Guru
        Arjan to whom he introduced sixteen other Brãhman minstrels from his community.
        They sang in his presence praises to God and the Gurus. Some of their
        compositions were included by Guru Arjan in the Guru Granth Sahib. One
        of the two stanzas by Bhikha recounts his wanderings in search of a true
        saint ending with his ultimate success by the grace of God. &quot;Lord,&quot;
        sang the hard, &quot;bath caused me to meet the Guru; as thou wilIest
        for me so must I receive O God!&quot;</p>
    <blockquote>
        <p><em>I</em><em>
                wandered all over the place <br/>
                <br/>
                Searching for a hermit with heavenly Grace. <br/>
                <br/>
                I met many a mendicant, high flying on the ego fleet Soft-spoken, polished,
                vainful and sweet. <br/>
                <br/>
