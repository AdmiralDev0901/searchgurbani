<h2>Bhatt Nalh</h2>
<hr/>
<div class="content">
    <p>Bhatt
        Nalh had authored sixteen Swaiyas, all in praise of Guru Ramdas the 4th
        Guru. Surprisingly the style of this poet is absolutely parallel to Bhatt
        Kalh. In certain cases, the use of images and mataphors is the same. For
        instance he has called Guru Ramdas, as Janak, as had been mentioned earlier
        by Bhatt Kalh. The vocabulary and meters are also similar. However, the
        speciality of this poet is that he has introduced two new Chhands called
        Jhoolna , and Radd. As in the case of Bhatt Kalshar, we had stated that
        this group of eleverr had come to the congregation of Guru Arjun Dev,
        this historical fact has also been confirmed by Bhatt Nalh. He says that
        after touching the sacred feet of Guru Arjun Dev, a sense of solemnity,
        peace and serenity prevailed upon him. </span> </p>
    <p><b>Specimen
            of poetry of Bhatt Nalh </b><br/>
        &nbsp;</span></p>
    <p> Sacrifice
        I am, unto the Name of the True Guru. How do I serve him and how do I
        praise him? <br/>
        <br/>
        I can only utter the Name and bow before him with my folded hands. Once
        the Name is realised through thoughts _and actions, <br/>
        <br/>
        Only then, I shall know the Peerless Lord, the only One, <br/>
        <br/>
        This knowledge is embedded in my heart, through the Grace of the Guru.
        Saith Nalh the poet, &quot;As the philosopher's stone, converts glass
        into gold And the Chandan tree with its association, infuses the neighbouring
        vegetation with fragrance.&quot; <br/>
        <br/>
        So does the devotion of Guru, transform a person. Only the glimpse of
        whose door is enough <br/>
        <br/>
        to rid a person of his sins, lust and wrath, <br/>
        <br/>
        I am sacrifice unto the Name of the True Guru. (3) </p>
    <strong><p> Adi Granth, p. 1398-99</p></strong>
