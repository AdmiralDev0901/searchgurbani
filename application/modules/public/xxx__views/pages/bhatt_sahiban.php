<p>Bhatt Bani (the hymns by the bards) comprising twenty pages is incorporated in the concluding part of Adi Granth. The
    Bhatts are supposed to be the descendants of Koshish Rishi and are linked with the Sarshat Brahmins. It is a fact
    that almost all the eleven Bhatts belonged to Punjab and were residents of Sultanpur Lodhi (district Kapurthala)
    where Guru Nanak had served in the Modikhana for 13 years. According to Bhatt chronicles they were sons or nephews
    of Bhikha and Toda Bhatts. </p>
<p> During the medieval times Bhatts were more popularly known as the baliad singers whose profession was to compose and
    sing martial songs. With their narrative and descriptive poetry of war, they led the warriors to the battlefield.
    When the warriors marched to the battlefield in order to wage a war against the enemy invaders they would narrate to
    them the heroic deeds of their forefathers, who had laid their lives for the honour of the motherland. They recited
    the martial poems and aroused passion of the heroes for war and love for the motherland. The compositions of these
    Bhatts as included in Adi Granth are in the form of eulogies to the first five Gurus. They used the popular form of
    poetry called Swaiya Chhand. They wrote their Swaiyas in a very distinctive style. Their language, very rich and
    full of poetic imagery, is packed with elegant and decorative vocabulary. The eleven Bhatts whose Bani is included
    in Adi Granth are : Kalshar, Jalap, Kirat, Bhika, Salh, Bhalh, Nal, Gyand, Mathura, Bal and Harbans. It is said that
    they had come to the fifth Guru in a group led by Bhatt Kalshar. It appears some of them had also attended earlier
    the congregations of Guru Amardas and Guru Ramdas. </p>
<p> According to the chronicler Sant Singh Bhatt as written in Bhatt Bahi, &quot;It was the ninth generation of Bhatt
    Bhagirath that was blessed with an eminent poet and scholar known as Raiya, who had six sons named Bhika, Sokha,
    Tokha, Gokha, Chokha and Toda. The Swaiyas entered in Adi Granth mostly are authored by sons and grandsons of Raiya,
    with the exception of Bhatt Nal. The compositions of these Bhatts popularly known as Bhatt Bani comprise of 123
    Swaiyas, written in praise of the first five Gurus.&quot; </p>
<p> A significant point to be noted in this respect is that these Bhatts were not mercenaries who wrote for money, on
    the other hand their compositions were the genuine expression of their love and regards for the Gurus. They were
    dedicated followers of Gurus and their poetry was the spontaneous articulation of their deeprooted respects for the
    house of Nanak. According to them right from Guru Nanak onwards, all the Gurus were the carriers of the same light,
    they were all the incarnations of Guru Nanak, who on his part was the Avtar (embodiment) of Lord 'Vishnu. Oneness of
    the spirit of the Gurus was the main theme of their Swaiyas. </p>
<p> Bhai Kahan Singh Nabha, author of Mahan Kosh has referred to an en the `Sooraj Prakash' by Gyani Santokh Singh which
    accepts the Bhatts incarnations of Vedas. In other words he has accepted their scholarship, acknowledging them as
    the store-house of Vedas. Dr. Mohan Singh Diwan . The History of Punjabi Literature has accepted them as the court
    poets of the G who endeavoured to establish the social, religious and the spiritual state of Gurus in beautiful
    language. </p>
<p> According to records it is stated that Bhatt Bani was incorporated Adi Granth in AD 1604 by Guru Arjun Dev, the
    fifth Guru, the compiler of the Ha Granth. During the life time of the fifth great spiritual leader of the Sikhs
    thei compositions were sung by balladeers, Satta and Balwand in the congregations melodious tunes to enthrall the
    audience with their devotional overtone. </p>
<p>BANI, recorded under the title Savaiyye, is the name popularly given to the compositions of the Bhatts as included in
    the Guru Granth Sahib (pp. 1389-1409). Bhatts were bards or panegyrists who recited poetry lauding the grandeur of a
    ruler or the gallantry of a warrior. Bhatt was also used as an epithet for a learned Br&atilde;hman. In the Sikh
    tradition, Bhatts are poets with the personal experience and vision of the spirituality of the Gurus whom they
    celebrate in their verse. According to Bh&atilde;i Santokh Singh, Sri Gur Prat&atilde;p S&uuml;raj Granth, &quot;They
    were the Vedas incarnate&quot; (p. 2121). The Bhatts are-said to have originally lived on the bank of the River
    Sarasvati which is also the name of the Indian mythological goddess of knowledge. They were thus called S&atilde;rasvat,
    i.e. the learned Br&atilde;hmans. Those living on the other side of the Sarasvat were called Gaur. They showed
    little interest in learning and contended themselves with alms given them by their patrons whose bans&atilde;valin&atilde;m&atilde;s
    or genealogies they recorded in their scrolls called vahis. They are still found on the bank of the Sarasvati in the
    Talaud&atilde; (Jind), Bh&atilde;dso&ntilde; (L&atilde;dva) and Karsindhu (Safido&ntilde;) villages in Hary&atilde;n&atilde;.
    Some of these families shifted over to Sultanpur Lodhi, now in Kapurthal&atilde; district of the Punjab, and settled
    there. Bhikh&atilde; and Tod&atilde; of these families embraced the Sikh faith during the time of Guru Amar D&atilde;s.</p>
<p>&nbsp;Bh&atilde;i Gurd&atilde;s also gives in his V&atilde;r&atilde;&ntilde;, XI. 21, a brief account of these
    Bhatts. What was the number of Bhatts whose compositions are included is a question not yet firmly answered.
    According to a tradition, Kalh, a leading Bhatt poet, took it upon himself to note down some of the verse of the
    Bhatts from the vahis and passed it on to Guru Arjan at the time of the compilation of the Holy Book. As for the
    number of Bhatt contributors to the Guru Granth Sahib, Sahib Si&ntilde;gh, Tej&atilde; Singh, T&atilde;ran Si&ntilde;gh
    and other modern scholars count 11 of them, whereas Santokh Si&ntilde;gh (Sri GurPrat&atilde;p Suraj Granth), Bhai
    Vir Singh (Guru Granth Kosh)and some others among the traditional scholars count 17, and Panclit Kart&atilde;r Singh
    D&atilde;kha puts the figure at 19. This variation in owed to the fact that the Bhatts used to sing in&nbsp; chorus
    and sometimes the chorus sung in&nbsp; a group went in the name of the leader at other times &nbsp;individually in
    the members of the group.</p>
<p>From among the 17 Bhatts positions figure in the Guru Granth Sahib, Bhikh&atilde;, son of Rayy&atilde;, was a
    resident of &nbsp;Sult&atilde;npur Lodhi and had been a follower of Guru Amar D&atilde;s. Of the total 123 savaiye
    in the Guru&nbsp; Granth Sahib two are of his composition, both in praise of Guru Amar Das Of the remaining sixteen
    Bhatt contributors four are his sons; Kalh, also called or Kal Th&atilde;kur, who is reckoned to be the most learned
    of all the Bhatts, has 10 in praise of Guru N&atilde;nak, 9 each&nbsp; of Guru A&ntilde;gad and Guru Amar Das, 13 in&nbsp;
    praise of Guru Ram Das and 12 in praise of Guru Arjan; Jalap who had migrated to Goindv&atilde;l with his father has
    four his name all of which are in praise of Guru Amar Das; Kirat (d. 1634) has eight savaiye, four each in praise of
    Guru Amar Das and&nbsp; Guru Ram D&atilde;s; and Mathur&atilde; 12, all in praise of&nbsp; Guru Ram D&atilde;s. Salh
    who has three savaiye extolling the pre-eminence of Guru Amar D&atilde;s (1) and Guru Ram D&atilde;s (2), Bhalh who
    has one savaiyya in praise of Guru Amar D&atilde;s were the sons of Sekh&atilde;, a brother of Rayya. Balh who has
    five savaiye stressing the spiritual oneness of the Gurus was son of Tokh&atilde;, another brother of Rayya. Hariba&ntilde;s,
    the eldest son of Gokha, a brother of Rayya, has two savaiyyes , both in praise of Guru Arjan. Nalh has five
    savaiyyes all in praise of Guru Ram Das. Das also spelt dasu or dasi, has composed ten savaiyyes including one
    conjointly written with Sevak, who in addition to this has four savaiyye of his own. Parmanand has five savaiyyes
    are in praise of Guru Ram das, Tal's single one in praise of Guru A&ntilde;gad .Jalan has two savaiye in praise of
    Guru Ram Das, Jalh one in praise of Guru Amar D&atilde;s and Gayand five which glorify Guru Ram D&atilde;s. Of the
    total 123 , ten each pay homage to Guru N&atilde;nak and Guru Angad, 22 to Guru Amar D&atilde;s, Ram D&atilde;s and
    21 to Guru Arjan.</p>
<p>The main purpose of these savaiye is to &nbsp;acclaim the Gurus, not as individuals but as the revelation they
    embodied.The Bhatts see the Gurus as one light, as one spirit passing from one body to the other. Bhatt Kirat for
    instance: &quot;Just as (Guru) Angad was ever the part of Guru N&atilde;nak's being so is Guru Ram D&atilde;s of
    (Guru) Amar D&atilde;s's&quot; Again, Bhatt Kalh: &quot;From Guru Nanak was Angad: from Angad, Amar D&atilde;s
    received sublime rank. From Guru Ram Das descended Guru Arjan, the great devotee of God&quot; (GG, 1407). This
    concept of all the Gurus being one light, one voice has informed all along the Sikh belief and development and
    constitutes today a fundamental&nbsp; principle of the faith.</p>
