                                    <td><p class="blue">Prabhati does not appear in the Ragmala: the nearest to it in
                                            name&nbsp; Prabal. Prabhati belongs to the Bhairav thata and is often
                                            combined with Raga Bhairav. Prabhati was the setting used for some 58 hymns
                                            by Guru Nanak, Guru ,Amar Das, Guru Ram Das and Guru Arjan. This is a
                                            morning raga to be performed in a slow and dignified manner. </p>
                                        <p class="blue"><b>Aroh </b>: Sa Re Ga Pa Dha Sa </p>
                                        <p class="blue"><b>Avroh :</b> Sa Ni Dha Ni Pa Ga Re Sa </p>
                                        <p class="blue"><b>Pakar </b>: Pa Pa Sa, Ni Dha Ni Pa, Pa Dha Ga Pa, Dha Pa Dha
                                        </p>
                                        <p class="blue"><b>Vadi </b>: Sa </p>
                                        <p class="blue"><b>Samvadi</b> : Pa </p></td>
