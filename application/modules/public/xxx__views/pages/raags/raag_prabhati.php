                <p><strong> Introduction : </strong></p>
                <p> This raga is made up of a mixture of many ragas. There is no mention of this raga in any ancient
                    book of Indian music, though it is quite popular in the religious songs. Some authors do classify it
                    as s a raga of Bharav thata. </p>
                <p>This raga is not mentioned in the Ragamala listed at the end of Guru Granth Sahib. </p>
                <p> The scale and notes of the raga are as follows: </p>
                <p> Arohi (ascending scale) - sa re, ga , dha, sa </p>
                <p> Avrohi (descending scale) - sa ni dha ni pa, dha ga pa, ga re sa </p>
                <p> The vadi (most popular) note is 'sa' and samvadi (second most popular) note is 'pa'. </p>
                <p>This raga is sung in the first part of the day i.e., 6 a.m. 9 a.m. The season of its recitation is
                    spring (basant) i.e., during February - March. In Guru Granth Sahib it has hymns from pages 1327 -
                    1351 (15 pages). </p>
                <p><strong> The Composers: </strong></p>
                <p> The composers of bani (hymns) in this raga are: </p>
                <p><strong><em>Gurus </em></strong></p>
                <ul>
                    <li> Nanak Dev</li>
                    <li> Amardas</li>
                    <li> Ramdas</li>
                    <li> Guru <strong>Arjan Dev </strong></li>
                </ul>
                <p><strong><em>Bhagats: </em></strong></p>
                <ul>
                    <li> Kabir</li>
                    <li> Namdev</li>
                    <li> Beni</li>
                </ul>
                <p style="font-weight: bold"><em>The structure: </em></p>
                <p> The sequence of the structure of compositions in this raga are: </p>
                <p style="font-weight: bold"><em>Gurubani: </em></p>
                <ul>
                    <li> Shabads (2 - 4padas)</li>
                    <li> Ashtpadis</li>
                </ul>
                <p><strong><em>Bhagatbani: </em></strong></p>
                <ul>
                    <li> Shabads 3-6 Padas</li>
                </ul>
                <p style="font-weight: bold"> Matrix </p>
                <p style="font-weight: bold"><em> VISUAL ANALYSIS </em></p>
                <p style="font-weight: bold"><em> Count of the use of Managals: </em></p>
                <ul>
                    <li> Complete Mool Mantar = 1 at page 1319</li>
                    <li> Ik-ongkar Satgur Prasadh = 4</li>
                </ul>
                <p style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                <p> In all shabads and ashtpadis the numbered rahau verses, but the placing varies between beginning of
                    the shabads to the end of the first padas of the shabads in various compositions. </p>
                <p> In the Bhagat Bani, the numbered rahau verses are placed at the end of the first padas of the
                    shabads. </p>
