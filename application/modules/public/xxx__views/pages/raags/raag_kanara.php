                <p style="font-weight: bold"><em> Count of the use of Managals: </em></p>
                <ul>
                    <li> Complete Mool Mantar = 1 at page 1294</li>
                    <li> Ik-ongkar Satgur Prasadh 15</li>
                </ul>
                <p style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                <p> In all shabads and ashtpadis the numbered rahau verse are is in the beginning of the
                    compositions. </p>
                <p>In the Bhagat Bani the rahau verses have numerals and are also placed in the beginning of the
                    shabads. </p>
