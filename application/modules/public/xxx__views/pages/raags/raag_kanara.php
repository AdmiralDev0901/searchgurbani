                        <td bgcolor="#eaeaea"><p class="blue">The modern name for this raga appears to be &quot;Kanada&quot;,
                                probably a mattcr of transliteration from its original name. Under the Kanara spelling
                                this raga was prevalent in the classifications of 16th and 17th centuries. However, in
                                one instance, Kanara and Kanada both appear in the same ragmala. This would indicate
                                that at one time these were two distinctly different ragas. Kanara was used by Guru Ram
                                Das and Guru Arjan for 69 hymns, a var, plus numerous slokas. In the Ragmala, Kanara is
                                a putra of Dipak. The modern Kanada is one of a group of many Kanada ragas which are
                                combinations of Kanada with other ragas; one of the most popular is Darbari Kanada
                                classified under the Asavari thata. Assigned to the night hours, its mood is quiet and
                                full of majesty. Darbari-Kanada is performed in slow tempo and is a popular concert form
                                today. The details of this raga: <br/>
                                <b>Aroh</b> : Sa Re Ca, Ma Pa, Ni Sa </p>
                            <p class="blue"><b>Avroh</b> : Sa, Ni Pa, Ma Pa, Ga Ma Re Sa </p>
                            <p class="blue"><b>Vadi</b> : Pa </p>
                            <p class="blue"><b>Samvadi</b> : Sa </p></td>
