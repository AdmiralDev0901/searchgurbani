                <p><strong> Introduction : </strong></p>
                <p> It is a not a very ancient raga. It became famous in the medieval historical period. Tansen, one of
                    the most famous musician of Emperor Akbar, who was contemporary of Guru Amardas, Guru Ramdas and
                    Guru Arjan Dev, used this raga in the court of Akbar, thus this raga became famous as Darbari
                    (court) Kanra. </p>
                <p>Kahn Singh Nabha has given 17 variations of this raga: </p>
                <ul>
                    <li> Darbari</li>
                    <li> Nayki</li>
                    <li> Kaski</li>
                    <li> Varetsi</li>
                    <li> Nut</li>
                    <li> Kafi</li>
                    <li> Kolahal</li>
                    <li> Mangal</li>
                    <li> Sayam</li>
                    <li> Tunk</li>
                    <li> Nagdvani</li>
                    <li> Adana</li>
                    <li> Shahana</li>
                    <li> Suha</li>
                    <li> Sughar</li>
                    <li> Hussani</li>
                    <li> Jay jayant</li>
                </ul>
                <p>This raga is mentioned as a son of raga Deepak in the Ragamala listed at the end of Guru Granth
                    Sahib. </p>
                <p>The scale and notes of the raga are as follows: </p>
                <p> Arohi (ascending scale) - sa re ga, ma pa, ni sa </p>
                <p> Avrohi (descending scale) - sa ni pa, ma pa, ga ma re sa </p>
                <p> The vadi (most popular) note is 'pa' and samvadi (second most popular) note is 'sa'. </p>
                <p>This raga is sung in the first part of the night i.e., 6 p.m. 9 p.m. The season of its recitation is
                    summer (grikham) i.e., during May - June. In Guru Granth Sahib it has hymns from pages 1294 - 1318
                    (25 pages). </p>
                <p><strong> The Composers: </strong></p>
                <p> The composers of bani (hymns) in this raga are: </p>
                <p style="font-weight: bold"><em> Gurus </em></p>
                <ul>
                    <li> Ramdas</li>
                    <li> Guru Arjan Dev</li>
                </ul>
                <p style="font-weight: bold"><em>Bhagats: </em></p>
                <ul>
                    <li> Namdev</li>
                </ul>
                <p style="font-weight: bold"><em>The structure: </em></p>
                <p> The sequence of the structure of compositions in this raga are: </p>
                <p><strong><em>Gurubani: </em></strong></p>
                <ul>
                    <li> Shabads (2 - 4padas)</li>
                    <li> Ashtpadis</li>
                    <li> Chhant</li>
                    <li> Var</li>
                </ul>
                <p><strong><em>Bhagatbani: </em></strong></p>
                <ul>
                    <li> Shabads ( 2 padas)</li>
                </ul>
                <p style="font-weight: bold">Matrix </p>
