                                    <td><p><span class="blue"><b>BILAVAL </b>(ancient name Velavali) Bilaval had become the basic scale for North Indian music by the early part of the 19th century. Its tonal relationships are comparable to the Western C- major scale. Bilaval appears in the Ragmala as a ragini of Bhairava, but today it is the head of the Bilaval thata. The Ragmala gives Bilaval as a putra (son) of Bhairav, but no relation between these two ragas is made today. Bilaval is a morning raga to be sung with a feeling of deep devotion and repose, often performed during the hot months. Over 170 hymns were composed to this raga by Guru Nanak, Guru Amar Das, Guru Ram Das, Guru Arjan and Guru Tegh Bahadur. <br/>
                  </span><span style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">Literally, </span><span
                                                class="blue">Bilaval means &lsquo;delight&rsquo;
                                                and therefore this </span><span
                                                style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">raga </span><span
                                                class="blue">is   often sung in the spring season. Guru Arjan sings of the bliss which spiritual   fulfilment brings:</span>
                                        </p>
                                        <p align="center"><span class="blue">&ldquo;I am blessed with great destiny for my God is my   Bridegroom.<br/>
                      In His court plays the spontaneous celestial strain.<br/>
                      Night and   day, I abide in bliss, listening happily to musical instruments;<br/>
                      Disease,   sorrow and pain harass not here, nor is there birth or death&rdquo;.<br/>
                    </span></p>
                                        <p class="blue"><b>Aroh</b> : Sa Re Ga, Ma Pa, Dha, Ni Sa </p>
                                        <p class="blue"><b>Avroh </b>: Sa Ni Dha, Pa, Ma Ga, Re Sa </p>
                                        <p class="blue"><b>Pakar </b>: Ga Re, Ga ~la Dha Pa, Ma Ga, Ma Re Sa </p>
                                        <p class="blue"><b>Vadi :</b> Dha </p>
                                        <p class="blue"><b>Samvadi </b>: Ga </p></td>
