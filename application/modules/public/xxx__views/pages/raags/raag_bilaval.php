                <p style="font-weight: bold"><span
                        style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">Introduction : </span>
                </p>
                <p class="blue"> This raga is a popular raga of Bilawal thaht. It has many forms e.g., Yamni Bilawal,
                    Shukal Bilawal, Sarparda Bilawal, Dkumb Bilawal, Devgiri Bilawal and Nut Bilawal etc. </p>
                <p class="blue"> This raga is mentioned as a son of rag Bharav in the Ragamala listed at the end of Guru
                    Granth Sahib. </p>
                <p class="blue"> The scale and notes of the raga are as follows: </p>
                <p class="blue"> Arohi (ascending scale) - sa re ga ma pa dha ni sa </p>
                <p class="blue"> Avrohi (descending scale) - sa ni dha pa ma ga, re sa </p>
                <p class="blue"> The vadi (most popular) note is 'dha' and samvadi (second most popular) note is
                    'ga'. </p>
                <p class="blue">This raga is sung at the first part of the day i.e., from 6 a.m. to 9 a.m. The season of
                    its recitation is spring (basant) i.e., during February and March. In Guru Granth Sahib it has hymns
                    from pages 795 - 858 (64 pages). </p>
                <p class="blue"><strong> The Composers: </strong></p>
                <p class="blue"> The composers of barn (hymns) in this raga are: </p>
                <p class="blue" style="font-weight: bold"><em>Gurus </em></p>
                <ul>
                    <li class="blue"> Guru Nanak</li>
                    <li class="blue"> Guru Amardas</li>
                    <li class="blue"> Guru Ramdas</li>
                    <li class="blue"> Guru Arjan Dev</li>
                    <li class="blue"> Guru Tegh Bahadur</li>
                </ul>
                <p class="blue"><strong><em>Bhagats: </em></strong></p>
                <ul>
                    <li class="blue"> Kabir</li>
                    <li class="blue"> Namdev</li>
                    <li class="blue"> Ravidas</li>
                    <li class="blue"> Sadhna</li>
                </ul>
                <p class="blue"><strong> The Structure: </strong></p>
                <p class="blue"> The sequence of the structure of compositions in this raga are: </p>
                <p class="blue"><strong><em>Gurubani: </em></strong></p>
                <ul>
                    <li class="blue"> Shabads (2,3,4, and 5 padas)</li>
                    <li class="blue"> Shabads (8 padas Ashtpadis)</li>
                    <li class="blue"> Shabads (Chhants)</li>
                    <li class="blue"> Shabads (Specialist compositions)</li>
                    <li class="blue"> Shabads (var)</li>
                </ul>
                <p class="blue"><strong> Matrix </strong></p>
                <p class="blue"><em> VISUAL ANALYSIS </em></p>
                <p class="blue" style="font-weight: bold"><em>Count of the use of Managals: </em></p>
                <ul>
                    <li class="blue"> Complete Mool Mantar = 1 at page 795</li>
                    <li class="blue"> Ik-ongkar Satgur Prasadh = 30</li>
                </ul>
                <p class="blue" style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                <p class="blue"> First 80 shabads of the Gurus have numbered rahau verses placed at the end of the first
                    padas of the shabads; next 52 shabads (including 3 shabads of Guru Tegh Bahadur) have numbered rahau
                    verses in the beginning of the shabads. </p>
                <p class="blue"> The ashtpadis which follow have numbered rahaus both in the beginning of the first
                    padas and at the end of the first padas of the ashtpadis. </p>
                <p class="blue"> Specialist compositions 'Thithe' and 'Var sat' have one verse of numbered rahau at the
                    end of the first pada. </p>
                <p class="blue"> The Bhagat Bani has rahau verse with numerals and is placed both at the end of of the
                    first padas of the shabads and the beginning of the shabads. </p>
