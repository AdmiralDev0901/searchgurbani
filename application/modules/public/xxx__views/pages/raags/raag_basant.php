<br/><h2>Gurbani Raag: Basant </h2>
<hr/>
<div class="blue">
    <p>The name Basant is from Sanskrit vasant meaning spring, and during that season of the year Basant may be
        performed at any time of the day or night. Otherwise, it is reserved for the night between 9 p.m. and midnight.
        The Ragmala gives Basant as a putra (son) of Hindol, also a spring raga. Today it belongs to the Purvi thata.
        The only variant noted in the Holy Book is Basant-Hindol. Basant is a very old raga dating from the 8th century.
        Guru Nanak, Guru Amar Das, Guru Ram Das, Guru Arjan and Guru Tegh Bahadur composed sabdas to this raga.
        Performed in slow tempo, this gentle melody depicts quiet joy. The descending scale is usually found at the
        beginning of a composition with the ascending form following later. </p>
    <p><b>Aroh</b> : Sa Ga Ma Dha Ni Sa </p>
    <p><b>Avroh</b> : Sa Ni Dha Pa Ma, Ga Re Sa </p>
    <p><b>Vadi</b> : Sa </p>
    <p><b>Samvadi </b>:Ma</p></div>

<p><strong> Introduction : </strong></p>
<p> According to Indian Schools of music this raga has very gentle mood and represents loneliness and quiet joy and is
    to be performed in a dignified slow tempo The raga belongs to Purvi thaht. </p>
<p> This raga is mentioned as a son of raga Hindol in the Ragamala listed at the end of Guru Granth Sahib. </p>
<p> There are many variations of this raga. It is believed that the Basant raga recorded in Guru Granth Sahib is in its
    original and true variation. According to a tradition this raga is sung in the Sikh Gurdwaras from the festival of
    Magi to the festival of Hola Mohalla. During this period the singing of raga Sarang is prohibited. The Sikh ragis
    start the singing of this raga on the first of the month of Mag, after saying their prayers and purifying the
    prasad. They sing five shabads of the raga and then sing the 'var' of Basant and declare that they have opened the
    raga Basant. On the day of Holla Mohalla they sing the shabad 'surah ki jaisi teri chaal.._' (Bhagat Kabair page
    1196) and end the singing of the raga. </p>
<p>The scale and notes of the raga are as follows: </p>
<p> Arohi (ascending scale) - sa ga ma ma ga ma dha ni sa re </p>
<p> Avrohi (descending scale) - sa ni dha, ni dha mm, ma ga sa </p>
The vadi (most popular) note is 'sa' and samvadi (second most popular) note is ' ga'.
<p>This raga can be sung at any time, though some prefer it to be sung at late night time. The season of its recitation
    is spring (basant) i.e., during February, March. </p>
<p> In Guru Granth Sahib it has hymns from pages 1168 - 1196 ( 29 pages). </p>
