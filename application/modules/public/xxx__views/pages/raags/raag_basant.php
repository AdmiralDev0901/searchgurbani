</ul>                <p style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
<p> All shabads have numbered rahau verses placed at the end of the first padas of the shabads, except a shabad at page
    1185 where it is unnumbered and it is placed in the beginning of the first pada, and Guru Tegh Bahadur's shabads
    (pages 1186 - 1187) where the numbered verses are placed in the beginning of the padas of the shabads. In the
    ashtpadis the rahau verse is numbered and is placed at the end of the first padas of the ashtpadis. </p>
<p> In the Bhagat Bani the numbered rahau verse is placed at the end of the first padas of the shabads, except the bani
    of Bhagat Ramanand, where the numbered rahau verse is placed in the beginning of the first pada of the shabad. </p>
