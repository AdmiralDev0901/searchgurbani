                <p><strong> Introduction : </strong></p>
                <p> According to Indian Schools of music this raga is also a very old, melodious and popular raga. It is
                    known as Janak raga of Bharav thaht. In the opinion of Pandit Ravi shankar, this raga imparts
                    morning invocation expression. Ustad Hafees Ahmed Khan also believes that Bhairav represents the
                    mood for prayers and invocation. It is masculine but tender in nature. Pandit Jitendra Abhishekhi
                    feels that Bhairav being a morning raga imparts an energetic mood as morning symbolises energy. </p>
                <p> This raga is mentioned as the first major raga in the Ragamala listed at the end of Guru Granth
                    Sahib. </p>
                <p>The scale and notes of the raga are as follows: </p>
                <p> Arohi (ascending scale) - ni sa ga ma dha ni sa re </p>
                <p> Avrohi (descending scale) - sa ni dha pa ma ga re sa </p>
                <p> The vadi (most popular) note is 'dha' and samvadi (second most popular) note is 're '. </p>
                <p>This raga is sung at the fourth part of the night i.e., from 3 a.m. to 6 a.m. The season of its
                    recitation is winter (sharad) i.e., during October, November. In Guru Granth Sahib it has hymns from
                    pages 1125- 1167 ( 43 pages). </p>
                <p><strong> The Composers: </strong></p>
                <p> The composers of bani (hymns) in this raga are: </p>
                <p style="font-weight: bold"><em>Gurus </em></p>
                <ul>
                    <li> Guru Nanak Dev</li>
                    <li> Guru Amardas</li>
                    <li> Ramdas</li>
                    <li> Guru Arjan Dev</li>
                </ul>
