                <p><span style="font-weight: bold"><em> Count of the use of Managals: </em></span></p>
                <ul>
                    <li> Complete Mool Mantar = 1 at page 1125</li>
                    <li> Ik-ongkar Satgur Prasadh = 16</li>
                </ul>
                <p style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                <p> All shabads (except two shabads: one on page 151 and second on page 1153, where rahau verse are at
                    the beginning of the shabads) have numbered rahau verses placed at the end of the first padas of the
                    shabads. </p>
                <p> In the Bhagat Bani, except the first shabad (page 1157), where the numbered rahau verse is placed in
                    the beginning of the first pada, an all other shabads it is placed at the end of the first padas of
                    the shabads. </p>
