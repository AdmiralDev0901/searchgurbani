                        <td bgcolor="#eaeaea"><p class="blue">Bhairon was an important raga at the time of Guru Nanak
                                and has continued to retain its significance and popularity. Bhairon (not to be confused
                                with Bhairavi) appears in the Rdgrnala as husband of Bhairavi and four other rdginis.
                                Today it is the head rdga for one of the ten thatas. The Rdga Sdgara, a treatise of
                                circa 8th century, describes this rdga as awe-inspiring and as expressing the &quot;fulfilment
                                of the desire of worship.&quot; Mesakarna (1509) calls this morning melody of the autumn
                                season one of awesome grandeur. Performed before sunrise, this raga was used by Guru
                                Nanak, Guru Amar Das, Guru Ram Das, and Gurft Arjan for 99 hymns. </p>
                            <p class="blue"><b>Aroh</b> : Sa Re, Ga Ma Pa Dha, Ni Sa </p>
                            <p class="blue"><b>Avroh</b> : Sa Ni, Dha Dha Pa, Ma Ga, Re Re Sa </p>
                            <p class="blue"><b>Vadi</b> : Dha </p>
                            <p class="blue"><b>Samvadi</b> : Re </p>
                            <p class="blue">The vadis&nbsp; are performed with a slow, wide vibrato which may begin with
                                the vadi itself or the highest limit to which it will extend. In descent the vibrato
                                must begin with upper limit. Otherwise Bhairon has few characteristic phrases. </p></td>
