                <p><strong> Introduction : </strong></p>
                <p> This raga was very popular about hundred years ago. In addition to Gurubani it was also sung for
                    other types of compositions. Guru Nanak used this raga to compose his 'Baramah'. According to a few
                    authors this raga is same as raga Madhvanti, but it is not true, Tukhari is a distinctive raga in
                    its own right. Bhai Kahn Singh Nabha, however, classifies it as a ragini. </p>
                <p> This raga is not mentioned in the Ragamala listed at the end of Guru Granth Sahib. </p>
                <p>The scale and notes of the raga are as follows: </p>
                <p> Arohi (ascending scale) - ni sa, ga re sa, ga ma pa ma pa ma pa, pa dha ni dha pa ma pa, sa na
                    sa </p>
                <p> Avrohi (descending scale) - sa ni sa ni sa, ni dha pa, pa ma pa ma pa, ma ga re sa, re m sa </p>
                <p> The vadi (most popular) note is 'pa' and samvadi (second most popular) note is ' sa'. </p>
                <p>This raga is sung in the first part of the day i.e., from 6 a.m. to 9 a.m. The season of its
                    recitation is winter (sharad) i.e., during October, November. In Guru Granth Sahib it has hymns from
                    pages 1107 - 1117 ( 11pages). </p>
                <p><strong>The Composers: </strong></p>
                <p> The composers of bani (hymns) in this raga are: </p>
