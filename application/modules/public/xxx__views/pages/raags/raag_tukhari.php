                        <td bgcolor="#eaeaea"><p class="blue">Tukhari was probably based on a folk tune and was very
                                likely developed by Guru Nanak into a raga for the singing certain sabdas. No raga of
                                this name appears in the classifications of the period when sabdas were being composed
                                and the Holy Book compiled. A raga called Mukhari may be found in the classifications of
                                Karnataka (South Indian) ragas during the period from the 15th to the 18th centuries.
                                Tukhari was used by Guru Nanak, Guru Ram Das and Guru Arjan. Guru Nanak's composition
                                Bara Maha is set to this Raga. It appears to be a raga for the morning hours to be sung
                                in winter. Its name Tukhari is the popular forth of tushar (Sanskrit for winter frost).
                                No melodic material for the Tukhari is available but, for the sake of comparison, the
                                scale of ,Mukhari is given: </p>
