    <blockquote>
        <blockquote>
            <p> 1.<strong>Sthayee :</strong> The first part of the composition. Mainly develops in the the lower and the
                middle octave.<br/>
                2<strong>.Antaraa :</strong> Second part of the composition. Develops in the middle or higher note.
                <br/>
                3.<strong>Mukhadaa :</strong> The first line of the composition.<br/>
