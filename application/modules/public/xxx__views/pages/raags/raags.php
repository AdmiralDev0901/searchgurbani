    <blockquote>
        <p class="red"> 2. <strong>Arohi </strong>- The ascending scale (sa re ga ma pa dha ni sa) .This is the pattern
            of notes in which a Raag ascends the scale. <br/>
            3. <strong>Avroh</strong>i - The descending scale (sa ni dha pa ma ga re sa) This is the pattern of notes in
            which a Raag decends the scale.<br/>
            4. <strong>Vadi</strong>- The most popular note ,This is a note which is strongly emphasised within a
            particular Raag. <br/>
            5. <strong>Samvadi</strong>- The second most popular note,This is a note which is emphasised within a
            particular Raag, but not as much as the Vadi. <br/>
            6. <strong>Aurav</strong>- A raga of five notes <br/>
            7. <strong>Khaurav</strong>- A raga of six notes <br/>
            8. <strong>Sampooran</strong>- A raga of seven notes <br/>
            9. <strong>Aurav-Khaurav</strong>- Where arohi has five notes, but avrohi has six notes. <br/>
            10.<strong> Khaurav-Aurav</strong>- Where arohi has six notes, but avrohi has five notes. <br/>
            11. <strong>Aurav-Sampooran</strong>- Where arohi has five notes, but avrohi has seven notes <br/>
            12. <strong>Khaurav-Sampooran</strong>- Where arohi has six notes, but avrohi has seven notes. <br/>
            13. <strong>Sampooran-Aurav</strong>- Where arohi has seven notes, but avrohi has five notes. <br/>
            14. <strong>Sampooran-Khaurav</strong>- Where arohi has seven notes, but avrohi has six notes. <br/>
            15. <strong>Saptaks</strong> - This refers to three divisions of a harmonium </p>
        <ul>
            <ul>
                <li> Mandar - first (top) part of seven notes</li>
                <li> Middle - central part of seven notes</li>
                <li> Tar - last part of seven notes.</li>
            </ul>
        </ul>
        <p> 16. The notes can be soft (komal) or sharp (teever) </p>
    </blockquote>
