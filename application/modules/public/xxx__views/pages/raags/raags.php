    <p>The ragas marked with asterix (*) sign mentioned above are listed in Guru Granth Sahib's ragamala, which has a
        mention of a total of 64 raga names, including 6 major ragas, 30 raginis and 48 sons of ragas. Thus out of a
        total of 64 ragas mentioned in the ragamala, the Sikh Gurus have used only 20 (17 major names, and 3 other raga
        names) ragas and have used 17 (14 major names and 3 other names) other ragas which are not mentioned in the Guru
        Granth Sahib's ragamala. </p>
    <p><strong> Specialist Terminology to understand musical terms used in the following pages. </strong></p>
    <blockquote>
        <p> 1. <strong>Thaat</strong>- The tune of seven ascending and descending notes is called 'Thath oio' ,</p>
        <ul>
            <li>A Thaat must have seven notes out of the twelve notes [Seven Shuddha, Four komal (Re, Ga, Dha , Ni), one
                teevra (Ma) ], placed in an ascending order. Both the forms of the notes can be used.
            </li>
            <li>Thaat has only an Aaroha.</li>
            <li>Thaats are not sung but the raags produced from the Thaats are sung.</li>
            <li>Thaats are named after the popular raag of that Thaat. For example Bhairavi is a popular raag and the
                thaat of the raag Bhairavi is named after the raag.
            </li>
        </ul>
