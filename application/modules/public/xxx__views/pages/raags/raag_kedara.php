                <p><strong> Introduction : </strong></p>
                <p> This raga is a very old raga and is considered as a complete raga of Bilawal thath. In different
                    schools of music it is considered as a ragini of ragas Nut (Ragaravan School), Deepak ( Bhart
                    School) and Sriraga (Shiv School). </p>
                <p> This raga is mentioned as a son of raga Meg in the Ragamala listed at the end of Guru Granth
                    Sahib. </p>
                <p>The scale and notes of the raga are as follows: </p>
                <p> Arohi (ascending scale) - sa mama, ma pa, - dha pa na dha sa Avrohi (descending scale) - sa ni dha,
                    pa ma ma, pa dha pa, ma ga re sa The vadi (most popular) note is 'ma' and samvadi (second most
                    popular) note is ' sa'. </p>
                <p>This raga is sung in the fourth part of the day i.e., from 3 p.m. to 6 p.m. The season of its
                    recitation is summer (grikham) i.e., during May and June. In Guru Granth Sahib it has hymns from
                    pages 1118 - 1124 ( <em> 7 </em> pages). </p>
                <p><strong>The Composers: </strong></p>
                <p> The composers of bani (hymns) in this raga are: </p>
                <p style="font-weight: bold"><em>Gurus </em></p>
                <ul>
                    <li> Ramdas</li>
                    <li> Guru Arjan <strong>Dev </strong></li>
                </ul>
                <p><strong><em>Bhagats: </em></strong></p>
                <ul>
                    <li> Kabir</li>
                    <li> Ravidas</li>
                </ul>
                <p><strong><em>The structure: </em></strong></p>
                <p> The sequence of the structure of compositions in this raga are: </p>
                <p><strong><em> Gurubani: </em></strong></p>
                <ul>
                    <li> Shabads <em> 2 </em> and 4 padas.</li>
                    <li> Chhants</li>
                </ul>
                <p><strong><em>Bhagatbani: </em></strong></p>
                <ul>
                    <li> Shabads 2-4 padas</li>
                </ul>
