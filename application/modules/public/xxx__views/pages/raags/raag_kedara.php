                        <td bgcolor="#eaeaea"><p class="blue">Kedara is an old raga dating from Guru Nanak's time or
                                even earlier which has become a very important and popular North Indian raga today. It
                                is supposed to possess magical qualities, if correctly performed, which can heal the
                                sick. In the Ragmala, Kedara is a putra (son) of Megha but currently is in the Kalvan
                                thata. Kedara was used by Guru Ram Das and Guru Arjan for a few short hymns. Several
                                forms of Kedara have been and still are in use. Thus considerable freedom of choice may
                                be exercised by the performer as to how this raga be performed in association with a
                                given test. In the most commonly used form, Kedara is performed during the first quarter
                                of the night and is particularly auspicious when the moon is visible, a planet with
                                which it has long been associated. The mood is one of contemplation associated with a
                                sort of ascetic idealism. The sadness expressed in Ragmala paintings suggests the
                                longing of man for the Supreme Being when this raga accompanies a sabda. The Kedara
                                scale is vakra (crooked) with unusual intervals: </p>
                            <p class="blue"><b>Aroh</b> : Sa Ma, Ma Pa, Dha Pa, Ni Dha Sa </p>
                            <p class="blue"><b>Avroh</b> : Sa Ni Dha Pa, M'a Pa Dha Pa Ma, Ma Re Sa </p>
                            <p class="blue"><b>Vadi</b> : Ma </p>
                            <p class="blue"><b>Samvadi</b> : Sa &nbsp;</p></td>
