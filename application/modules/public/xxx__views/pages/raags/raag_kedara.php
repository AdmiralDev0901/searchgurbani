                <p style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                <p> First four shabads have no rahau verses in them, next all shabads have rahau verses, without numeral
                    '1', and are placed in the beginning of the first padas of the shabads. The Bhagat bani has numbered
                    rahau verses placed at the end of the first padas of the shabads. </p>
