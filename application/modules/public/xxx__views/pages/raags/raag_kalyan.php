                        <td bgcolor="#eaeaea"><p class="blue">The Indian Sanskrit name for this raga is Kalyan and the
                                Persian is Yuman. In the Ragmala, Kalian is the son of Dipak while today it is the head
                                of the Kalian thata. It is performed during the first part of the night and is
                                considered a blessing bringing all good into one's life. Guru Ram Das and Guru Arjan
                                composed 23 hymns to this raga. The texts exalt the far-reaching and all-pervading power
                                of the Lord. In the Holy Book the only raga variant given is Bhopali (Bhupali). </p>
                            <p class="blue"><b>Aroh</b> : Ni Re Ga, tif'a Pa, Dha, Ni Sa .</p>
                            <p class="blue"><b>Avroh</b> : Sa Ni Dha, Pa, M'a Ga, Re Sa </p>
                            <p class="blue"><b>Pakar</b> : Ni Re Ga, Re Sa, Pa M'a Ga, Re Sa </p>
                            <p class="blue"><b>Vadi</b> : Ga </p>
                            <p class="blue"><b>Samvadi</b> : Ni </p></td>
