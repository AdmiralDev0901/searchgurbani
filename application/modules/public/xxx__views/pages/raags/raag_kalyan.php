                <p><strong> Introduction </strong></p>
                <p> It is a very famous raga. Its form is very beautiful and melodious. It is a raga of Kalyan
                    thata. </p>
                <p> There are 14 variations of this raga: </p>
                <ul>
                    <li> Shudh Kalyan</li>
                    <li> Eaemen Kalyan</li>
                    <li> Bhopal Kalyan</li>
                    <li> Puria Kalyan</li>
                    <li> Hamir Kalyan</li>
                    <li> Khem Kalyan</li>
                    <li> Sanvri Kalyan</li>
                    <li> Sayam Kalyan</li>
                    <li> Kamodhi Kalyan</li>
                    <li> Nut Kalyan</li>
                    <li> Home Kalyan</li>
                    <li> Jaet Kalyan</li>
                    <li> Nayki Kalyan</li>
                    <li> Binod Kalyan</li>
                </ul>
                <p>This raga is mentioned as a son of of raga Deepak in the Ragamala listed at the end of Guru Granth
                    Sahib. </p>
                <p>The scale and notes of the raga are as follows: </p>
                <p> Arohi (ascending scale) - sa, ni re ga ma pa, dha ni sa </p>
                <p> Avrohi (descending scale) - sa ni dha pa, ma ga re sa </p>
                <p> The vadi (most popular) note is 'ga' and samvadi (second most popular) note is 'pa'. </p>
                <p>This raga is sung in the fourth part of the day i.e., 3 p.m. 6 p.m. The season of its recitation is
                    rainy (varsha) i.e., during July-August. In Guru Granth Sahib it has hymns from pages 1319 - 1326 (8
                    pages). </p>
                <p><strong> The Composers: </strong></p>
                <p> The composers of bani (hymns) in this raga are: </p>
                <p style="font-weight: bold"><em>Gurus </em></p>
                <ul>
                    <li> Ramdas</li>
                    <li> Guru Arjan Dev</li>
                </ul>
                <p style="font-weight: bold"><em>Bhagats: </em></p>
                <p> There is no Bhagat Bani in this raga </p>
                <p><strong><em>The structure: </em></strong></p>
                <p> The sequence of the structure of compositions in this raga are: </p>
                <p style="font-weight: bold"><em>Gurubani: </em></p>
                <ul>
                    <li> Shabads (2 - 4padas)</li>
                    <li> Ashtpadis</li>
                    <li></li>
                </ul>
                <p style="font-weight: bold"><em>Bhagatbani:</em></p>
                <p> none </p>
                <p style="font-weight: bold">Matrix </p>
                <p style="font-weight: bold"><em>VISUAL ANALYSIS </em></p>
                <p style="font-weight: bold"><em>Count of the use of Managals: </em></p>
                <ul>
                    <li> Complete Mool Mantar = 1 at page 1319</li>
                    <li> Ik-ongkar Satgur Prasadh = 4</li>
                </ul>
                <p style="font-weight: bold"><em>Placement and count of rahau verses: </em></p>
                <p> In all shabads and ashtpadis the numbered rahau verse are placed in the beginning of the
                    compositions. </p>
