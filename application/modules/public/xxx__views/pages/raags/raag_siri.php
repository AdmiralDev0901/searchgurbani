                <p style="font-weight: bold" class="green"><span>Introduction </span></p>
                <p class="green">According to an ancient school of Music the roots of the origin of raga 'Sri' is god
                    Shiv, a god of Hindu trinity, who is also called by the name 'Sri'. The raga represents death i.e.,
                    the beginning of a new life. 'Sri' is a complete raga of eastern thaht. The notes used are: </p>
                <p class="green"> Arohi (ascending scale)- sa re ma pa ni sa (ga and dha are omitted) </p>
                <p class="green"> Avrohi (descending scale)- sa ni dha pa ma ga re sa (all seven svars) </p>
                <p class="green"> It is the first raga in Guru Granth Sahib and listed as fifth major raga in the
                    Ragamala (list of ragas) recorded at the end of Guru Granth Sahib. It is believed that there are 11
                    ragamalas available in the Indian Musical School composed by different authors and the ragamala
                    included in Guru Granth Sahib was composed by Guru Nanak Dev. </p>
                <p class="green"> The raga is recommended to be sung at the third part of the day i.e., from 12 noon to
                    3 p.m. The season of its recitation is winter (hement) i.e., during November and December. </p>
                <p align="left" class="green"> In Guru Granth Sahib this raga has hymns from pages 14-93 (79
                    pages). </p>
                <p class="green"><strong> The Composers: </strong></p>
