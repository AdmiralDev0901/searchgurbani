                        <td bgcolor="#eaeaea"><p class="blue"><span style="font-weight: bold">Raga Siri</span> was
                                favoured by the Hindus for religious occasions and is found in many of the old
                                treatises. In the Ragmala listed as a parent raga, it currently is a member of the puroi
                                thata. Still a popular concert raga today, it is considered one of the most famous from
                                among the North Indian classical system. Guru Nanak, Guru Amar Das, Guru Ram Das, and
                                Guru Arjan composed to this raga. Traditionally performed at sunset, it is assigned to
                                the rainy season as well as the months of November and December. Its mood is one of
                                majesty combined with prayerful meditation. This raga is always referred to as &quot;Siri
                                Raga&quot; rather than placing the term raga before the name. It accompanies about 142
                                shabads. </p>
                            <p align="left" class="blue"><strong>Sri raga </strong>is one of the parent ragas from which
                                other ragas have been derived. The word <strong>Sri</strong> means supreme or exalted
                                and as such this raga powerful. Bhai Gurdas calls it a supreme raga, supreme like the
                                philosopher&rsquo;s stone among other stones, because it has the power of converting
                                baser metals into gold. The gurus gave it the first place. This raga is sung in the
                                evening-a period of dusk and darkness. Man&rsquo;s mind and his inner state as a mortal
                                is one of darkness-caused by maya-and ignorance of his spiritual potentialities. So from
                                darkness to light is the law of nature. In Sri raga, Guru Nanak has dealt with the
                                existing ignorance and superstition and the neglect of spiritual values on account of
                                man&rsquo;s ego, greed and love of wordly pleasures:<br/>
                                <span style="font-weight: bold"><br/>
                                    &ldquo;The foolish and greedy soul is attached   to and lured by greed,</span><br/>
                                <br/>
                                Being materialistic and evil-minded, the individual is not soaked in God&rsquo;s Name
                                and continues coming and going&rdquo; [4]. Some writers state that this raga is
                                associated either with extreme heat or extreme cold. In the hot season, we need water;
                                in the cold season, we need warmth and fire. As such, the Guru has referred to man&rsquo;s
                                thirst for water and compared the soul to a fish&rsquo; and likened man&rsquo;s passions
                                to a dreadful fire&rdquo; [5]. Basically the Gurus have pinpointed in this
                                <strong>raga </strong>the longing of the individual soul for the Universal Soul. <br/>
                                <br/>
                                <br/>
                                <b>Aroh</b> : Sa Re M'a, Pa Ni Sa <br/>
                                <br/>
                                <b>Avroh</b> : Sa Ni Dha, Pa M'a Ga Re Sa </p>
