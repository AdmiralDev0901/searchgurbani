                        <td bgcolor="#eaeaea"><p class="blue">Favoured by Muslims, this raga occurs in the Ragmala as a
                                ragini of Hindol. Today, it belongs to the Khamaj thata. Tilang was used by Guru Nanak
                                (6 hymns), Guru Ram Das (3), Guru Arjan (5), Guru Tegh Bahadur (3), Kabir (1) and Namdev
                                (2) for a total of 20 hymns. Tilang is performed at night and has a calm and pleasing
                                mood. In the texts composed for this raga, the question is asked why man should cling to
                                all the evils of this life when Guru Nanak has shown the wav to true happiness and
                                fulfilment. </p>
                            <p class="blue"><b>Aroh</b> : Sa Ga Ma Pa Ni Sa :</p>
                            <p class="blue"><b>Avroh</b> : Sa Ni Pa Ma Ga Sa </p>
                            <p class="blue"><b>Vadi</b> : Ga </p>
