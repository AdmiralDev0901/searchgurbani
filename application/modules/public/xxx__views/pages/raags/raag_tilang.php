                <p><strong> Introduction </strong></p>
                <p> According to Mahan Kosh of Kahn Singh Nabha, raga Tilang belongs to Bilawal thath. This raga is also
                    called raga Trikiling and Triling associating it with three Shivlings found at mountains Shail,
                    Kalesar and Bhimeser in Southern India. According to Hanumant School of music it is a ragini of raga
                    Hindol. </p>
                <p> This raga is included in the Ragamala listed at the end of Guru Granth Sahib. </p>
                <p> The scale and notes of the raga are as follows: </p>
                <p> Arohi (ascending scale) - sa ga ma pa ni sa </p>
                <p> Avrohi (descending scale) - sa ni pa ma ga sa </p>
                <p> The vadi (most popular) note is 'ga' and samvadi (second most popular) note is 'na'. </p>
                <p>This raga is sung at the third part of the day i.e., from 12 a.m. to 3 p.m. The season of its
                    recitation is rainy (varsha) i.e., during July and Agust. In Guru Granth Sahib it has hymns from
                    pages 721 - 727( 7 pages). </p>
                <p><strong>The Composers: </strong></p>
                <p> The composers of bani (hymns) in this raga are: </p>
                <p><strong><em>Gurus </em></strong></p>
                <ul>
                    <li> Guru Nanak</li>
                    <li> Guru Ramdas</li>
                    <li> Guru Arjan Dev</li>
                    <li> Guru Tegh Bahadur</li>
                </ul>
                <p><strong><em>Bhagats: </em></strong></p>
                <ul>
                    <li> Kabir</li>
                    <li> Namdev</li>
                </ul>
                <p><strong> The structure: </strong></p>
                <p> The sequence of the structure of compositions in this raga are: </p>
                <p><strong><em>Gurubani: </em></strong></p>
                <ul>
                    <li> Shabads (2, 3 and 4 padas)</li>
                </ul>
                <p><strong> Matrix </strong></p>
                <p style="font-weight: bold"><em> VISUAL ANALYSIS </em></p>
                <p><span style="font-weight: bold"><em> Count of the use of Managals: </em></span></p>
                <ul>
                    <li> Complete Mool Mantar = 1 at page 721</li>
                    <li> Ik-ongkar Satgur Prasadh = 7</li>
                </ul>
                <p><strong><em>Placement and count </em></strong><em>of rahau verses: </em></p>
                <p> In the first five shabads of Guru Nanak Dev, three shabads have rahau verses with numeral '1' and
                    are placed after the first padas of the shabads; the next two shabads have no rahaus. The next 7
                    shabads have rahaus without numerals and are placed after the first padas of the shabads. The next
                    two long compositions , one by Guru Nanak Dev and one by Guru Ramdas have no rahau verses. The three
                    shabads of Guru Tegh Bahadur, which follow, have rahau verses in the beginning of the shabads and
                    have numeral with them. </p>
                <p> The Bhagat Bani has rahau verses with numeral and are placed at the end of the first padas of the
                    shabads. </p>
