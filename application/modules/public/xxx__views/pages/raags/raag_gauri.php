                <p class="blue"><strong> The Structure: </strong></p>
                <p class="blue"> The sequence of the structure of compositions in this raga are: </p>
                <p class="blue"><strong><em>Gurubani: </em></strong></p>
                <ul>
                    <li class="blue"> Shabads (divine songs) (2-6 padas (stanzas))</li>
                    <li class="blue"> Shabads - Ashtpadis ( divine songs of 8 padas)</li>
                    <li class="blue"> Shabad - Chhant (divine song of special praise)</li>
                    <li class="blue"> Specialist compositions titled 'Bawan Akhri' (52 letters of the Sanskrit
                        alphabet), 'Sukhmani'( divine song of peace), and 'Thithe' (15 days of the lunar calendar)
                    </li>
                    <li class="blue"> Var (ode/ballad)</li>
                </ul>
                <p class="blue"><strong><em>Bhagatbani; </em></strong></p>
                <ul>
                    <li class="blue"> Shabads of various padas</li>
                    <li class="blue"> Shabad - Ashtpadi</li>
                    <li class="blue"> Specialist compositions titled 'Bawan Akhri', 'Thithe' and 'wars' (seven days of a
                        week).
                    </li>
                    <li class="blue"> Shabads of various padas.</li>
                </ul>
