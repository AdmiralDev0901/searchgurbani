                        <td><p class="blue"><strong>Gauri </strong>has the largest number of composition which cover
                                about 14 per cent of the total number of pages of the Scripture. It contains two
                                important compositions of Guru Arjan, <strong>Sukhmani and Bavan-akhri (</strong>acrostic).
                                As it is a serious <strong>raga, </strong>the Gurus have given herein the exposition of
                                profound concepts like fear, consciousness, soul and salvation.<br/>
                                <br/>
                                Gauri is one of several Gauri ragas and appears in the Ragmala as a ragini of Siri Raga.
                                This is an evening raga assigned to autumn and its mood is contemplative. The
                                composition in Gauri is very voluminous. Gauri was used by Guru Nanak, Guru Amar Das,
                                Guru Ram Das, Guru Arjan and Guru Tegh Bahadur. Several forms of Gauri exist
                                historically and this probably accounts for the large number of variants: Gauri Cheti,
                                Gauri Bairagan, Gauri Dipaki, Gauri PurbiDipaki, Gauri Guareri, Gauri-Majh, Gauri
                                Malava, Gauri Mala, Gauri Sorath, Gauri Dakhani. </p>
                            <p class="blue"
                            ><b>Aroh</b> : Sa Re Ga Re Ma Pa Ni Sa </p>
                            <p class="blue"
                            ><b>Avroh</b> : Sa Ni Dha Nfa Pa, Dha Pa IIa Ga, Ga _Re Sa Ni Sa </p>
                            <p class="blue"
                            ><b>Vadi</b> : _Re </p>
                            <p class="blue"
                            ><b>Samvadi</b> : Pa </p>
                            <p class="blue"
                            >Occasionally Re is performed with a vibrate as in Siri Raga which has the same vadis. Ni is
                                given prominence through either stopping or lingering on this note. </p></td>
