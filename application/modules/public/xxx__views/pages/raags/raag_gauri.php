                <p style="font-weight: bold"><span class="blue" style="font-weight: bold">Introduction : </span></p>
                <p class="blue"> The literal meaning of Gauri is 'style' i.e., the style and mode to remember God and
                    sing his glories. According to the Indian school of Music Gauri is a ragini (female raga). Its roots
                    are in the eastern thaht, and it is a consort of raga Sri. In Guru Granth Sahib maximum compositions
                    are composed in this raga. There are 22 forms of Gauri tunes according to the Indian School of
                    Music, whereas in Guru Granth Sahib there is a mention of 12 of such forms, they are: </p>
                <ul>
                    <li class="blue"> Gauri (refer page 151)</li>
                    <li class="blue"> Gauri Gurareree (refer page 151)</li>
                    <li class="blue"> Gauri Dakhni (refer page 152)</li>
                    <li class="blue"> Gauri Cheti (refer page 154)</li>
                    <li class="blue"> Gauri Bairagan (refer page 156)</li>
                    <li class="blue"> Gauri Deepki (refer page 156)</li>
                    <li class="blue"> Gauri Purbi Deepki ( refer page 167)</li>
                    <li class="blue"> Gauri Purbi (refer page 168)</li>
                    <li class="blue"> Gauri Maj (refer page 172)</li>
                    <li class="blue"> Gauri Malwa (refer page 214)</li>
                    <li class="blue"> Gauri Mala (refer page 214)</li>
                    <li class="blue"> Gauri Sorath (refer page 330)</li>
                </ul>
                <p class="blue">The above forms of Gauris were developed by mixing pure Gauri with other regional tunes,
                    notes and tals. The thaht of Pure Gauri is Bhairo. The notes are: Arohi (ascending scale) - sa re ma
                    pa ni sa (omitted notes are ga dha) Avrohi - sa ni da pa ma re sa (omitted note is ga) </p>
                <p class="blue"> The most popular note is 're' soft (komal) and the second popular note is 'pa'. </p>
                <p class="blue"> The recommended time of its singing is the third part of the day, 12 noon - 3 p.m., and
                    the season of its recitation is winter (shisher), December -January. In certain forms of Gauris the
                    time of its singing is different from the pure Gauri, e.g., the popular time of singing Gauri Mala
                    is the first quarter of night rather than third part of the day. </p>
                <p class="blue"> In Guru Granth Sahib this is the third raga, whereas in the Ragamal it is listed in the
                    last lines as Gavri, rather than Gauri, and is classified as a wife of Sri raga. </p>
                <p class="blue"> In Guru Granth Sahib the hymns recorded in this raga are on pages 151 -347 (197
                    pages). </p>
