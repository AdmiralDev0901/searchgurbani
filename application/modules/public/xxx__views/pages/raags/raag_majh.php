                <p style="font-weight: bold"><span
                        style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">Introduction : </span>
                </p>
                <p class="blue"> The raga is sung to describe the lacerations of a beloved in the memory of his/her
                    lover. The shabad of Guru Arjan 'mera maan lochey.... is the best example to describe the depth of
                    this raga. The notes used in this raga are: </p>
                <p class="blue"> Arohi (ascending scale) - sa re ma pa ni sa (omitted notes are -ga, dha)<br/>
                    Avrohi (descending scale) - sa ni-(soft note) dha pa ma ga-(soft note) re sa<br/>
                    The vadi (most popular) note is 're' and samvadi (second most popular) note is 'pa'. </p>
                <p class="blue">Raga Majh is sung at the third part of the day i.e., from 12 noon to 3 p.m. The season
                    of its recitation is rainy (varsha) i.e., during July and August. </p>
                <p class="blue">This raga is not listed in any of the Ragamala (there are 11 of such Ragmalas available
                    in the Indian musical circles) including the Ragamala listed at the end of Guru Granth Sahib. It is
                    believed that this raga was formed/invented by the Sikh Gurus. </p>
                <p class="blue"> In Guru Granth Sahib it has hymns from pages 94-151 (157 pages). </p>
                <p class="blue"><strong> The Composers: </strong></p>
                <p class="blue"> The composers of bani (hymns) in this raga are: </p>
                <p class="blue" style="font-weight: bold"><em>Gurus: </em></p>
                <ul>
                    <li class="blue"> Guru Nanak Dev</li>
                    <li class="blue"> Guru Angad</li>
                    <li class="blue"> Guru Amardas</li>
                    <li class="blue"> Guru Ramdas</li>
                    <li class="blue"> Guru Arjan Dev</li>
                </ul>
                <p class="blue"><strong><em>Bhagats: </em></strong></p>
                <p class="blue"> There is no Bhagat bani in this raga. </p>
                <p class="blue"><strong> The structure: </strong></p>
                <p class="blue"> The sequence of the structure of compositions in this raga are: </p>
                <p class="blue"><strong><em>Gurubani: </em></strong></p>
                <ul>
                    <li class="blue"> Shabads (4 padas)</li>
                    <li class="blue"> Shabads - Ashtpadis</li>
                    <li class="blue"> Specialist compositions titled 'Baramah' and 'Dinrein'</li>
                    <li class="blue"> Var</li>
                </ul>
                <span class="blue">The word 'Shud' is written at the end of the var. </span>
                <p class="blue" style="font-weight: bold"><em>Matrix </em></p>
                <p class="blue" style="font-weight: bold"><em>VISUAL ANALYSIS </em></p>
                <p class="blue" style="font-weight: bold"><em>Count of the use of Managals: </em></p>
                <p class="blue"> Complete Mool Mantar = 1 at page 93 <br/>
                    Ik-ongkar Satnam Kartarpurkh Gurprasadh = 1 at page 137 <br/>
                    Ik-ongkar Satguru Prasadh = 4 at pages 109, 132, 133, 136 </p>
                <p class="blue" style="font-weight: bold"><em>Placement and count of rahau verse/s: </em></p>
                <p class="blue"> The (chaupada) shabads have no rahau verse/s in them except one shabad
                    <strong>on </strong>pages 96-97 which has four rahau verses in it. </p>
                <p class="blue"> The ashtpadis of Guru Nanak, Guru Amardas and Guru Ramdas have one rahau verse in each
                    one of them placed after the first padas. The rahau verses in the ashtpadis of Guru Arjan are as
                    follows: </p>
                <ul>
                    <li class="blue"> First and third ashtpadis have a rahau verse placed after the first padas.</li>
                    <li class="blue"> Second and fourth ashtpadis have no rahau verses.</li>
                    <li class="blue"> Fifth ashtpadi has one rahau verse placed in the beginning of the ashtpadi.</li>
                </ul>
                <p class="blue"> The two specialist compositions, Baramah and Din rein, have no rahau verses in
                    them. </p>
