                        <td><p class="blue">Raga Dhanasri appears in the Ragmala as a ragini of Malkaunsa and currently
                                is a member of the Kafi thata, It closely resembles Bhimpalasi in musical content but
                                the vadis and moods are different. Dhanasri is performed in the early afternoon and
                                presents a cheerful, happy mood. It provided the setting for hymns by Guru Nanak, Guru
                                Ainar Das, Guru Ram Das, Guru Arjan and Gurfi Tegh Bahadur for a total of 101 hymns.
                                These texts stress that man reaps what he sows. Only in the Lord may be found the riches
                                that dispel fear and ignorance and thus cause man to realize his true self. This raga is
                                very appropriate for worship and supplication. Guru Nanak many <span
                                    style="font-size: 11px; color: #003366; ; border-style: none; font-family: Tahoma, sans-serif, Arial, Verdana;">Bhagats </span>have
                                composed<strong> Aarti</strong> in this raga. The Guru has described the adoration of
                                God by the objects of nature:<br/>
                                <br/>
                                &ldquo;What other worship can be compared to Nature&rsquo;s own Festival of Lights,<br/>
                                While the Divine Music resounds within!&rdquo;[9].<br/>
                                <br/>
                                Ravidas, Sain, Pippa and Dhana have composed <strong>Aarti-hymns in this raga.</strong>
                            </p>
                            <p class="blue"><b>Aroh</b> : Sa Ga'Ma Pa Ni Sa </p>
                            <p class="blue"><b>Avroh </b>: Sa _Ni Dha Pa&nbsp;&nbsp;&nbsp;Nla P a Ga Re Sa </p>
                            <p class="blue"><b>Vadi</b> : Pa </p>
                            <p class="blue"><b>Samvadi </b>: Sa </p>
                            <p class="blue">Pa is given considerable emphasis and _Ni and Pa receive sliding approaches,
                                a characteristic of this raga. The pentatonic ascent provides some of the melodic
                                features of this raga.</p></td>
