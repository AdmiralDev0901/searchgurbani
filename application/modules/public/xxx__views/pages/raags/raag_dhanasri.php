                <p class="blue" style="font-weight: bold">Introduction</p>
                <p class="blue"> According to one school of Music this raga is a ragini (consort) of raga Malkaus. It is
                    made up of a mixture of Todi, Asawari and Marwa ragas. It is sung in four different forms: Kafi
                    form, Multani form, Puria form and Bharvi form This raga is included in the Ragamala listed at the
                    end of Guru Granth Sahib. The scale and notes of the four forms listed above are the same and are as
                    follows: </p>
                <p class="blue"> Arohi (ascending scale) - sa ga ma pa ni sa </p>
                <p class="blue"> Avrohi (descending scale) - sa ni dha pa ma ga re sa </p>
                <p class="blue"> The vadi (most popular) note is 'pa' and samvadi (second most popular) note is
                    'sa'. </p>
                <p class="blue"> This raga is sung at the third part of the day i.e., from 12 p.m. to 3 p.m. The season
                    of its recitation is winter (shisher) i.e., during December and January. <br/>
                    In Guru Granth Sahib it has hymns from pages 660 - 695 (36 pages). </p>
                <p class="blue"><strong>The Composers: </strong></p>
