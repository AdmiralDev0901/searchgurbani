                <p class="blue" style="font-weight: bold"><em>VISUAL ANALYSIS </em></p>
                <p class="blue"><em>Count of the use of Managals: </em></p>
                <p class="blue"><em>Placement and count of rahau verses: </em></p>
                <p class="blue"> All shabads and ashtpadis of the Sikh Gurus, except Guru Tegh Bahadur, have rahau
                    verses in them, some with numeral '1' and some without numeral '1' (page 663-665). All rahau verses
                    have been placed at the end of the first padas of the shabads. </p>
                <p class="blue"> In Guru Tegh Bahadur's shabads, all rahau verses have numeral '1' with them and all
                    verses have been placed in the beginning of the shabads. </p>
                <p class="blue"> In the Bhagat Bani all the shabads have rahau verses in them, some with numerals and
                    some without numerals (page 694), and most of them have been placed at the end of the first padas of
                    the shabads. In the last shabad of Bhagat Ravidas (page 694), it is placed in the beginning of the
                    shabad. </p>
