<p>The Guru sent Hukamnamas to his followers all over the country to visit Anandpur at the Baisakhi festival to be held
    in Sambat 1756 (1699 A.D.). It seemed as if the whole of Punjab was on the move; and they came from all parts of the
    country.</p>
<p>A small tent was pitched on a small hill now called Kesgarh Sahib at Anandpur and an open air dewan(assembly) was
    held. The Guru drew his sword and in a thundering voice said,&quot; I want one head, is there any one who can offer
    me?&quot; This most unusual call caused some terror in the gathering and the people were stunned. There was dead
    silence. The Guru made a second call. Nobody came forward. There was still more silence. On the third call there
    rose Daya Ram, a khatri of Lahore who said,&quot; O true king, my head is at thy service.&quot; The Guru took Daya
    Ram by the arm and led him inside the tent. A blow and thud were heard. Then the Guru, with his sword dripping with
    blood, came out and said,&quot; I want another head, is there anyone who can offer?&quot;</p>
<p>NOTE: Most of the writers including many Sikh writers, state that the Guru had concealed five goats inside the tent
    on the previous night without letting anybody know. Therefore, when he took Daya Ram inside the tent, he cut off
    goat's head instead of Daya Ram's. It is difficult for these writers to perceive Guru's supernatural acts. They
    cannot comprehend that the Guru could behead Daya Ram, and then bring him back alive from the tent. They need to
    understand that the Guru was a Divine Jot, sitting on the Divine throne of Guru Nanak. They are showing complete
    disrespect to the Guru by implying that he was incapable of performing supernatural acts. With these types of
    thoughts, these writers are committing sacrilege upon the Guruship. The Guru had the power to raise the dead. The
    Divine Word confirms:</p>
<p>&quot;Satgur mera mar jiwalei.&quot; (Bhairon Mohalla 5, p-1142)</p>
<p>'My lord can raise the dead to life.' (Translation of the above)</p>
<p>This was not an ordinary feat, this was the most unparallel and supernatural act which was performed</p>
<p>through the direct Will of God. The Guru himself authenticates this act:</p>
<p>&quot;Khalsa is the army of God</p>
<p>Khalsa is created with the Will of God.&quot;</p>
<p><strong>(Guru Gobind Singh- Sarbloh Granth)</strong></p>
<p>Again on third call Dharam Das, a Jat from Delhi came forward and said,&quot; O true king! My head is at thy
    disposal.&quot; The Guru took Dharam Das inside the tent, again a blow and thud were heard, and he came out with his
    sword dripping with blood and repeated,&quot; I want another head, is there any beloved Sikh who can offer
    it?&quot;</p>
<p>Upon this some people in the assembly remarked that the Guru had lost all reason and went to his mother to complain.
    Mohkam Chand, a washerman of Dwarka (west coast of India) offered himself as a sacrifice. The Guru took him inside
    the tent and went through the same process. When he came out, he made a call for the fourth head. The Sikhs began to
    think that he was going to kill all of them. Some of them ran away and the others hung their heads down. Himmat
    Chand, a cook of Jagan Nath Puri, offered himself as a fourth sacrifice. Then the Guru made a fifth and the last
    call for a fifth head. Sahib Chand, a barber of Bidar (in central India), came forward and the Guru took him inside
    the tent. A blow and thud were heard.</p>
<p>The last time he stayed longer in the tent. People began to breath with relief. The Guru clad them in splendid
    garments. They offered their heads to the Guru, and the Guru had now given them himself and his glory. When they
    were brought outside, they were in the most radiant form. There were exclamations of wonder and the sighs of regret
    on all sides. Now people were sorry for not offering their heads.</p>
<p>Since the time of Guru Nanak, Charanpauhal had been customary form of initiation. People were to drink the holy water
    which had been touched or washed by the Guru's toe or feet. The Guru proceeded to initiate them to his new order by
    asking five faithful Sikhs to stand up. He put pure water into an iron vessel or Bowl (Batta of Sarbloh) and stirred
    it with a Khanda (two edged small sword). While stirring the water with Khanda, he recited Gurbani or Divine Word (
    Five Banis- Japji, Jap Sahib, Anand Sahib, Swayas, and Chaupai). Sugar crystals called 'Patasas' which incidently
    the Guru's wife, Mata Sahib Kaur, had brought at that moment, were mixed in the water.</p>
<p>The Guru then stood up with the sacred Amrit ( nectar) prepared in the steel bowl. Each of the five Faithfuls, by
    turn, each kneeling upon his left knee, looked up to the Master to receive his Eternal Light. Hegave five palmfuls
    of Amrit to each of them to drink and sprinkled it five times in the eyes, asking them to repeat aloud with each
    sprinkle, &quot;Waheguru ji ka Khalsa, Waheguru ji ki Fateh.&quot; (This meant: Khalsa belongs to God and all
    triumph be to His Name) Then he anointed with five sprinkles in the hair. In this way Amrit was administered to the
    five faithfuls from the same bowl. After that he asked them to sip Amrit from the same bowl to signify their
    initiation into the casteless fraternity of the Khalsa. All the five faithfuls were baptized in this way by the Guru
    who then called them 'PANJ PYARE' or Five Beloved Ones. He gave them the appellation of SINGHS or lions and they
    were named from Daya Ram to Daya Singh, Dharam Das to Dharam Singh, Mohkam Chand to Mohkam Singh, Himmat Chand to
    Himmat Singh, and Sahib Chand to Sahib Singh. The Guru then addressed them as the supreme, the liberated ones, pure
    ones and he called them THE KHALSA.</p>
<p>He then ordained them to do the following:</p>
<p>I. First they must wear the following articles whose names begin with 'K':</p>
<p>1. Kes- unshorn hair. This represents the natural appearance of saintlihood. This is the first token of</p>
<p>Sikh faith.</p>
<p>2. Kanga- A comb to clean the hair.</p>
<p>3. Kachha- An underwear to denote chastity.</p>
<p>4. Kara- A steel bracelet on the wrist, a symbol of dedication to the Divine Bridegroom.</p>
<p>5. Kirpan- A sword for self-defence and a symbol of dignity, power and unconquerable spirit.</p>
<p>II. They must observe the following guidelines:</p>
<p>1. Not to remove hair from the body.</p>
<p>2. Not to use Tobacco or other intoxicants.</p>
<p>3. Not to eat 'Kutha', a meat of an animal slaughtered by slow degrees as done by the Muslims.</p>
<p>4. Not to commit adultery- 'Par nari ki sej, bhul supne hun na jayo' (never enjoy, even in dream, the bed of a</p>
<p>woman an other than your own wife) (A supplementary ordinance was issued that any one who did not</p>
<p>observe any of the four directives, must be re- baptized, pay a fine, and promise not to offend</p>
<p>any more; or he must be excommunicated from the Khalsa).</p>
<p>III. They must rise at dawn, bathe, meditate on Gurmantar- 'Waheguru', Moolmantar- the preamble of</p>
<p>Japji, and recite five banis- Japji, Jap Sahib and Swayas in the morning; Rehras in the evening; and Kirtan</p>
<p>Sohela at bed time at night.</p>
<p>IV. They must not have matrimonial relations with smokers, with persons who killed their daughters, with</p>
<p>the descendants or followers of Prithi Chand, Dhir Mal, Ram Rai, or masands who had strayed away from</p>
<p>the tenets and principles of Guru Nanak.</p>
<p>V. They must not worship idols, cemeteries, or cremation grounds, and must believe only in One Immortal</p>
<p>God. The Guru further spelled out that they should practise arms, and never show their backs to the foe in</p>
<p>the battle field. They should always be ready to help the poor and protect those who sought their protection.</p>
<p>They were to consider their previous castes erased, and deem themselves all brothers of one family. Sikhs</p>
<p>were to intermarry among themselves.</p>
