<p>Bhai Nand Lal Goya, born at Ghazni in Afghanistan in 1643, was an accomplished persian scholar who composed verses in
    praise of God and Guru Gobind Singh. He was hardly nineteen when his parents passed away and after that he moved to
    the city of Multan. The Nawab of Multan being impressed with his scholastic talents and personality, appointed him
    as his 'Mir Munshi' (Revenue officer). At the age of 45 Nand Lal left the service and set out in pursuit of peace.
    At last he reached Anandpur. Nand Lal wanted to test the Guru before he could accept him. He took a small house and
    started living quietly in that and made up his mind that he would go to the Guru only when the Guru beckoned him.
    The Guru did not call for sometimes. During this period Nand Lal became very restless which he recorded:</p>
<p>&quot;How long shall I patiently wait? My heart is restless for a vision of thee, My tearful eyes, says Goya,</p>
<p>Have become flooding streams of love Flowing in a passionate affection towards thee.&quot;</p>
<p>(Nand Lal- Translated)</p>
<p>At last the Guru called Nand Lal. When he reached there for his holy sight, the Guru was sitting in a trance with his
    eyes closed. As Nand Lal saw the Master, he was wonder-stuck and he recorded:</p>
<p>&quot;My life and faith are held in bondage,</p>
<p>By His sweet and angelic face;</p>
<p>The glory of Heaven and earth,</p>
<p>Is hardly worth,</p>
<p>A hair of His golden looks.</p>
<p>O! How can I bear the light,</p>
<p>Shed by the piercing glance of His love,</p>
<p>To ennoble and enlighten life,</p>
<p>A glimpse of the Beloved is enough.&quot;</p>
<p>(Bhai Nand Lal)</p>
<p>&nbsp;</p>
<p>After a short while the Master opened his eyes and smiled as he looked towards Nand Lal. By mere opening of his eyes,
    he enabled Nand Lal to see the Divine. His one glance of Grace opened the spiritual eyes of Nand Lal. He bowed down
    saying,&quot;Lord, my doubts are dispelled. I have known the Truth. The doors of my heart are opened and I have
    attained peace.&quot;</p>
<p>Nand Lal, thus, continued to live at Anandpur in the service and love of the Master. One day the Gurucommanded him,&quot;
    You left the home and renounced the world; such a renunciation is not acceptable to me. Go back and live in the
    world, work for your living and serve the humanity; but remain unattached to Maya (materialism), keeping God alive
    in thy mind.&quot; Nand Lal asked,&quot; Whither should I go, O Master?&quot; The Guru replied,&quot; To whichever
    direction your feet carry thee.&quot;</p>
<p>Bhai Nand Lal bowed and left Anandpur and after sometimes he reached Agra, the city of Taj Mahal where Prince Bahadur
    Shah was holding his court. There were some poets, scholars and artists patronized by the prince. Nand Lal was soon
    recognized at Agra as a great scholar which earned him a high office and emoluments from the prince. It is said that
    Emperor Aurangzeb had to send a letter to the King of Persia and Nand Lal's draft of that letter was deemed as the
    most suitable. Upon this Aurangzeb sent for Nand Lal, and after an interview he remarked to his courtiers that it
    was a pity that such a learned man should remain a Hindu. Aurangzeb told Prince Bahadur Shah to convert Nand Lal to
    Islam by persuasion if possible, and by force otherwise. This news leaked out and Bhai Nand Lal with the help of
    Ghiasuddin, a Muslim admirer and follower of him, escaped from Agra one night, and fled to Anandpur, the only place
    where such refugees could find safe asylum.</p>
<p>Enjoying the blissful life at the Master's feet at Anandpur, Bhai Nand Lal then settled down to a routine of a
    devoted disciple. He presented to the Guru a Persian work called Bandagi Nama in praise of God, a titlewhich the
    Guru changed to Zindagi Nama, or 'Bestower of Eternal life'. The following few extracts are from that work:</p>
<p>&quot;Both worlds, here and hereafter, are filled with God's light; The sun and moon are merely servants</p>
<p>who hold His torches.</p>
<p>They who search for God are ever civil.</p>
<p>(Bhai Nand Lal- Translated)</p>
