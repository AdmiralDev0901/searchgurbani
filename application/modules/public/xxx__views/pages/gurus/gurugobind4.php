<p>After the Guru had administered Amrit to his Five Beloved Ones, he stood up in supplication and withfolded hands,
    begged them to baptize him in the same way as he had baptized them. This was the height of this remarkable episode
    setting up unparallel example in the world that first as Guru, he created the Khalsa blessing them with power,
    supremacy and glory, and then he himself became their disciple- Wonderful is Guru Gobind Singh, himself the Master
    and himself the disciple. In the annals of human history a disciple could become a Guru but never a Guru became a
    disciple. The Five Beloved Ones were astonished at such a proposal, and represented their own unworthiness, and the
    greatness of the Guru, whom they deemed God's Vicar upon earth. They asked him why he made such a request and why he
    stood in a supplicant posture before them. He replied,&quot; I am the son of the Immortal God. It is by His order I
    have been born and have established this form of baptism. They who accept it shall henceforth be known as the
    KHALSA. The Khalsa is the Guru and the Guru is the Khalsa. There is no difference between you and me. As Guru Nanak
    seated Guru Angad on the throne, so have I made you also a Guru. Wherefore administer the baptismal nectar to me
    without any hesitation.&quot; Accordingly the Five Beloved Ones baptized the Guru with the same ceremonies and
    injunctions he himself had employed. The Guru was then named Gobind Singh instead of Gobind Rai.</p>
<p>Guru Gobind Singh was the first one to take Amrit from the Khalsa, the Five Beloved Ones. About 80,000 men and women
    were baptized within a few days at Anandpur. By creating the Khalsa, the Guru embedded two qualities in one person.
    A Khalsa is a Saint-Soldier. A Sikh is a saint because he worships the All-Pervading Divine Spirit and in whom that
    Spirit shines day and night like a full moon. A Sikh is a soldier because he is ever ready to take up the arms to
    uphold righteousness.</p>
<p>The Guru promised the Five Beloved Ones (The Khalsa) that whenever they called upon him, he wouldagree to their
    proposal. This was the establishment of democratic Khalsa. The Guru fulfilled this promise by submitting to the
    demand of the Five Beloved Ones at the battle of Chamkaur and left the Garhi. The Guru himself gives the definition
    of his beloved Khalsa:</p>
<p>&quot;He who constantly keeps in mind Intent upon Ever Awake Living Light of Consciousness And never</p>
<p>swerves from the thought of One God; And he who is adorned with full faith in Him And is wholly</p>
<p>steeped in the Love of the Lord, And even by mistake never puts his faith in fasting Or in worship of</p>
<p>tombs, sepulchre or crematoriums, Caring not for pilgrimages, alms, charities, Penances or austerities;</p>
<p>Or anything else but devotion to One God; And in whose heart and soul the Divine Light Shines forth</p>
<p>as the full moon He is known as Khalsa, the purest of the pure.&quot;</p>
<p>(Guru Gobind Singh- Swayas)</p>
<p>The Persian historian Gulam-ul-din, the newswriter of that period, sent Emperor Aurangzeb a copy of the Guru's
    address to his Sikhs on the first of Baisakh, Sambat 1756 (1699 A.D.) which reads as follows:</p>
<p>&quot;Let all embrace one creed and obliterate differences of religion. Let the four Hindu castes who have</p>
<p>different rules for their guidance abandon them all, adopt the one form of adoration, and become</p>
<p>brothers. Let no one deem himself superior to another. Let none pay heed to the Ganges, and other</p>
<p>places of pilgrimage which are spoken of with reverence in the Shastras, or adore incarnations such as</p>
<p>Rama, Krishna, Brahma, and Durga, but believe in Guru Nanak and the other Sikh Gurus. Let men of</p>
<p>the four castes receive my baptism, eat out of one dish, and feel no disgust or contempt for one</p>
<p>another.&quot;</p>
<p>When the Guru addressed the gathering, several Brahmans and Khatris stood up and accepted the religion of Guru Nanak
    while others insisted that they would never accept any religion which was opposed to the teachings of the Vedas and
    Shastras.</p>
<p>So far the leadership had remained in the hands of non- militant urban Khatris from whom the majority of the masands
    were drawn, but now the situation had completely changed. Peasantry and other classes of rural areas formed the bulk
    of the converts. Even those people who had been considered the dregs of humanity were changed like a magic into
    something rich and super. The sweepers, the barbers and confectioners who had never touched a sword and whose whole
    generations had lived as slaves of the higher castes, became doughty warriors under the stimulating leadership of
    the Guru.</p>
<p>Ideologically, the Khalsa was created to be aimed at a balanced combination of the ideals of Bhakti andShakti, of
    moral and spiritual excellence and militant valor or heroism of the highest order; or in other words the Khalsa was
    to be a brotherhood in faith and brotherhood in arms at one and the same time. The Khalsa symbolized in itself the
    determination to complete the social and religious revolution inaugurated by Guru Nanak. The code of conduct
    prescribed for the newly created Khalsa was so devised as to impose a strict discipline on the Sikhs to ensure firm
    coherence and commitment on their part to the holy and lofty ideals of Sikhism.</p>
<p>With the creation of the Khalsa, some new doctrines were also established. The first doctrine of the Khalsa was the
    doctrine of the theocratic democracy by his selected, not elected, five representatives of the people from amongst
    the thousands of the devotees from all over the country while second was the doctrine of collective responsibility
    by authorizing the Five Beloved Ones only, in the presence of the holy Guru Granth Sahib to assume authority
    implicitly to be obeyed by the whole nation.</p>
<p>The Guru set the souls of the Khalsa free and filled their hearts with a lofty longing for religious and social
    freedom and national ascendancy. The Khalsa, therefore, accepted the challenge to combat terror inspired by tyranny
    of he powerful Mughal empire and embarked upon a national struggle of liberation.</p>
