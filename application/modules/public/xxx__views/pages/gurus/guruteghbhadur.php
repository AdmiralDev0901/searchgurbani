        <td align="left" valign="top"><p>'Baba Bakale', was the only clue given by Guru Har Kishen for his successor. As
                this word reached the village Bakala, twenty-two Sodhis including Baba Dhir Mal, the grandson of Guru
                Har Gobind, set up their shops and claimed themselves as the ninth Guru. The Sikhs were in great
                confusion as they could not know who the real Guru was. Makhan Shah Labana of Jehlem district was a
                trade merchant. When his vessel full of merchandise was sinking, he had invoked Guru Nanak and had vowed
                to offer five hundred gold mohars (coins) if the vessel reached the shore safely (some say that he vowed
                101 gold mohars). Makhan Shah came to the village of Bakala to pay his offering to the Guru. He was
                surprised to find that twenty-two Sodhis had installed themselves as Gurus. In that state of confusion
                and uncertainty, he resolved to try the Pretenders. He thought to put two mohars before each impostor
                and the real Guru being the searcher of hearts, would ask for the balance of his promised offering. He
                visited all the 22 impostors and made each of them offering of two gold mohars, but none of them asked
                for the balance.</p>
