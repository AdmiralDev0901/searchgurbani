            <p>When Guru Har Gobind shifted his headquarters to Kiratpur, most of his disciples had also moved to that
                place with him and the Golden Temple at Amritsar fell ultimately into the hands of the impostors like
                Harji Minas. In November 1664, Guru Tegh Bahadur went to Amritsar. He bathed in the sacred tank but the
                Pujaris (or the ministrants) closed the doors of the Har Mandar against him. He saluted it and remarked
                that it was they who were rotten within, who through greed of offerings, had entered the temple. When
                the news spread, the people of Amritsar went in a body and poured their souls at his feet. The women of
                the city took the lead, welcome him with the Guru's hymns and went with him singing all the way to the
                village Wadala (or Walla) where he stayed in the humble abode of a devout disciple. The Master blessed
                the women of Amritsar and Amritsar itself. On seeing their devotion he blessed them with these words,&quot;God's
                love and devotion shall ever abide among you.&quot;</p>
            <p><strong><a name="2" id="2">FOUNDATION OF CITY OF ANANDPUR:</a></strong></p>
            <p>Leaving Amritsar the Guru passed through the Majha and Malwa regions before reaching Kiratpur sometimes
                in May, 1665. He attended the last rites of Raja Dip Singh of Bilaspur and expressed his desire to build
                a new settlement near Kiratpur and also showed his inclination to buy a suitable land fo that purpose.
                The Rani of Bilaspur offered to donate the site of Makhowal. The offer was accepted, a token price of
                about five hundred rupees was paid. The foundation stone of new settlement, Chak Nanaki was laid in
                June, 1665, after the revered name of Guru's mother. In the course of time, a beautiful town called
                Anandpur grew up around it.</p>
            <p><strong><a name="3" id="3">GURU ON MISSIONARY TOUR:</a></strong></p>
