                    <td><p align="center">I HAVE FOUND THE GURU </p>
                        <p align="left"><strong>On finding his ship, full of rich merchandise, sinking, Bhai Makhan Shah
                                Lubana prayed that if his ship survived he would offer a certain amount to the Guru. But
                                when he came to Baba Bakala to make the offering, he found that there were too many
                                claimants to guruship. Presuming that the Real Guru would himself know and question him
                                to make the promised offering, he went around placing two coins before each one of them.
                                When the ninth Guru asked for the promised offer, Bhai Makhan Shah went to the top of
                                the roof and declared, </strong></p>
