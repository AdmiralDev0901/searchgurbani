            <p>He then inquired if there was any one else in Bakala. Someone informed him about Guru Tegh Bahadur.
                Makhan Shah went and as usual made his offering of two gold mohars. Upon this Guru Tegh Bahadur asked,&quot;
                How now, O Sikh, thou art trying to wheedle the Guru by presenting him with only two gold mohars? Where
                are the balance of five hundred gold mohars you had promised when your ship was sinking?&quot; Mukhan
                Shah was delighted and prostrated himself before the Guru. He then went to the roof of the house and
                screamed,&quot; Guru Ladho! Guru Ladho!&quot; 'I have found the Guru! I have found the Guru!'</p>
            <p>Guru Tegh Bahadur was the fifth and the youngest son of Guru Har Gobind and was born on first of April,
                1621 to Mata Nanaki at Amritsar, Guru ke Mahal. He was married to Mata Gujri, daughter of Lal Chand of
                Kartarpur in Jullundhur district. After Guru Har Gobind, he with his mother, Mata Nanaki and his wife
                went to live in Bakala.</p>
            <p>Makhan Shah's discovery of the genuine Guru put an end to the pretensions of the false Gurus. Dhir Mal
                could not reconcile with the situation and was determined to snatch the Guruship by force. One day he
                communicated his feelings to his masand, Sihan who promised to put an end to his enemy (Guru).
                Accordingly the masand, along with a score of people, set forth to kill the Guru. He fired and the
                bullet struck Guru's shoulder without serious wound who remained calm and full of composure. The other
                men plundered the property of the Guru and went away. When Makhan Shah heard of this incident, he
                proceeded with a body of Sikhs to Dhir Mal's residence. Dhir Mal closed his doors but they burst it open
                and seized him and his accomplices, tied his masand's hands at his back and brought them before the
                Guru. They brought back all the property of the Guru and they also took Dhir Mal's property. They
                brought back the original copy of the Adi Granth which was in the possession of Dhir Mal and placed it
                before the Guru. The masand Sihan fell at the feet of the Guru and asked for forgiveness for his sins.
                The Guru pardoned the masand and ordered Makhan Shah to return all the property of Dhir Mal including
                the Adi Granth. He preached Makhan Shah and his other Sikhs that the holy Guru Nanak gave them the
                wealth of Nam which was sufficient for all their wants.</p>
            <p><strong><a name="1" id="1">GURU VISITS AMRITSAR:</a></strong></p>
