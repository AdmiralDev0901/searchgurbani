            <p>He arrived at Maldah where he stayed with a Sikh who was a confectioner. From there he went to
                Murshidabad and next halt was at Dacca. There lived a devout masand, Bulaki Das whose mother had
                prepared a beautiful couch for the Guru. Knowing about her devotion, the Guru went to her residence. She
                was overjoyed and fell on his feet. He blessed her for her devotion. The Sikh inhabitants came in crowds
                to behold the Guru and to receive his instructions and benedictions. He asked them to build a dharamsal
                (Gurdwara) in their city where God's praises should be sung.</p>
            <p>Raja Ram Singh went to the Guru and said,&quot; The inhabitants of Kamrup and Assam became rebels against
                the rule of Delhi. The Emperor had recently sent Mir Jumla to subdue them but after some success, he
                died before eaching Dacca on his return journey. The Emperor has now ordered me to go and subdue the
                Kamrup country. If I conquer that country, it will be an addition to the Emperor's sovereignty; but if I
                am killed, the Emperor may annex my whole state of Rajputana. O true King, I have come to seek
                protection of thy holy feet.&quot;</p>
            <p>The Guru replied,&quot; God's Name is the medicine for all diseases, so meditate continually on Name.
                Guru Nanak will assist you and you will conquer Kamrup.&quot;</p>
            <p>Raja Ram Singh and the Guru left Dacca and reached Dhubri. The Guru encamped there and Raja Ram Singh set
                out for the city of Rangmati on the right bank of the Brahamputra. Soon after the battle between Raja
                Ram Singh's army and the army of the king of Kamrup ensued. The decisive victory for the Raja's forces
                was not easy because of difficult mountainous terrain, climate and rainy season.</p>
            <p>In addition to the attack, the king of Kamrup also began to make incantations and spells, and sent for
                all the women of his land who had magical skills, but none succeeded. After that he went to worship at
                the temple of goddess Kamakhsha. His mother- in-law saw in a vision, the goddess, who said,&quot; Guru
                Nanak has taken birth in this age. On his throne is now seated Guru Tegh Bahadur. Raja Ram Singh has
                become his disciple. The Guru is sovereign and nobody has the strength to oppose him. Go and make
                obeisance to him and ask for pardon otherwise your rule will perish.&quot;</p>
            <p>The king proceeded to the camp of the Guru and after prostration he said that he had come by the order of
                the goddess to pray for pardon and protection. He begged the Guru not to allow him to fall under the
                power of the Muslims. The Guru replied that Raja Ram Singh was a very religious person and he should
                meet with him. He, however, told the king not to fear, his empire would be permanent.</p>
            <p>The Guru sent for Raja Ram Singh and both of them were received by the king in a friendly manner. The
                Guru sat down placing the royal disputants on either side and effected a reconciliation. He putting his
                dagger in the ground declared,&quot; Let the territory on this side belong to the Emperor and the land
                on the other side belong to the king of Kamrup. Let both monarchs forget the enmity.&quot; Both sides
                agreed to the settlement and serious bloodshed on both sides was avoided.</p>
            <p>The Guru informed Raja Ram Singh that Guru Nanak had visited Dhubri and rendered it holy by his
                footsteps. He further asked that each soldier should bring five shieldfuls of earth to raise a tall
                mound in the memory of the founder of Sikh religion. A pavilion was erected at the top of the mound. The
                Guru spent a few more days there. Hearing his fame, the crowds came from far and near to behold him and
                also to receive religious instructions. Raja Ram of Assam, having heard Guru's praise, came to pay his
                homage. The Raja had no offspring and was desirous of a son. He brought his Ranis (queens) with him and
                after prostration beseeched the Guru,&quot; O true king, bring this sinking vessel to the shore.&quot;
                The Guru took off his signet ring and stamped its impression on the Raja's thigh and then said,&quot;
                The impression of my seal shall be on thy son's forehead. By this know it is Guru Nanak who hath
                mercifully granted thee offspring.&quot; While in Assam he also visited Cooch Behar, Chander Bhanga,
                Kishen Ganj and Purnea.</p></td>
