                    <td> When Guru Ji visited the Harimandir Sahib, the pujaris locked the gates and did not allow Guru
                        Ji to enter the Gurdwara. However the womenfolk of Amritsar welcomed Him. It was here that he
                        uttered these words, &quot;Amritsariey Ander sariey - Maian Rab Rajaian&quot; (Black hearted are
                        the men of Amritsar, but the women are blessed by God) .This episode is from the &quot;Suraj
