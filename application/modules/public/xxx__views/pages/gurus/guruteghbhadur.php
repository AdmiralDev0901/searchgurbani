            <p>After founding the new settlement, the Guru did not stay there long. However he entrusted the
                construction work to his trustworthy followers. It is said that the Guru undertook his missionary tour
                of the east in response to the invitation of his Sikhs from that area, Bhai Bulaki Das and Bhai Hulas
                Chand from Dacca and Bhai Darbara and Bhai Chain Sukh from Patna. These Sikhs had met the Guru at
                Kiratpur and begged him to visit their land in the east with his family. The Guru left Anandpur in
                August, 1665.</p>
            <p>After leaving Anandpur he passed through Ghanauli, Rupar, Dadoomajra and Lung village and then reached
                Mulowal in Patiala state. The people of Mulowal complained to him that they did not have drinking water
                nearby and or that purpose they had to travel a long distance. There was a well nearby but its water was
                brackish and unwholesome. The Guru told them to first repeat God's Name, then draw water, and they would
                find it pure and sweet. From that day the well yielded sweet water and it is known Guru's well.</p>
            <p>He then proceeded to Pharwali, Handiaya, Bhandehar, Khiwa and Bhikki. He gave religious instructions
                wherever he stopped and instructed the people not to worship idols and tombs, but worship only One God,
                the Formless. He passed through the villages of Dhaleo, Alisheir, Khiala and reached Maur where he was
                awaited by a great concourse of people to whom he preached true Name. He induced the people to sink a
                well over there. He then went to Maisarkhana and thence to Sabo ki Talwandi, now known as Damdama Sahib
                and then travelled to Kot Dharmwala, Bachhoana, Gobindpura, Sangheri, Gurna and reached Dhamdhan in the
                Bangar tract. The Guru presented the Chaudhri of the village with funds to construct a well and a
                dharmsala for the reception of the travellers.</p>
            <p>The Guru was accompanied by a Sikh, Ramdev, who was totally devoted to his service. He drew water,
                brought firewood from the forest for the kitchen, and performed all the services for the Guru. He always
                kept a cushion on his head to lift the burdens and it was continually wet from water and as a
                consequence his head festered. One day as he put his pitcher of water down, his cushion and turban fell
                off when maggots were seen from a sore in his head. It was brought to the notice of the Guru who sent
                for him. Being pleased with his devotion to service, the Guru gave him a robe of honor, named him Bhai
                Mihan and promised him that he would be a Mahant or a superior of religious order. On the Guru's
                instruction he preached Sikh religion. His generation is called today Mihan Shahi or Mihan Dasiay.</p>
            <p>The Guru then proceeded to Tekpur and he stayed for a few days in the house of a carpenter who conducted
                him as far as Kaithal. He reached Barna and preached here against the use of tobacco.</p>
            <p align="center">&nbsp;</p>
            <p><strong><a name="4" id="4">GURU AT KURUKSHETRA</a>:</strong></p>
            <p>The Guru reached Kurukshetra on the occasion of solar eclipse. He was received with great honor and
                distinction by all the holy men present there. During his stay he preached the true Name. From there he
                went to Bani Badarpur where he contributed money for the excavation of a well. Then he crossed the Jamna
                river and hunted on the way. He shot an animal and hung it to his saddle and reached Kara Manak where a
                saint called Maluk Das was living. Having heard that the Guru hunted and killed the animals, he refused
                to see him. It is said that when Maluk Das laid down food before his idol of worship next day, he found
                it turned into meat. He felt that it was a miracle wrought by the Guru. Then he wanted to see the Guru
                and bow before him, but he thought that the Guru being the searcher of hearts, should sent for him. The
                Guru knew what was going on in Maluk Das's mind, sent his Sikhs and a palki (litter) to fetch him. He
                went to the Guru, received religious instruction and initiation, and became one of his most devoted
                followers.</p>
            <p align="center">&nbsp;</p>
            <p><strong><a name="5" id="5">GURU IN UTTAR PRADESH:</a></strong></p>
            <p>From Kara Manak he proceeded to Mathura and reached Agra. There is a Gurdwara to symbolize his visit.
                Through Itawa he reached Kanpur where there is a Gurdwara at the bank of the Ganges. Then he reached
                Priyag (Allahabad). The Guru's mother told him that her late husband Guru Har Gobind, had promised that
                a great being would be born in the house of Guru Tegh Bahadur and so she was awaiting for that event. He
                replied that her desire would soon be fulfilled but she had to meditate continually on Guru Nanak. He
                stayed about six months at Priyag and to the great joy of his mother, his wife got pregnant. From Priyag
                he proceeded to Mirzapur where there is a Gurdwara on the bank of the Ganges. He reached Banaras (Kashi)
                and stayed in Resham Katra where a Gurdwara marks the memory of the Guru. Hundreds of people came to
                behold him.</p>
            <p align="center">&nbsp;</p>
            <p><strong><a name="6" id="6">GURU IN BIHAR PROVINCE:</a></strong></p>
            <p>He arrived at the village of Sasram where lived a very devout disciple called Chacha Phagoo who had built
                a mansion and within it placed a superb couch for the Guru. Every morning he used to perfume it and then
                would close the doors declaring that he would not live in it until the Guru had come, entered and
                hallowed it with his footsteps. Chacha Phagoo's desire was fulfilled and he had the happiness to
                entertain the Guru in that mansion. He he proceeded to Gaya. There the Brahmans met him in a body and
                explained the virtues of pilgrimage of Gaya. They said if barley rolls were offered to Brahmans at Gaya
                for the souls of ancestors, they would go to heaven even if they were already in hell. So they pressed
                the Guru to give money to perform such a ceremony for him. He refused to accept their argument rather
                exhorted them to meditate on God and instructed them on divine knowledge. The Guru then reached Patna
                and encamped at first in a garden outside the city. That place is called Guru ka Bag. Bhai Jaita, a
                devout disciple, took the Guru to his residence. The Master gave religious instructions to the people.
                One day he told his mother, Mata Nanaki that many Sikhs were waiting for him in a distant land, so he
                must go to them. He wanted the family to remain at Patna. On their remonstrance the Guru told his wife,&quot;
                The prophecy of my father is now about to be accomplished. A son shall be born to thee, who shall be
                great and powerful, extend the faith, establish Sikh supremacy, root out the wicked, and destroy the
                enemies of truth and true religion. You would suffer great hardship in travelling, so be happy here.&quot;
                He offered words of consolation to his mother and wife, thus, entrusting them to his brother-in-law
                Kirpal Chand, bade farewell and proceeded to Munger, Bhagalpur and Rajmahal.</p>
