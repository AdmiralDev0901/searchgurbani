<h2>Sri Guru Nanak Dev Ji</h2>
<p><strong>COMPOSITION AND COLLECTION OF BANI:</strong></p>
<p>These were the years when most of the Guru's disciples received religious instructions from him and who recorded what
    they received. Many devotees, it is said, copied the daily prayers and hymns. These collections were called 'Bani
    Pothis' (books of hymns). The Bani Pothi compiled during his life time was passed on to the second Guru, Guru Angad
    Dev.<br/>
</p>
<p><a name="20" id="20"></a><strong>FURTHER TRAVELS FROM KARTARPUR:</strong></p>
<p>Although the Guru had settled down at Kartarpur, but he still took small tours within the radius of 100 to 200 miles
    around Kartarpur. He went to many places and preached his gospel of Nam. At many of these places, the people became
    Guru's followers and they set up Gurdwaras in his honor.</p>
<p><strong>GURU AT ACHAL BATALA:</strong></p>
<p>About 25 miles from Kartarpur, there was a place called Achal Batala where on the occasion of Shivratri festival,
    hundreds of Jogis used to come to take part in the festival. The Guru also went to Achal <a
        onmouseover="drs('The Yogi.'); return true;" onmouseout="nd(); return true;"
        href="<?php echo base_url(); ?>gurus/page4_1.htm">Batala </a>to preach his doctrine. Thousands of people came
    from far and near to see and hear him. There were three camps- one of the Jogis, another of the Guru and the third
    one of a party of musicians. More and more people gathered around the Guru's camp than that of the Jogis. This made
    the Jogis very angry and jealous and they were determined to humble the Guru.</p>
<p>Whatever the money the musicians were getting from the audience, they put it in a bowl. Somehow the Jogis stole their
    bowl full of money and hid it someplace thinking that the musicians would go to the Guru for help and if the Guru
    was unable to locate the bowl, he would be humbled.</p>
<p>Knowing about the greatness of the Guru, the musicians went to the Guru for help to find their bowl of money. The
    wonderful Guru told them about the mischief of the Jogis and recovered their bowl from the hiding place. Thus the
    Jogis suffered a tremendous defeat.</p>
<p>Next attack from the Jogis came through a discussion. As mentioned before the Guru after his travels, laid aside the
    pilgrim's apparel and had put up ordinary dress of a family man. The Jogis said,&quot;O Guru, you are a holy man but
    you are wearing the garb of a family person. Why does a holy man lead a family life?&quot; Jogi <a
        onmouseover="drs('Bhangarnath was a head Jogi.'); return true;" onmouseout="nd(); return true;"
        href="<?php echo base_url(); ?>gurus/page4_1.htm">Bhagarnath</a> further asked the Guru,&quot;When the milk
    becomes sour, no butter is produced by churning it, why have you cast away your hermit's dress and donned ordinary
    clothes?&quot;</p>
<p>The Guru replied,&quot;O Bhangarnath, your mother was an unskilled woman. She knew not how to wash the churn, and so
    spoilt the butter in producing thee. Thou hast become an anchoret after abandoning thy family life, and yet thou
    goest to beg to the houses of family men.&quot;</p>
<p>Upon this reply the Jogis were enraged and through their miraculous powers, they started to harass the Guru. One Jogi
    became a cobra to frighten the Guru, the other became wolf and other started rain of fire. The powerful Guru sat
    calmly unperturbed and unharmed. When the Jogis were beaten badly, Bhangarnath asked the Guru that he exhibited
    miracles to the world, why he was slow to exhibit the same to them?</p>
<p>The Guru replied that he had no miracles except the True Name, and he uttered the following Sabad: </p>
<p>&quot;Were I to put on a dress of fire, construct a house of snow and eat iron;<br/>
    Were I to turn all my troubles into water, drink it, and drive the earth as a steed;<br/>
    Were I able to put the firmament into one scale and weigh it with a <a
