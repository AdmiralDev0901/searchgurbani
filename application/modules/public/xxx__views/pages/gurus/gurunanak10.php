    and were I to lead every one by the nose;<br/>
    Had I such power in myself that I could perform such things or cause others to perform them, it would be all in
    vain.<br/>
    As great as the Lord is, so great are His gifts; He bestoweth according to His pleasure.<br/>
    Nanak, he on whom God looketh with favor obtaineth the glory of the True Name.&quot;<br/>
    (Majh di Var, Slok Mohalla 1, p-147) </p>
