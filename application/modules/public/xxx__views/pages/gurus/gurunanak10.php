<p>The Jogis then finally complimented the Guru on his success and said,&quot;Hail, O Nanak, great are thy deeds! Thou
    hast arisen a great being, and lit a light in this age of falsehood (kalyug) in the world.&quot;<br/>
</p>
<p><a name="25" id="25"></a><strong>ASCENSION OF GURU NANAK:</strong></p>
<p>The Guru, knowing that his time to depart was approaching, had to appoint his successor. His sons had not obeyed him
    and so they did not prove themselves to be worthy of Guruship.</p>
<p>On September 2, 1539 (2 Asu, 1596 Asu vadi 5) Guru Nanak placed five Paise (Indian currency) before Bhai Lehna and
    bowed to him in token of his succession to the Guruship. He placed the umbrella of Spiritual Sovereignty over Bhai
    Lehna's head. <strong>Thus, he created another Nanak and called him GURU ANGAD DEV.</strong></p>
<p>&quot;Jot uha jugat sai seih kaya feir paltiai.&quot; (Ramkali ki Var- Rai Balwand, p-966) </p>
<p>'Divine Light is the same<br/>
    The Way and Mode are the same<br/>
    The Master has merely changed the body.' (Translation of the above)</p>

<p>When Guruship was passed on to Guru Angad, people realized that Guru Nanak was soon to depart bodily from the world
    (As a Divine Light and Spirit, the Guru is always present). The Sikhs, the Hindus and the Muslims came from all over
    to have holy glimpse of Guru Nanak.</p>
<p>After the proclamation of Guru Angad, the sons asked their father, what provision he had made for them. Guru Nanak
    replied,&quot;O my sons, God is the Cherisher of His creatures; you shall obtain food and clothing in abundance, and
    if you repeat God's name you shall be saved at last.&quot;</p>
<p>Guru's Muslim devotees wanted to bury him after his death. His Hindu followers desired to cremate his body. When the
    Guru was asked for his decision, he replied,&quot;Let the Hindus place flowers on my right and the Muslims on my
    left. Those whose flowers are found fresh in the morning, may have the disposal rights of my body.&quot;</p>
<p>The Guru drew a sheet over him. When the sheet was removed next morning, body was not found underneath, but the
    flowers on both sides were afresh. The light blended with Light and the spirit went back and merged with the Master
    Spirit. It confirms that the Guru was not a body but it was the Divine Light.</p>
<p>The Hindus and the Muslims removed their respective flowers and cut the sheet into two. The former cremated the sheet
    and the latter buried it. It happened at Kartarpur on September 22, 1539 (23rd day of Asu, Vadi 10, Sambat 1596). He
    was about seventy and a half years of age.</p>
<p>The Sikhs built a Gurdwara and the Muslims a tomb in his honor on the bank of river Ravi. Both had since been washed
    away by the river, perhaps by a superact, so as to avoid idolatrous worship of the Guru's last resting place.</p>
<p>Rituals and superstitions earned the sanctions of old times. Religion had degenerated into ceremonial acts only. The
    life and teachings of Guru Nanak offer consistent evidence of fruitlessness of rituals. He exposed their hollowness
    and exhorted human beings to rise above such customs. Guru Nanak's religion excluded all senseless dogmas and
    meaningless rituals. With no sword or stick armed with Divine Word, he preached that only Impersonal Absolute is to
    be worshiped. Any religion which does not guard its values indicates a lower level of development and is deemed to
    disappear in the long run.</p>
