<h2>Sri Guru Nanak Dev Ji </h2>
<p><a name="1" id="1">SUJJAN THUG</a><a name="sujjan" id="sujjan"></a><br/>
    <br/>
    According to Puratan Janamsakhi, before Guru Nanak turned towards east, he went to Tolumba (now known as Makadampur
    in west Pakistan) and met Sajjan Thug. Sajjan always wore a white dress, displayed his rosary and thus posed to be a
    holyman. He had built a Hindu temple and a Muslim mosque at the courtyard of his residence. He would invite
    wayfarers to his residence to rest for the night. But at night, he would take away their goods and money and
    sometimes kill them. The Guru went and stayed with him for the night. At night the Guru did not go to bed early
    which made Sajjan a bit too nervous to perform his nefarious act of robbing. Sajjan asked the Guru to take rest and
    sleep but the Guru replied,&quot;God's minstrel does not go to sleep, till God sends word that he should retire.&quot;
    The Guru then asked Mardana to play the rebec and he sang the following Sabad:<br/>
    <br/>
    <em>&quot;Bronze is bright and shining, but, by rubbing, its sable<br/>
        <br/>
        blackness appeareth,<br/>
        <br/>
        Which cannot be removed even by washing a hundred times.<br/>
        <br/>
        They are friends who travel with me as I go along,<br/>
        <br/>
        And who are found standing ready whenever their accounts<br/>
        <br/>
        are called for.<br/>
        <br/>
        Houses, mansions, palaces painted on all sides,<br/>
        <br/>
        When hollow within, are as it were crumbled and useless.<br/>
        <br/>
        Herons arrayed in white dwell at places of pilgrimage;<br/>
        <br/>
        Yet they rend and devour living things, and therefore should<br/>
        <br/>
        not be called white.<br/>
        <br/>
        My body is like the simmal tree; men beholding me mistake me.<br/>
        <br/>
        Its fruit is useless: such qualities my body possesseth.<br/>
        <br/>
        I am a blind man carrying a burden while the mountainous<br/>
        <br/>
        way is long.<br/>
        <br/>
        I want eyes which I cannot get; how can I ascend and traverse<br/>
        <br/>
        the journey?<br/>
        <br/>
        Of what avail are services, virtues, and cleverness?<br/>
        <br/>
        Nanak, remember the Name, so mayest thou be released<br/>
        <br/>
        from thy shackles.&quot; (Suhi Mohalla 1, p-729)&nbsp;<br/>
    </em> <br/>
    When Sajjan listened the Divine melody, he realized that the Guru's words were actually addressed to him. Upon this
    he made his obeisance and fell at the Guru's feet, and prayed to him to pardon his sins. The Guru said,&quot;Sajjan,
    in the Sovereignty of God, grace is obtained by two things, open confession and reparation for wrong.&quot; Sajjan
    stood in submission. The Guru asked him to give all his ill-gotten wealth to the poor. He obeyed the mandate and
    became a follower of the Guru after receiving Charanpauhal. It is said that the first historical Sikh temple was
    constructed on the spot where this conversation was held.</p>
<p align="center">&nbsp;</p>
<p align="left"><a name="2" id="2">GURU NANAK AT HARDWAR</a><a name="hardwar" id="hardwar"><br/>
    </a><br/>
    Hardwar is one of the Hindu pilgrimage places on the bank of river Ganges. It was a Baisakhi day and the pilgrims
    got up early in the morning and bathed in the river. As the sun came out, they started throwing water towards the
    sun. When Guru Nanak asked them as to what they were doing, one priest replied,&quot;We are offering water to our
    dead ancestors in the region of Sun to quench their thirst.&quot;<br/>
    <br/>
    Upon this the Guru started throwing water towards the west. The pilgrims laughed and asked what he was doing. The
    Guru replied,&quot;I am watering my fields in my village in the Punjab.&quot; The priest asked,&quot;How can your
    water reach such a distance?&quot; The Guru retorted,&quot;How far your ancestors are from here?&quot; One of them
    replied,&quot;in the other world.&quot;<br/>
    <br/>
    The Guru stated,&quot;If the water cannot reach my fields which are about four hundred miles away from here, how can
    your water reach your ancestors who are not even on this earth?&quot; The crowd stood in dumb realization. The Guru
    preached against superstitions and false rituals, worship of gods and goddesses, penances and renunciation. He
    stressed that only One God, the Formless, was to be glorified. In this way he showed the path of truth and
    enlightenment. There is a Gurdwara called Nanakwara in Hardwar on the bank of the river Ganges where the Guru had
    stayed.</p>
<p><a name="3" id="3">GURU AT GORAKHMATA</a><a name="13" id="13"></a><a name="13" id="13"><br/>
    </a><br/>
    After Hardwar, the Guru took his route towards Gorakhmata, about twenty miles north of Pilibhit, and reached there
    via Joshi Math and Almora. Almora was ruled by the rulers of Chand family and they used to do their offerings of
    human beings to please their goddess Chandi. The Guru showed them the path of truth and thus stopped them from
    massacring innocent people to please their goddess. From there he reached Gorakhmata which was the abode of Jogis of
    Gorakhnath clan. These Jogis had powers of Ridhi-Sidhi (supernatural powers). Their blessings were eagerly sought by
    the family men. People avoided their curses at all costs. Public from far and near had heard about these Jogis and
    their popularity was widespread. It is said that these Jogis (Yogis) had also heard about the Guru. When he reached
    there, they received him with great courtesy and invited him to adopt their cult, wear their garb and join them as a
    Yogi. The Guru explained to them that the life of seclusion which was not in the service of their fellow beings, was
    worthless. The Guru uttered the following Sabad:<br/>
    <br/>
    <em>&quot;Religion consisteth not in a patched coat, or in a Jogi's staff,<br/>
        <br/>
        or in ashes smeared over the body;<br/>
        <br/>
        Religion consisteth not in earrings worn, or a shaven head,<br/>
        <br/>
        or in the blowing of horns.<br/>
        <br/>
        Abide pure amid the impurities of the world; thus shalt thou<br/>
        <br/>
        find the way of religion.<br/>
        <br/>
        &nbsp;Religion consisteth not in mere words;<br/>
        <br/>
        He who looketh on all men as equal is religious.<br/>
        <br/>
        Religion consisteth not in wandering to tombs or places of<br/>
        <br/>
        cremation, or sitting in attitudes of contemplation;<br/>
        <br/>
        Religion consisteth not in wandering in foreign countries,<br/>
        <br/>
        or in bathing at places of pilgrimages.<br/>
        <br/>
        Abide pure amid the impurities of the world; thus shalt thou<br/>
        <br/>
        find the way of religion.<br/>
        <br/>
        <br/>
        On meeting a true Guru doubt is dispelled and the<br/>
        <br/>
        wanderings of the mind restrained.<br/>
        <br/>
        It raineth nectar, slow ecstatic music is heard, and man is<br/>
        <br/>
        happy within himself.<br/>
        <br/>
        Abide pure amid the impurities of the world; thus shalt thou<br/>
        <br/>
        find the way of religion.<br/>
        <br/>
        &nbsp;Nanak, in the midst of life be in death; practice such<br/>
        <br/>
        religion.<br/>
        <br/>
        When thy horn soundeth without being blown, thou shalt<br/>
        <br/>
        obtain the fearless dignity-<br/>
        <br/>
        Abide pure amid the impurities of the world, thus shalt thou<br/>
        <br/>
        find the way of religion.&quot; (Suhi Mohalla 1, p-730)<br/>
        <br/>
        &nbsp;On hearing this, the Yogis made Guru Nanak obeisanceeisance. The Guru's teaching became so effective that
        Gorakhmata became Nanakmata.</em></p>
<p align="left">&nbsp;</p>
<p align="left"><a name="4" id="4">REETHA SAHIB</a><br/>
    <br/>
    There were forests around Gorakhmata. About forty miles from there, the Guru met another group of Yogis. He sat
    under a soapnut tree and told them that by discarding family life and living in the forests away from worldly life,
    could not bring salvation. The inner change for attainment of peace and everlasting joy and happiness, could be
    obtained anywhere by contemplating on God's name. The Yogis asked,&quot;Master, the fire of desire is not quenched
    even by endless subjection of the body to discipline. Pray tell us a way to quench it.&quot; The Guru replied,<br/>
    <br/>
    <em>&quot;Destroy the feeling of egoism<br/>
        <br/>
        Destroy the sense of duality and attain oneness with Lord,<br/>
        <br/>
        The path is hard for ignorant and egoistic;<br/>
        <br/>
        But those who take shelter in the Word and absorbed in it,<br/>
        <br/>
        And he who realizes that He is both within and without,<br/>
        <br/>
        His fire of desire is destroyed by the Grace of the Guru,<br/>
        <br/>
        says Nanak.&quot; (Ramkali Mohalla 1, Sidh Gosht-46, p-943)<br/>
        <br/>
    </em><br/>
    The shrewd mind of the Yogis wanted to test the Guru still further. Knowing that the Guru did not have anything to
    offer, they asked him to give them something to eat. The Guru was sitting under the soapnut tree and soapnuts are
    always bitter. He gave them soapnuts to eat. To the utter surprise of the Yogis, the soapnuts were very sweet. By
    the Grace of God, the soapnuts of half-side of the tree where the Guru was sitting, became sweet and the other half
    of the same tree had bitter soapnuts. The same is true even to-day. That place is called Reetha Sahib and there is a
    Gurdwara in the memory of the Guru.</p>
<p align="left"><a name="5" id="5">GURU AT BANARAS</a><a name="banaras" id="banaras"></a><a name="15" id="15"><br/>
    </a><br/>
    After Gorakhmata, the Guru took southernly route and passing through Gola, Ayudhya and Prayag (Allahabad), reached
    Banaras, also called Varanasi- which was said to be the seat of Hindu religious learning and abode of Lord Shiva.
    The Guru and his companion Mardana encamped in a public square of the city. Pandit Chatur Das was the chief Brahman
    of the city. Guru's dress was neither of a family man nor of a Sanyasi (ascetic). Seeing this Pandit Chatur Das held
    a long discussion with the Guru. The Guru asked the Pandit what did he read, what did he teach to the people and
    what type of knowledge did he impart to his disciples? The Pandit replied,&quot;By the will of God I teach the
    people the fourteen sciences- reading, swimming, medicine, alchemy,astrology, singing the six rags and their
    raginis, the science of sexual enjoyment, grammar, music, horsemanship, dancing, archery, theology, and
    statesmanship.&quot; The Guru explained that better than all these was the knowledge of God. Upon this the Guru
    uttered the fifty-four stanzas of Ramkali Mohalla 1- Dakhni Omkar. The true God is superior to all other gods. The
    first two stanzas are as follows:<br/>
    <br/>
    <em>&quot;It is the one God who created Brahma;<br/>
        <br/>
        It is the one God who created our understanding;<br/>
        <br/>
        It is from the one God the mountains and the ages of the<br/>
        <br/>
        world emanated;<br/>
        <br/>
        It is the one God who bestowed knowledge.<br/>
        <br/>
        It is by the Word of God man is saved.<br/>
        <br/>
        It is by the name of the one God the pious are saved.<br/>
        <br/>
        Hear an account of the letter O-<br/>
        <br/>
        O is the best letter in the three worlds.<br/>
        <br/>
        Hear, O Pandit, why writest thou puzzles?<br/>
        <br/>
        Write under the instruction of the Guru the name of God, the<br/>
        <br/>
        Cherisher of the world.<br/>
        <br/>
        He created the world with ease; in the three worlds there is<br/>
        <br/>
        one Lord of Light.<br/>
        <br/>
        Under the Guru's instruction select gems and pearls, and<br/>
        <br/>
        thou shalt obtain God the real thing.<br/>
        <br/>
        If man understand, reflect, and comprehend what he readeth,<br/>
        <br/>
        he shall know at last the True One is everywhere.<br/>
        <br/>
        The pious man knoweth and remembereth the truth- that<br/>
        <br/>
        without the True One the world is unreal.&quot; (Ramkali Mohalla 1- Dakhni Omkar, p-929)</em><br/>
    <br/>
    <br/>
    On hearing the Sabad of Ramkali, Pandit Chatur Das fell at the feet of the Guru, and became a Sikh, and did much to
    spread Sikh religion in that area. The place where the Guru stayed, is now called as Guru ka Bagh Gurdwara.</p>
