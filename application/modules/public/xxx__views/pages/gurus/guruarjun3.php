<p><a name="JEALOUSY" id="JEALOUSY">PRITHI CHAND'S JEALOUSY CONTINUED:</a></p>
<p>Prithia addressed the Qazis and the Pandits who had enmity towards the Guru on account of his compilation of Adi
    Granth and he induced them to make a complaint to the Emperor that Guru Arjan had compiled a Granth in which Muslim
    and Hindu prophets were reviled. Upon this the Emperor sent for the Guru and the Granth. The Guru did not go himself
    but sent Bhai Buddha and Bhai Gurdas to read to the Emperor from the Granth. Various stanzas (Sabads) were read to
    him and Emperor Akbar was very much pleased and said,&quot;Except love and devotion to God, I find neither praise
    nor blame of any one in the Granth. It is a volume worthy of reverence.&quot; Guru's slanderers and enemies were
    stunned. Akbar gave Siropas (dresse s of honor) to Bhai Buddha and Bhai Gurdas, and promised to visit the Guru on
    his way back from Lahore.</p>
