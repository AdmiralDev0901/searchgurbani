<p>As promised Akbar visited the Guru on his return journey to Delhi. He was charmed and fascinated with Guru's saintly
    bearing. The Emperor partook of the Guru's hospitality and prayed that he be allowed to make contributions to secure
    spiritual and temporal welfare and happiness. The Guru replied,&quot;The welfare and happiness of monarchs depend on
    cherishing their subjects and doing justice.&quot; The Guru then stated that there was a severe famine in the land
    and cultivators required His Majesty's consideration. The Emperor remitted the revenue of the Punjab for that year.
    The Guru's fame and influence had largely increased owing to the respect the Emperor had shown to him. This became a
    cause of greater agony to Prithia.</p>
