<p>Prithia was composing his own religious hymns which he described as compositions of Guru Nanak and his successors.
    The ignorant people did not have sufficient intelligence to discriminate. Guru Arjan, therefore, felt the need to
    lay down rules to guide his followers in their daily religious duties. He made plans for the compilation of Adi
    Granth. For that purpose he chose a secluded spot outside the city which is now called Ramsar. He got a tank
    excavated there. Tents were erected for the accommodation. Guru Arjan took abode near the tank and dictated hymns to
    Bhai Gurdas who wrote them down. The verses were arranged according to Rags or musical measures. The hymns of the
    first Guru came first as Mohalla 1 (read as Mohalla pehla), then those of the second Guru- Mohalla II (read as
    Mohalla Duja) and so on. After the Bani of the Gurus, came the verses of the Bhagats or the Indian saints. The hymns
    of the Adi Granth were thus set according to thirty-one Indian Classical Ragas.</p>
<p>When the composition was completed, the Guru then wrote Mandawni as a conclusion and affixed his seal thereto:</p>
<p><em>&quot;Three things have been put into the vessel- truth, patience, and meditation.</em></p>
<p><em>The ambrosial Name of God, the support of all, hath also been put therein.</em></p>
<p><em>He who eateth and digesteth it, shall be saved.</em></p>
<p><em>This provision should never be abandoned; ever clasp it to your hearts.</em></p>
<p><em>By remembering God's feet, we cross the world of Maya; Nanak, everything is extension of God.&quot;</em></p>
<p><em>(Mundawni Mohalla 5, p-1429)</em></p>
<p><em>After this the Guru uttered the following Slok:</em></p>
<p><em>&quot;I can't appreciate what Thou didst for me, and yet Thou madest me worthy. I am virtueless;</em></p>
<p><em>I possess no merit, and yet Thou Thyself hast compassion on me.</em></p>
<p><em>Thou showest compassion and kindness unto me; I have found true Guru, the friend.</em></p>
<p><em>Nanak, If I obtain the Name, I shall live, and my body and soul shall be refreshed.&quot;</em></p>
<p><em>(Slok Mohalla 5, p-1429)</em></p>
<p>A Muslim might never like to read a hymn of a Hindu saint, and by the same token a Hindu might not like to hear the
    religious verse of a Muslim saint. The Hindus did not allow a saint, born in low caste family, to enter the Hindu
    temple. This was the religious fanaticism prevailing at that time. Guru Arjan, therefore, created an ocean in which
    all rivers and rivulets could fall and assume the appearance of the ocean itself. The composition of such an ocean
    was completed on Bhadon Vadi 1, Sambat 1661 (1604 A.D.) and was called Adi Granth. It was by no means a bible for
    the Sikhs alone, but it is universal in character. It contained no life story of the Gurus but only the Universal
    Truth, each and every word of which was dedicated to the Glory of the Almighty God only.</p>
<p>The composition of Adi Granth consisted of the hymns of the first five Gurus, Hindu saints (Brahmans as well as
    Sudras) and Muslim Sufis. These saints were: Beni, Bhikhan, Dhanna, Farid, Jai Dev, Kabir, Nam Dev, Parmanand, Pipa,
    Ramanand, Ravidas, Sain, Sadhna, Sur Das and Trilochan. It also contained the hymns of Minstrels (Bhats and Bards).
    These minstrels were all Brahmans and then became Sikhs of the Guru, they were- Kal, Jalap, Bhika, Sal, Bhal, Nal,
    Bal, Gyand, Mathura, Kirat and Harbans. It also consisted of Var of Satta and Balwand, Ramkali Sad by Sundar and
    five Sabads of Mardana, the minstrel of Guru Nanak.</p>
<p>On Bhadon Sudi first, Sambat 1661 (1604 A.D.), Adi Granth was installed in the Hari Mandar and Bhai Buddha was
    appointed as the first Granthi (priest).</p>
