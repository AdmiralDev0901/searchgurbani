<p align="center">&nbsp;</p>
<p>Guru Angad Dev was born on March 31, 1504 in a village called Harike in Ferozepur district of the Punjab. His father,
    Bhai Pheru was a trader. His parents called him Lehna. He was married at the age of fifteen. His wife, Khivi was a
    native of Mattei di Sarai in Ferozepur district. His father grew weary of Harike and with his family returned to his
    ancestral place, Mattei di Sarai and lived there. Bhai Lehna's wife gave birth to two daughters, Amro and Anokhi,
    and two sons called Dasu and Datu.</p>
<p>When Mattei di Sarai was sacked by the Mughals and Baloches, Bhai Lehna and his father moved to Khadur, now a famous
    town near Tarn Taran. Bhai Lehna grew very religious under the influence of his mother, Daya Kaur, and became a
    devotee of Durga, the goddess of Shakti. He used to organize yearly pilgrimage of devout Hindus to Jawalamukhi, a
    place of Durga temple in the lower Himalayas where fire issued from the mountains. He used to lead Durga dance
    around the fire in a harness of jingling bells.</p>
<p>Bhai Jodha, a Guru's Sikh, lived in Khadur and it was his daily routine to rise early very morning and recite Japji
    and Asa di Var. One day as Bhai Lehna attentively listened the Divine Sabad recited by Bhai Jodha, his mind obtained<br/>
    peace. After the day break he asked Jodha who had composed that stimulating hymn. Bhai Jodha then told him all about
    Guru Nanak, who was living at Kartarpur at that time. The touch of divine Sabad made such an impact on Bhai Lehna's
    mind that he got impatient to meet the Guru. When he was on his annual pilgrimage to Jawalamukhi, he broke his
    journey at Kartarpur to offer his obeisance to the Guru.</p>
