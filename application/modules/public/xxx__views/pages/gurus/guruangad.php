<p>During his meeting, the Guru spoke to him of the True Creator, leaving such an mpression on Bhai Lehna that he threw
    away the jingling bells, which he was carrying with him to dance before the goddess. He had obtained such a peace of
    mind that he decided to discontinue his pilgrimage and abide with the Guru. On seeing his ncreasing devotion, the
    Guru said to him one day that he should go home and settle his affairs and on his return he would initiate him as
    his Sikh.<br/>
    Upon this Bhai Lehna returned to Khadur for some time.<br/>
    A detailed account has been given in the last chapter regarding the circumstances which led to his succession to
    Guruship. One day as Sikhs assembled, Guru Nanak seated Bhai Lehna on his throne, put five paise and a coco-nut in
    front of him and bowed before him and then said to Bhai Buddha,&quot;This is my successor- Guru Angad; put a tilak
    on his forehead in token of his appointment to the Guruship.&quot; Bhai Buddha did so. The Guru then ordered his
    followers to obey and serve Guru Angad; who was in his own image. Bhai Gurdas describes the succession to Guru Angad
    (Var 1, pauri-45):<br/>
    &quot;Angad got the same tilak, the same umbrella over his head, and was seated on the same true throne as Guru
    Nanak. The seal of Guru Nanak's hand entered Guru Angad's, and proclaimed his sovereignty.&quot;<br/>
    After his appointment to the Guruship, Guru Nanak directed Guru Angad to return to Khadur. Upon this Guru Angad
    returned to Khadur and lived there.</p>
<p><strong><a name="SECLUSION" id="SECLUSION">GURU ANGAD IN SECLUSION</a></strong></p>
<p>The Guru sat in a room locked from outside near Khadur, and meditated on God without ny distraction or interruption.
    He did not eat or drink anything except a pot of milk daily. About six months passed like this and the Sikhs did not
    know the whereabouts of the Guru. One day Bhai Lalo, Bhai Saido and Bhai Ajita and other Sikhs came to Bhai Buddha
    and asked him the whereabouts of the Guru.They had searched Khadur and other places but could not find him anywhere.
    It is said that Bhai Buddha concentrated his thoughts on the Guru and was able to visualize his place of meditation.
    Next morning they all went to the house near Khadur where the Guru was sitting in seclusion. The owner of the house
    gave them no information but went inside the house and told the Guru about the visit of four Sikhs. The Guru told
    the owner that they should be shown inside. He embraced Bhai Buddha and uttered the following Slok:<br/>
    <br/>
    &quot;Cut off the head which boweth not to the Lord, Nanak, take and burn the<br/>
    wretched body which feeleth not the pain of separation.&quot; (Slok Mohalla 2, p-89)<br/>
    <br/>
    Bhai Buddha requested him to take his seat as Guru and receive the Sikhs publicly. fter this Guru Angad came forth
    from his seclusion. When the Guru came out, crowds went to see him and presented to him their offerings.
    Whatever<br/>
    he received, the Guru passed on to his kitchen. There were continuous preaching, singing of hymns and repetition of
    Name.<br/>
</p>
