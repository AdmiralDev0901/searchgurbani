<p> Emperor Humayun succeeded his father Baber but he was badly defeated by Sher Shah. umayun inquired for some saint
    who could help him regain his throne and kingdom. He was advised to seek assistance of Guru Angad. Upon this Humayun
    came to Khadur. At that time the Guru was in a trance and the minstrels were singing the hymns. The Emperor was kept
    standing unattended. Humayun felt offended and in moment of rage, he put his hand on the hilt of his sword with the
    intention of striking the Guru. The word, however, did not come out of the sheath<br/>
    which gave Humayun time to repent his act. Upon this the Guru addressed to him,</p>
<p>&quot;Where was your sword when you were facing Sher Shah? Now when you have come amongst the priests, instead of
    saluting them respectfully, you want to draw your sword on them. In a cowardly manner you fled from the battle
    ground, now posing as a hero you wish to attack the priests engaged in their devotion.&quot;<br/>
</p>
<p>Humayun repented and begged for Guru's spiritual assistance. The Gurureplied,&quot;Hadst thou not put thy hand on the
    hilt of thy sword, thou shouldst at once obtained thy kingdom. Thou shalt now proceed for a time to thine own<br/>
    country, and when thou returnest thou shalt recover thy kingdom.&quot; Humayun went back to his country and having
    obtained a reinforcement of cavalry from the king of Persia, he returned to India. After fighting a pitched battle
    he recovered his empire and captured Delhi.</p>
