<p>FN-2:It is said that Aurangzeb was at Hasan Abdal at that time but he was being continually reported about the Guru's
    activities.</p>
<p>FN-3:Some writers say that Emperor Aurangzeb was not at Delhi at that time as he had gone on an expedition to Hasan
    Abdal, but Guru's execution was carried on as per orders of him. Others say that all conversation took place
    directly between the Guru and Aurangzeb because this opportunity was unique and of utmost importance to achieve his
    goal of ultimate mass conversion toIslam.</p>
<p>FN-4:It is said that the Guru had told Bhai Jaita that his head would fall into his lap, and he should fear nobody,
    take it to Anandpur and cremate it there. It is also said that Bhai Jaita shared this secret with his neighbor Bhai
    Nanu and he also took Bhai Uda, a resident of Ladwa, into confidence and three of them took Guru's head to
    Anandpur.</p>
