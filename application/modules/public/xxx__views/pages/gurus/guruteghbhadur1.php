<p>The Guru saw a shepherd boy in the garden whom he gave his gold ring studded with diamonds and asked him to pledge it
    and bring him two rupees worth of sweets. When the boy told him that he had no cloth to wrap the sweets, the Guru
    gave him his valuable shawl for that purpose. The boy took his grandfather, Hasan Ali along with him and stopped at
    the confectioner's shop, gave him the ring and asked for sweets to be wrapped in the shawl. On seeing the ring and
    the valuable shawl, the confectioner was astonished and asked the boy from whom he had received those articles. The
    boy told him the truth but the confectionerbecame suspicious and took them to the police. The police went to the
    garden with the boy and asked the Guru who he was. When he disclosed his identity, the police officer was delighted
    that he would get a large reward from the Emperor for his capture. The Guru was thus imprisoned. The Governor of the
    fort reported the arrest to the Emperor. Ultimately he was brought to Delhi. There were three Sikhs, Bhai Mati Das,
    Bhai Dayala and Bhai Sati Das with the Guru (Some writers account for five Sikhs- Mati Das, Gurditta, Uda, Chima and
    Dayala) who were arrested with him and were brought to Delhi.</p>
<p>The Emperor explained that God appeared to him in a vision and told him to convert the whole world to Islam. Those
    who were to embrace Islam, would be rewarded with wealth, appointments, land revenue grants and lands. The Emperor
    tried to lure him,&quot; In this way you will have many disciples, and you will become a great priest of Islam.
    Therefore accept my religion- Islam, and you will receive from me whatever your heart desire.&quot; The Guru asked
    for one and one-quarter of maan (about 100 pounds) of black pepper. When it was brought, it was put into a heap and
    was ignited. The heap of pepper was let burning for twenty-four hours and was apparently reduced to ashes. The heap
    was then pounded and sifted. Three pepper pods came out as whole. The Guru addressed,&quot; O Emperor, you desire
    that there should be only one religion (Islam) out of two religions (Hinduism and Islam), but as these three pepper
    pods have been saved from the fire, God wishes to make three religions out of two. So there shall be three
    religions- Hinduism, Islam, and Sikhism in the future.&quot;</p>
<p>Upon this it was ordered that the Guru be imprisoned with sufficient guards around him. Again he was sent or and was
    told that if he embraced Islam, every service would be performed for him otherwise he would be severely tortured. He
    replied that he would never embrace Islam and thus, remained in Delhi jail for eight days. He was given three
    choices: firstly to embrace Islam; secondly to perform a miracle; and thirdly to prepare himself to court death. The
    Guru responded that to show a miracle was against the Will of God and thus he would not consent to the Emperor's
    proposals and the Emperor might act as he pleased. He was then put to extreme tortures.</p>
<p><img src="<?php echo base_url(); ?>images/gurus/gtb10.jpg" align="right" height="285" width="221"/>It is said that
    there was conversation between the Guru and his disciple Bhai Mati Das. He told Mati Das that Guru Nanak had blessed
    Emperor Babar with the continuation of his empire for a long time. Since the Mughal Emperors started committing
    great enormities, their line would be exterminated if he (Guru) laid down his life. In consequence of this
    conversation which was overheard by a priest, Bhai Mati Das was bound between two pillars and his body was sawn
    asunder. When the executioners put saw on his head, he began to recite Japji (the first Bani in Guru Granth Sahib).
    It is said that when his body was cut into two, he continued reciting Japji and he was silent only when the
    recitation of Japji was complete. This was a wonder of Guru's Grace. Bhai Dayala was boiled to death in a cauldron
    of hot water. It is said that the third companion Bhai Sati Das was roasted alive with cotton wrapped round his
    body. The authorities thought that these tortures of his Sikhs might shake the Guru. Nothing could and nothing can
    shake the Divine Light (the Guru).</p>
<p>It is generally believed in Sikh circles that there was some communication between the Guru and his son when the Guru
    was being detained in Delhi jail before his execution. The story runs that Guru Tegh Bahadur foreseeing his
    execution, wanted to test the capability of his nine years old son and so he wrote the following couplet (Slok) and
    sent it through a messenger to Anandpur:</p>
<p>&quot;All power shattered, humanity in fetters and availeth no resource;</p>
<p>Saith Nanak, God is now only refuge; He should succor as He did the elephant.&quot;</p>
<p>(Slok Mohalla 9 (53), p-1429)</p>
<p>It is being assumed that the Guru's nine years old son wrote back:</p>
<p>&quot;With power, fetters break, availeth all in grace Divine;</p>
<p>Nanak, everything is in Thy power, it is only Thou Who canst assist.&quot;</p>
<p>(Slok Mohalla 9 (54), p-1429)</p>
<p>It is the common opinion that upon receiving this reply, the Guru was convinced that his son was capable to take
    reigns of the Guruship.</p>
<p>Let us examine the merits of the above story which is prevalent in Sikh circles:</p>
<p>Firstly there are 57 Sloks (couplets) at pages 1426-29 of Guru Granth Sahib which begin under the heading</p>
<p>'Slok Mohalla 9'. This heading means that all the Sloks under this heading were uttered by the</p>
<p>Ninth Guru.</p>
<p>Secondly how far it is valid to say that the Guru wanted to test the capability of his son? Being a</p>
<p>Divine Guru and sitting on the throne of Guru Nanak, did he not know himself whether the son was</p>
<p>capable? Did he have to ask him?</p>
<p>Thirdly if it is argued that the Guru was worried about the young age of his son to take command of</p>
<p>Guruship, what about the eighth Guru? The eighth Guru was only five years old when he was installed</p>
<p>on the throne of Guru Nanak. How much this argument of being too young holds good?</p>
<p>Fourthly as explained earlier also, in the case of Guruship, the age, experience and intelligence of a</p>
<p>person did not matter. Once the person was invested with the Guruship, then the Divine Power</p>
<p>worked itself, and the age, experience and intelligence of a person in question was of no consequence.</p>
<p>In the light of the above circumstances, it seems evident that all the 57 Sloks belong to the Ninth Guru</p>
<p>and none to the Tenth Guru.</p>
<p>The final message was given to the Guru,&quot; You are to accept the religion of Islam or show a miracle. If you work
    a miracle, you may remain a Guru. If you accept Islam, then you will be advanced to an exalted position. If you fail
    to accept these offers, you shall be put to death. This is the final decision.&quot;</p>
<p>The Guru emphasized,&quot; I will never abandon my faith. I want no honor in this life; I want honor hereafter. The
    threat of death possesses no terrors for me. For death I am prepared and I cheerfully accept it.&quot;</p>
