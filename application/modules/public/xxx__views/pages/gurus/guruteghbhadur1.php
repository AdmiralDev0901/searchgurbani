<p>A deputation of Kashmiri Pandits (Brahmans) came to Anandpur and among tears of agony, they narrated their tales of
    woe and suffering to the Master. The Guru's eight years old son appeared on the scene and asked his father why those
    people had tears in their eyes. He replied,&quot; The Emperor of India is converting the Hindus to Islam at the
    point of the sword and thus there is no end to the misery of these people.&quot;</p>
<p>&quot;What is the remedy, father?&quot; asked the son.</p>
<p>The Guru replied,&quot; This requires sacrifice- sacrifi ce of a holy and supreme soul.&quot; His son responded,&quot;
    O dear father, who is more holy than you in this age? Go and offer yourself and save these people and their
    religion.&quot; On hearing this the Guru asked the Kashmiri Brahmans to go to the Emperor and make the following
    representation to him,&quot; Guru Tegh Bahadur, the ninth Sikh Guru is now seated on the throne of the great Guru
    Nanak, who is the protector of faith and religion. First make him a Musalman and then all the people, including
    ourselves, will of our own accord adopt the faith of Islam.&quot;</p>
<p align="center">&nbsp;</p>
<p>&nbsp;</p>
<p><strong><a name="11" id="11">GURU SUMMONED TO DELHI:</a></strong></p>
<p>The Pandits obeyed the Guru and conveyed the proposal to the Emperor. On hearing this proposal, the Emperor was very
    much pleased because he thought that it was much easier to convert one person than the whole lot. He retorted,&quot;
    If the Guru does not become Musalman, he will then at least show us a miracle.&quot; He was hopeful that once the
    Guru was converted, there would be a large accession of Hindu and Sikh converts. The Emperor, therefore, sent his
    emissary to the Guru to invite him to Delhi. The Guru received Emperor's message and wrote in reply that he would
    come to Delhi after the rainy season.</p>
<p><strong><a name="12" id="12">MARTYRDOM OF GURU TEGH BAHADUR:</a></strong></p>
<p>The Guru took leave of his family and his devoted Sikhs and began his journey to Delhi sometimes in June-July. rom
    Anandpur he passed through Kiratpur, Rupar and various villages before reaching Saifabad in Patiala state to see his
    Muslim friend Saif-ul-din. He stayed for sometimes with him. Saif-ul-din became his disciple. He went to Samana
    where he met another disciple called Mohammad Bakhsh. The Guru continued his journey through Kaithal, Lakhan Majra,
    Rohtak and other places, conferring temporal and spiritual favors on his disciples, and finally he reached Agra
    where he encamped in a garden outside the city.</p>
<p>After the rainy season, the Emperor again sent his messenger to hasten the Guru's presence to Delhi. When the
    messengers were unable to find the Guru, they reported that he had fled. Orders were issued all over the empire to
    find and arrest him. There are different views about the place of Guru's arrest. Some writers say that he was
    arrested at Dhamdhan; some say that he was arrested at Malikpur near Rupar and others say that the arrest was
    effected at Delhi while others still account for his arrest at Agra. According to Sikh accounts there lived a poor
    old man, Hasan Ali, at Agra. He knew that there were orders about Guru's arrest and the person who could effect his
    arrest, would receive one thousand rupees as a reward. Hasan Ali prayed,&quot; O true Guru, if ever you want to get
    arrested, please do it through me. This will fetch me some money to bring my family out of the clutches of miserable
    poverty.&quot; The Guru being the searcher of hearts, came to Agra to get arrested through Hasan Ali.</p>
