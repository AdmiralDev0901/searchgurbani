<p>Hearing this reply it was ordered that the Guru should be executed. Saiyid Adam Shah accompanied by courtiers and
    Muslim priests came with a warrant for his execution. Many people turned out to witness the execution. He was then
    taken out of his cage and allowed to perform his ablutions. He sat under the banyan-tree where he recited Japji. The
    executioner, Jalal-ud-din of Samana (some say it was Adam Shah) took his sword and in a split of second, severed
    Guru's head from the body. This happened on the afternoon of Thursday, the fifth day of the light half of the month
    of Maghar in Sambat 1732 (November 11, 1675) at Chandni Chowk, Delhi where now stands Gurdwara Sis Ganj in his
    memory. This Gurdwara was constructed by Sardar Baghel Singh Karor-Singheiye in 1790.</p>
<p>History has recorded that a furious storm raged immediately after this brutal deed which filled every one's eyes with
    dust. Bhai Jaita dashed out of the crowd and instantaneously took away the holy head of the Guru to Anandpur. He
    reached Kiratpur on the 15th of November, 1675. From there the Guru's head was taken to Anandpur with full honor and
    on the 16th of November, 1675, it was cremated with full ceremonies. There is a Gurdwara called Sis Ganj at Anandpur
    where the hallowed head of the Guru was cremated. The tenth Guru received Bhai Jaita who belonged to backward
    classes, embraced him and said,&quot; Rangrettei Guruke bettei&quot; (Rangrettei were the sons of the Guru,
    Rangrettei was Bhai Jaita's caste). Bhai Jaita told the young Guru and his family how Guru Tegh Bahadur had sent for
    five paise and a coco- nut and bowed to his son Gobind Rai, made him the successor and infused his Light unto
    him.</p>
<p>Lakhi Shah Labana was a famous contractor in Delhi and he was also a follower of the Guru. He emptied his carts laden
    with lime near the Red Fort, taking advantage of the darkness and the carelessness of the Mughal sentries, and with
    the help of his sons, Nagahiya, Hema, Harhi and his friend Dhuma, whisked away the sacred body of the Guru, in one
    of their carts. Apprehensive of the government reprisal, Lakhi Shah and his sons then built up a pyre inside their
    own house and set fire to it. When the body was duly reduced to ashes, they cried out that their house had caught
    fire and called upon their neighbors to assist them in extinguishing it. Next day they collected the Guru's remains
    and buried them in a copper vessel called 'gaggar' in the earth under his funeral pyre. On this spot there stands a
    Gurdwara, Rakab Ganj, near Parliament House in New Delhi.</p>
<p>&quot;Having broken his potsherd on the head of the king of Delhi, he departed for Paradise;</p>
<p>No one else coming into the world acted like Tegh Bahadur. The world was in mourning for the</p>
<p>departure of Tegh Bahadur;</p>
<p>There was weeping for him in the whole world, but rejoicing in paradise.&quot;</p>
<p>(Guru Gobind Singh- Bachitar Natak)</p>
