<h2>Sri Guru Tegh Bhadur Sahib Ji</h2>
<p><strong><a name="9" id="9">AURANGZEB'S CAMPAIGN OF RELIGIOUS PERSECUTION:</a></strong></p>
<p>As Aurangzeb ascended the throne of India by imprisoning his father and murdering his brothers, he decided to enlist
    the sympathies of the fanatical section of his co-religionists. His idea was to exterminate the idolatrous Hindus
    and to convert the whole of India to Islam. In order to achieve this objective he tried to go through four
    fundamental means to deal with the Hindus. Firstly he made peaceful overtures; secondly he offered money; thirdly he
    threatened punishment and lastly he tried to cause dissention among them. When all these measures failed, he
    resorted to forcible conversion. Orders were issued to the governors of all the provinces that they should destroy
    the schools and temples of the infidels and thereby put an end to educational activities as well as the practices of
    the religion of the Kafirs (non-Muslims meant Hindus). Many temples at Mathura and Banaras were destroyed. Even a
    Sikh temple in Buriya in Khizrabad pargna of Sirhind had been demolished and a mosque was built on the site. Some
    Sikhs, however, attacked the mosque and killed the priest. This type of incidents had become common occurrences. In
    order to force conversion to Islam, all possible means were adopted. In the field of taxation, the policy of
    discrimination was launched with great vigor. Jaziya and pilgrimage taxes were re- evied. Five percent custom duty
    was levied on the Hindus while the Muslims were charged only half of that.</p>
<p>The proselytizing zeal of the officials, with their campaign of religious persecution and their conversion at the
    point of the sword, had sent the wave of terror throughout the country. Sher Afghan Khan, the Emperor's viceroy in
    Kashmir, set about converting the Kashmiri Hindus by force and massacred those who opposed to embrace Islam. Even
    the Mohammadans who in any way assisted the Hindus, were mercilessly put to death. In extreme agony of too much
    slaughter, the Brahman priests of Kashmir prayed to their gods. It is said that the Kashmiri Brahmans heard a
    supernatural voice who told them,&quot; Guru Nanak is the spiritual king in this age. Guru Tegh Bahadur is now
    seated on his throne. Go to him, he will protect your honor and your religion.&quot;</p>
