            <p>Mai Das was a renowned Pandit and a devout worshipper of Lord Krishna. He was a strict Vaishnav, he would
                eat only what he had cooked with his own hands within a purified square. The Guru rejects these purified
                squares:</p>
            <p>&quot;All outlined purified squares are false; O Nanak, Only God is pure.&quot; (Maru ki Var, Slok
                Mohalla3, p-1090)</p>
            <p>When he came to see the Guru, he was informed that unless he had eaten from Guru's kitchen, he could not
                see him. Being a strict Vaishnav he could not do that, so he left for Dwarka where he thought to have a
                glimpse of Lord Krishna. On the eleventh day of lunar month Mai Das used to fast and would eat just
                fruits during that period. Due to winter season the fruits were not available in the forest. Mai Das
                wandered hungry in the forest looking for fruits but could not find them. Finally he started calling on
                his gods for help.</p>
            <p>At last he heard a voice,&quot;You have not eaten food from the Guru's kitchen, and you have not had holy
                sight of him; therefore shalt thou not obtain perfection. If you desire to do so, then first behold Guru
                Amar Das.&quot; Upon this Mai Das returned to Goindwal. He partook of food from the Guru's kitchen, and
                then was allowed to see the Guru. The Guru welcomed him,&quot; Come, Mai Das, thou art a special saint
                of God.&quot; The Guru initiated him as his Sikh, blessed him with Nam and bestowed on him the spiritual
                power of conferring salvation on others. Mai Das held one of the 22 Manjis (dioceses) set up by Guru
                Amar Das.</p>
