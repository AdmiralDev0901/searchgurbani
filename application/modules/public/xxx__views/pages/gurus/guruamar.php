            <p>Akbar, the Emperor of India, on his way to Lahore, paid a visit to the Guru at Goindwal. He was informed
                that he could not see the Guru until he had dined with others from the Guru's kitchen. Akbar partook of
                the food in the Langar, the more he had it, the more he relished it. After that the Emperor had an
                interview with the Master. It is said that the Guru rose to receive the Emperor in his arms, but Akbar
                spontaneously bowed to touch the feet of the Master. The monarch felt a thrill of joy and peace by the
                holy touch.</p>
            <p>Having seen the large number of people fed from the Guru's kitchen, Akbar requested the Guru to accept
                his services and his offerings. But the Guru replied,&quot; I have obtained lands and rent- free tenures
                from my Creator. Whatever comes daily is spent daily, and for the morrow my trust is in God.&quot; Akbar
                then replied,&quot; I see you desire nothing. From thy treasury and thy kitchen countless people receive
                bounties, and I also entertain similar wishes, I will grant these 84 villages to thy daughter, Bibi
                Bhani.&quot; This was the estate where Guru Ram Das built the city of Ramdaspur which is now called
                Amritsar.</p>
            <p><a name="COMPLAINT" id="COMPLAINT">A COMPLAINT AGAINST GURU TO AKBAR</a></p>
            <p>When the Brahmans and the Khatris failed in their mission to derail the Guru from Guruship by inciting
                Datu to declare himself as Guru, they made a special complaint to Emperor Akbar. In their complaint they
                alleged,&quot; Every man's religion is dear to him. Guru Amar Das of Goindwal has abandoned the
                religious and social customs of the Hindus, and abolished the distinction of the four castes. He makes
                his followers of all castes sit in a line and eat together from his kitchen irrespective of caste or
                religion. There is no offering of water to ancestors, no pilgrimages, no worship of idols of gods or
                goddesses. The Guru revereneth not Jogis, Jatis or Brahmans. We, therefore, pray thee to restrain him
                now, else it will be difficult later on.&quot;</p>
            <p>Akbar sent a high official to Goindwal to request Guru's attendance. The summons was not a brutal order
                of a modern court,&quot;Herein fail not, but kindly grant me a sight of thee.&quot; The Guru sent Jetha
                saying,&quot; Thou art in my image; Guru Nanak will be with thee and none shall prevail against thee.
                Fear no body and give suitable reply.&quot;</p>
            <p>Jetha gave suitable replies to all the questions and satisfied the Emperor who then gave his decision,&quot;
                I see no hostility to Hinduism in this man, nor do I find any fault with his compositions.&quot; The
                Brahmans left the court in utter defeat. Macauliffe quotes 'Suraj Parkash', &quot;Upon this the Emperor
                took Jetha aside, and told him to request Guru Amar Das, who before his conversion to Sikhism used to
                make yearly pilgrimages to the Ganges, to make one pilgrimage more in order to divert the wrath of the
                Hindus. The Emperor added that he would issue an order that no tax should be levied on the Guru's
                party...... The Guru in compliance with the Emperor's suggestion, and also in order to have an
                opportunity of preaching his religion, set out for Hardwar.&quot; 'The Guru's going to Hardwar for one
                more pilgrimage to avert the wrath of the Hindus', seems totally unfounded because it is against the
                tenets of the Guru who says:</p>
            <p><em>&quot;Tirath nawan jao, tirath nam hai</em></p>
            <p><em>Tirath sabad bichar untar gian hai.&quot;</em></p>
            <p><em>(Dhanasri Mohalla 1, p-687)</em></p>
            <p><em>'What is pilgrimage? Pilgrimage is Nam,</em></p>
            <p><em>Pilgrimage is contemplation on Word and realization of inner knowledge.'</em></p>
            <p><em>(Translation of the above)</em></p>
            <p><em>&quot;If mind is sinful, everything is sinful,</em></p>
            <p><em>By washing the body, mind will not become pure.&quot;</em></p>
            <p><em>(Wadhans Mohalla 3, p-558)</em></p>
            <p>It is not right to assert that Guru Amar Das went to Hardwar for one more pilgrimage to please the
                Hindus. Guru Amar Das went to Hardwar and Kurukshetra not to make a pilgrimage but to preach his
                doctrine and gospel of Nam to the thousands of battered souls.</p></td>
