        <td colspan="2" valign="top"><p>Guru Amar Das was born on April 5, 1479 at Basarka village in Amritsar district.
                He was the eldest son of his parents, Bhai Tej Bhan and Mata Lakhmi. At the age of 24, he was married to
                Mansa Devi who gave birth to two sons, Mohan and Mohri, and two daughters, Bibi Dani and Bibi Bhani. The
                early history of Guru Amar Das has been given in the last chapter.</p>
            <p><a name="DATU'S ANIMOSITY" id="DATU'S ANIMOSITY">DATU'S ANIMOSITY</a></p>
