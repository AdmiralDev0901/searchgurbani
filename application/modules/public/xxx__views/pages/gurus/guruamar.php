            <p>Guru Angad's sons were upset because they claimed that after their father, they were the legitimate heirs
                to Guruship. Guru Angad's son Datu, therefore, proclaimed himself as Guru in Khadur; but the Sikhs did
                not accept him as such. Secondly under Guru Amar Das's strict dictum, it was mandatory that all persons,
                high or low, rich or poor, king or the commoner, Brahmans or Sudras, and Hindus or Muslims, must sit in
                the same row as equals to dine in the Guru's langar (kitchen). This had upset the Brahmans very much and
                they were on the search of an opportunity to rectify this situation. These Brahmans and other higher
                caste leaders saw some chance in Datu's revolt to capture the Guruship. Upon their support, Datu
                proceeded to Goindwal where the Guru was stationing.</p>
            <p>Guru Amar Das was sitting on his religious throne and was delivering instructions to the congregation.
                Datu came along with a large number of his companions and kicked the Guru, who fell down the platform.
                Datu took possession of the platform and proclaimed himself as the Guru. Guru Amar Das got up and said
                in extreme humility,&quot; Sir, pardon me, my hard bones might have hurt your tender feet.&quot; After
                this the Guru left Goindwal and went to his village Basarka. He confined himself in a house outside the
                town without letting anybody know about his whereabouts.</p>
            <p>Datu sat on Guru's throne in Goindwal and was very proud of his position. The Sikhs, however, did not
                accept him as Guru, and all the pilgrims to Goindwal went away on hearing of the insult to their Guru.
                On seeing the Sikhs' contempt towards him, one day Datu loaded his newly acquired wealth on a camel and
                returned to Khadur. On his way he was encountered with some robbers who seized the camel with the load,
                and one of the robbers struck Datu on the same foot with which he had kicked the Guru. Datu's foot
                swelled up and caused him great pain.</p>
            <p>The Sikhs were very much distressed at loosing their Guru. They searched all over but could not find him
                anywhere. Under the leadership of Bhai Buddha, they prayed and then let Guru's mare loose and anxiously
                followed it for a short distance. The mare made her way to the Guru's house in Basarka, and stood before
                his door. It was written on the door,&quot; Whoever openeth this door is no Sikh of mine, nor I am his
                Guru.&quot; They did not open the door, but made an opening in the wall and made supplication before the
                Guru. The Guru could not disregard the love and devotion of his Sikhs and returned to Goindwal. The
                Guru's return was celebrated with illuminations, rejoicing and feasting.</p>
            <p><a name="NOTABLE SIKHS" id="NOTABLE SIKHS">SOME NOTABLE SIKHS</a></p>
            <p>Bhai Paro belonged to a village, Dalla in Doaba, an area between the rivers Beas and Satluj. He received
                religious instructions and emancipation from the Guru. A rich Muslim horse- dealer of Delhi, Alayar,
                brought five hundred horses from Arabia and arrived at Beas. He could not continue his journey because
                the river Beas was flooded and the boatmen refused to take the risk in crossing the swollen river. Next
                morning Alayar saw Bhai Paro plunge his horse into the foaming river and reaching the opposite shore in
                safety. Alayar met Paro on his return and complimented his daring feat of crossing the river. Bhai Paro
                told him that it was through the blessings of the Guru that he could cross the swollen river. He further
                informed Alayar about Guru's glory. Alayar became anxious to meet the Guru. Next morning they both went
                to see him.</p>
            <p>Alayar (Ala means God, and yar means friend) was delighted to see the Guru. Hearing his name the Guru
                said to him,&quot;It is difficult to become friend (yar) of God (Ala), but I will make God thy Master
                and thee His servant.&quot; Alayar was blessed by the Guru and he became his disciple. Alayar was made
                in charge of the first Manji (diocese) of the 22 Manjis that were set up by Guru Amar Das later on to
                spread the fragrance of Name. There are numerous such stories of the Sikhs who were blessed by the
                Guru.</p>
            <p><a name="BAWLI" id="BAWLI">BAWLI AT GOINDWAL</a></p>
