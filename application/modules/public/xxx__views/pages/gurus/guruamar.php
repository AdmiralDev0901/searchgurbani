            <p>Guru Amar Das purchased some land in Goindwal and laid the foundation of a Bawli (a well with descending
                steps) in 1559. All Sikhs joined in the work of digging the Bawli. There was great activity throughout
                the construction of the Bawli.</p>
            <p>Hari Das, a Khatri of Sodhi tribe, lived with his wife, Daya Kaur, in Chuna Mandi, a suburb of Lahore.
                Both husband and wife were very religious. After twelve years of their marriage, a son was born to them
                on September 24, 1534. They called him Ram Das, who was generally known as Jetha meaning first-born. He
                was very handsome having fair complexion with pleasing personality. As he grew up he liked the company
                of holy men. One day his mother boiled some pulse, put it into a basket and gave it to him to sell and
                make profit. Jetha went to the river Ravi. Soon he saw a company of holy men, and Jetha gave the boiled
                pulse to them and went home. The holy men were very much pleased and prayed for the boy.</p>
            <p>One day Jetha saw a company of Sikhs singing the hymns and proceeding with great rejoicing. He asked
                whither they were going, one of them said,&quot; We are going to Goindwal where Guru Amar Das holds his
                court. Every blessing in this world and the next is obtained by his favor. Come with us.&quot; On
                hearing this Jetha was delighted and he joined them in their pilgrimage.</p>
            <p>On seeing the Guru, Jetha's heart was filled with love and devotion. When he made his obeisance to the
                Guru, he was attracted by his pleasing personality. The Guru remarked,&quot; If you have come abandoning
                all worldly desires, you shall obtain a true sovereignty. Perform work and service.&quot; Jetha happily
                applied himself to the Guru's service. He worked in the kitchen, cleaned dishes, shampooed his Master
                and brought firewood from the forest. He worked in the excavation of the Bawli during his leisure
                time.</p>
            <p>Guru's eldest daughter, Bibi Dani (also known as Sulakhni) was married to Rama. The other daughter, Bibi
                Bhani was very religious from the very childhood. When she was of marriageable age, her mother reminded
                the Guru that it was time to search for a match for her. The Guru ordered the search. When his agent was
                ready to depart, Bibi Bhani's mother saw a young person standing outside and she said to the agent,&quot;
                Search for a boy like him.&quot; The Guru heard her remarks and exclaimed,&quot; He is his own parallel,
                for God had made none other like unto him.&quot; The young man thus chosen was Jethaji (Ram Das).</p>
            <p>At the time of marriage, the bridegroom was asked by the Guru to choose a gift for himself, as it was
                customary to do so. Jethaji replied,&quot; Sir, bless me with the gift of Hari Nam.&quot; Bibi Bhani not
                only considered the Guru as her father but her Guru also. In the same way she served Jetha not only as
                her husband but as a saint also. Prithi Chand was their first son and three years later Mahadev, the
                second son made his appearance. On April 15, 1563 Jetha and Bibi Bhani were blessed with their third
                son, Arjan, at whose birth there were unusual rejoicing.</p>
            <p>Meanwhile the Sikhs continued excavation of the Bawli. After digging very deep they found large stones
                which hindered the progress. The Guru asked the Sikhs if there was any one who would be courageous to
                drive a peg into the base to remove the obstruction. He had, however, warned that the operation had
                great danger because if the person could not avert the gush of the water, he might be drowned. All the
                Sikhs remained silent and no one came forward to take such a risk. At last Manak Chand of Vairowal, who
                was married to a niece of the Guru, offered his services. This was the same Manak Chand whose parents
                were blessed with a son (Manak Chand) by Guru Nanak.</p>
            <p>Manak Chand, invoking God's name and through the grace of the Guru, was able to wedge through the stone
                and the stream of water immediately overflowed the Bawli. He was overtaken by the gush of the water. He
                almost drowned but by the grace of the Guru, he came to the top from where he was taken out and was
                revived. Therefore, he was called 'Marjiwra' (revived after death).</p>
            <p>The Bawli when finished yielded sweet drinking water. The Sikhs rejoiced at the successful completion of
                their labor. There were eighty-four steps reaching down the Bawli. It is believed that whosoever recites
                Japji attentively and reverently at each step, is saved from the cycle of transmigration.</p>
            <p><a name="LANGAR" id="LANGAR">GURU KA LANGAR (FREE KITCHEN)</a></p>
            <p>Guru's free kitchen (Guru ka Langar) which was started by Guru Nanak and developed by Guru Angad, was
                further strengthened by Guru Amar Das. It was the injunction of Guru Amar Das that none would have his
                audience unless he had first eaten from the Langar. The Guru intended to remove the caste restrictions
                and prejudices of untouchability. It was, therefore, declared unequivocally that all persons of all
                castes, high or low, rich or poor, Brahmans or Sudras, Hindus or Muslims, must sit in the same line and
                eat the same food from Guru's kitchen. When Raja of Haripur or even Akbar, the Mughal Emperor of India,
                came to see the Guru, they had to sit with common people and dine with them before they could have
                audience with the Guru. In this way people were lifted above the hypocrisy of caste system and were able
                to look at one another as brothers and equals.</p>
