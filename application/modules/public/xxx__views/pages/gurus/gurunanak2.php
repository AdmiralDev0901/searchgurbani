<h2>Sri Guru Nanak Dev Ji </h2>
<p align="left"><a name="1" id="1">TRUE BARGAIN</a><br/>
    In spite of the accumulating evidence about the spiritual greatness of the Guru, Mehta Kalu was not convinced and
    thought that his son was wasting time in profitless contemplation. So he wanted to put him to trade. He gave the
    Guru twenty rupees (Indian currency) and sent him to the nearest town- Chuharkana, to buy goods of common use and
    then sell them at a profit. The family servant Bala was also sent with him.<br/>
    <br/>
    On his way the Guru met a group of faqirs (ascetics) who were hungry for several days. The Guru spent all the money
    in feeding the faqirs and called it a true bargain. He realized the nature of his act and did not go home but sat
    under a tree outside his village. Bala went home and he narrated the whole story to his father. The father became
    very angry but the Guru explained to him that he could not think of a more profitable bargain. The aged tree under
    which he sat is still preserved. It is called Thumb Sahib or the holy tree in memory of the Guru.<br/>
    <br/>
    All this failed to have any effect on Guru's disinclination towards ordinary world affairs and he remained deeply
    immersed in meditation.</p>
<p align="left"><a name="2" id="2">GURU'S MARRIAGE<br/>
    </a><br/>
    In order to bring him around the worldly affairs, the next step came the marriage. The marriage date is given
    different in different Janamsakhis (birth stories), and it is presumed that he was between 14 to 18 years of age
    when he got married. His wife, Sulakhni, was the daughter of Bhai Mula, a resident of Batala in Gurdaspur district.
    She gave birth to two sons, Sri Chand and Lakhmi Das. His father soon found out that even the married life did not
    divest him of his pre-occupation with matters pertaining to his Divine mission. As a matter of fact, his concept of
    duty was not to serve himself and his family rather to transcend it so that the self might participate in the divine
    scheme of things and spiritualize the world around him. Humanity was his family and serving the humanity was the
    service of the Lord. Bhai Gurdas writes that the Guru saw the whole world in flames; flames of falsehood, tyranny,
    hypocrisy and bigotry. He had to go and extinguish that fire with eternal love, truth and dedication. He had the
    divine mission to teach to humanity, the lesson of the brotherhood of mankind and the fatherhood of God. &quot;The
    Primal Being created the Light; all men are the creation of Providence: all human beings have sprung from one Light.
    Who, then, is bad and who is good?&quot;</p>
<p align="left">&nbsp;</p>
<p align="left"><a name="3" id="3">GURU NANAK COMES TO SULTANPUR<br/>
    </a><br/>
    Jai Ram, Guru's brother-in-law was serving as Dewan (steward) to the governor, Nawab Daulat Khan Lodhi of Sultanpur.
    It is said that both Jai Ram and Rai Bular were of the opinion that Nanak was a saint ill-treated by his father; and
    thus Jai Ram promised to find a job for him in Sultanpur. Guru's sister was deeply devoted to her younger brother.
    On their annual visit to Talwandi, when she noticed her father's impatience at her brother's indifference towards
    worldly activities, she decided to take him to Sultanpur. Her father gave his consent.<br/>
    <br/>
    Jai Ram got the Guru the post of a store-keeper of Nawab's state granary where the grain was collected as a part of
    land revenue and later sold. The Guru carried out the duties of the store-keeper very efficiently. The minstrel
    Mardana subsequently joined the Guru and other friends too followed. Guru Nanak introduced them to the Khan, who
    provided them suitable jobs in his administration. Every night there was Sabad-Kirtan (singing divine hymns).<br/>
    <br/>
    One day he was weighing provisions and was counting each weighing as 'one, two, three.........ten, eleven, twelve,
    thirteen'. When he reached the number thirteen (13)- 'Tera' (in Punjabi language Tera means number 13, and Tera also
    means 'thine', that is 'I am Thine, O Lord'), he went into ecstasy. He went on weighing by saying,&quot;Tera, tera,
    tera,.......&quot; The customers did not know how to carry the bountiful gifts of this store-keeper. They could not
    understand the bounties of the Lord.<br/>
    <br/>
    Ultimately the situation reached its climax when a charge was levied against the Guru that he was recklessly giving
    away the grain. The Nawab ordered an inquiry which was conducted very carefully. The Guru's detractors were
    surprised when the stores were found full and the accounts showed a balance in favor of the Guru. After that the
    Guru sent in his resignation to the employer to embark on his divine mission.</p>
<p align="left"><a name="4" id="4">GURU'S DISAPPEARANCE</a><br/>
    <br/>
    The Janamsakhis narrate that one morning, Guru Nanak went to bathe in the neighboring river called Baeen. While
    bathing he disappeared in water and remained as such for three days. During that period he had a vision of God's
    presence where he was entrusted by the Almighty with the task of preaching the Divine Name (NAM) to the world. The
    Almighty gave him a goblet brimming with nectar of 'NAM' which Master Nanak drank and then Almighty commanded:<br/>
    <br/>
    <em>&quot;Thou art welcome, O Nanak, that hath absorbed thyself in Nam.<br/>
        <br/>
        Do go hence and do the work for which thou wast born.<br/>
        <br/>
        People of Kalyug have adopted horrible practices and are<br/>
        <br/>
        extremely degraded in mind.<br/>
        <br/>
        They worship a variety of gods, have forsaken the Name<br/>
        <br/>
        and are immersed in sin.<br/>
        <br/>
        Go thou, spread Love and Devotion to the Name, and lighten<br/>
        <br/>
        the burden of the earth.<br/>
        <br/>
        Go thou, and glorify the name of God and destroy<br/>
        <br/>
        hypocrisy.&quot;<br/>
        <br/>
        <br/>
        The Guru then sang the following Sabad:<br/>
        <br/>
        &quot;Were I to live for millions of years and drink the air for<br/>
        <br/>
        my nourishment;<br/>
        <br/>
        Were I to dwell in a cave where I beheld not sun or moon, and<br/>
        <br/>
        could not even dream of sleeping;<br/>
        <br/>
        I should still not be able to express Thy worth; how great<br/>
        <br/>
        shall I call Thy Name?<br/>
        <br/>
        O true Formless One, Thou art in Thine Own place-<br/>
        <br/>
        As I have often heard I tell my tale- if it please Thee, show<br/>
        <br/>
        Thy favor unto me.<br/>
        <br/>
        Were I to be felled and cut in pieces, were I to be grounded<br/>
        <br/>
        in a mill;<br/>
        <br/>
        Were I to be burned in a fire, and blended with its ashes,<br/>
        <br/>
        I should still not be able to express Thy worth; how great<br/>
        <br/>
        shall I call Thy Name?<br/>
        <br/>
        Were I to become a bird and fly to a hundred heavens;<br/>
        <br/>
        Were I to vanish from human gaze and neither eat nor drink,<br/>
        <br/>
        I should still not be able to express Thy worth; how great<br/>
        <br/>
        shall I call Thy Name?<br/>
        <br/>
        Nanak, had I hundreds of thousands of tons of paper and a<br/>
        <br/>
        desire to write on it all after the deepest research;<br/>
        <br/>
        Were ink never to fail me, and could I move my pen like the<br/>
        <br/>
        wind,<br/>
        <br/>
        I should still not be able to express Thy worth; how great<br/>
        <br/>
        shall I call Thy Name?&quot; (Sri Rag Mohalla 1, p-14)<br/>
    </em><br/>
    <br/>
    Then a voice was heard,&quot;O Nanak, to him upon whom My look of kindness resteth, be thou merciful, as I too shall
    be merciful. My name is God, the Primal Brahm, and thou art the Divine Guru (Mei aad Parmeshar aur tu Gur
    Parmeshar).&quot;<br/>
    <br/>
    This has been the revelation of the Puratan Janamsakhi. The Guru himself confirms that the Almighty asked him to go
    to the world and sing His praises. The Guru says that after he had done his duty in this world, the Almighty called
    him again:<br/>
    <br/>
    <em>&quot;Me, a minstrel out of work, God applieth to His work;<br/>
        <br/>
        Thus spake the Almighty unto me<br/>
        <br/>
        Night and day, go and sing My praises.<br/>
        <br/>
        The Almighty again did summon this minstrel to His most<br/>
        <br/>
        Exalted Court.<br/>
        <br/>
        On me He bestowed the robe of Honor of His praise and<br/>
        <br/>
        prayer,<br/>
        <br/>
        On me He bestowed the goblet brimming with Nectar of His<br/>
        <br/>
        Holy Name,<br/>
        <br/>
        Those who at the bidding of the Guru<br/>
        <br/>
        Feast and take their fill of the Lord's Holiness attain Peace<br/>
        <br/>
        and Joy.<br/>
        <br/>
        Thy minstrel spreadeth Thy Glory by singing Thy Word;<br/>
        <br/>
        Nanak, he who uttereth true praises obtaineth the Perfect<br/>
        <br/>
        One.&quot; (Majh di Var-pauri 27, p-150)<br/>
    </em><br/>
    <br/>
    It is said that after three days when he reappeared, some people saw hallow around his head. Some people say that
    Guru Nanak's Guruship started with his reappearance from the water. It should be pointed here in this respect that
    there are Three Entities in Sikhism- God, Guru, and Gurbani (Divine Word). According to Sikhism there is One but One
    God; He sends His emissary called Guru, who is embodiment of Divine Light. God then delivers His message (Gurbani)
    through His emissary, the Guru.<br/>
    <br/>
    Without the Guru, there can be no Gurbani. Guru is a channel through whom Gurbani is delivered. Therefore, when at
    the age of seven, Guru Nanak delivered the first Divine message to his teacher (Rag Asa Mohalla 1, Patti Likhi,
    p-432), he had to be and he was the Guru. Before disappearing in the river, a lot of Gurbani was already delivered
    by the Guru. He was, thus, born as Guru and his Guruship started from the very birth.<br/>
    <br/>
    Bhai Gurdas, a Sikh apostle writes that first the Almighty bestowed His blessings on Baba (Guru Nanak) and then He
    sent him to this world to spread the Divine Word:<br/>
    <br/>
    &quot;Pehlan babei paya bakhash dar, pichhon dei phir ghal kamaee.&quot; (Bhai Gurdas- Var 1, pauri 24)<br/>
    <br/>
    Who was Guru Nanak's Guru? When he was asked, who his Guru was, Guru Nanak replied that God Himself was his
    Guru:<br/>
    <br/>
    &quot;Unfathomable and Infinite is God Who acted as Guru of Nanak.&quot; (Sorath Mohalla 1, p-599)<br/>
    <br/>
    The Guru remained silent for a day after the reappearance and then he made this announcement, &quot;There is no
    Hindu and no Musalman.&quot; This meant that there was no difference between man and man. (But some interpret that
    both Hindus and Muslims had forgotten the precepts of their religions). This declaration made Nawab Daulat Khan and
    his Qazi very mad. The Nawab asked the Guru to explain whether his Qazi was not a true Muslim. The Guru described
    the traits of a true Muslim:<br/>
    <br/>
    <em>&quot;He who is firm in his faith,<br/>
        <br/>
        Has a right to be called a Muslim.<br/>
        <br/>
        His acts must be in accord with his faith in Prophet,<br/>
        <br/>
        He must clean his heart of his pride and greed,<br/>
        <br/>
        Not troubled by the two impostors- life and death,<br/>
        <br/>
        Resigned to the Will of God;<br/>
        <br/>
        Knowing Him as the Doer,<br/>
        <br/>
        Free himself from the self, and<br/>
        <br/>
        Be compassionate towards all beings, O Nanak,<br/>
        <br/>
        Such a one may call himself a Muslim.&quot; (Majh ki Var Mohalla 1, p-141)</em><br/>
    <br/>
    The Nawab then asked the Guru, &quot;If there is no difference between the Hindus and the Muslims, why don't you
    join us in our Namaz (Muslim prayer)?&quot; The Guru agreed to join them to take part in their prayer in the mosque
    where Qazi led the Namaz. When Namaz was offered, the Qazi and the Nawab both stood, kneeled and bowed in their
    prayer but the Guru remained standing. After the Namaz was over, the Qazi said,&quot;Why did you not take part in
    the prayer?&quot; The Guru replied,&quot;I did take part in the prayer but both of you did not.&quot; Then he
    explained,&quot;While the Qazi performed the service, he remembered that there was a well in his courtyard, and his
    mind was filled with apprehension lest his newly-born filly should fall in the well. The Qazi's mind was, therefore,
    not present in the prayer. Also while the Nawab was pretending to pray, his mind was set on purchasing horses in
    Kabul.&quot;<br/>
    <br/>
    Both admitted the truth of Guru's statements and the Nawab cried aloud to the Qazi,&quot;Thou seest not Khuda (God)
    speaking to us through Nanak?&quot; The Muslims perform five Namaz at five different times a day. The Guru addressed
    the meaning and virtue of Namaz:<br/>
    <br/>
    <em>&quot;Five prayers thou sayest five times a day,<br/>
        <br/>
        With five different names;<br/>
        <br/>
        But if Truth be thy first prayer,<br/>
        <br/>
        The second to honestly earn your daily living,<br/>
        <br/>
        The third to give in God's name,<br/>
        <br/>
        Purity of mind by thy fourth prayer,<br/>
        <br/>
        And praise and prayer to God thy fifth;<br/>
        <br/>
        If thou practiseth these five virtues,<br/>
        <br/>
        And good deeds be thine Kalma- the article of faith,<br/>
        <br/>
        Then thy can call thyself a true Muslim.<br/>
        <br/>
        By mere hypocrisy, O Nanak,<br/>
        <br/>
        A man is deemed false through and through.&quot; (Majh ki Var Mohalla 1, p-141)<br/>
    </em><br/>
    <br/>
    Guru Nanak never asked a Muslim or a Hindu to become his disciple to get a place in heaven after death. He told the
    Muslim to become a true Muslim and to a Hindu to become a true Hindu in order to get salvation.</p>
<p align="left"><a name="5" id="5">TRAVELS OF GURU NANAK</a><br/>
    <br/>
    Guru Nanak Dev saw the world suffering out of hatred, fanaticism, falsehood and hypocrisy. The world had sunk in
    wickedness and sin. So he set out for the regeneration of humanity on this earth. He carried the torch of truth,
    heavenly love, peace and joy for mankind. He embarked on his Divine Mission and went towards east, west, north and
    south and visited various centers of Hindus, Muslims, Buddhists, Jainis, Sufis, Yogis and Sidhas. He met people of
    different religions, tribes, cultures and races. He travelled on foot with his Muslim companion named Mardana, a
    minstrel. His travels are called Udasis.<br/>
    <br/>
    In his first Udasi (travel), Guru Nanak covered east and south of India and returned home after spending a little
    more than eight years. He started from Sultanpur in August, 1507 and went to his village Talwandi to meet and inform
    his parents about his long journey. The old parents wanted comfort and protection from their young son in their old
    age and so they asked him not to go. But there were thousands and thousands others waiting for the Divine Master for
    comfort, love and salvation. The Guru, therefore, told his parents,&quot;There is a call from Heaven, I must go
    whither He directs me to go.&quot;</p>
