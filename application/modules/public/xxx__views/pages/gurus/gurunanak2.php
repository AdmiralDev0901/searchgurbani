<p align="left"><a name="6" id="6">FIRST STOP AT EMINABAD</a><a name="10" id="10"><br/>
    </a><br/>
    Accompanied by Mardana, the Guru embarked on his mission and left his family behind. He made his first stop at
    Saidpur, now known as Eminabad, and there he met a poor carpenter named Lalo. The Master looked at poor Lalo
    graciously and he was blessed with Divine love and lo, he was a blessed man. The Guru chose to stay with Lalo for
    sometimes as a guest. The news reached Malik Bhago, the chief of the town, that a holy person was staying with Lalo.
    Malik Bhago was a corrupt man and he had amassed wealth through unfair means. He held a big gathering and invited
    all holy men including the Guru. The Guru, however, did not accept his invitation. The Malik then made a special
    arrangement for the Guru and requested him to come and eat at his residence. At last the Guru went there and Malik
    Bhago said,&quot;O holy man, I have prepared so many dishes for you, but you are staying with a poor rpenter and
    eating his dry bread. Please stay with me.&quot; The Guru replied,&quot;I cannot eat your food because your bread is
    ill-begotten and has been made with money sucked from the poor through unfair means, while Lalo's bread is made from
    the hard-earned money.&quot; This made Malik Bhago very mad and he asked the Guru to prove his point. The Guru then
    sent for a loaf of bread from Lalo's house. In one hand the Guru held Lalo's bread and in the other that of Malik
    Bhago's, and when he squeezed both, milk came out from Lalo's bread and blood dripped from Malik Bhago's bread.
    Malik Bhago was completely shaken by his guilt and asked for forgiveness. The Guru asked him to distribute his
    ill-gotten wealth among the poor and henceforth live an honest life. Malik Bhago was re-born with the Guru's
    blessing</p>
