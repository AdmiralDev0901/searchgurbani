<p> 2. Arohi - The ascending scale (sa re ga ma pa dha ni sa) .This is the pattern of notes in which a Raag ascends the
    scale. <br/>
    3. Avrohi - The descending scale (sa ni dha pa ma ga re sa) This is the pattern of notes in which a Raag decends the
    scale.<br/>
    4. Vadi - The most popular note ,This is a note which is strongly emphasised within a particular Raag. <br/>
    5. Samvadi - The second most popular note,This is a note which is emphasised within a particular Raag, but not as
    much as the Vadi. <br/>
    6. Aurav - A raga of five notes <br/>
    7. Khaurav - A raga of six notes <br/>
    8. Sampooran - A raga of seven notes <br/>
    9. Aurav-Khaurav - Where arohi has five notes, but avrohi has six notes. <br/>
    10. Khaurav-Aurav - Where arohi has six notes, but avrohi has five notes. <br/>
    11. Aurav-Sampooran - Where arohi has five notes, but avrohi has seven notes <br/>
    12. Khaurav-Sampooran - Where arohi has six notes, but avrohi has seven notes. <br/>
    13. Sampooran-Aurav - Where arohi has seven notes, but avrohi has five notes. <br/>
    14. Sampooran-Khaurav- Where arohi has seven notes, but avrohi has six notes. <br/>
    15. Saptaks - This refers to three divisions of a harmonium </p>
<ul>
    <li> Mandar - first (top) part of seven notes</li>
    <li> Middle - central part of seven notes</li>
    <li> Tar - last part of seven notes.</li>
</ul>
<p> 16. The notes can be soft (komal) or sharp (teever) </p>
<p><strong>Musical terms regarding a presentation of a raag in vocal style</strong><br/>
    <br/>
    1.<strong>Sthayee :</strong> The first part of the composition. Mainly develops in the the lower and the middle
    octave.<br/>
    2<strong>.Antaraa :</strong> Second part of the composition. Develops in the middle or higher note. <br/>
    3.<strong>Mukhadaa :</strong> The first line of the composition.<br/>
</p>
