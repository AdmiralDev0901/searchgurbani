<p>The Indian Classical Music has three built in pillars called elements of music, they are: </p>
<ul>
    <li> Raga</li>
    <li> Mood</li>
    <li> Expression</li>
</ul>
<p><strong> A. Raga: </strong></p>
<p>Raag, in the Sanskrit dictionary, is defined as &quot;the act of coloring or dyeing&quot; (the mind in this context)
    and &quot;any feeling or passion especially love, affection, sympathy, vehement desire, interest, joy, or delight&quot;.
    In music, these descriptions apply to the impressions of melodic sounds on both the artist(s) and listener(s). A
    raag consists of required and optional rules governing the melodic movements of notes within a performance. </p>
<p>Indian classical music is based on raga system, which has continuously evolved over the millenniums, from pre Aryan
    period until today. The music and ragas also have their roots in the Hindu mythology e.g., Shiv's tandav dance, and
    Narad's and Saraswati's playing of divine musical instruments are legendary evidences of the existence of music in
    the world of gods. </p>
<p> The definition and meaning of raga has changed many a times over the long period of history and mythology. The root
    word for the term 'Raga' is the Sanskrit word 'Ranja' meaning to please, to colour or to tinge. The meaning of the
    raga revolves around three basic elements: </p>
<ul>
    <li> colour</li>
    <li> passion, and</li>
    <li> melody.</li>
</ul>
<p>The melodic performer utilizes a raag as the foundation for improvisation. A recital explores a raag in an
    non-metered form and/or within the confines of a cyclical rhythmic structure, using intricate ornamentation of
    notes. First the raag is introduced with a note or group of notes, and then the improvisation progresses to a more
    melodically and rhythmically complex form. </p>
<p>The manner in which raags originate is a fascinating subject. Many raags are polished forms of a family of regional
    folk melodies while others have been created through the imagination of musicians. Some of the latter are raags with
    their own distinct characteristics whereas other creations are a combination of one or more existing raags. The
    names of some established raags have changed with time and the characteristics/ definitions of raags also are not as
    rigid as claimed in theory.</p>
<blockquote>
    <p> History of ragas: </p>
</blockquote>
<ul>
    <li> Bharata is considered to be the earliest authority on Indian Music. In his work 'Natya Shastra' written in the
        third century A.D.. he has explained the meaning of ragas but has not given any coherent definition of the word.
        Later works of this period were produced by Kayshap and Matanga
    </li>
    <li> Pandit Somnath's work titled 'Rag - Vobodh' was produced in the medieval period. He said that a raga was an
        arrangement of sounds, consisting of musical notes, which possess Varna. Varna here means manner and order in
        which Svaras (notes) are applied.
    </li>
    <li> The modern writers have given the following definitions of a raga:</li>
</ul>
<p> i. Fox Strangeway defines a raga as: &quot;An arbitrary series of notes characterised as far as possible as
    individuals, by proximity to or remoteness from the note which marks the general level of melody, by a special order
    in which they are usually reinforced by a drone&quot;. 1<br/>
    Music of Hindustan, Oxford University, 1914, pp. 107 </p>
<p align="left">ii. Herbert A. Popley defines a raga as: &quot;Different series of notes within the octave, which form
    each other by the prominence of certain fixed notes and by the sequence of particular notes&quot;. 2 </p>
<p> B .Mood</p>
<p> Mood is the frame of mind or state of feelings. It is the comprehensive term for any state of mind in which one
    emotion or desire or a set of them is ascendant. </p>
<p><strong> C. Expression </strong></p>
<p>It is primarily an act or process of representation. It deals with the role of expression in a musical performance,
    the means of expression and the factors responsible for expression while presenting any raga. </p>
<p><strong>Ragamala </strong></p>
<p> The literal meaning of the word Ragmala is: a chain/necklace (mala) of ragas i.e., a list of ragas. This list
    differs according to the author and the music school it is based upon. Thus there exists a number of such lists in
    the music text books. </p>
<p> The Ragamala listed in Guru Granth Sahib belongs to Hanumant school of music. According to Bhai Vir Singh, a Sikh
    scholar, the ragamala included in Guru Granth Sahib was prepared by Guru Nanak Dev and it contains a list of popular
    ragas of that period of time. This ragamala must not be read as an index of ragas of Guru Granth Sahib, for it is
    only a list of popular ragas sung at the time period of Sikh Gurus. Moreover there are ragas mentioned in the
    Ragamala which are not included in Guru Granth Sahib, and there are ragas used in Guru Granth Sahib which are not
    mentioned in the Ragamala. In fact the Ragamala included in Guru Granth Sahib is no way related to the ragas used by
    the Sikh Gurus to compose their hymns. It is just an independent list of ragas based on Hanumant School of
    music. </p>
<p> It is also important to note that unlike various Schools of Music, there is no mention of raginis (consort of ragas)
    or their sons in Guru Granth Sahib. There is a mention of 37 ragas. Thirty one ragas refer to 31 chapters in the
    musical section of Guru Granth Sahib (pages 14 - 1353), and six other ragas mentioned therein have been mixed with
    the 31 major names used in Guru Granth Sahib. </p>
