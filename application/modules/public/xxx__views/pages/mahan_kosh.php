<h2 align="center">Encyclopedia of Sikh Literature</h2>
<h6 class="blue"><strong>Gur Shabad Ratanakar Mahankosh</strong> by Kahan Singh Nabha, popularly known as <strong>Mahankosh</strong>
    is a not only the first dictionary of Sikh Scripture and books on Sikh Religion on western concept of lexis but also
    a classical reference book of Sikh History, Philosophy and contemporary Sikh States . Even after a century of its
    compilation, it still remains a unique reference document.</h6>

