    <p class="blue">It was first published in 1930 after many years of painstaking research from 1912 to 1927. Mahan
        Kosh as it is generally called is a model encyclopaedia. It modestly claims in it subtitle to be an
        encyclopaedia of Sikh literature, but it is, in fact, much more. Its remarkable coverage and exemplary accuracy
        has a multitude of entries ranging from brief definitions of difficult words from the scriptures and tradition,
        through descriptive notes on various doctrines, individuals and institutions to accounts of the Gurus. It gives
        careful treatment of ter-minology, which has dropped out of usage or changed its meaning.</p>
    <p class="blue">The Mahan Kosh is indispensable for any serious student of Sikh studies, its qualities undimmed by
        over half a century which has passed since it first appeared in print. Bhai Kahn Singh ranks as one of the
        modern world's greatest encyclopaedists. The fact that he chose to present all his work in Punjabi has limited
        his contribution to those who are able to read Punjabi, and although his reputation extends much farther, it is
        largely confined to the general area of Sikh studies. This does him less than justice. The range of his
        coverage, the meticulous care with which he collected and arranged his material, a scrupulous concern for
        accuracy and the succinct nature of his presentation, distinguishes his work. These are the qualities of a great
        encyclopaedist and their manifest presence in the works of Bhai Kahn Singh qualifies him as one of the truly
        great theologians. </p>
