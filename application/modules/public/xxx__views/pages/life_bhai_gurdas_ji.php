<h2><strong>Life Bhai Gurdas Ji </strong></h2>
<p><img src="<?php echo base_url(); ?>images/content/bgurdas.jpg" alt="." width="216" align="left" height="298"/>Bhai
    Gurdas is considered the first interpreter of Gurbani. His writings are considered key to understanding the Sikh
    holy scriptures. He wrote 40 vars (ballads) and 556 kabits (both forms of Punjabi poetry). These writings are
    considered the best specimens of Sikh literature and philosophy. He also had the opportunity to be the scribe of
    Guru Granth Sahib or Adi Granth, the holiest Sikh scripture that was compiled by Guru Arjan Dev Ji, the fifth Sikh
    Guru, in 1604. </p>
<p>The exact date of birth of Bhai Gurdas is not known but it is somewhere between 1543-1553 A.D. </p>
<p>Bhai Gurdas became a Sikh under the kind influence of Guru Ram Das, the fourth Sikh Guru in 1579 AD. Bhai Gurdas was
    the cousin brother of Mata Bhani, the mother of Guru Arjan Dev Ji. Bhai Gurdas received his early education under
    the guidance of Guru Amar Das. Bhai Gurdas travelled to far away places like Agra, Lucknow, Burhanpur, and Rajasthan
    to spread Guru's word under the direction of Guru Amar Das.</p>
<p>Bhai Gurdas came back to Punjab after Guru Ram Das left for heavenly abode. He had the opportunity to study and
    observe Sikhism closely in the company of Guru Arjan Dev Ji. Bhai Gurdas also played a key role in the construction
    of the Harimandir Sahib (Golden Temple Amritsar). </p>
<p>This was a very difficult time for the fifth Guru as his own brother Pirthi Chand was very jealous of him. At the
    same time the Muslim ruler Jahangir had become jealous of growing popularity of Sikhism and Guru Arjan Dev Ji. He
    wanted to put an end to it. This was a period of great challenges and difficulties for the Sikhs. Bhai Gurdas was
    the first custodian of the Akal Bunga (Akal Takhat Sahib). Baba Buddha Ji was the first Granthi of the Harimandir
    Sahib. During the time of Guru Har Gobind Sahib, Bhai Gurdas went to many far away places like Kabul, Kanshi,
    Banaras, to spread the message of the Guru. The Sikh congregation was so impressed by Bhai Gurdas they that erected
    a Gurdwara in his memory in Kabul.</p>
<p>Bhai Gurdas passed away some time between 1629 and 1637 AD at Goindwal. Guru Har Gobind Sahib personally cremated his
    body. Bhai Gurdas had the good fortune to have had the the company of four Gurus. </p>
<p><strong>Bhai Gurdas's Contribution to Sikh Literature Scribe of Guru Granth Sahib</strong></p>
<p>The compilation of Guru Granth Sahib was completed in 1601. It took almost 11 years to complete this task. Bhai
    Gurdas not only wrote the Adi Granth as dictated by Guru Arjan Dev, he also supervised the writings of four other
    scribes, namely Bhai Haria, Bhai Sant Das, Bhai Sukha and Bhai Manasa Ram who were writing various Sikh
    scriptures.</p>
<p>Bhai Gurdas was not only an interpreter of Sikh scriptures and preacher of Sikhism, he was a walking encyclopaedia of
    Sikhism. </p>
<p>Bhai Gurdas was a great scholar of Persian and Sankrit and of comparative religion. He was a poet of superb beauty.
    His most famous compositions are Vars, (Punjabi ballads, 40 in number). </p>
<p><strong>Bhai Gurdas as a Sikh Historian</strong></p>
<p>Bhai Gurdas has documented the Sikh history in his writings and has solved some of the historical riddles about Guru
    Nanak Dev's visit to Mecca, Medina, and other parts of the world:</p>
<p><em>Fir Baba gaya Baghdad no bahar jae kiya asthana | <br/>
        Ek Baba Akal roop, dooja rababi Mardana |</em></p>
<p><em>Then Baba (Guru Nanak Dev Ji) went to Baghdad and camped outside the city. In addition to Baba Nanak, who was a
        Divine personality, Mardana, the musician also went along.</em></p>
<p><strong>Bhai Gurdas's Account of Martyrdom of Guru Arjan Dev</strong></p>
<p>Guru Arjan Dev was martyred as per orders of emperor Jahangir on May 30, 1606. Jahangir wrote in his
    Tuzak-i-Jahangiri only 20 days after the martyrdom of Guru Arjan Dev that he ordered his execution.</p>
<p>Bhai Gurdas had documented the martyrdom of Guru Arjan Dev as follows:</p>
<p><em>Rehnde Gur dariayo vich, meen kuleen het nirbani |<br/>
        Darsan dekh patang jio(n) joti andar jot samani |<br/>
        Sabad surat(i) liv mirg jio, bhirh payee chit avar na jani |<br/>
        Gur Arjan vith(u) kurbani | (Bhai Gurdas, Var 24)</em></p>
<p><em>To achieve martyrdom, Guru Arjan Dev ji immersed in the God-like ocean like a fish. The Guru merged into the
        heavenly light like the moth that immolates itself after seeing the light. <br/>
        .. I sacrifice my life to Guru Arjan. </em></p>
