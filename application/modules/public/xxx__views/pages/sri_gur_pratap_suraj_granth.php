<h2 align="center"><strong>Sri Gur Pratap Suraj Granth</strong></h2>
<hr/>
<p class="subhead">Sri Gur Pratap Suraj Granth</strong> </p>
<p class="blue">Authored by &lsquo;Kavi Churamani&rsquo; Bhai Santokh Singh ji, Doyen of Nirmala Sect, &lsquo;Sri Gur
    Partap Suraj Granth&rsquo; popularly known as &lsquo;Suraj Parkash&rsquo; is a voluminous classical medieval source
    of Sikh History and Philosophy. Its &lsquo;Katha&rsquo; (Religious Discourse) in almost all prominent Gurdwaras is
    the gauge of its authenticity and popularity among the Sikh masses. </p>
<p class="blue">Bhai Santokh Singh's monumental work in Braj verse portraying in comprehensive detail the lives of the
    Ten Gurus of the Sikh faith and the career of Banda Singh Bahadur. Besides being an historical narrative of great
    significance, it is an outstanding creation in the style epic, and is the most voluminous of all poetic compositions
    in Hindi/Punjabi literature. Its language is Braj Bhasa which was the literary Hindi of that time though its script
    is Gurmukhi. Notwithstanding certain drawbacks which scholars with training in modern historiography may point out,
    it remains the most valuable source book on Sikh history of the period of the Gurus and, indeed, on the very roots
    of the entire Sikh tradition. For the massive flow of its poetry, the vast range of its figures and images and for
    the abundance of detail, Sri Gur Pratap Suraj Granth, Suraj Prakash in shorter, popular form, is worthy to rank with
    the classics in this genre.</p>
<p class="blue">The title of the main work carries a symbolic meaning summed up in the cosmic metaphor of suraj, i.e.
    the sun. The poet himself explains, &quot;As the sun rises, the darkness of the night vanishes, thieves and thugs
    hide themselves, owls and bats go to slumber and the stars disappear, so with the advent of the Gurus, the rays of
    their spiritual light spread all around dispelling the darkness of ignorance.&quot;</p>
<p class="blue">The work is divided into two parts. The first, Sri Gur Nanak Prakash in two sections, is the story of
    the life of Guru Nank. The second, Sri Gur Pratap Suraj proper, is divided into portions, rut (season), according to
    the twelve signs of the zodiac, sub-divided into chapters called arisu (rays). In the Sri Gur Nanak Parkash portion,
    the style of the narrative tends to be more elaborate, with many a stanza given to homage to the Gurus, the Guru
    Granth Sahib and to the patron deities of learning. The latter part, which deals with the lives of succeeding nine
    Gurus and Banda Singh Bahadur, contains 51,829 verse pieces in 22 cantos. The expression here is less rhetoric. Both
    the parts are further sub-divided into numerous sections according to the episodes narrated, each named after the
    sun's course, viz. the twelve zodaical signs, the six seasons and the two solstices (winter and summer solstices)
    which in turn comprise 1151 sunbeams, each one comprising a chapter. The phrase and imagery in both the parts of the
    book generally require expert explanation. This has been provided, painstakingly and exhaustively, by Bhai Vir Singh
    in a 14-volume annotated edition brought out in 1927-35. Bhai Vir Singh has also added notes where necessary.</p>
<p class="blue">It is usual for gianis (learned scholars) to hold serial discourses on the text of Suraj Prakash in
    gurdwaras, normally in the afternoons or evenings.</p>
